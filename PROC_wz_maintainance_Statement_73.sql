


IF NOT EXISTS(SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_PMT_REP_ID' AND VALUE='1')
BEGIN
	UPDATE A SET REP_ID=''
	FROM PMT01106 A
	JOIN STMH01106 B ON A.REP_ID=B.REP_ID
	WHERE B.RECONCILED=1
	
	IF NOT EXISTS(SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_PMT_REP_ID')
       INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
	   VALUES ('UPDATE_PMT_REP_ID','1','',GETDATE())	
	ELSE	
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_PMT_REP_ID'	
		
END
