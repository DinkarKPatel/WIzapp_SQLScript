

IF NOT EXISTS(SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CREDITREFUND_NEWPAYMODECODE' AND VALUE='1')
BEGIN
	PRINT 'UPDATE NEW PAYMENT CODE FOR CREDIT REFUND ENTRIES' 
	UPDATE A SET PAYMODE_CODE='CMR0001' FROM PAYMODE_XN_DET A
	JOIN CMM01106 B ON A.MEMO_ID=B.CM_ID WHERE A.XN_TYPE='SLS' AND A.PAYMODE_CODE='0000004'
	AND A.AMOUNT<0 AND SUBSTRING(B.CM_NO,5,1)<>'N'
	
	IF NOT EXISTS(SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CREDITREFUND_NEWPAYMODECODE')
       INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
	   VALUES ('UPDATE_CREDITREFUND_NEWPAYMODECODE','1','',GETDATE())	
	ELSE	
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_CREDITREFUND_NEWPAYMODECODE'	
		
END
