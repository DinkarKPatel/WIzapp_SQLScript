CREATE PROCEDURE SP_GV_CANCELLATION      
@NQUERYID  NUMERIC (2,0),      
@CMEMOID  VARCHAR(40),      
@CWHERE   VARCHAR(500),      
@CPRODUCTCODE VARCHAR(50),      
@CFINYEAR  VARCHAR(5),      
@NNAVMODE  NUMERIC(2,0)  
--WITH ENCRYPTION     
AS      
BEGIN      
DECLARE @CCMD NVARCHAR(MAX)      
      
IF       
@NQUERYID = 1      
GOTO LBLNAVIGATE      
      
ELSE IF       
@NQUERYID = 2      
GOTO LBLGETMASTERS      
      
ELSE IF       
@NQUERYID = 3      
GOTO LBLGETDETAILS      
      
ELSE IF      
@NQUERYID = 4      
GOTO LBLGETPRODUCTLIST      
  
ELSE
	GOTO LAST      
    
      
LBLNAVIGATE:      
 EXECUTE SP_NAVIGATE 'GV_CNC_MST',@NNAVMODE,@CMEMOID,@CFINYEAR,'MEMO_NO','MEMO_DT','MEMO_ID',@CWHERE      
GOTO LAST      
      
LBLGETMASTERS:      

  SELECT *       
  FROM GV_CNC_MST T1        
  WHERE T1.MEMO_ID = @CMEMOID         
       
GOTO LAST      
      
LBLGETDETAILS:      
        
  SELECT D.*,S.*
  FROM GV_CNC_DET D      
  JOIN GV_CNC_MST M ON D.MEMO_ID = M.MEMO_ID       
  JOIN SKU_GV_MST S ON D.GV_SRNO = S.GV_SRNO       
  LEFT OUTER JOIN PMT_GV_MST P ON D.GV_SRNO = P.GV_SRNO      
  WHERE M.MEMO_ID = @CMEMOID   
   
        
 GOTO LAST      
      
      
     
LBLGETPRODUCTLIST:      
	SELECT * FROM SKU_GV_MST A 
	JOIN  PMT_GV_MST P ON P.GV_SRNO=A.GV_SRNO
	WHERE P.QUANTITY_IN_STOCK>0
      
GOTO LAST      
      
      
LAST: 
     
END
