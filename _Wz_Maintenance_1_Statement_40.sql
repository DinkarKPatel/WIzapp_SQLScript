

IF EXISTS (SELECT B.NAME FROM SYS.TABLES A JOIN SYS.COLUMNS B ON A.OBJECT_ID=B.OBJECT_ID WHERE 
            A.NAME='SECTIOND' AND B.NAME='HSN_CODE')
    BEGIN
      IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SKU_HSN_FROM_SECTIOND_NEW' AND VALUE=1)
		BEGIN

		   DECLARE @CCMD NVARCHAR(MAX)
			SET @CCMD=N'UPDATE SKU SET HSN_CODE=SECTIOND.HSN_CODE FROM SKU JOIN ARTICLE ON SKU.ARTICLE_CODE=ARTICLE.ARTICLE_CODE
					JOIN SECTIOND ON SECTIOND.SUB_SECTION_CODE=ARTICLE.SUB_SECTION_CODE
					WHERE ISNULL(SKU.HSN_CODE,'''') IN ('''',''0000000000'')'
		EXEC SP_EXECUTESQL @CCMD			
					
			IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SKU_HSN_FROM_SECTIOND_NEW')
				INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
					   VALUES ('UPDATE_SKU_HSN_FROM_SECTIOND_NEW',1,'',GETDATE())	
			ELSE
				UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_SKU_HSN_FROM_SECTIOND_NEW'

		END	
    END

	
