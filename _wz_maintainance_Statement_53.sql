

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='REMOVE_CMDT_TIMECOMPONENT' AND VALUE='1')
BEGIN

	UPDATE CMM01106 SET CM_DT=CONVERT(DATETIME,CONVERT(VARCHAR(10),CM_DT,120))
	WHERE CM_DT<>CONVERT(DATETIME,CONVERT(VARCHAR(10),CM_DT,120))

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='REMOVE_CMDT_TIMECOMPONENT')
       INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
	   VALUES ('REMOVE_CMDT_TIMECOMPONENT','1','',GETDATE())	
	ELSE	
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='REMOVE_CMDT_TIMECOMPONENT'
	
END
