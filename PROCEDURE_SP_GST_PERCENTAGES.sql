CREATE PROCEDURE SP_GST_PERCENTAGES
(    
  @DTFROM DATETIME =''    
 ,@DTTO	 DATETIME =''    
)
AS    
BEGIN    
SET NOCOUNT ON
SELECT DISTINCT CAST(0 AS BIT)CHK,D.gst_percentage GST
FROM CMM01106 M (NOLOCK) JOIN CMD01106 D (NOLOCK) ON M.cm_id=D.cm_id
WHERE M.CANCELLED=0 AND M.CM_DT BETWEEN @DTFROM AND @DTTO
SET NOCOUNT OFF
END
--END OF PROCEDURE - SP_GST_PERCENTAGES