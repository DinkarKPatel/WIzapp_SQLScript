CREATE VIEW VW_XNRECONC_PRINT

AS
SELECT A.PRODUCT_CODE,A.QUANTITY,T7.MRP,T7.PURCHASE_PRICE,T7.WHOLESALE_PRICE,
		T7.ARTICLE_NAME ,T7.ARTICLE_NO ,C1.PARA1_NAME ,
		D1.PARA2_NAME ,F1.PARA3_NAME ,G1.PARA4_NAME ,
		H1.PARA5_NAME ,I1.PARA6_NAME ,SM1.SECTION_NAME ,
		SD1.SUB_SECTION_NAME ,T8.UOM_NAME ,'' AS RECON_ID
		FROM 
		(SELECT T1.PRODUCT_CODE,T1.QUANTITY 
		FROM CND01106 T1 WHERE 1=2) A 
		JOIN SKU T6 ON T6.PRODUCT_CODE = A.PRODUCT_CODE
		JOIN ARTICLE T7 ON T6.ARTICLE_CODE = T7.ARTICLE_CODE
		JOIN SECTIOND SD1 ON T7.SUB_SECTION_CODE = SD1.SUB_SECTION_CODE
		JOIN SECTIONM SM1 ON SD1.SECTION_CODE = SM1.SECTION_CODE
		JOIN PARA1 C1 ON T6.PARA1_CODE = C1.PARA1_CODE  
		JOIN PARA2 D1 ON T6.PARA2_CODE = D1.PARA2_CODE  
		JOIN PARA3 F1 ON T6.PARA3_CODE = F1.PARA3_CODE  
		JOIN PARA4 G1 ON T6.PARA4_CODE = G1.PARA4_CODE  
		JOIN PARA5 H1 ON T6.PARA5_CODE = H1.PARA5_CODE  
		JOIN PARA6 I1 ON T6.PARA6_CODE = I1.PARA6_CODE  
		JOIN UOM T8 ON T8.UOM_CODE = T7.UOM_CODE
