CREATE PROCEDURE SP3S_VALIDATEXN_PRTMERGE
@cSpId VARCHAR(50),
@cMemoId VARCHAR(40),
@cErrormsg VARCHAR(MAX) OUTPUT
AS
BEGIN
	DECLARE @NNETAMOUNT NUMERIC(10,2),@NCMMSUBTOTAL NUMERIC(10,2),@NPAYMODETOTAMT NUMERIC(10,2),
	@NTotPRTQty NUMERIC(10,2),@NTotRmdQty NUMERIC(10,2),@cStep VARCHAR(4),@nPaymode NUMERIC(1,0)

	
	SET @CSTEP=10.8
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CSPID,'',1


	SELECT @NTotPRTQty=total_quantity FROM rmm01106 (NOLOCK) WHERE rm_id=@cMemoId

	SET @CSTEP=11.2
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CSPID,'',1
		
	SELECT @NTotRmdQty = SUM(quantity) FROM rmd01106 (NOLOCK) WHERE rm_id=@cMemoId 

	SET @CSTEP=11.4
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CSPID,'',1		  
	SELECT @NTotPRTQty=ISNULL(@NTotPRTQty,0),@NTotRmdQty = ISNULL(@NTotRmdQty,0)  
	
	IF @NTotPRTQty<>@NTotRmdQty
	BEGIN  
		PRINT 'SP3S_VALIDATEXN_PRTMERGE Quantity match FAILED'
		SET @CERRORMSG='Bill level Total quantity '+STR(@NTotPRTQty,14,2)+' SHOULD BE EQUAL TO THE SUM OF Item level quantity '+STR(@NTotRmdQty,10,2)+'...PLEASE CHECK'  
		GOTO END_PROC
	END

END_PROC:
	
END