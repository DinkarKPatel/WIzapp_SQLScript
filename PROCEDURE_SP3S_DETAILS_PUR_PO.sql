create PROCEDURE  SP3S_DETAILS_PUR_PO
(
	@CWHERE VARCHAR(100)=''
)
AS
BEGIN
	--DECLARE @CWHERE VARCHAR(100)=''
	

	SELECT POM.PO_NO AS		PONO ,CONVERT(VARCHAR(10), POM.PO_DT ,105) AS PODT
	FROM PID01106 A (NOLOCK)
	join PurchaseOrderProcessingNew p (nolock) on a.row_id= p.rowid 
	JOIN POD01106 B (NOLOCK) ON p.RefRowId  =B.ROW_ID 
	JOIN POM01106 POM (NOLOCK) ON POM.po_id =B.PO_ID 
	WHERE A.MRR_ID =@CWHERE and p.XnType ='PurchaseInvoice'
	GROUP BY POM.PO_NO ,POM.PO_DT ,POM.PO_ID 
	
END

