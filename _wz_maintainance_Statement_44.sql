		


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_INMRMM_EXPORTED' AND VALUE=1)
BEGIN
	PRINT 'UPDATE EXPORTED COLUMN IN INM01106 & RMM01106 FOR OLDER ENTRIES'
	
	
	UPDATE INM01106 SET EXPORTED=1 WHERE INV_MODE=2 AND INV_DT<=DATEADD(DD,-30,CONVERT(VARCHAR,GETDATE(),110))
	
	UPDATE RMM01106 SET EXPORTED=1 WHERE MODE=2 AND RM_DT<=DATEADD(DD,-30,CONVERT(VARCHAR,GETDATE(),110))

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_INMRMM_EXPORTED')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_INMRMM_EXPORTED','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_INMRMM_EXPORTED'
	
END	
