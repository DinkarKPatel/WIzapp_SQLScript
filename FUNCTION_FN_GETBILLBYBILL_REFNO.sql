CREATE FUNCTION FN_GETBILLBYBILL_REFNO(@CACCODE CHAR(10),@CREFNO VARCHAR(100),@DREFDATE DATETIME,@CXTYPE CHAR(2),
									   @CVMID VARCHAR(40))
RETURNS VARCHAR(100)
AS
BEGIN
	DECLARE @CRETREFNO VARCHAR(100)
	
	SET @CRETREFNO=@CREFNO
	
	DECLARE @CDATESTR VARCHAR(10)
	SET @CDATESTR=CONVERT(VARCHAR,@DREFDATE,112)
	SET @CRETREFNO=LTRIM(RTRIM(@CRETREFNO))+'/'+LTRIM(RTRIM(@CDATESTR))
	
	RETURN @CRETREFNO
END
