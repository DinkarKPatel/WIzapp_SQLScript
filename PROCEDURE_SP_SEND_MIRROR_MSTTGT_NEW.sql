CREATE PROCEDURE DBO.SP_SEND_MIRROR_MSTTGT_NEW
(
 @CTARGETLOCID VARCHAR(5)
)
AS
 BEGIN
   DECLARE @DTSQL NVARCHAR(MAX),@CSTEP VARCHAR(10),@CFILTERCONDITION NVARCHAR(MAX)
           ,@CTABLENAME VARCHAR(100),@CTEMPTABLE VARCHAR(100),@CTEMPLMTABLE VARCHAR(100)
           ,@CTEMPLMPTABLE VARCHAR(100),@CTEMPDBNAME VARCHAR(200),@CCURDEPTID VARCHAR(5)
           ,@CHODEPTID VARCHAR(5),@CERRORMSG VARCHAR(MAX),@CREPLOCTABLE VARCHAR(500),@NLOCTYPE INT
           ,@CLOCMERGINGTHRUWIZCOM VARCHAR(2),@JOINMASTERTABLE VARCHAR(200),@JOINTEMPTABLE VARCHAR(200)

    DECLARE @TXNSSENDINFO TABLE (ORG_TABLENAME VARCHAR(100),TMP_TABLENAME VARCHAR(100),ERRMSG VARCHAR(MAX)) 
    
    BEGIN TRY
    
	SET @CSTEP=0

	SET @CTEMPDBNAME = ''
	
	SET @CSTEP=10
	
	PRINT @CSTEP+':'+CONVERT(VARCHAR,GETDATE(),113)
	SET @CFILTERCONDITION=''
	 
	SET @CSTEP=20

	SELECT @CCURDEPTID = VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'
	SELECT @CHODEPTID = VALUE FROM CONFIG WHERE CONFIG_OPTION='HO_LOCATION_ID'		
	
	SELECT @NLOCTYPE=LOC_TYPE FROM LOCATION WHERE DEPT_ID=@CTARGETLOCID
	
	PRINT @CSTEP+':'+CONVERT(VARCHAR,GETDATE(),113)
	
	IF @CCURDEPTID<>@CHODEPTID
	BEGIN
		SELECT '' AS LAST_UPDATE,'' AS ERRMSG
		
		GOTO END_PROC
	END		
    SET @CSTEP=30
	
	
	--INSERT DATA FROM MAIN TABLE TO TEMP TABLE
    SET @CSTEP=40
    ---SELECT DATA FROM LOC_SALE_TARGET TABLE
	SET @CTABLENAME = 'LOC_SALE_TARGET'

	SELECT DISTINCT  'MSTTGT_LOC_SALE_TARGET_MIRROR' AS TARGET_TABLENAME,LOC_SALE_TARGET.*,1 AS PICK_FROM_CURRENT_DB FROM LOC_SALE_TARGET (NOLOCK)
	
	
	--INSERT DATA FROM MAIN TABLE TO TEMP TABLE
    SET @CSTEP=50
    ---SELECT DATA FROM SALEPERSON_TARGET_MST TABLE
	SET @CTABLENAME = 'SALEPERSON_TARGET_MST'

	SELECT DISTINCT  'MSTTGT_SALEPERSON_TARGET_MST_MIRROR' AS TARGET_TABLENAME,SALEPERSON_TARGET_MST.*,1 AS PICK_FROM_CURRENT_DB FROM SALEPERSON_TARGET_MST (NOLOCK)
	
	--INSERT DATA FROM MAIN TABLE TO TEMP TABLE
    SET @CSTEP=60
    ---SELECT DATA FROM SALEPERSON_TARGET_DET TABLE
	SET @CTABLENAME = 'SALEPERSON_TARGET_DET'

	SELECT DISTINCT  'MSTTGT_SALEPERSON_TARGET_DET_MIRROR' AS TARGET_TABLENAME,SALEPERSON_TARGET_DET.*,1 AS PICK_FROM_CURRENT_DB FROM SALEPERSON_TARGET_DET (NOLOCK)
	


	--INSERT DATA FROM MAIN TABLE TO TEMP TABLE
    SET @CSTEP=70
    ---SELECT DATA FROM SALEPERSON_TARGET_SUBDET TABLE
	SET @CTABLENAME = 'SALEPERSON_TARGET_SUBDET'

	SELECT DISTINCT  'MSTTGT_SALEPERSON_TARGET_SUBDET_MIRROR' AS TARGET_TABLENAME,SALEPERSON_TARGET_SUBDET.*,1 AS PICK_FROM_CURRENT_DB FROM SALEPERSON_TARGET_SUBDET (NOLOCK)

	
	END TRY
	
	BEGIN CATCH
	    SET @CERRORMSG='P: SP_SEND_MIRROR_MSTTGT_NEW, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()+ 'ERROR QUERY || '+@DTSQL
	    GOTO END_PROC
    END CATCH 
    
    END_PROC:
    
   SELECT ISNULL(@CERRORMSG,'') AS ERRMSG
 END
