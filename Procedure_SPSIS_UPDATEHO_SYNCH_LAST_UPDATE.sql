
CREATE PROCEDURE SPSIS_UPDATEHO_SYNCH_LAST_UPDATE
(
 @CTABLENAME VARCHAR(100),
 @CCOLUMNNAME VARCHAR(50),
 @CXN_ID VARCHAR(50),
 @DLASTUPDATE DATETIME 
)
AS
BEGIN
    

    DECLARE @DTSQL NVARCHAR(MAX)
	SET @DTSQL='UPDATE A  SET PUMA_HO_SYNCH_LAST_UPDATE ='''+CONVERT(VARCHAR(100),@DLASTUPDATE,121)+'''
	FROM  '+@CTABLENAME+'  A (NOLOCK) WHERE '+@CCOLUMNNAME+'='''+@CXN_ID+''' '
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL

END

