CREATE PROCEDURE SP_PPC_COMPANY_DETAILS
(
@COMPANY_CODE CHAR(2)
)
AS
BEGIN
    
       SELECT CIN,BL_COMPANY_NAME,PAN_NO,EMAIL_ID,PWD,STATE,COUNTRY,MOBILE,CONTACT_NAME,TDS_AC_NO,COMPANY_CODE,
COMPANY_NAME,ALIAS,ADDRESS1,ADDRESS2,CITY,TIN_NO,TAN_NO,PHONES_FAX,CST_NO,CONVERT(VARCHAR,CST_DT,101) AS CST_DT,SST_NO,SST_DT,GRP_CODE,
ADDRESS9,AREA_CODE,PRINT_ADDRESS,WORKABLE,PIN,LOGO_PATH,WEB_ADDRESS,SSPL_FIRST_HDSR,POLICY_NO,GRP_NAME,GST_NO
 FROM COMPANY WHERE COMPANY_CODE=@COMPANY_CODE

END
