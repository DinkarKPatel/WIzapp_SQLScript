create PROCEDURE SP3S_SYNCH_UPLOADDATA_POADJ_OPT
(
    @CSPID VARCHAR(50)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_PO_DATA_208_2014_02_01 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
						 
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CSOURCEDB VARCHAR(100),@CMERGEDB VARCHAR(100),@CMEMOID VARCHAR(30)
	   ,@CMEMOIDSEARCH VARCHAR(40),@BADDMODE BIT
	   
DECLARE @TXNSSENDINFO TABLE (ORG_TABLENAME VARCHAR(50),TMP_TABLENAME VARCHAR(50),XN_ID VARCHAR(40))  	

BEGIN TRY

SET @CSTEP=20
	SET @CTABLE_SUFFIX='UPLOAD'
BEGIN TRANSACTION 
    
	SELECT @CSOURCEDB='',@CMERGEDB=''
	
	
    SELECT TOP 1 @CMEMOID = B.MEMO_ID 
    FROM  POADJ_PO_ADJ_MST_UPLOAD B (NOLOCK)
    WHERE SP_ID=@CSPID 
	
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		

    SET @CFILTERCONDITION = 'B.memo_ID='''+@CMEMOID+''' AND B.SP_ID='''+LTRIM(RTRIM((@CSPID)))+''''
	
	SET @CMEMOIDSEARCH=''
	SELECT TOP 1 @CMEMOIDSEARCH=A.memo_ID FROM PO_ADJ_MST A (NOLOCK) WHERE A.memo_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1

	
	
	SET @CSTEP=320
	SET @CTABLENAME='PO_ADJ_MST'
	SET @CTMP_TABLENAME='POADJ_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=330
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
			
			
					  
							 
	SET @CSTEP=340
	SET @CTABLENAME='PO_ADJ_DET'
	SET @CTMP_TABLENAME='POADJ_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ROW_ID'
	SET @CSTEP=350

	IF @BADDMODE=0
	BEGIN	

				
		SET @CSTEP=115
		IF @CFILTERCONDITION<>''
		BEGIN

			SET @DTSQL=N'DELETE A FROM PO_ADJ_DET A WHERE MEMO_ID='''+@CMEMOID+''' '
			PRINT @DTSQL
			EXEC SP_EXECUTESQL @DTSQL

		END
	END

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
   

   IF EXISTS (SELECT TOP 1 'U' FROM POADJ_POD_UPLOAD (NOLOCK) WHERE SP_ID=@CSPID)
	BEGIN
	     

		 UPDATE A SET ADJ_QUANTITY=B.ADJ_QUANTITY 
		 FROM POD01106 A (NOLOCK)
		 JOIN POADJ_POD_UPLOAD B (NOLOCK) ON A.ROW_ID =B.ROW_ID
		 WHERE B.SP_ID=@CSPID


	END


  DELETE FROM POADJ_PO_ADJ_DET_UPLOAD WHERE SP_ID=@CSPID
  
  DELETE FROM POADJ_PO_ADJ_MST_UPLOAD WHERE SP_ID=@CSPID

  DELETE FROM POADJ_POD_UPLOAD WHERE SP_ID=@CSPID


END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP3S_SYNCH_UPLOADDATA_POADJ_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_POADJ_DATA


