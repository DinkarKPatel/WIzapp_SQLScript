


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_ARTICLE_HSN_FROM_SECTIOND' AND VALUE=1)
BEGIN

    DECLARE @CCMD NVARCHAR(MAX)
	
	SET @CCMD=N'UPDATE ARTICLE SET HSN_CODE=ISNULL(SECTIOND.HSN_CODE,''0000000000'') FROM ARTICLE 
			    JOIN SECTIOND ON SECTIOND.SUB_SECTION_CODE=ARTICLE.SUB_SECTION_CODE
			WHERE ISNULL(ARTICLE.HSN_CODE,'''') IN ('''',''0000000000'')'
	EXEC SP_EXECUTESQL @CCMD			
			
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_ARTICLE_HSN_FROM_SECTIOND')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_ARTICLE_HSN_FROM_SECTIOND',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_ARTICLE_HSN_FROM_SECTIOND'
END		
