CREATE PROCEDURE SP3S_INSERT_WIZBIT_ATTRIBUTE_DETAILS_NEW
AS
BEGIN
	   
	   DELETE FROM ATTRIBUTE_DETAILS_NEW
	    
	INSERT ATTRIBUTE_DETAILS_NEW( TABLENAME, COLNAME, USERDISPLAY,  CHK )  
	SELECT 	'SECTIONM' TABLENAME,'SECTION_NAME' COLNAME, 'SECTION NAME'USERDISPLAY,0  CHK
	UNION ALL
	SELECT ' SECTIOND'TABLENAME,'SUB_SECTION_NAME' COLNAME,'SUB SECTION NAME' USERDISPLAY,0 CHK
	UNION ALL
	SELECT ' ARTICLE'TABLENAME,'ARTICLE_NO' COLNAME,'ARTICLE NO' USERDISPLAY,0 CHK
	UNION ALL
	SELECT ' PARA1'TABLENAME,'PARA1_NAME' COLNAME,'PARA1 NAME' USERDISPLAY,0 CHK
	UNION ALL
	SELECT ' PARA2'TABLENAME,'PARA2_NAME' COLNAME,'PARA2 NAME' USERDISPLAY,0 CHK
	UNION ALL
	SELECT ' PARA3'TABLENAME,'PARA3_NAME' COLNAME,'PARA3 NAME' USERDISPLAY,0 CHK
	UNION ALL
	SELECT ' PARA4'TABLENAME,'PARA4_NAME' COLNAME,'BRAND' USERDISPLAY,0 CHK
	UNION ALL
	SELECT ' PARA5'TABLENAME,'PARA5_NAME' COLNAME,'PARA5 NAME' USERDISPLAY,0 CHK
	UNION ALL
	SELECT ' PARA6'TABLENAME,'PARA6_NAME' COLNAME,'PARA6 NAME' USERDISPLAY,0 CHK


END
