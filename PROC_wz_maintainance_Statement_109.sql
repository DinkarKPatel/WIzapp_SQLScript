

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SCHEME_FILTERS_2' AND VALUE=1)
BEGIN
	PRINT 'UPDATING FILTER CRITERIAS OF SCHEME SET TABLES RELATED TO MRP'
	
	UPDATE SCHEME_SETUP_DET SET GET_FILTER_CRITERIA=REPLACE(GET_FILTER_CRITERIA,'MRP','ITV.MRP')
	WHERE GET_FILTER_CRITERIA NOT LIKE '%ITV.MRP%' AND GET_FILTER_CRITERIA LIKE '%MRP%'

	UPDATE SCHEME_SETUP_DET SET BUY_FILTER_CRITERIA=REPLACE(BUY_FILTER_CRITERIA,'MRP','ITV.MRP')
	WHERE BUY_FILTER_CRITERIA NOT LIKE '%ITV.MRP%' AND BUY_FILTER_CRITERIA LIKE '%MRP%'
	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SCHEME_FILTERS_2')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_SCHEME_FILTERS_2',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_SCHEME_FILTERS_2'
END
