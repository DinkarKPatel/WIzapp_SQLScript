CREATE VIEW VW_BOX_QBF
AS         
SELECT A.BOX_ID, A.BOX_NO ,A.BOX_DT ,A.FIN_YEAR,
       A.REF_MRR_ID,A.CANCELLED,B.PRODUCT_CODE,
	   B.QUANTITY,B.ROW_ID,C.ARTICLE_NO ,
	   C.SUB_SECTION_NAME ,C.SECTION_NAME ,C.PARA1_NAME ,
	   C.PARA2_NAME,C.PARA3_NAME ,C.PARA4_NAME ,C.PARA5_NAME ,C.PARA6_NAME,
	   C.PP AS PURCHASE_PRICE  ,C.MRP,UOM.UOM_NAME
FROM BOXM A (NOLOCK) 
JOIN BOXD B (NOLOCK) ON A.BOX_ID = B.BOX_ID
JOIN SKU S (NOLOCK) ON S.product_code =B.PRODUCT_CODE 
JOIN ARTICLE ART (NOLOCK) ON ART.ARTICLE_CODE =S.article_code
JOIN UOM (NOLOCK) ON UOM.UOM_CODE=ART.UOM_CODE 
JOIN SKU_NAMES (NOLOCK) C ON B.PRODUCT_CODE = C.PRODUCT_CODE


 