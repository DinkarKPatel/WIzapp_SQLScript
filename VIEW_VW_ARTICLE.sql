CREATE VIEW VW_ARTICLE

AS
	SELECT	G.USERNAME AS EDIT_USERNAME, F.USERNAME, D.SECTION_NAME, 
			C.SUB_SECTION_NAME, C.MFG_CATEGORY, B.UOM_NAME, B.UOM_TYPE,
			A.* 
	FROM ARTICLE A
	JOIN UOM B ON A.UOM_CODE = B.UOM_CODE 
	JOIN SECTIOND C ON A.SUB_SECTION_CODE = C.SUB_SECTION_CODE 
	JOIN SECTIONM D ON C.SECTION_CODE = D.SECTION_CODE 
	JOIN USERS F ON A.USER_CODE = F.USER_CODE
	JOIN USERS G ON A.EDT_USER_CODE = G.USER_CODE

