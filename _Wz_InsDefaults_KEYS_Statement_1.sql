-- SETTING THE FINYEAR WITH BLANK IN KEYS TABLE FOR PRODUCT_CODE SO THAT IT SHOULD NOT
-- CREATE A NEW ENTRY IN KEY FOR EVERY FIN_YEAR.
IF EXISTS ( SELECT * FROM KEYS WHERE COLUMNNAME = 'PRODUCT_CODE' AND FINYEAR = '01108' )
	UPDATE KEYS SET FINYEAR = '' WHERE COLUMNNAME = 'PRODUCT_CODE' AND FINYEAR = '01108'
ELSE
BEGIN
	IF EXISTS ( SELECT * FROM KEYS WHERE COLUMNNAME = 'PRODUCT_CODE' AND FINYEAR = '01107' )
		UPDATE KEYS SET FINYEAR = '' WHERE COLUMNNAME = 'PRODUCT_CODE' AND FINYEAR = '01107'
END
