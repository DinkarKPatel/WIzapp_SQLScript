CREATE VIEW VW_FIX_ITEM_RATE_REVISION    

AS    
  SELECT (FMST.MEMO_NO)AS MEMO_NO,(FMST.MEMO_ID)AS MEMO_ID,(FMST.MEMO_DT)AS MST_MEMO_DT,FMST.REMARKS,(FMST.TYPE)AS MST_TYPE ,FMST.FIN_YEAR,FMST.USER_CODE,U.USERNAME,     
  F.PRODUCT_CODE,F.QUANTITY,F.ROW_ID,SK.PURCHASE_PRICE,SK.ARTICLE_CODE,    
  A.ARTICLE_NO,A.ARTICLE_NAME,SM.SECTION_NAME, FLOC.NEW_MRP,FLOC.NEW_WSP,FLOC.REF_ROW_ID,    
  LOC.DEPT_NAME,C.CITY,S.STATE,FLOC.OLD_MRP,FLOC.OLD_WSP,    
  SD.SUB_SECTION_NAME,A.CODING_SCHEME,P1.PARA1_NAME,P2.PARA2_NAME,P3.PARA3_NAME,    
  P4.PARA4_NAME,P5.PARA5_NAME,P6.PARA6_NAME,P1.PARA1_CODE,P2.PARA2_CODE,    
  P3.PARA3_CODE,P4.PARA4_CODE,P5.PARA5_CODE,P6.PARA6_CODE      
  FROM FIXITEM_RATE_REVISION_DET F (NOLOCK)      
  JOIN FIXITEM_RATE_REVISION_MST FMST ON FMST.MEMO_ID=F.MEMO_ID     
  JOIN FIXITEM_RATE_REVISION_LOC_DET FLOC ON F.ROW_ID=FLOC.REF_ROW_ID    
  JOIN USERS U ON U.USER_CODE=FMST.USER_CODE     
  JOIN SKU SK (NOLOCK) ON SK.PRODUCT_CODE=F.PRODUCT_CODE      
  JOIN ARTICLE A (NOLOCK) ON A.ARTICLE_CODE=SK.ARTICLE_CODE       
  JOIN SECTIOND SD (NOLOCK) ON SD.SUB_SECTION_CODE=A.SUB_SECTION_CODE      
  JOIN SECTIONM SM (NOLOCK) ON SM.SECTION_CODE=SD.SECTION_CODE      
  JOIN PARA1 P1 (NOLOCK) ON SK.PARA1_CODE=P1.PARA1_CODE      
  JOIN PARA2 P2 (NOLOCK) ON  SK.PARA2_CODE=P2.PARA2_CODE      
  JOIN PARA3 P3 (NOLOCK) ON SK.PARA3_CODE=P3.PARA3_CODE      
  JOIN PARA4 P4 (NOLOCK) ON SK.PARA4_CODE=P4.PARA4_CODE      
  JOIN PARA5 P5 (NOLOCK) ON SK.PARA5_CODE=P5.PARA5_CODE      
  JOIN PARA6 P6 (NOLOCK) ON SK.PARA6_CODE=P6.PARA6_CODE      
  LEFT OUTER JOIN LOCATION LOC ON LOC.DEPT_ID=FLOC.DEPT_ID         
  LEFT OUTER JOIN AREA AR (NOLOCK) ON AR.AREA_CODE=LOC.AREA_CODE          
  LEFT OUTER JOIN CITY C (NOLOCK) ON C.CITY_CODE=AR.CITY_CODE          
  LEFT OUTER JOIN STATE S (NOLOCK) ON S.STATE_CODE=C.STATE_CODE
