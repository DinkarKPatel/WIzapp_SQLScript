





--**** UPDATE UPLOADED TO ACTIVSTREAM COLUMN IN INM01106 SO THAT NEW UPDATE DOES NOT SEND ALL INVOICES
IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CNM_RECIEPT_DT' AND VALUE=1)
BEGIN
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CNM_RECIEPT_DT')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_CNM_RECIEPT_DT',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_CNM_RECIEPT_DT'
	
	UPDATE CNM01106 SET RECEIPT_DT=CN_DT
END		
