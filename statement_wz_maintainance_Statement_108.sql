

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SCHEME_FILTERS_1' AND VALUE=1)
BEGIN
	PRINT 'UPDATING FILTER CRITERIAS OF SCHEME SET TABLES FOR ATTR_VALUE/LMV'
	--FOR ATTR_VALUE
	UPDATE SCHEME_SETUP_DET SET GET_FILTER_CRITERIA=REPLACE(GET_FILTER_CRITERIA,'ATTR_VALUE.','')

	UPDATE SCHEME_SETUP_DET SET BUY_FILTER_CRITERIA=REPLACE(BUY_FILTER_CRITERIA,'ATTR_VALUE.','')

	--FOR LMV01106
	UPDATE SCHEME_SETUP_DET SET GET_FILTER_CRITERIA=REPLACE(GET_FILTER_CRITERIA,'LMV01106.','')

	UPDATE SCHEME_SETUP_DET SET BUY_FILTER_CRITERIA=REPLACE(BUY_FILTER_CRITERIA,'LMV01106.','')

		
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SCHEME_FILTERS_1')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_SCHEME_FILTERS_1',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_SCHEME_FILTERS_1'
END
