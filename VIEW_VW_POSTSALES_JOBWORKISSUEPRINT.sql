CREATE VIEW  [DBO].[VW_POSTSALES_JOBWORKISSUEPRINT]
AS
    SELECT A.REMARKS AS MST_REMARKS,  A.ISSUE_ID,G.PRODUCT_CODE,G.JOB_RATE,G.QUANTITY,G.ROW_ID,A.CANCELLED,A.ISSUE_NO,A.ISSUE_DT,
	A.TOTAL_AMOUNT ,M.CM_NO,M.CM_DT, F.JOB_NAME,
	ART.ARTICLE_CODE, ART.ARTICLE_NO, ART.ARTICLE_NAME, P1.PARA1_CODE,  
	PARA1_NAME, P2.PARA2_CODE,PARA2_NAME,P3.PARA3_CODE,PARA3_NAME,UOM_NAME,     
	CODING_SCHEME,CONVERT(NUMERIC(10,2),0) AS QUANTITY_IN_STOCK,SKU.PURCHASE_PRICE,
	SKU.MRP,SKU.WS_PRICE, C.MRP as CMD_MRP,  SM.SECTION_NAME, SD.SUB_SECTION_NAME,  
	P4.PARA4_CODE,P5.PARA5_CODE,P6.PARA6_CODE,  
	PARA4_NAME,PARA5_NAME,PARA6_NAME,E.UOM_CODE,ISNULL(E.UOM_TYPE,0) AS [UOM_TYPE],  
	ART.DT_CREATED AS [ART_DT_CREATED],P3.DT_CREATED AS [PARA3_DT_CREATED],SKU.DT_CREATED AS [SKU_DT_CREATED],  
	ART.STOCK_NA,D.AGENCY_NAME,D.AGENCY_CODE ,G.REMARKS
	,ART.DT_CREATED,'' AS ADDRESS ,G.DUE_DT ,G.REF_HBD_ROW_ID ,A.ISSUE_TYPE
	,CUST.CUSTOMER_FNAME +''+ CUST.CUSTOMER_LNAME  AS CUSTOMER_NAME,CUST.MOBILE  
	,EMP.EMP_NAME,EMP1.EMP_NAME AS [EMP_NAME2],EMP2.EMP_NAME AS [EMP_NAME3]
	,M.CUSTOMER_CODE   
	,G.JOB_CODE 
	,COM.COMPANY_CODE AS COMP_COMPANYCODE
,COM.PAN_NO AS COMP_PANNO
,COM.EMAIL_ID AS COMP_EMAILID
,COM.PWD AS COMP_PWD
,COM.STATE AS COMP_STATE 
,COM.COUNTRY AS COMP_CONTRY
,COM.MOBILE AS COMP_MOBILE
,COM.CONTACT_NAME AS COMP_CONTACTNAME
,COM.TDS_AC_NO AS COMP_TDSACNO
,COM.COMPANY_NAME AS COMP_COMPANYNAME
,COM.ALIAS AS COMP_ALIAS
,COM.ADDRESS1 AS COMP_ADDRESS1
,COM.ADDRESS2 AS COMP_ADDRESS2 
,COM.CITY AS COMP_CITY
,COM.TAN_NO AS COMP_TANNO
,COM.PHONES_FAX AS COMP_PHONES_FAX
,COM.CST_DT AS COMP_CST_DT
,COM.SST_NO AS COMP_SSTNO
,COM.SST_DT AS COMP_SSTDT
,COM.GRP_CODE AS COMP_GRPCODE
,COM.ADDRESS9 AS COMP_ADDRESS9
,COM.AREA_CODE AS COMP_AREACODE
,COM.PRINT_ADDRESS AS COMP_PRINTADDRESS
,COM.WORKABLE AS COMP_WORKABLE
,COM.PIN AS COMP_PIN
,COM.LOGO_PATH AS COMP_LOGOPATH
,COM.WEB_ADDRESS AS COMP_WEBADDRESS
,COM.SSPL_FIRST_HDSR AS COMP_SSPLFIRSTHDSR
,COM.POLICY_NO AS COMP_POLICYNO
,COM.GRP_NAME AS COMP_GRPNAME 
,COM.CIN AS COMP_CIN
,C.PRODUCT_CODE AS OLD_PRODUCT_CODE
,LMP.ADDRESS1 AS AGENCY_ADDRESS1
,LMP.ADDRESS2 AS AGENCY_ADDRESS2
,LMP.AREA_NAME AS AGENCY_AREA_NAME
,LMP.PINCODE AS AGENCY_PINCODE
,LMP.STATE AS AGENCY_STATE
,LMP.MOBILE AS AGENCY_MOBILE

FROM POST_SALES_JOBWORK_ISSUE_MST A (NOLOCK)           
JOIN POST_SALES_JOBWORK_ISSUE_DET G (NOLOCK)  ON A.ISSUE_ID = G.ISSUE_ID     
JOIN HOLD_BACK_DELIVER_DET B (NOLOCK) ON G.REF_HBD_ROW_ID = B.ROW_ID    
JOIN HOLD_BACK_DELIVER_MST HM (NOLOCK) ON HM.MEMO_ID=B.MEMO_ID  
JOIN CUSTDYM CUST (NOLOCK) ON CUST.CUSTOMER_CODE = HM.CUSTOMER_CODE  
LEFT JOIN SKU (NOLOCK)   ON SKU.PRODUCT_CODE=G.PRODUCT_CODE    
JOIN ARTICLE ART (NOLOCK) ON SKU.ARTICLE_CODE = ART.ARTICLE_CODE        
JOIN SECTIOND SD (NOLOCK) ON ART.SUB_SECTION_CODE = SD.SUB_SECTION_CODE      
JOIN SECTIONM SM (NOLOCK) ON SD.SECTION_CODE = SM.SECTION_CODE      
JOIN PARA1 P1 (NOLOCK) ON SKU.PARA1_CODE = P1.PARA1_CODE        
JOIN PARA2 P2 (NOLOCK) ON SKU.PARA2_CODE = P2.PARA2_CODE        
JOIN PARA3 P3 (NOLOCK)ON SKU.PARA3_CODE = P3.PARA3_CODE        
JOIN PARA4 P4 (NOLOCK)ON SKU.PARA4_CODE = P4.PARA4_CODE        
JOIN PARA5 P5 (NOLOCK)ON SKU.PARA5_CODE = P5.PARA5_CODE        
JOIN PARA6 P6 (NOLOCK)ON SKU.PARA6_CODE = P6.PARA6_CODE        
JOIN UOM E (NOLOCK)ON ART.UOM_CODE = E.UOM_CODE  
LEFT JOIN CMD01106 C (NOLOCK)   ON C.ROW_ID = B.REF_CMD_ROW_ID 
LEFT JOIN CMM01106 M (NOLOCK) ON C.CM_ID = M.CM_ID  
left JOIN EMPLOYEE EMP(NOLOCK) ON EMP.EMP_CODE=C.EMP_CODE    
left JOIN EMPLOYEE EMP1(NOLOCK) ON EMP1.EMP_CODE=C.EMP_CODE1    
left JOIN EMPLOYEE EMP2(NOLOCK) ON EMP2.EMP_CODE=C.EMP_CODE2    
JOIN JOBS F (NOLOCK) ON F.JOB_CODE = G.JOB_CODE             
JOIN PRD_AGENCY_MST D (NOLOCK) ON A.AGENCY_CODE = D.AGENCY_CODE 
JOIN LMV01106  LMP (NOLOCK) ON D.AC_CODE =LMP.AC_CODE       
JOIN COMPANY COM ON COM.COMPANY_CODE ='01'


