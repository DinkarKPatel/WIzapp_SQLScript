IF NOT EXISTS(SELECT TOP 1 'U' FROM CONFIG WHERE CONFIG_OPTION ='UPDATE_KEYS_PO' AND VALUE =1)
BEGIN
 UPDATE A SET
 LASTKEYVAL=  
 CASE WHEN LEN(REPLACE(LASTKEYVAL,PREFIX,''))<=6 THEN
  PREFIX+CAST(REPLICATE ('0',6-LEN(REPLACE(LASTKEYVAL,PREFIX,''))) AS VARCHAR(100))
       +REPLACE(LASTKEYVAL,PREFIX,'')   
       ELSE
     PREFIX+CAST(RIGHT(LASTKEYVAL,6) AS VARCHAR(100)) END
 FROM KEYS  A 
 WHERE TABLENAME ='POM01106' AND FINYEAR ='01119'
 
  INSERT CONFIG	(  CONFIG_OPTION, VALUE, ROW_ID, LAST_UPDATE,  REMARKS )  
  SELECT 	   CONFIG_OPTION='UPDATE_KEYS_PO', VALUE=1, ROW_ID=NEWID(), 
  LAST_UPDATE=GETDATE (),  REMARKS =''
  
END
