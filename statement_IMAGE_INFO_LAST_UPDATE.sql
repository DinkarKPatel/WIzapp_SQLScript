DECLARE @cSTR NVARCHAR(MAX)
DECLARE @cDBNAME NVARCHAR(100)
SET @cDBNAME=DB_NAME()+'_IMAGE'
SET @cSTR=N'IF NOT EXISTS(SELECT COLUMN_NAME FROM '+@cDBNAME+'.INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME=''IMAGE_INFO'' AND COLUMN_NAME=''LAST_UPDATE'')
BEGIN
	ALTER TABLE '+@cDBNAME+'..IMAGE_INFO ADD LAST_UPDATE DATETIME
END'

PRINT @cSTR

EXEC SP_EXECUTESQL @cSTR

