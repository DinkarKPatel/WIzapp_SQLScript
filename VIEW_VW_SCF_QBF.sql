CREATE VIEW VW_SCF_QBF

AS  
SELECT A.MEMO_DT AS MST_MEMO_DT,
       A.MEMO_ID,
       A.MEMO_NO AS MST_MEMO_NO, 
       A.REMARKS AS MST_REMARKS,
       A.FIN_YEAR AS MST_FIN_YEAR,
       A.MODE,
       B.PRODUCT_CODE AS MST_PRODUCT_CODE,
       B.QUANTITY AS MST_QUANTITY, 
       B.ADJ_QUANTITY AS MST_ADJ_QUANTITY,
       B.AVG_QUANTITY AS MST_AVG_QUANTITY,
        
      ARTICLE.ARTICLE_NO ,ARTICLE.ARTICLE_NAME,PARA1.PARA1_NAME,PARA2.PARA2_NAME,    
      PARA3.PARA3_NAME,PARA4.PARA4_NAME,PARA5.PARA5_NAME,PARA6.PARA6_NAME,UOM.UOM_NAME,     
      SECTIOND.SUB_SECTION_NAME ,SECTIONM.SECTION_NAME,  
      SECTIOND.SUB_SECTION_CODE ,SECTIONM.SECTION_CODE,ARTICLE.CODING_SCHEME,
      CMP.COMPANY_CODE , 
      C.PRODUCT_CODE

              
FROM SCM01106 A (NOLOCK)       
JOIN SCC01106 B (NOLOCK) ON A.MEMO_ID= B.MEMO_ID     
JOIN SCF01106 C (NOLOCK) ON C.MEMO_ID= A.MEMO_ID     
JOIN SKU  (NOLOCK) ON C.PRODUCT_CODE = SKU.PRODUCT_CODE     
JOIN ARTICLE (NOLOCK) ON SKU.ARTICLE_CODE= ARTICLE.ARTICLE_CODE    
JOIN SECTIOND   (NOLOCK) ON ARTICLE.SUB_SECTION_CODE= SECTIOND.SUB_SECTION_CODE       
JOIN SECTIONM   (NOLOCK) ON SECTIOND.SECTION_CODE= SECTIONM.SECTION_CODE       
JOIN PARA1  (NOLOCK) ON C.PARA1_CODE= PARA1.PARA1_CODE       
JOIN PARA2  (NOLOCK) ON C.PARA2_CODE= PARA2.PARA2_CODE     
JOIN PARA3  (NOLOCK) ON C.PARA3_CODE= PARA3.PARA3_CODE       
JOIN PARA4  (NOLOCK) ON C.PARA4_CODE= PARA4.PARA4_CODE     
JOIN PARA5  (NOLOCK) ON C.PARA5_CODE= PARA5.PARA5_CODE       
JOIN PARA6  (NOLOCK) ON C.PARA6_CODE= PARA6.PARA6_CODE     
JOIN UOM  (NOLOCK) ON ARTICLE.UOM_CODE= UOM.UOM_CODE   
JOIN COMPANY CMP ON C .COMPANY_CODE=CMP.COMPANY_CODE  
JOIN AREA CA ON CMP.AREA_CODE=CA.AREA_CODE  
JOIN CITY CC ON CA.CITY_CODE=CC.CITY_CODE  
JOIN STATE CS ON CC.CITY_CODE=CS.STATE_CODE  
JOIN REGIONM CRG ON CS.REGION_CODE=CRG.REGION_CODE  
JOIN LOCATION LCT ON A .DEPT_ID=LCT.DEPT_ID  
JOIN AREA LA ON LCT.AREA_CODE=LA.AREA_CODE  
JOIN CITY LC ON LA.CITY_CODE=LC.CITY_CODE  
JOIN STATE LS ON LC.STATE_CODE=LS.STATE_CODE
