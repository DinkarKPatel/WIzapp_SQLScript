CREATE PROCEDURE [SPPPC_PRD_GETARTICLETYPE]    
    
AS    
BEGIN    
    
 SELECT DISTINCT ARTICLE_TYPE,      
  CASE WHEN ARTICLE_TYPE=2 THEN 'RAW MATERIAL'        
       WHEN ARTICLE_TYPE=3 THEN 'FABRIC'         
       WHEN ARTICLE_TYPE=4 THEN 'TRIM'  ELSE  'PACKING'  END ARTICLE_TYPE1 
   FROM ARTICLE WHERE ARTICLE_TYPE>1 ORDER BY ARTICLE_TYPE    
    
    
END
