CREATE  VIEW VW_APPROVALSALE_QBF_PRODUCTION   
    
AS    
 SELECT A.MEMO_NO AS 'MST_MEMO_NO',A.MEMO_ID,A.MEMO_DT AS 'MST_MEMO_DT',    
 A.SUBTOTAL AS 'MST_SUBTOTAL',CONVERT(NUMERIC(5,2),A.DISCOUNT_PERCENTAGE) AS  'MST_DISCOUNT_PERCENTAGE',    
 A.ATD_CHARGES AS 'MST_ATD_CHARGES',CONVERT(NUMERIC(14,2),A.DISCOUNT_AMOUNT) AS 'MST_DISCOUNT_AMOUNT',    
 A.NET_AMOUNT AS 'MST_NET_AMOUNT',A.ROUND_OFF AS 'MST_ROUND_OFF',A.FIN_YEAR AS 'MST_FIN_YEAR',    
 A.REMARKS AS 'MST_REMARKS',K.USER_CUSTOMER_CODE AS 'MST_USER_CUSTOMER_CODE',    
 B.PRODUCT_CODE,B.QUANTITY,B.MRP,B.NET,    
 E.ARTICLE_NO,F.PARA1_CODE,F.PARA1_NAME ,G.PARA2_CODE ,G.PARA2_NAME,H.SUB_SECTION_NAME, I.SECTION_NAME,    
    J.PARA3_NAME,L.AC_NAME AS 'MST_AC_NAME',M.PARA4_NAME,N.PARA5_NAME,O.PARA6_NAME     
     , UOM.UOM_NAME,    
       EMP1.EMP_NAME,EMP1.EMP_NAME AS EMP_NAME1 , EMP1.EMP_NAME AS EMP_NAME2,       
     EMP1.EMP_NAME AS MST_EMP_NAME,EMP1.EMP_NAME AS MST_EMP_NAME1 , EMP1.EMP_NAME AS MST_EMP_NAME2,    
     A.REF_NO AS MST_REF_NO,B.DISCOUNT_PERCENTAGE,B.DISCOUNT_AMOUNT,B.MANUAL_DISCOUNT,    
    ((B.QUANTITY*B.MRP)- B.DISCOUNT_AMOUNT) AS NET_AMOUNT     
     FROM PRD_APM01106 A     
    JOIN PRD_APD01106 B ON A.MEMO_ID = B.MEMO_ID     
    JOIN PRD_SKU D ON B.PRODUCT_CODE = D.PRODUCT_CODE    
    LEFT JOIN ARTICLE E ON D.ARTICLE_CODE = E.ARTICLE_CODE         
    LEFT JOIN PARA1 F ON D.PARA1_CODE = F.PARA1_CODE         
    LEFT JOIN PARA2 G ON D.PARA2_CODE = G.PARA2_CODE         
    LEFT JOIN SECTIOND H ON E.SUB_SECTION_CODE = H.SUB_SECTION_CODE         
    LEFT JOIN SECTIONM I ON H.SECTION_CODE = I.SECTION_CODE       
    LEFT JOIN PARA3 J ON D.PARA3_CODE = J.PARA3_CODE     
    LEFT JOIN CUSTDYM K ON A.CUSTOMER_CODE = K.CUSTOMER_CODE     
 LEFT JOIN LMV01106 L ON A.AC_CODE = L.AC_CODE    
    LEFT JOIN PARA4 M ON D.PARA4_CODE = M.PARA4_CODE    
    LEFT JOIN PARA5 N ON D.PARA5_CODE = N.PARA5_CODE    
    LEFT JOIN PARA6 O ON D.PARA6_CODE = O.PARA6_CODE    
    LEFT JOIN UOM UOM ON UOM.UOM_CODE =E.UOM_CODE      
   LEFT JOIN EMPLOYEE EMP1 ON EMP1.EMP_CODE =B.EMP_CODE      
   LEFT JOIN EMPLOYEE EMP2 ON EMP2.EMP_CODE =B.EMP_CODE1      
   LEFT JOIN EMPLOYEE EMP3 ON EMP3.EMP_CODE =B.EMP_CODE2
