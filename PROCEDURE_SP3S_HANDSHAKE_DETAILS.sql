CREATE PROCEDURE  SP3S_HANDSHAKE_DETAILS
--WITH ENCRYPTION
AS
BEGIN

SELECT B.DEPT_ALIAS,
SUBSTRING(CONVERT(VARCHAR(MAX), A.LAST_UPDATE, 120),1,LEN(CONVERT(VARCHAR(MAX), A.LAST_UPDATE, 120))-3) AS HANDSHAKE_DETAILS,
CAST(DATEDIFF(HOUR, A.LAST_UPDATE, GETDATE ()) AS VARCHAR(50))+'H '+
CAST(DATEDIFF(MINUTE, A.LAST_UPDATE, GETDATE ())-(DATEDIFF(HOUR, A.LAST_UPDATE, GETDATE ())*60) AS VARCHAR(50))+'M' AS PENDING
FROM 
MIRROR_HANDSHAKE_LOG A (NOLOCK)
JOIN LOCATION B (NOLOCK) ON A.DEPT_ID =B.DEPT_ID 
ORDER BY  A.LAST_UPDATE 


END
