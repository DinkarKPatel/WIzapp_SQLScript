CREATE PROCEDURE SPsis_SYNCH_UPLOADDATA_JWR_OPT
(
    @CSPID VARCHAR(50),
	@CDEPTID VARCHAR(5)/*Rohit 01-11-2024*/,
   @CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		208_2014_01_13 : THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
						 TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
						 
		NOTE : EMPLOYEE_HEAD WILL BE MERGED TO EMPLOYEE				 
	*/

DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@CFILTERCONDITION2 VARCHAR(500),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CMIRRORINGENABLED VARCHAR(5)
	   ,@CCURDEPTID VARCHAR(5),@CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE VARCHAR(200)
	   ,@CTEMPPAYMODETABLE VARCHAR(200),@CTEMPTABLE VARCHAR(100),@CDONOTRESETPOSTEDINAC VARCHAR(1)
	   ,@BADDMODE BIT,@CCUTOFFDATE VARCHAR(20),@CWHERECLAUSE VARCHAR(2000),@CMEMONOLEN VARCHAR(5),@NMEMONOLEN INT
	   ,@DMINMEMODT DATETIME,@DMAXMEMODT DATETIME,@CFINYEAR VARCHAR(5),@CMEMOPREFIX VARCHAR(10)
	   ,@CMINMEMONO VARCHAR(20),@CMAXMEMONO VARCHAR(20),@CSOURCEDB VARCHAR(200),@CMERGEDB VARCHAR(200)
	   ,@BMSTINSERTONLY BIT,@BSERIESMISMATCHFOUND BIT,@CMEMOID VARCHAR(40),@CPREVMEMONO VARCHAR(20)
	   ,@NPREVMEMONO NUMERIC(5,0),@NLENVALUE INT,@CPREVMEMONOSEARCH VARCHAR(20),@CMEMOIDSEARCH VARCHAR(40)
	   ,@CRETPRODUCTCODE VARCHAR(50),@CJOINSTR VARCHAR(MAX),@CTEMPSKUTABLE VARCHAR(500),@CTEMPDETAILTABLE1 VARCHAR(100)
	   ,@bCancelled BIT
	

BEGIN TRY
	
	
	SET @CSTEP=10

	SELECT @CSOURCEDB='',@CMERGEDB='',@BMSTINSERTONLY=1,@BSERIESMISMATCHFOUND=0
	
	SET @CSTEP=15
    	
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'

	SELECT @CTEMPMASTERTABLE=@CSOURCEDB+'JWR_JOBWORK_RECEIPT_MST_UPLOAD',
		   @CTEMPDETAILTABLE=@CSOURCEDB+'JWR_JOBWORK_RECEIPT_DET_UPLOAD'
		
	
LBLSTART:

	BEGIN TRANSACTION	
	
    SELECT @CMEMOID=''

	UPDATE A SET 
	             agency_code ='0000000000',
				 USER_CODE ='0000000',
				 BIN_ID='000',
				 receipt_id=@CDEPTID+SUBSTRING(receipt_id,len(@CDEPTID)+1 ,LEN(receipt_id))/*Rohit 01-11-2024*/ ,
				 receipt_no =@CDEPTID+SUBSTRING(receipt_no,LEN(@CDEPTID)+1,LEN(receipt_no))/*Rohit 01-11-2024*/,
				 location_Code =@CDEPTID
	FROM JWR_JOBWORK_RECEIPT_MST_UPLOAD A (NOLOCK)
	WHERE SP_ID =@CSPID

	UPDATE A SET BIN_ID ='000',
	          receipt_id=@CDEPTID++SUBSTRING(receipt_id,LEN(@CDEPTID)+1,LEN(receipt_id)) /*Rohit 01-11-2024*/
	FROM JWR_JOBWORK_RECEIPT_det_UPLOAD A (NOLOCK)
	WHERE SP_ID =@CSPID

	
	 UPDATE A SET PRODUCT_CODE=LEFT(A.PRODUCT_CODE, ISNULL(NULLIF(CHARINDEX ('@',A.PRODUCT_CODE)-1,-1),LEN(A.PRODUCT_CODE )))  
	 FROM JWR_JOBWORK_RECEIPT_det_UPLOAD A (NOLOCK)	WHERE SP_ID=@CSPID
	 AND CHARINDEX('@',PRODUCT_CODE)<>0


	
    SELECT TOP 1 @CMEMOID = B.RECEIPT_ID 
    FROM JWR_JOBWORK_RECEIPT_MST_UPLOAD B (NOLOCK)
    WHERE  SP_ID=@CSPID
        
    
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		
    SET @CFILTERCONDITION = 'B.RECEIPT_ID='''+@CMEMOID+''' AND B.SP_ID='''+LTRIM(RTRIM((@CSPID)))+''''
	
	
	

	SET @CSTEP=110
	SET @CMEMOIDSEARCH=''
	SELECT TOP 1 @CMEMOIDSEARCH=A.RECEIPT_ID FROM JOBWORK_RECEIPT_MST A (NOLOCK) WHERE A.RECEIPT_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1

IF @BADDMODE=1 AND NOT EXISTS (SELECT TOP 1 'U' FROM JWR_JOBWORK_RECEIPT_det_UPLOAD WHERE SP_ID =@CSPID)
	    GOTO EXIT_PROC
    IF @BADDMODE=0 AND NOT EXISTS (SELECT TOP 1 'U' FROM JWR_JOBWORK_RECEIPT_det_UPLOAD WHERE SP_ID =@CSPID)
		 SET @BCANCELLED=1

	
LBLMERGE:
	---DELETING EXISTING RECORD IF BILL COMES AGAIN FOR MERGING
	
	--SELECT @CTEMPMASTERTABLE,@CFILTERCONDITION
	IF @BADDMODE=0
		PRINT 'ADDMODE:N'
	ELSE
		PRINT 'ADDMODE:Y'	
	
	IF @BADDMODE=0
	BEGIN	

	     
		 INSERT SAVETRAN_BARCODE_NETQTY	( BIN_ID, DEPT_ID, PRODUCT_CODE, sp_id, XN_QTY ) 
		 SELECT 	'000'  BIN_ID,/*LEFT(A.receipt_id,2)*//*Rohit 01-11-2024*/@CDEPTID DEPT_ID, PRODUCT_CODE,@CSPID SP_ID,-1*A.quantity AS XN_QTY 
		 FROM jobwork_receipt_det A (NOLOCK)
		 WHERE A.receipt_id =@CMEMOID
	      
		  IF @bCancelled=1
		  BEGIN
		     UPDATE jobwork_receipt_MST WITH (ROWLOCK) SET cancelled=1 WHERE receipt_id=@cMemoid
			 GOTO lblupdatepmt

		  END

		SET @CSTEP=115
		SET @DTSQL=N'DELETE A FROM JOBWORK_RECEIPT_DET A JOIN '+@CTEMPMASTERTABLE+' B (NOLOCK) ON A.RECEIPT_ID=B.RECEIPT_ID
					 LEFT OUTER JOIN 
					 (SELECT ROW_ID FROM '+@CTEMPDETAILTABLE+' B WHERE '+@CFILTERCONDITION+') C ON A.ROW_ID = C.ROW_ID 
					 WHERE C.ROW_ID IS NULL AND '+@CFILTERCONDITION

		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
	
	END
	
	---UPDATING TRANSACTION TABLES
	SET @CSTEP=250
	SET @CTABLENAME='JOBWORK_RECEIPT_MST'
	SET @CTMP_TABLENAME='JWR_JOBWORK_RECEIPT_MST_UPLOAD'
	SET @CKEYFIELD='RECEIPT_ID'
	
	SET @DTSQL = N'UPDATE '+@CSOURCEDB+@CTMP_TABLENAME+'  WITH (ROWLOCK) SET RECEIPT_DT = CONVERT(DATETIME,CONVERT(VARCHAR(10),RECEIPT_DT,120))'
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL

	
							  
	SET @CTABLENAME='JOBWORK_RECEIPT_MST'
	SET @CTMP_TABLENAME='JWR_JOBWORK_RECEIPT_MST_UPLOAD'
	SET @CKEYFIELD='RECEIPT_ID'

	
	SET @CSTEP=270
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 
	
	  SELECT a.product_code as new_product_code into #tmpbarcode FROM JWR_JOBWORK_RECEIPT_DET_UPLOAD A (NOLOCK)
		LEFT JOIN SKU B (nolock) ON A.PRODUCT_CODE =B.PRODUCT_CODE 
		WHERE A.SP_ID=@CSPID AND B.PRODUCT_CODE IS NULL
		group by a.product_code

		IF EXISTS (SELECT TOP 1 'U' FROM #tmpbarcode)
		BEGIN
			SET @CSTEP=84.2
			EXEC SP_CHKXNSAVELOG 'JWR',@CSTEP,0,@CSPID,'',1

			 EXEC SPSIS_INSERTSKUBARCODE @CSPID, 'JWR',@CERRMSG OUTPUT

			 IF ISNULL(@CERRMSG,'')<>''
			    GOTO EXIT_PROC

          end


	SET @CTABLENAME='JOBWORK_RECEIPT_DET'
	SET @CTMP_TABLENAME='JWR_JOBWORK_RECEIPT_DET_UPLOAD'
	SET @CKEYFIELD='ROW_ID'
	 
	SET @CSTEP=280
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 

	  

	     INSERT SAVETRAN_BARCODE_NETQTY	( BIN_ID, DEPT_ID, PRODUCT_CODE, sp_id, XN_QTY ) 
		 SELECT 	'000'  BIN_ID,/*LEFT(A.RECEIPT_ID,2)*//*Rohit 01-11-2024*/@CDEPTID DEPT_ID, PRODUCT_CODE,@CSPID SP_ID,A.quantity AS XN_QTY 
		 FROM jobwork_receipt_det A (NOLOCK)
		 WHERE A.RECEIPT_ID =@CMEMOID

	lblupdatepmt:
	EXEC SPSIS_UPDATEPMT @CSPID
    
	    		
		DELETE A FROM JWR_JOBWORK_RECEIPT_MST_UPLOAD A WHERE A.SP_ID=@CSPID
		DELETE A FROM JWR_JOBWORK_RECEIPT_DET_UPLOAD A WHERE A.SP_ID=@CSPID
	
		
	

END TRY

BEGIN CATCH
	SET @CERRMSG='P:SPsis_SYNCH_UPLOADDATA_JWR_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO EXIT_PROC
END CATCH

EXIT_PROC:

	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRMSG,'')='' AND @BSERIESMISMATCHFOUND<>1
			COMMIT
		ELSE
			ROLLBACK
    END
	
END	
---END OF PROCEDURE - SP_SYNCH_MIRRORDATA_JWR_SINGLE
