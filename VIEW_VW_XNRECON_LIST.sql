CREATE VIEW VW_XNRECON_LIST

AS 
SELECT A.RECON_ID AS MEMO_ID,
RECON_NO AS MEMO_NO,
RECON_DT AS MEMO_DATE,
FIN_YEAR,
CASE WHEN RECONCILED = 0 THEN 'NOT COMPLETE' ELSE 'COMPLETED' END AS RECONCILED,
XN_TYPE ,
B.SOURCE_MEMO_NO
FROM XNRECONM A (NOLOCK)
JOIN XNRECONMEMO B (NOLOCK) ON B.RECON_ID=A.RECON_ID