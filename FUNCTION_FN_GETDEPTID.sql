CREATE FUNCTION FN_GETDEPTID(@CDEPTID CHAR(2))
	RETURNS VARCHAR(1000)	
--	WITH ENCRYPTION
AS
BEGIN
	--*** PARAMETERS:
	-- @CDEPTID : LOCATION ID 
	DECLARE @CRETVAL CHAR(1)
	IF NOT EXISTS (SELECT DEPT_ID FROM LOCATION WHERE DEPT_ID=@CDEPTID) 
		SET @CRETVAL=''
	ELSE
		SET @CRETVAL='1'

	RETURN @CRETVAL
END
