CREATE PROCEDURE SP3S_WIZCLIPBILLUPLOAD_STATUS
(
  @cFromDt varchar(10),
  @cToDt varchar(10),
  @cWhere Varchar(100)
)
AS BEGIN


SELECT A.CM_NO,A.CM_DT ,B.WIZCLIP_XN_ID,B.ERR_MSG,B.LAST_UPDATE,A.location_Code  AS DEPT_ID 
FROM CMM01106 A 
LEFT OUTER JOIN  CAMPAIGN_CMM B ON A.CM_ID= B.CM_ID  
WHERE A.CM_DT BETWEEN @CFROMDT AND  @CTODT
ORDER BY  LEFT (A.CM_ID,2) ,CM_DT,CM_NO
End


