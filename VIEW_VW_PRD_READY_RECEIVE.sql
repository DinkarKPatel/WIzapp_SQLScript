CREATE VIEW VW_PRD_READY_RECEIVE      

AS      
SELECT 
T1.MEMO_ID
,T1.MEMO_NO
,T1.MEMO_DT
,ISNULL(T4.AC_NAME,'') AS AC_NAME,  
T4.ADDRESS0+' '+T4.ADDRESS1+' '+T4.ADDRESS2 +' '+ T4.AREA_NAME AS [ADDRESS]
,T4.CITY
,T4.[STATE]
,T4.PINCODE,    
ISNULL(T6.DEPARTMENT_NAME,'') AS DEPARTMENT_NAME ,  
(CASE WHEN T1.CANCELLED =1 THEN 'CANCELLED' ELSE '' END) AS  CANCELLED,    
T1.REMARKS,T1.SUBTOTAL,T1.OTHER_CHARGE,  T1.NET_AMOUNT ,T1.FIN_YEAR 
 FROM PRD_READY_RECEIVE_MST T1         
 JOIN LMV01106 T4 ON T4.AC_CODE = T1.AC_CODE        
 LEFT OUTER JOIN PRD_DEPARTMENT_MST T6 ON T6.DEPARTMENT_ID = T1.DEPARTMENT_ID
