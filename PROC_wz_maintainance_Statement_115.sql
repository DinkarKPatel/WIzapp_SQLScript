


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CNM01106_BILLEDFROMDEPTID' AND VALUE=1)
BEGIN
	PRINT 'UPDATING BILLED FROM DEPT ID COLUMN IN CNM01106'
	
	UPDATE CNM01106 SET BILLED_FROM_DEPT_ID=LEFT(CN_ID,2) WHERE BILLED_FROM_DEPT_ID=PARTY_DEPT_ID
	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CNM01106_BILLEDFROMDEPTID')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_CNM01106_BILLEDFROMDEPTID',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_CNM01106_BILLEDFROMDEPTID'	
END	
