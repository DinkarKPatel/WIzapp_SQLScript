CREATE PROCEDURE SP_PPC_ORDER_SECOND_ISSUE
(
  @CJOB_CODE VARCHAR(10)=''
)
AS
BEGIN 
     IF OBJECT_ID('TEMPDB..#TMPORDER','U') IS NOT NULL
        DROP TABLE #TMPORDER
        
     SELECT  B.BILL_NO,B.ORDER_ID ,JOBS.JOB_CODE,C.JOB_ORDER  
    -- INTO #TMPORDER
	 FROM PPC_BUYER_ORDER_DET A
	 JOIN PPC_BUYER_ORDER_MST B ON A.ORDER_ID=B.ORDER_ID
	 JOIN PPC_BO_ART_JOBS C ON A.ROW_ID=C.REF_ROW_ID
	 JOIN JOBS ON JOBS.JOB_CODE=C.JOB_CODE
	 JOIN
	 (   SELECT C.BO_DET_ROW_ID 
	     FROM PPC_AGENCY_REC_FG_DET A
	     JOIN   PPC_AGENCY_REC_FG_MST MST ON MST.MEMO_ID =A.MEMO_ID
         JOIN PPC_FG_SKU B ON A.PRODUCT_CODE =B.PRODUCT_CODE 
         JOIN PPC_FGBCG_DET C ON C.ROW_ID =B.PPC_FGBCG_DET_ROW_ID
         JOIN PPC_FGBCG_MST D ON C.MEMO_ID =D.MEMO_ID 
       --  WHERE 
        -- MST.AC_CODE =@CAC_CODE AND
       --   MST.JOB_CODE =@CJOB_CODE
         GROUP BY C.BO_DET_ROW_ID 
	 ) REC ON A.ROW_ID =REC.BO_DET_ROW_ID 
	 WHERE B.CANCELLED=0
	 AND C.JOB_CODE=@CJOB_CODE
	 GROUP BY B.BILL_NO,B.ORDER_ID ,JOBS.JOB_CODE ,C.JOB_ORDER 
	 ORDER BY B.ORDER_ID ,C.JOB_ORDER
	 
END
