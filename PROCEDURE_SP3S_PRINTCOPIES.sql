CREATE PROC SP3S_PRINTCOPIES
AS
BEGIN

SELECT A.TRAN_TYPE ,ISNULL(B.NO_OF_COPIES,1) AS  NO_OF_COPIES
FROM 
(
SELECT  'CASH MEMO' AS TRAN_TYPE,1 AS NO_OF_COPIES
UNION ALL
SELECT  'CREDIT NOTE' AS TRAN_TYPE,1 AS NO_OF_COPIES
UNION ALL
SELECT  'CASH REFUND' AS TRAN_TYPE,1 AS NO_OF_COPIES
UNION ALL
SELECT  'CC' AS TRAN_TYPE,1 AS NO_OF_COPIES
)  A
LEFT OUTER  JOIN PRINTCOPIES B ON A.TRAN_TYPE = B.TRAN_TYPE 
END
