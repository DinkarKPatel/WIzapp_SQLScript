CREATE PROCEDURE SP_PRDUPC_BARCODE
(
 @CORDER_ID VARCHAR(100)=''
)
AS
BEGIN

    SELECT A.PRODUCT_CODE, A.ORDER_ID,MST.ORDER_NO AS ORDER_NO,MST.ORDER_DT ,
           A.WO_ID ,WO.MEMO_NO AS WO_NO,WO.MEMO_DT AS WO_DT,
           A.PARA1_CODE,P1.PARA1_NAME  ,A.PARA2_CODE, P2.PARA2_NAME , 
           A.QUANTITY_IN_STOCK,A.JOB_CODE,JOBS.JOB_NAME 
    FROM PRD_UPCPMT A
    JOIN PARA1 P1 ON P1.PARA1_CODE =A.PARA1_CODE 
    JOIN PARA2 P2 ON P2.PARA2_CODE =A.PARA2_CODE 
    JOIN PRD_WO_MST WO ON WO.MEMO_ID =A.WO_ID
    LEFT JOIN JOBS ON JOBS.JOB_CODE =A.JOB_CODE 
    LEFT JOIN BUYER_ORDER_MST MST ON MST.ORDER_ID=A.ORDER_ID
    WHERE A.WO_ID =@CORDER_ID 
    
  

END
