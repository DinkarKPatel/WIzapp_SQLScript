CREATE FUNCTION FN_GETLMTERMS(@CACCODE VARCHAR(50))
RETURNS VARCHAR(MAX)

AS
BEGIN

DECLARE @LIST VARCHAR(MAX)
SELECT @LIST = COALESCE(@LIST + ';', '') + CAST(B.TERMS_NAME  AS VARCHAR(1000))
FROM LM_TERMS A
JOIN LEDGER_TERMS B  ON A.TERMS_CODE= B.TERMS_CODE
WHERE A.AC_CODE= @CACCODE

RETURN @LIST
END
