CREATE PROCEDURE SP3S_GET_CUSTDYM_FKEYDATA
@CSOURCEDB VARCHAR(200),
@CSOURCETABLE VARCHAR(200),
@CSOURCEKEYFIELD VARCHAR(200),
@CXNTYPE VARCHAR(10),
@CTABLESUFFIX VARCHAR(40),
@CLOCID VARCHAR(5),
@CERRORMSG VARCHAR(MAX) OUTPUT
--WITH ENCRYPTION
AS
BEGIN
	BEGIN TRY
		DECLARE @CCMD NVARCHAR(MAX),@CTEMPTABLE VARCHAR(1000),@CTEMPTABLE1 VARCHAR(1000),@CTEMPTABLE2 VARCHAR(1000),
				@CTEMPTABLE3 VARCHAR(1000),@CTEMPTABLE4 VARCHAR(1000),@CTEMPTABLE5 VARCHAR(1000),
				@CKEYFIELDVAL VARCHAR(40),@CDESTDB VARCHAR(200),@CFILTERCONDITION VARCHAR(MAX),@NSTEP INT
		
		SET @CDESTDB='WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.'
		
		SET @NSTEP=10		
		IF @CSOURCETABLE<>'#TMPMISSINGCUSTDATA'
		BEGIN			
			IF OBJECT_ID('TEMPDB..#TMPMISSINGCUSTDATA','U') IS NOT NULL
				DROP TABLE #TMPMISSINGCUSTDATA
			
			SELECT CUSTOMER_CODE INTO #TMPMISSINGCUSTDATA FROM CUSTDYM WHERE 1=2
		END
		
		IF OBJECT_ID('TEMPDB..#TMPMISSINGAREADATA','U') IS NOT NULL
			DROP TABLE #TMPMISSINGAREADATA
		
		SET @NSTEP=20
		SELECT AREA_CODE INTO #TMPMISSINGAREADATA FROM AREA WHERE 1=2
										
		SET @CTEMPTABLE=(CASE WHEN LEFT(@CSOURCETABLE,1)='#' THEN @CSOURCETABLE ELSE @CSOURCEDB+'TMP_'+@CXNTYPE+'_'+@CSOURCETABLE+'_'+@CTABLESUFFIX END)
		SET @CTEMPTABLE1=@CSOURCEDB+'TMP_'+@CXNTYPE+'_AREA_'+@CTABLESUFFIX
		SET @CTEMPTABLE2=@CSOURCEDB+'TMP_'+@CXNTYPE+'_CUSTDYM_'+@CTABLESUFFIX
		
		IF @CSOURCETABLE<>'#TMPMISSINGCUSTDATA'
		BEGIN			
			SET @NSTEP=30
			IF OBJECT_ID(@CTEMPTABLE2,'U') IS NOT NULL
				SET @CCMD=N'SELECT DISTINCT A.'+@CSOURCEKEYFIELD+' FROM '+@CTEMPTABLE+'  A
							LEFT OUTER JOIN '+@CTEMPTABLE2+'  B ON B.CUSTOMER_CODE=A.'+@CSOURCEKEYFIELD+'
							LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.CUSTDYM C ON C.CUSTOMER_CODE=A.'+@CSOURCEKEYFIELD+'
							WHERE B.CUSTOMER_CODE IS NULL AND C.CUSTOMER_CODE IS NULL AND A.'+@CSOURCEKEYFIELD+' IS NOT NULL'
			ELSE
				SET @CCMD=N'SELECT DISTINCT A.'+@CSOURCEKEYFIELD+' FROM '+@CTEMPTABLE+'  A
							LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.LM01106 B ON B.AC_CODE=A.'+@CSOURCEKEYFIELD+'
							WHERE B.AC_CODE IS NULL AND A.'+@CSOURCEKEYFIELD+' IS NOT NULL
							UNION
							SELECT DISTINCT A.'+@CSOURCEKEYFIELD+' FROM '+@CTEMPTABLE+'  A
							JOIN CUSTDYM B ON  B.CUSTOMER_CODE=A.'+@CSOURCEKEYFIELD+'
							LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.CUSTDYM C ON C.CUSTOMER_CODE=A.'+@CSOURCEKEYFIELD+'
							WHERE C.CUSTOMER_CODE IS NULL AND A.'+@CSOURCEKEYFIELD+' IS NOT NULL'

				
			INSERT #TMPMISSINGCUSTDATA
			EXEC SP_EXECUTESQL @CCMD
		END
		
		SET @NSTEP=40
		IF OBJECT_ID(@CTEMPTABLE1,'U') IS NOT NULL AND OBJECT_ID(@CTEMPTABLE2,'U') IS NOT NULL
			SET @CCMD=N'SELECT DISTINCT B.AREA_CODE FROM '+@CTEMPTABLE+'  A
						JOIN '+@CTEMPTABLE2+'  B ON B.CUSTOMER_CODE=A.CUSTOMER_CODE
						LEFT OUTER JOIN '+@CTEMPTABLE1+'  C ON C.AREA_CODE=B.AREA_CODE
						LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.AREA D ON D.AREA_CODE=B.AREA_CODE
						WHERE C.AREA_CODE IS NULL AND D.AREA_CODE IS NULL
						UNION
						SELECT DISTINCT B.AREA_CODE FROM '+@CTEMPTABLE+'  A
						JOIN CUSTDYM B ON B.CUSTOMER_CODE=A.'+@CSOURCEKEYFIELD+'
						LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.AREA C ON C.AREA_CODE=B.AREA_CODE
						WHERE C.AREA_CODE IS NULL'
		ELSE	
		IF OBJECT_ID(@CTEMPTABLE2,'U') IS NOT NULL
			SET @CCMD=N'SELECT DISTINCT B.AREA_CODE FROM '+@CTEMPTABLE+'  A
						JOIN '+@CTEMPTABLE2+'  B ON B.CUSTOMER_CODE=A.'+@CSOURCEKEYFIELD+'
						LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.AREA C ON C.AREA_CODE=B.AREA_CODE
						WHERE C.AREA_CODE IS NULL
						UNION
						SELECT DISTINCT B.AREA_CODE FROM '+@CTEMPTABLE+'  A
						JOIN CUSTDYM B ON B.CUSTOMER_CODE=A.'+@CSOURCEKEYFIELD+'
						LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.AREA C ON C.AREA_CODE=B.AREA_CODE
						WHERE C.AREA_CODE IS NULL'
		ELSE
		IF OBJECT_ID(@CTEMPTABLE2,'U') IS NULL
			SET @CCMD=N'SELECT DISTINCT B.AREA_CODE FROM '+@CTEMPTABLE+'  A
						JOIN CUSTDYM B ON B.CUSTOMER_CODE=A.'+@CSOURCEKEYFIELD+'
						LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.AREA C ON C.AREA_CODE=B.AREA_CODE
						WHERE C.AREA_CODE IS NULL'
		
		PRINT @CCMD	
		INSERT #TMPMISSINGAREADATA
		EXEC SP_EXECUTESQL @CCMD
		


		IF EXISTS (SELECT TOP 1 AREA_CODE FROM #TMPMISSINGAREADATA)
		BEGIN
			SET @NSTEP=50
			EXEC SP3S_GET_AREA_FKEYDATA
			@CSOURCEDB='',
			@CSOURCETABLE='#TMPMISSINGAREADATA',
			@CSOURCEKEYFIELD='AREA_CODE',
			@CXNTYPE=@CXNTYPE,
			@CTABLESUFFIX=@CTABLESUFFIX,
			@CLOCID=@CLOCID,
			@CERRORMSG=@CERRORMSG OUTPUT
		END	

		IF EXISTS (SELECT TOP 1 CUSTOMER_CODE FROM #TMPMISSINGCUSTDATA)
		BEGIN
			SET @NSTEP=60
			SET @CFILTERCONDITION= 'B.CUSTOMER_CODE IN (SELECT CUSTOMER_CODE FROM #TMPMISSINGCUSTDATA)'
			
			EXEC UPDATEMASTERXN_MIRROR
			@CSOURCEDB='',
			@CSOURCETABLE='CUSTDYM',
			@CDESTDB=@CDESTDB,
			@CDESTTABLE='CUSTDYM',
			@CKEYFIELD1='CUSTOMER_CODE',
			@LINSERTONLY=0,
			@CFILTERCONDITION=@CFILTERCONDITION,
			
			
			@LUPDATEONLY=0,
			@BALWAYSUPDATE=1
		END
							
		GOTO END_PROC									
	END TRY
	
	BEGIN CATCH
		SELECT @CERRORMSG='PROCEDURE SP3S_GET_CUSTDYM_FKEYDATA : '+ISNULL(ERROR_PROCEDURE(),'NULL P')+'STEP :'+STR(ISNULL(@NSTEP,0))+ ' LINE NO. :'+
		ISNULL(LTRIM(RTRIM(STR(ERROR_LINE()))),'NULL LINE')+'MSG :'+ISNULL(ERROR_MESSAGE(),'NULL MSG')
		
		GOTO END_PROC
	END CATCH
	
END_PROC:
		
END
----'END OF PROCEDURE SP3S_GET_CUSTDYM_FKEYDATA'
