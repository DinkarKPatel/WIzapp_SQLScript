


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_MIRRORLOG_ENTRIES' AND VALUE=1)
BEGIN
	PRINT 'UPDATING/INSERTING MIRRORLOG ENTRIES PRIOR TO 01-04-2016'
	
	EXEC SP_INSERT_MIRRORLOG '2016-04-01'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_MIRRORLOG_ENTRIES')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_MIRRORLOG_ENTRIES',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_MIRRORLOG_ENTRIES'
	
END		
