CREATE VIEW VW_WL_ARTICLE
--WITH ENCRYPTION
AS
SELECT	D.SECTION_NAME, C.SUB_SECTION_NAME, B.UOM_NAME, B.UOM_TYPE, A.* 
FROM ARTICLE A
JOIN UOM B ON A.UOM_CODE = B.UOM_CODE 
JOIN SECTIOND C ON A.SUB_SECTION_CODE = C.SUB_SECTION_CODE 
JOIN SECTIONM D ON C.SECTION_CODE = D.SECTION_CODE 
--******************************** END OF PROCEDURE VW_WL_ARTICLE
