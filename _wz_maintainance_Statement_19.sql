

--**** UPDATING NEWLY INSERTED GROSS_PURCHASE_PRICE COLUMN IN RMD01106 WITH QUANTITY COLUMN
IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_RMD01106_GROSSPP' AND VALUE=1)
BEGIN
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_RMD01106_GROSSPP')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_RMD01106_GROSSPP',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_RMD01106_GROSSPP'
	
	UPDATE RMD01106 SET GROSS_PURCHASE_PRICE=PURCHASE_PRICE WHERE GROSS_PURCHASE_PRICE=0
	
	UPDATE RMD01106 SET PURCHASE_PRICE=GROSS_PURCHASE_PRICE-(GROSS_PURCHASE_PRICE*DISCOUNT_PERCENTAGE/100)

END		
