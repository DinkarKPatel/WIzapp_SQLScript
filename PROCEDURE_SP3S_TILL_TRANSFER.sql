CREATE PROCEDURE SP3S_TILL_TRANSFER
(
	 @NQUERYID NUMERIC(2)
	,@CMEMOID VARCHAR(100)=''
	,@CUSERCODE VARCHAR(10)=''

)
AS 
BEGIN
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	     

IF @NQUERYID=1
	GOTO LBL_GETMASTER

ELSE 
	GOTO END_PROC
	
LBL_GETMASTER:
	
SELECT 	
A.*,B.USERNAME,LM.AC_NAME,
(CASE WHEN ISNULL(C.VALUE,'')='' OR ISNULL(A.MEMO_ID,'')='' THEN '' 
ELSE C.VALUE+'TILLTRANSFER\'+A.MEMO_ID+'.JPG' END) AS IMAGE_PATH 
FROM TILL_BANK_TRANSFER A
JOIN USERS B ON A.USER_CODE=B.USER_CODE	
JOIN LMV01106 LM ON A.AC_CODE=LM.AC_CODE	
LEFT JOIN CONFIG C ON CONFIG_OPTION='ART_PICT_PATH' AND ISNULL(VALUE,'')<>''
WHERE A.MEMO_ID=@CMEMOID
	
GOTO END_PROC


END_PROC:	
END
