create PROCEDURE UPDATEPMT_JWI  
  @CXNTYPE VARCHAR(10),  
  @CXNNO VARCHAR(40),  
  @CXNID VARCHAR(40),  
  @NREVERTFLAG BIT = 0,  
  @NALLOWNEGSTOCK BIT = 0,  
  @NCHKDELBARCODES BIT = 0,  
  @NUPDATEMODE INT=0,   
  @CCMD NVARCHAR(4000) OUTPUT  
  
  --*** PARAMETERS :  
  --*** @CXNTYPE -		TRANSACTION TYPE (MODULE SPECIFIC)  
  --*** @CXNNO -		TRANSACTION NO ( MEMO NO OF MASTER TABLE )  
  --*** @CXNID -		TRANSACTION ID ( MEMO ID OF MASTER TABLE )  
  --*** @NREVERTFLAG -	A FLAG TO INDICATE WHETHER THIS PROCEDURE IS CALLED TO REVERT STOCK  
  --*** @NALLOWNEGSTOCK - FLAG TO INDICATE WHETHER OR NOT ALLOW NEGATIVE STOCK  
  --*** @NRETVAL - OUTPUT PARAMETER RETURNED BY THIS PROCEDURE (BIT 1-SUCCESS, 0-UNSUCCESS)  
--WITH ENCRYPTION
AS  
BEGIN  
	DECLARE @NOUTFLAG INT, @NRETVAL BIT,@CXNTABLE VARCHAR(50),@CEXPR NVARCHAR(500),@CXNIDPARA VARCHAR(50),  
	@BCANCELLED BIT  

	SET @NRETVAL = 0  
	SET @CCMD = ''  

	IF @NREVERTFLAG = 1  
		SET @NOUTFLAG = -1  
	ELSE  
		SET @NOUTFLAG = 1  

   
	PRINT 'UPDATEPMT - @NOUTFLAG ' + CONVERT(VARCHAR, @NOUTFLAG )  


    DECLARE @NISSUEMODE INT,@nwip int
	SELECT @NISSUEMODE=ISSUE_MODE FROM JOBWORK_ISSUE_MST (NOLOCK) WHERE ISSUE_ID=@CXNID
	

	 INSERT PMT01106 (PRODUCT_CODE,  QUANTITY_IN_STOCK, DEPT_ID,BIN_ID, LAST_UPDATE )  
     SELECT B.PRODUCT_CODE,0 AS QUANTITY_IN_STOCK,C.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/ DEPT_ID,B.BIN_ID,GETDATE() AS LAST_UPDATE  
     FROM JOBWORK_ISSUE_DET B  
     JOIN JOBWORK_ISSUE_MST C ON C.ISSUE_ID=B.ISSUE_ID  
     JOIN SKU D ON B.PRODUCT_CODE = D.PRODUCT_CODE  
     JOIN ARTICLE E ON D.ARTICLE_CODE = E.ARTICLE_CODE  
     LEFT OUTER JOIN PMT01106 PMT ON PMT.PRODUCT_CODE = B.PRODUCT_CODE AND PMT.DEPT_ID = C.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/  AND PMT.BIN_ID=B.BIN_ID  
     WHERE B.ISSUE_ID = @CXNID AND PMT.PRODUCT_CODE IS NULL

	IF ISNULL(@NISSUEMODE,0)=1 
	BEGIN

	    
	
			UPDATE A SET QUANTITY_IN_STOCK = QUANTITY_IN_STOCK - ( @NOUTFLAG * B.QUANTITY )
			FROM  JOBWORK_PMT A (NOLOCK)
			JOIN 
			( 
				SELECT B.PRODUCT_CODE,A.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/ DEPT_ID,B.BIN_ID,JOB_CODE, SUM(QUANTITY) AS QUANTITY   
				FROM JOBWORK_ISSUE_DET B (NOLOCK)   
				JOIN JOBWORK_ISSUE_MST A (NOLOCK) ON A.ISSUE_ID=B.ISSUE_ID  
				WHERE  ISNULL(A.ISSUE_MODE  ,0)=1 AND ISNULL(A.ISSUE_TYPE   ,0)=1 
				AND B.ISSUE_ID = @CXNID AND A.CANCELLED=0  
				GROUP BY B.PRODUCT_CODE,A.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/   ,B.BIN_ID,JOB_CODE
			) B  ON A.PRODUCT_CODE = B.PRODUCT_CODE   
			AND A.DEPT_ID = B.DEPT_ID  
			AND A.BIN_ID = B.BIN_ID 

			
		SELECT @BCANCELLED=CANCELLED FROM JOBWORK_ISSUE_MST WHERE ISSUE_ID=@CXNID  
		SET @NRETVAL = 1  --*** SUCCESS  


	IF @NALLOWNEGSTOCK = 0 OR @BCANCELLED=1  
	BEGIN  
		IF EXISTS (  
	
				SELECT A.PRODUCT_CODE,SUM(A.QUANTITY_IN_STOCK) 
				FROM JOBWORK_PMT A 
				JOIN JOBWORK_ISSUE_DET B  ON B.PRODUCT_CODE=A.PRODUCT_CODE
				JOIN JOBWORK_ISSUE_MST C ON C.ISSUE_ID=B.ISSUE_ID  
				JOIN SKU D ON B.PRODUCT_CODE = D.PRODUCT_CODE  
				JOIN ARTICLE E ON D.ARTICLE_CODE = E.ARTICLE_CODE  
				WHERE   B.ISSUE_ID = @CXNID AND E.STOCK_NA=0 AND ISNULL(C.ISSUE_MODE  ,0)=1  
				AND B.PRODUCT_CODE=A.PRODUCT_CODE AND C.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/ =A.DEPT_ID   
				AND B.BIN_ID=A.BIN_ID 
				GROUP BY A.PRODUCT_CODE
				HAVING SUM(A.QUANTITY_IN_STOCK)<0
			)
		BEGIN  
		SET @NRETVAL = 0  --*** UNSUCCESS  
		SET @CCMD = N'  
			SELECT A.PRODUCT_CODE,SUM(A.QUANTITY_IN_STOCK) 
			FROM JOBWORK_PMT A 
			JOIN JOBWORK_ISSUE_DET B  ON B.PRODUCT_CODE=A.PRODUCT_CODE
			JOIN JOBWORK_ISSUE_MST C ON C.ISSUE_ID=B.ISSUE_ID  
			JOIN SKU D ON B.PRODUCT_CODE = D.PRODUCT_CODE  
			JOIN ARTICLE E ON D.ARTICLE_CODE = E.ARTICLE_CODE  
			WHERE   B.ISSUE_ID = '''+@CXNID+''' AND E.STOCK_NA=0  AND ISNULL(C.ISSUE_MODE  ,0)=1
			AND B.PRODUCT_CODE=A.PRODUCT_CODE AND C.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/ =A.DEPT_ID   
			AND B.BIN_ID=A.BIN_ID 
			GROUP BY A.PRODUCT_CODE
			HAVING SUM(A.QUANTITY_IN_STOCK)<0
			'  
		PRINT @CCMD    
		END  
	END   

		 GOTO END_PROC
	END



  
	--*** UPDATING THE QUANTITY IN STOCK FROM PMT FOR THE GIVEN MEMO  
	UPDATE A  SET QUANTITY_IN_STOCK = QUANTITY_IN_STOCK - ( @NOUTFLAG * B.QUANTITY )  
	FROM  PMT01106 A
	JOIN 
	( 
		SELECT B.PRODUCT_CODE, A.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/ DEPT_ID,B.BIN_ID, SUM(QUANTITY) AS QUANTITY   
		FROM JOBWORK_ISSUE_DET B   
		JOIN JOBWORK_ISSUE_MST A ON A.ISSUE_ID=B.ISSUE_ID  
		WHERE A.WIP=0 AND B.ISSUE_ID = @CXNID  AND ISNULL(A.ISSUE_MODE  ,0)=0 AND ISNULL(A.ISSUE_TYPE   ,0)=1 
		GROUP BY B.PRODUCT_CODE,A.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/   ,B.BIN_ID
	) B  ON A.PRODUCT_CODE = B.PRODUCT_CODE   
	AND A.DEPT_ID = B.DEPT_ID  
	AND A.BIN_ID = B.BIN_ID  
  
  --*** UPDATING THE QUANTITY IN STOCK FROM PMT FOR THE GIVEN MEMO  
	UPDATE A  SET QUANTITY_IN_STOCK = QUANTITY_IN_STOCK - ( @NOUTFLAG * B.QUANTITY )  
	FROM  WIP_PMT A
	JOIN 
	( 
		SELECT B.PRODUCT_CODE, A.Location_code/*C.DEPT_ID*//*Rohit 06-11-2024*/ DEPT_ID,B.BIN_ID, SUM(QUANTITY) AS QUANTITY   
		FROM JOBWORK_ISSUE_DET B   
		JOIN JOBWORK_ISSUE_MST A ON A.ISSUE_ID=B.ISSUE_ID  
		WHERE A.WIP=1 AND ISNULL(A.ISSUE_MODE  ,0)=0 AND ISNULL(A.ISSUE_TYPE   ,0)=1 
		AND B.ISSUE_ID = @CXNID   
		GROUP BY B.PRODUCT_CODE,A.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/   ,B.BIN_ID
	) B  ON A.PRODUCT_CODE = B.PRODUCT_CODE   
	AND A.DEPT_ID = B.DEPT_ID  
	AND A.BIN_ID = B.BIN_ID 
	--AND A.WIP_UID=B.WIP_UID
	
	


	
	SELECT @BCANCELLED=CANCELLED FROM JOBWORK_ISSUE_MST WHERE ISSUE_ID=@CXNID  
	
	 --*** UPDATING THE QUANTITY IN STOCK FROM JOBWORK FOR THE GIVEN MEMO  
	
	SET @NRETVAL = 1  --*** SUCCESS  

    
	--*** CHECKING FOR NEGATIVE STOCK  
	--*** IF USER OPTED NOT TO ALLOW NEGATIVE STOCK AND STOCK IS GOING OUT  
	IF @NALLOWNEGSTOCK = 0 OR @BCANCELLED=1  
	BEGIN  
		IF EXISTS (  
				SELECT A.PRODUCT_CODE,SUM(A.QUANTITY_IN_STOCK) 
				FROM PMT01106 A 
				JOIN JOBWORK_ISSUE_DET B  ON B.PRODUCT_CODE=A.PRODUCT_CODE
				JOIN JOBWORK_ISSUE_MST C ON C.ISSUE_ID=B.ISSUE_ID  
				JOIN SKU D ON B.PRODUCT_CODE = D.PRODUCT_CODE  
				JOIN ARTICLE E ON D.ARTICLE_CODE = E.ARTICLE_CODE  
				WHERE C.WIP=0 AND  B.ISSUE_ID = @CXNID  AND ISNULL(C.ISSUE_MODE  ,0)=0
				AND ISNULL(C.ISSUE_TYPE   ,0)=1 
				AND E.STOCK_NA=0  AND B.PRODUCT_CODE=A.PRODUCT_CODE 
				AND C.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/ =A.DEPT_ID   AND B.BIN_ID=A.BIN_ID 
				GROUP BY A.PRODUCT_CODE
				HAVING SUM(A.QUANTITY_IN_STOCK)<0
				UNION ALL 
				SELECT A.PRODUCT_CODE,SUM(A.QUANTITY_IN_STOCK) 
				FROM WIP_PMT A 
				JOIN JOBWORK_ISSUE_DET B  ON B.PRODUCT_CODE=A.PRODUCT_CODE
				JOIN JOBWORK_ISSUE_MST C ON C.ISSUE_ID=B.ISSUE_ID  
				JOIN SKU D ON B.PRODUCT_CODE = D.PRODUCT_CODE  
				JOIN ARTICLE E ON D.ARTICLE_CODE = E.ARTICLE_CODE  
				WHERE C.WIP=1 AND   B.ISSUE_ID = @CXNID AND E.STOCK_NA=0  AND ISNULL(C.ISSUE_MODE  ,0)=0
				AND ISNULL(C.ISSUE_TYPE   ,0)=1 
				AND B.PRODUCT_CODE=A.PRODUCT_CODE AND C.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/ =A.DEPT_ID   
				AND B.BIN_ID=A.BIN_ID 
				--AND A.WIP_UID=B.WIP_UID
				GROUP BY A.PRODUCT_CODE
				HAVING SUM(A.QUANTITY_IN_STOCK)<0

			)
		BEGIN  
		SET @NRETVAL = 0  --*** UNSUCCESS  
		SET @CCMD = N'  
			SELECT A.PRODUCT_CODE,SUM(A.QUANTITY_IN_STOCK) 
			FROM PMT01106 A 
			JOIN  JOBWORK_ISSUE_DET B  ON B.PRODUCT_CODE=A.PRODUCT_CODE
			JOIN JOBWORK_ISSUE_MST C ON C.ISSUE_ID=B.ISSUE_ID  
			JOIN SKU D ON B.PRODUCT_CODE = D.PRODUCT_CODE  
			JOIN ARTICLE E ON D.ARTICLE_CODE = E.ARTICLE_CODE  
			WHERE C.WIP=0 AND  B.ISSUE_ID = '''+@CXNID+''' AND E.STOCK_NA=0  AND ISNULL(C.ISSUE_MODE  ,0)=0
			AND ISNULL(C.ISSUE_TYPE   ,0)=1 
			AND B.PRODUCT_CODE=A.PRODUCT_CODE AND C.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/ =A.DEPT_ID   AND B.BIN_ID=A.BIN_ID 
			GROUP BY A.PRODUCT_CODE
			HAVING SUM(A.QUANTITY_IN_STOCK)<0 
			UNION ALL 
			SELECT A.PRODUCT_CODE,SUM(A.QUANTITY_IN_STOCK) 
			FROM WIP_PMT A 
			JOIN JOBWORK_ISSUE_DET B  ON B.PRODUCT_CODE=A.PRODUCT_CODE
			JOIN JOBWORK_ISSUE_MST C ON C.ISSUE_ID=B.ISSUE_ID  
			JOIN SKU D ON B.PRODUCT_CODE = D.PRODUCT_CODE  
			JOIN ARTICLE E ON D.ARTICLE_CODE = E.ARTICLE_CODE  
			WHERE C.WIP=1 AND    B.ISSUE_ID = '''+@CXNID+''' AND E.STOCK_NA=0  AND ISNULL(C.ISSUE_MODE  ,0)=0
			AND ISNULL(C.ISSUE_TYPE   ,0)=1 
			AND B.PRODUCT_CODE=A.PRODUCT_CODE AND C.Location_code/*B.DEPT_ID*//*Rohit 06-11-2024*/ =A.DEPT_ID   
			AND B.BIN_ID=A.BIN_ID AND A.WIP_UID=B.WIP_UID
			GROUP BY A.PRODUCT_CODE
			HAVING SUM(A.QUANTITY_IN_STOCK)<0
			'  
		PRINT @CCMD    
		END  
	END   
END_PROC:  
	PRINT 'UPDATEPMT_JWI STEP OUT'  
END  
--***************************** END OF PROCEDURE UPDATEPMT_JWI
