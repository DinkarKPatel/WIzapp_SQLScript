

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CUSTDYM_CARD_CODE' AND VALUE=1)
BEGIN
	IF EXISTS(SELECT A.NAME FROM SYS.COLUMNS A JOIN SYS.TABLES B ON A.OBJECT_ID=B.OBJECT_ID
			  WHERE A.NAME='DISCOUNTED_CARD_TYPE' AND B.NAME='CUSTDYM')
	BEGIN		  
		DECLARE @CCMD NVARCHAR(MAX)
		SET @CCMD=N'UPDATE CUSTDYM SET CARD_CODE=B.MEMO_ID FROM BWD_MST B
		WHERE B.CARD_NAME=CUSTDYM.DISCOUNTED_CARD_TYPE'
		EXEC SP_EXECUTESQL @CCMD
	END

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE  CONFIG_OPTION='UPDATE_CUSTDYM_CARD_CODE')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_CUSTDYM_CARD_CODE',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_CUSTDYM_CARD_CODE'
		 
END
