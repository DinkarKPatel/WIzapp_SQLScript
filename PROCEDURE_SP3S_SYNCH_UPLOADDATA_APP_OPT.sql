create PROCEDURE SP3S_SYNCH_UPLOADDATA_APP_OPT
(
    @NSPID VARCHAR(50)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)

AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_APP_DATA_208_2014_02_04 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CTEMPTABLE VARCHAR(200),@CCMD NVARCHAR(MAX),@CSOURCEDB VARCHAR(10),
		@CMERGEDB VARCHAR(10),@BMSTINSERTONLY BIT,@CMEMOID VARCHAR(50),@BCANCELLED BIT,
		@BADDMODE BIT,@nUpdateMode numeric(1,0)
	   
BEGIN TRY

SET @CSTEP=20
	SET @CTABLE_SUFFIX='UPLOAD'
BEGIN TRANSACTION 



   
	SELECT @CSOURCEDB='',@CMERGEDB=''
	
	
	SELECT TOP 1 @CMEMOID = B.MEMO_ID ,@BCANCELLED=CANCELLED 
    FROM APP_APM01106_UPLOAD B (NOLOCK)
	WHERE SP_ID=@nSpId
   
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		
    

	DECLARE @CMEMOIDSEARCH VARCHAR(100)
	SELECT TOP 1 @CMEMOIDSEARCH=A.MEMO_ID FROM APM01106 A (NOLOCK) WHERE A.MEMO_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1

	
	SET @nUpdateMode=(CASE WHEN @BADDMODE=1 THEN 1 WHEN @bCancelled=1 THEN 3 ELSE 2 END)

	EXEC SP3S_upd_qty_lastupdate
	@nUpdateMode=@NUPDATEMODE,
	@cXnType='APP',
	@nSpId=@nSpId,
	@cMasterTable='Apm01106',
	@cMemoIdCol='memo_id',
	@cXnDtCol='memo_dt',
	@cMemoId=@CMEMOIDSEARCH,
	@bCalledfromMerging=1,
	@CERRORMSG=@CERRMSG OUTPUT

	IF ISNULL(@cErrmsg,'')<>''
		GOTO EXIT_PROC

    IF @BADDMODE=0
	BEGIN	
		
		 IF @bCancelled=1 
		BEGIN
			UPDATE APM01106 WITH (ROWLOCK) SET cancelled=1 WHERE MEMO_ID=@cMemoid
			GOTO lblUpdaePmt
		END

		SET @CSTEP=80
		SET @CTABLENAME='APD01106'
		SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
		SET @CTEMPTABLE=@CSOURCEDB+'APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
		SET @CKEYFIELD='MEMO_ID'
		SET @CSTEP=90
		SET @DTSQL=N'DELETE FROM '+@CMERGEDB+'['+@CTABLENAME+'] WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''''   
		PRINT @DTSQL 
		EXEC SP_EXECUTESQL @DTSQL

	END
    
	SET @CFILTERCONDITION = ' B.SP_ID='''+LTRIM(RTRIM((@NSPID )))+''''
	

	SET @CSTEP=100
	SET @CTABLENAME='STATE'
	SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='STATE_CODE'
	SET @CSTEP=110
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  
	SET @CSTEP=120
	SET @CTABLENAME='CITY'
	SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='CITY_CODE'
	SET @CSTEP=130
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  

	SET @CSTEP=140
	SET @CTABLENAME='AREA'
	SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='AREA_CODE'
	SET @CSTEP=150
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
	
	--SET @CSTEP=160
	--SET @CTABLENAME='HD01106'
	--SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	--SET @CKEYFIELD='HEAD_CODE'
	--SET @CSTEP=170
	--EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
	--						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
	--						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
	--						  ,@BALWAYSUPDATE=1 
							  
	--SET @CSTEP=180
	--SET @CTABLENAME='LM01106'
	--SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	--SET @CKEYFIELD='AC_CODE'
	--SET @CSTEP=190
	--EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
	--						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
	--						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
	--						  ,@BALWAYSUPDATE=1 							  

	--SET @CSTEP=200
	--SET @CTABLENAME='LMP01106'
	--SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	--SET @CKEYFIELD='AC_CODE'
	--SET @CSTEP=210
	--EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
	--						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
	--						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
	--						  ,@BALWAYSUPDATE=1 							  							  

	SET @CSTEP=212
	SET @CTABLENAME='CUSTDYM'
	SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='CUSTOMER_CODE'
	SET @CSTEP=214
	
	SET @CCMD=N'UPDATE '+@CSOURCEDB+@CTMP_TABLENAME+' SET REF_CUSTOMER_CODE=''000000000000'' WHERE 
	REF_CUSTOMER_CODE NOT IN (SELECT CUSTOMER_CODE FROM CUSTDYM) AND SP_ID='''+@NSPID+''''
	EXEC SP_EXECUTESQL @CCMD
	
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  							  
	
	
	
							  
	SET @CSTEP=220
	SET @CTABLENAME='APM01106'
	SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=230
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  
							  
    SET @CSTEP=240
	SET @CTABLENAME='APD01106'
	SET @CTMP_TABLENAME='APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ROW_ID'
	SET @CSTEP=250
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  
							  

	SET @CSTEP=250 
	

	lblUpdaePmt:
	EXEC SP3S_MERGE_LOCPMT  
	@cTempTable='APP_PMT01106_UPLOAD',  
	@cMemoIdCol='SP_ID',  
	@cMemoId =@nSpId

	IF ISNULL(@cErrmsg,'')=''
	BEGIN
		SET @CSTEP=260
		EXEC SP_CHKXNSAVELOG 'APPMERGE',@CSTEP,0,@CMEMOID,'',1

		EXEC SP3S_VALIDATE_DATAMERGED_SINGLECHANNEL
		@cMasterTable='APM01106',
		@cDetTable='apd01106',
		@cMemoIdCol='memo_id',
		@cMemoId=@cMemoId,
		@cUploadTable='app_apm01106_upload',
		@nSpId=@nSpId,
		@cErrormsg=@CERRMSG OUTPUT

		IF ISNULL(@cErrmsg,'')=''
			GOTO EXIT_PROC
    END

	
END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP3S_SYNCH_UPLOADDATA_APP_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK

		

	DELETE A FROM APP_APD01106_UPLOAD A WITH (ROWLOCK) WHERE A.SP_ID=@nSpId 
	DELETE A FROM APP_APM01106_UPLOAD A WITH (ROWLOCK)WHERE A.SP_ID=@nSpId 
	DELETE A FROM APP_AREA_UPLOAD A WITH (ROWLOCK)WHERE A.SP_ID=@nSpId 
	DELETE A FROM APP_city_UPLOAD A WITH (ROWLOCK)WHERE A.SP_ID=@nSpId 
	DELETE A FROM APP_CUSTDYM_UPLOAD A WITH (ROWLOCK)WHERE A.SP_ID=@nSpId 
	DELETE A FROM APP_pmt01106_UPLOAD A WITH (ROWLOCK)WHERE A.SP_ID=@nSpId 
	DELETE A FROM APR_pmt01106_UPLOAD A WITH (ROWLOCK)WHERE A.SP_ID=@nSpId ---NEed to do this step specially because of mistake in sending procedure sending apr_pmt01106_upload in older version 
	DELETE A FROM APP_state_UPLOAD A WITH (ROWLOCK)WHERE A.SP_ID=@nSpId 
	
END	
---END OF PROCEDURE - SP3S_SYNCH_UPLOADDATA_APP_OPT
