CREATE PROCEDURE SP_PPC_PRT_PRINT
(
 @CMEMO_ID VARCHAR(100)=''
)
AS
BEGIN

	SELECT CAST(0 AS BIT) AS CHK,
	       B.RM_NO ,B.RM_DT AS RM_DT,B.CANCELLED ,
	       A.PRODUCT_CODE ,A.QUANTITY,
	       ART.ARTICLE_CODE ,ART.ARTICLE_NO,
	       P1.PARA1_CODE,P1.PARA1_NAME ,
	       P2.PARA2_CODE,P2.PARA2_NAME ,
	       P3.PARA3_CODE,P3.PARA3_NAME,
	       SD.SUB_SECTION_CODE,
	       SM.SECTION_CODE 
	FROM PPC_RMD01106  A
	JOIN PPC_RMM01106  B ON A.RM_ID=B.RM_ID
	JOIN PPC_SKU SKU ON SKU.PRODUCT_UID=A.PRODUCT_UID
	JOIN ARTICLE ART ON ART.ARTICLE_CODE=SKU.ARTICLE_CODE 
	JOIN SECTIOND SD ON SD.SUB_SECTION_CODE =ART.SUB_SECTION_CODE
	JOIN SECTIONM SM ON SD.SECTION_CODE =SM.SECTION_CODE 
	JOIN PARA1 P1 ON P1.PARA1_CODE =SKU.PARA1_CODE 
	JOIN PARA2 P2 ON P2.PARA2_CODE =SKU.PARA2_CODE 
	JOIN PARA3 P3 ON P3.PARA3_CODE =SKU.PARA3_CODE 
	WHERE  B.RM_ID  =@CMEMO_ID
	

END
