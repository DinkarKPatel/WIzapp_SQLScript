CREATE FUNCTION FN_CHECK_LMP_DUP_CUSTOMER_CODE 
(
@CUSTOMER_CODE VARCHAR(12),
@AC_CODE VARCHAR(10)
)
RETURNS BIT
AS
BEGIN
           DECLARE @LRETVAL BIT
           SET @LRETVAL=1
           
  
	
	  IF EXISTS(SELECT CUSTOMER_CODE FROM LMP01106 WHERE CUSTOMER_CODE=@CUSTOMER_CODE AND AC_CODE<>@AC_CODE
	            AND ISNULL(CUSTOMER_CODE,'')NOT IN ('','000000000000'))
	   SET @LRETVAL=0
	  
       RETURN @LRETVAL
      
           
END