CREATE PROCEDURE SP_MERGE_MIRROR_BCO_DATA
(
	@CMEMOID VARCHAR(50)
   ,@CLOCID VARCHAR(3)
   ,@CSOURCEDB VARCHAR(300)
   ,@CMERGEDB VARCHAR(300)
   ,@BMSTINSERTONLY BIT
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	print 'Call bco merging'
	/*
		SP_MERGE_MIRROR_BCO_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(1000),@bAddmode BIT
	   
DECLARE @TXNSSENDINFO TABLE (ORG_TABLENAME VARCHAR(50),TMP_TABLENAME VARCHAR(50),XN_ID VARCHAR(40))  	

BEGIN TRY
SET @CSTEP=00

	SET @CTABLE_SUFFIX=REPLACE(@CMEMOID,'-','_')
BEGIN TRANSACTION 
	
	IF EXISTS (SELECT TOP 1 MEMO_ID FROM FLOOR_ST_MST (NOLOCK) WHERE MEMO_ID=@CMEMOID)
		SET @bAddmode=0
	ELSE
		SET @bAddmode=1	
	SET @CSTEP=80
	SET @CTABLENAME='FLOOR_ST_DET'
	SET @CTMP_TABLENAME='TMP_BCO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=90
	
	IF @bAddmode=0
	BEGIN
		SET @DTSQL=N'DELETE '+@CMERGEDB+'['+@CTABLENAME+']  WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''''
		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
	END
	
	SET @CSTEP=100
	SET @CTABLENAME='SKU'
	SET @CTMP_TABLENAME='TMP_BCO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PRODUCT_CODE'
	SET @CSTEP=110
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  

    SET @CSTEP=120
	SET @CTABLENAME='FLOOR_ST_MST'
	SET @CTMP_TABLENAME='TMP_BCO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=130
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@bAddmode,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  
							  
	SET @CSTEP=140
	SET @CTABLENAME='FLOOR_ST_DET'
	SET @CTMP_TABLENAME='TMP_BCO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=150
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  
	
	SET @CSTEP=150 
	
	SET @CTMP_TABLENAME=@CSOURCEDB+'TMP_BCO_pmt01106_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	EXEC SP3S_MERGE_LOCPMT  
	@cTempTable=@CTMP_TABLENAME

	SET @CSTEP=160						  
	SET @CTABLESSTR='FLOOR_ST_MST,FLOOR_ST_DET,SKU,XNSTABLELIST'

    EXEC SP3S_DROPTEMPTABLES_MIRRORDATA
    @CXNTYPE='BCO',
    @CTABLESUFFIX=@CTABLE_SUFFIX,
    @CTABLESSTR=@CTABLESSTR

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_MERGE_MIRROR_BCO_DATA, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')=''
	BEGIN
	EXEC SP3SBUILDDCO
			 @CXNID=@CMEMOID
			,@NUPDATEMODE=2
			,@CERRMSG=@CERRMSG OUTPUT  
	END

	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_BCO_DATA