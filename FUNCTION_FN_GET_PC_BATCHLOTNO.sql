CREATE FUNCTION DBO.FN_GET_PC_BATCHLOTNO(@CPRODUCT_CODE VARCHAR(50))
RETURNS VARCHAR(50)
AS
BEGIN
      DECLARE @CBATCHLOTNO VARCHAR(50)
      
      IF CHARINDEX('@',@CPRODUCT_CODE)>0
		    SELECT @CBATCHLOTNO=SUBSTRING(@CPRODUCT_CODE,CHARINDEX('@',@CPRODUCT_CODE)+1,LEN(@CPRODUCT_CODE))
      ELSE
        SET @CBATCHLOTNO=''
       
RETURN  @CBATCHLOTNO    
      
END
