CREATE PROCEDURE SP3S_VERIFY_MISDATA_MERGE_CHANGES
@nSpId VARCHAR(50),
@cMemoId VARCHAR(40)
AS
BEGIN
	
	DECLARE @CFILTERCONDITION VARCHAR(1000),@CINSSPID VARCHAR(40),@CJOINSTR VARCHAR(500)
	PRINT 'gen tempdata for 1'
	SET @CFILTERCONDITION=' b.ISSUE_ID='''+@cMemoId+''''
	set @CINSSPID=LEFT(@nSPId,38)+LEFT(@cMemoId,2)
	

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB='',@CSOURCETABLE='BOM_ISSUE_MST',@CDESTDB=''
								,@CDESTTABLE='MIS_BOM_ISSUE_MST_upload',@CKEYFIELD1='ISSUE_ID',@CKEYFIELD2='',@CKEYFIELD3=''
								,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
								,@BALWAYSUPDATE=0,@bUpdateXns=1,@CINSSPID=@cInSSPId,@CSEARCHTABLE='BOM_ISSUE_MST'

	PRINT 'gen tempdata for 2'
	EXEC SP3S_GENFILTERED_UPDATESTR
	@cSpId=@nSpId ,
	@cInsSpId=@cInsSpId,
	@cTableName='BOM_ISSUE_MST',
	@cUploadTableName='MIS_BOM_ISSUE_MST_upload',
	@cKeyfield='ISSUE_ID'
	


	PRINT 'gen tempdata for 3'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB='',@CSOURCETABLE='BOM_ISSUE_DET',@CDESTDB=''
								,@CDESTTABLE='MIS_BOM_ISSUE_DET_upload',@CKEYFIELD1='ISSUE_ID',@CKEYFIELD2='',@CKEYFIELD3=''
								,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
								,@BALWAYSUPDATE=0,@bUpdateXns=1,@CINSSPID=@cInsSPId,@CSEARCHTABLE='BOM_ISSUE_DET'
	
	PRINT 'gen tempdata for 4'
	EXEC SP3S_GENFILTERED_UPDATESTR
	@cSpId=@nSpId ,
	@cInsSpId=@cInsSpId,
	@cTableName='BOM_ISSUE_DET',
	@cUploadTableName='MIS_BOM_ISSUE_DET_upload',
	@cKeyfield='row_id',
	@bDonotChkLastUpdate=1

	SET @CFILTERCONDITION=' b.BOM_ISSUE_ID='''+@cMemoId+''''
	
	PRINT 'gen tempdata for 3'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB='',@CSOURCETABLE='BOM_ISSUE_REF',@CDESTDB=''
								,@CDESTTABLE='MIS_BOM_ISSUE_REF_upload',@CKEYFIELD1='ISSUE_ID',@CKEYFIELD2='',@CKEYFIELD3=''
								,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
								,@BALWAYSUPDATE=0,@bUpdateXns=1,@CINSSPID=@cInsSPId,@CSEARCHTABLE='BOM_ISSUE_REF'
	
	PRINT 'gen tempdata for 4'
	EXEC SP3S_GENFILTERED_UPDATESTR
	@cSpId=@nSpId ,
	@cInsSpId=@cInsSpId,
	@cTableName='BOM_ISSUE_REF',
	@cUploadTableName='MIS_BOM_ISSUE_REF_upload',
	@cKeyfield='row_id',
	@bDonotChkLastUpdate=1




END