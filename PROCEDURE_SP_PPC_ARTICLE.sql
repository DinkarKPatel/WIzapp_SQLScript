CREATE PROCEDURE SP_PPC_ARTICLE
(
 @CSUB_SECTION_CODE VARCHAR(10)=''
)
AS
BEGIN
    
    
    SELECT ARTICLE_NO,ARTICLE_NAME,ARTICLE_CODE FROM 
    ARTICLE A
    JOIN SECTIOND B ON A.SUB_SECTION_CODE =B.SUB_SECTION_CODE 
    WHERE A.INACTIVE =0
    AND ARTICLE_NO<>''
    AND (@CSUB_SECTION_CODE='' OR A.SUB_SECTION_CODE =@CSUB_SECTION_CODE)
    
  
END
