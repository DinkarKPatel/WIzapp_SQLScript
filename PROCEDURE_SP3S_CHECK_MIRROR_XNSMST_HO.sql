CREATE PROCEDURE SP3S_CHECK_MIRROR_XNSMST_HO
@CXNTYPE VARCHAR(15),
@CXNID VARCHAR(40)
AS
BEGIN
	DECLARE @CCHECKMSTCODE VARCHAR(20),@BFOUND BIT,@CERRORMSG VARCHAR(MAX),@CSTEP VARCHAR(10)

BEGIN TRY	
	SET @BFOUND=0
	
	IF @CXNTYPE='CUS'
	BEGIN
		SET @CSTEP='10'
		
		SELECT TOP 1 @CCHECKMSTCODE=CUSTOMER_CODE FROM CUSTDYM (NOLOCK) WHERE CUSTOMER_CODE=@CXNID
		
		SET @CSTEP='20'
		IF ISNULL(@CCHECKMSTCODE,'')<>''
			SET @BFOUND=1

		GOTO END_PROC
	END
END TRY

BEGIN CATCH
	SET @CERRORMSG = 'P:SP3S_CHECK_MIRROR_XNSMST_HO, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH

END_PROC:
	SELECT @BFOUND AS MSTFOUND,ISNULL(@CERRORMSG,'') AS ERRMSG
END
----- 'END OF CREATING PROCEDURE - SP3S_CHECK_MIRROR_XNSMST_HO'
