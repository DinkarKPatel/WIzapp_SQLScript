

IF OBJECT_ID ('UPLOAD_MIRRORXN','U') IS NOT NULL
   DROP TABLE UPLOAD_MIRRORXN


   IF OBJECT_ID ('TEMPDB..#TMPTABLE ','U') IS NOT NULL
      DROP TABLE #TMPTABLE 

   SELECT TABLE_NAME 
   INTO #TMPTABLE  
   FROM INFORMATION_SCHEMA.TABLES WHERE RIGHT(TABLE_NAME ,7)='_MIRROR'
   AND LEFT (TABLE_NAME,3) NOT IN('DOC','MST','PPC')
   

   DECLARE @TABLENAME VARCHAR(100),@DTSQL NVARCHAR(MAX)

   WHILE EXISTS (SELECT TOP 1 'U' FROM #TMPTABLE)
   BEGIN
      
	  SELECT TOP 1 @TABLENAME=TABLE_NAME FROM  #TMPTABLE
	  SET @DTSQL=' DROP TABLE '+@TABLENAME+''
	  PRINT @DTSQL
	  EXEC SP_EXECUTESQL  @DTSQL

	  DELETE FROM #TMPTABLE WHERE TABLE_NAME=@TABLENAME

   END