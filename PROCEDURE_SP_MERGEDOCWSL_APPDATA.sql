CREATE PROCEDURE SP_MERGEDOCWSL_APPDATA  
(  
 @CMEMOID VARCHAR(40)
)  
--WITH ENCRYPTION
AS  
BEGIN    
	  DECLARE @CERRORMSG VARCHAR(1000),@CCURDEPTID CHAR(2),@CHODEPTID CHAR(2),@CCMD NVARCHAR(MAX),
			  @NSTEP INT,@BPROCEED BIT,@BLOCDATAEXISTS BIT,@CTABLEPREFIX VARCHAR(100),@CXNTYPE VARCHAR(15)	
		
	    
	  BEGIN TRANSACTION  
	    
	  SET @NSTEP = 10  
	  DECLARE @TRETMSG TABLE  (MEMO_ID VARCHAR(40),ERRMSG VARCHAR(MAX))  
	   
	  BEGIN TRY  
	       
	  SELECT @CERRORMSG='',@BPROCEED=1,@CXNTYPE='DOCWSL_APP'
	    
	  SET @CTABLEPREFIX='TMP_DOCWSL_APP'
	   
	  SET @NSTEP = 20  
	  	
	  SET @CCMD=N'IF OBJECT_ID(''TMP_DOCWSL_APP_INM01106_'+LTRIM(RTRIM(@CMEMOID))+''',''U'') IS NOT NULL  
		  SELECT @BLOCDATAEXISTSOUT=1   
		 ELSE  
		  SELECT @BLOCDATAEXISTSOUT=0'  
	  EXEC SP_EXECUTESQL @CCMD,N'@BLOCDATAEXISTSOUT BIT OUTPUT',@BLOCDATAEXISTSOUT=@BLOCDATAEXISTS OUTPUT  
	    
	  IF @BLOCDATAEXISTS=0  
	  BEGIN  
		   SET @NSTEP = 25  
		     
		   SET @CERRORMSG='NO STOCK TRANSFER APPROVAL DATA FOUND TO BE MERGED'  
		   GOTO LBLLAST  
	  END  
	    
	  SET @NSTEP=30
	  IF EXISTS (SELECT INV_ID FROM INM01106 (NOLOCK) WHERE INV_ID=@CMEMOID AND APPROVED=3)
	  BEGIN
			SET @CERRORMSG='STOCK TRANSFER HAS ALREADY BEEN DISAPPROVED AND MARKED AS CANCELLED....CANNOT REPROCESS'
			GOTO LBLLAST	
	  END 	
	  
	  SET @NSTEP=35
	  
	  SET @CCMD=N'UPDATE INM01106 SET APPROVED=B.APPROVED,EXPORTED=(CASE WHEN B.APPROVED=2 THEN 1 ELSE EXPORTED END)
				  FROM [TMP_DOCWSL_APP_INM01106_'+LTRIM(RTRIM(@CMEMOID))+'] B WHERE B.INV_ID=INM01106.INV_ID'	 
	  
	  EXEC SP_EXECUTESQL @CCMD

	  SET @NSTEP=40
		
	  ----- JUST CANCEL THE INVOICE IF IT HAS BEEN DISAPPROVED BY HEAD OFFICE		  
	  IF EXISTS (SELECT INV_ID FROM INM01106 (NOLOCK) WHERE INV_ID=@CMEMOID AND APPROVED=3 AND CANCELLED=0)	      
		  EXEC SAVETRAN_WSL
		  @NUPDATEMODE=3,
		  @CMEMOID=@CMEMOID,
		  @NSPID=0

	  
	  GOTO LBLLAST
	    
	 END TRY  
	  
	 BEGIN CATCH  
		  
		  PRINT 'UNTRAPPED ERROR'    
		       
		  DECLARE @CERRORPROCNAME VARCHAR(100),@CLINENO VARCHAR(5),@CERRTEXT VARCHAR(MAX)  
		    
		  SELECT @CERRORPROCNAME=ISNULL(ERROR_PROCEDURE(),'NULL P'),@CLINENO=ISNULL(LTRIM(RTRIM(STR(ERROR_LINE()))),'NULL LINE'),  
		  @CERRTEXT='STEP : '+LTRIM(RTRIM(STR(@NSTEP)))+' '+ISNULL(ERROR_MESSAGE(),'NULL MSG')  
		    
		  SELECT @CERRORMSG=@CERRTEXT  
		  
		  GOTO LBLLAST  
     END CATCH  

LBLLAST:  	 
	 
	      
	IF @@TRANCOUNT>0  
	BEGIN
		IF ISNULL(@CERRORMSG,'')=''
			COMMIT TRANSACTION  
		 ELSE
			ROLLBACK
	END	
	 
 	INSERT @TRETMSG
	SELECT @CMEMOID,@CERRORMSG
			
	SELECT * FROM @TRETMSG

	EXEC SP_DROPTEMPTABLES_XNS 'DOCWSL_APP',0,@CMEMOID,'TMP_DOCWSL_APP'  

END  
--- 'END OF CREATING PROCEDURE SP_MERGEDOCWSL_APPDATA'
