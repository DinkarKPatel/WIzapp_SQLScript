CREATE PROCEDURE DBO.SP_SEND_MIRROR_MSTXNAPP_NEW
(
 @CTARGETLOCID VARCHAR(5)
)
AS
 BEGIN
   DECLARE @DTSQL NVARCHAR(MAX),@CSTEP VARCHAR(10),@CFILTERCONDITION NVARCHAR(MAX)
           ,@CTABLENAME VARCHAR(100),@CTEMPTABLE VARCHAR(100),@CTEMPLMTABLE VARCHAR(100)
           ,@CTEMPLMPTABLE VARCHAR(100),@CTEMPDBNAME VARCHAR(200),@CCURDEPTID VARCHAR(5)
           ,@CHODEPTID VARCHAR(5),@CERRORMSG VARCHAR(MAX),@CREPLOCTABLE VARCHAR(500),@NLOCTYPE INT
           ,@CLOCMERGINGTHRUWIZCOM VARCHAR(2),@JOINMASTERTABLE VARCHAR(200),@JOINTEMPTABLE VARCHAR(200)

    DECLARE @TXNSSENDINFO TABLE (ORG_TABLENAME VARCHAR(100),TMP_TABLENAME VARCHAR(100),ERRMSG VARCHAR(MAX)) 
    
    BEGIN TRY
    
	SET @CSTEP=0

	SET @CTEMPDBNAME = ''
	
	SET @CSTEP=10
	
	PRINT @CSTEP+':'+CONVERT(VARCHAR,GETDATE(),113)
	SET @CFILTERCONDITION=''
	 
	SET @CSTEP=20

	SELECT @CCURDEPTID = VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'
	SELECT @CHODEPTID = VALUE FROM CONFIG WHERE CONFIG_OPTION='HO_LOCATION_ID'		
	
	SELECT @NLOCTYPE=LOC_TYPE FROM LOCATION WHERE DEPT_ID=@CTARGETLOCID
	
	PRINT @CSTEP+':'+CONVERT(VARCHAR,GETDATE(),113)
	
	IF @CCURDEPTID<>@CHODEPTID
	BEGIN
		SELECT '' AS LAST_UPDATE,'' AS ERRMSG
		
		GOTO END_PROC
	END		
    SET @CSTEP=30
	
	
	--INSERT DATA FROM MAIN TABLE TO TEMP TABLE
    SET @CSTEP=40
    ---SELECT DATA FROM LOC_BILLING_RULES TABLE
	SET @CTABLENAME = 'XN_APPROVAL_CHECKLIST_MST'
	SELECT DISTINCT  'MSTXNAPP_XN_APPROVAL_CHECKLIST_MST_MIRROR' AS TARGET_TABLENAME,XN_APPROVAL_CHECKLIST_MST.*,1 AS PICK_FROM_CURRENT_DB FROM XN_APPROVAL_CHECKLIST_MST (NOLOCK)
	
	
	---INSERT TEMP TABLE AND ORGINAL TABLE NAME INTO OUTPUT TABLE
	   INSERT INTO @TXNSSENDINFO(ORG_TABLENAME,TMP_TABLENAME) SELECT @CTABLENAME,'TMP_MSTXNAPP_'+@CTABLENAME+'_'+@CTARGETLOCID

	--INSERT DATA FROM MAIN TABLE TO TEMP TABLE
    SET @CSTEP=50
    ---SELECT DATA FROM LOC_BILLING_RULES TABLE
	SET @CTABLENAME = 'XN_APPROVAL_CHECKLIST_LEVELS'
	SELECT DISTINCT  'MSTXNAPP_XN_APPROVAL_CHECKLIST_LEVELS_MIRROR' AS TARGET_TABLENAME,XN_APPROVAL_CHECKLIST_LEVELS.*,1 AS PICK_FROM_CURRENT_DB FROM XN_APPROVAL_CHECKLIST_LEVELS (NOLOCK)
	 
     
    --INSERT DATA FROM MAIN TABLE TO TEMP TABLE
    SET @CSTEP=60
    ---SELECT DATA FROM LOC_BILLING_RULES TABLE
	SET @CTABLENAME = 'XN_APPROVAL_CHECKLIST_LEVEL_DETAILS'
	SELECT DISTINCT  'MSTXNAPP_XN_APPROVAL_CHECKLIST_LEVEL_DETAILS_MIRROR' AS TARGET_TABLENAME,XN_APPROVAL_CHECKLIST_LEVEL_DETAILS.*,1 AS PICK_FROM_CURRENT_DB FROM XN_APPROVAL_CHECKLIST_LEVEL_DETAILS (NOLOCK)
	 
     
      
    --INSERT DATA FROM MAIN TABLE TO TEMP TABLE
    SET @CSTEP=70
    ---SELECT DATA FROM LOC_BILLING_RULES TABLE
	SET @CTABLENAME = 'XN_APPROVAL_CHECKLIST_LEVEL_USERS'
	SELECT DISTINCT  'MSTXNAPP_XN_APPROVAL_CHECKLIST_LEVEL_USERS_MIRROR' AS TARGET_TABLENAME,XN_APPROVAL_CHECKLIST_LEVEL_USERS.*,1 AS PICK_FROM_CURRENT_DB FROM XN_APPROVAL_CHECKLIST_LEVEL_USERS (NOLOCK)
	WHERE DEPT_ID=@CTARGETLOCID

	
     
       
    --INSERT DATA FROM MAIN TABLE TO TEMP TABLE
    SET @CSTEP=80
    ---SELECT DATA FROM LOC_BILLING_RULES TABLE
	SET @CTABLENAME = 'LOC_XNSAPPROVAL'
	SELECT DISTINCT  'MSTXNAPP_LOC_XNSAPPROVAL_MIRROR' AS TARGET_TABLENAME,LOC_XNSAPPROVAL.*,1 AS PICK_FROM_CURRENT_DB FROM LOC_XNSAPPROVAL (NOLOCK)
	 
     

	END TRY
	
	BEGIN CATCH
	    SET @CERRORMSG='P: SP_SEND_MIRROR_MSTXNAPP, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()+ 'ERROR QUERY || '+@DTSQL
	    GOTO END_PROC
    END CATCH 
    
    END_PROC:
    
   SELECT ISNULL(@CERRORMSG,'')  AS [ERRMSG]
 END
