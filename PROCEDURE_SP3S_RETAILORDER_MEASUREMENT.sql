CREATE PROCEDURE SP3S_RETAILORDER_MEASUREMENT
(
	@CPRODUCT_CODE VARCHAR(100)
)
AS
BEGIN
         SELECT A.PRODUCT_CODE, MM.M_NAME,ISNULL(MM.M_ALIAS,'') AS M_ALIAS,TDD.MEASURMENT  
         FROM WSL_ORDER_DET A (NOLOCK)
         JOIN TDD01106 TDD (NOLOCK) ON A.ROW_ID=TDD.TMD_ROW_ID
		 JOIN ARTICLE_MEASUREMENTS AM (NOLOCK) ON AM.ARTICLE_CODE =A.ARTICLE_CODE AND AM.M_CODE=TDD.ATTRIBUTE_CODE 
		 JOIN MEASUREMENT_MST (NOLOCK) MM ON MM.M_CODE =AM.M_CODE
		 WHERE PRODUCT_CODE=@CPRODUCT_CODE  AND ISNULL(PRODUCT_CODE,'')<>''
		 ORDER BY MM.ORDER_NO
		
END
--END OF PROCEDURE - SP3S_RETAILORDER_MEASUREMENT
