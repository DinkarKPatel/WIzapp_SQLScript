CREATE FUNCTION FN3S_GETLEVELUSERS  
 (  
 @CLEVELCODE VARCHAR(20),  
 @CXNTYPE VARCHAR(20),  
 @CDEPTID VARCHAR(5)  
) RETURNS VARCHAR(MAX)   
AS  
BEGIN  
DECLARE @CRETVAL VARCHAR(MAX)  
	SELECT @CRETVAL=(CASE WHEN ISNULL(@CRETVAL,'')='' THEN '' ELSE ISNULL(@CRETVAL,'')+',' END)+ B.USERNAME  
	FROM XN_APPROVAL_CHECKLIST_LEVEL_USERS A  
	JOIN USERS B ON B.USER_CODE=A.USER_CODE  
	JOIN XN_APPROVAL_CHECKLIST_LEVELS C ON C.LEVEL_CODE=A.LEVEL_CODE  
	WHERE A.LEVEL_CODE=@CLEVELCODE AND A.XN_TYPE=@CXNTYPE AND A.DEPT_ID=@CDEPTID --AND A.USER_CODE=@CUSERCODE    
RETURN ISNULL(@CRETVAL,'')  
END
