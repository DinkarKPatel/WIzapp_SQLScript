CREATE VIEW VW_PRD_ITEMRATEREVISION_LIST

AS 
SELECT IRM_MEMO_NO AS MEMO_NO,
IRM_MEMO_DT AS MEMO_DT,
REMARKS,
(CASE WHEN A.TYPE = 1 THEN 'RATE REVISION' ELSE CASE WHEN A.TYPE =2 THEN 'TAG PRINGITNG'  ELSE 'MASTER REVISION' END END)  AS MEMO_TYPE, 
A.IRM_MEMO_ID AS MEMO_ID,
BARCODE_PREFIX,
A.FIN_YEAR,
U.USERNAME AS CREATED_BY
FROM PRD_IRM01106 A (NOLOCK)
JOIN USERS U(NOLOCK) ON U.USER_CODE=A.USER_CODE
