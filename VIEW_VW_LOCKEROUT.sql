CREATE VIEW VW_LOCKEROUT
AS
SELECT A.MEMO_ID,MEMO_NO,MEMO_DT,USERNAME,AC_NAME,
      AMOUNT,REMARKS,CANCELLED,A.FIN_YEAR 
FROM TILL_BANK_TRANSFER (NOLOCK)  A
JOIN LM01106  (NOLOCK) B ON A.AC_CODE =B.AC_CODE 
JOIN USERS  (NOLOCK) C ON A.USER_CODE =C.USER_CODE
WHERE ISNULL(REFLIFTID, '')=''
AND CANCELLED =0
