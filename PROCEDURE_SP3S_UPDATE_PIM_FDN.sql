CREATE PROCEDURE SP3S_UPDATE_PIM_FDN
(
	@NMRRID			VARCHAR(50)
)
AS
BEGIN
	DECLARE @CERR NVARCHAR(MAX)
	BEGIN TRAN
	BEGIN TRY
		UPDATE PIM01106	SET DO_NOT_CREATE_FDN=1 WHERE MRR_ID=@NMRRID
		COMMIT TRAN
	END TRY
	BEGIN CATCH
		SET @CERR='ERROR : SP3S_UPDATE_PIM_FDN : ' + ERROR_MESSAGE()
		ROLLBACK TRAN
	END CATCH
	
	SELECT @NMRRID AS MEMO_ID,ISNULL(@CERR,'') AS ERRMSG
END
