CREATE  VIEW VW_POADJUSTMENT_LIST

AS               
SELECT       
MST.MEMO_DT AS 'MEMO_DT',  
MST.MEMO_ID AS 'MEMO_ID',       
MST.MEMO_NO AS 'MEMO_NO',       
A.REF_NO AS 'REF_NO',       
MST.REMARKS AS 'REMARKS',              
B.AC_NAME AS 'SUPPLIER_NAME',        
H.USERNAME AS 'CREATED_BY_USER', 
MST.FIN_YEAR ,MST.location_code
FROM PO_ADJ_MST MST (NOLOCK)
JOIN POM01106 A (NOLOCK) ON A.PO_ID=MST.REF_PO_ID                 
JOIN LM01106 B (NOLOCK)  ON A.AC_CODE = B.AC_CODE         
JOIN USERS H (NOLOCK) ON MST.USER_CODE = H.USER_CODE
