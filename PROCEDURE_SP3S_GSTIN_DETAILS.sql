CREATE PROCEDURE SP3S_GSTIN_DETAILS
AS
BEGIN
	SELECT A.GSTIN 
	FROM TAXPRO_USED_BY_CLIENT A
	LEFT OUTER JOIN lmv01106 B1 ON B1.Ac_gst_no=A.GSTIN
	LEFT OUTER JOIN TAXPRO_CLIENT_DETAILS B ON B.TCD_AC_GST_NO=A.GSTIN
	WHERE ISNULL(A.GSTIN,'')<>'' AND  B.TCD_AC_GST_NO IS NULL AND  B1.AC_GST_NO IS NULL
	GROUP BY A.GSTIN
END
