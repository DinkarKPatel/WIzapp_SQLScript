CREATE PROCEDURE SP_PPC_RMLIST
(
 @ARTICLE_TYPE INT=0
)
AS
BEGIN
    
    IF @ARTICLE_TYPE=1
    BEGIN
		SELECT ARTICLE_CODE,ARTICLE_NO,ARTICLE_NAME
		FROM ARTICLE
		WHERE (@ARTICLE_TYPE=0 OR ARTICLE_TYPE=@ARTICLE_TYPE)
    END 
    ELSE
    BEGIN
        SELECT ARTICLE_CODE,ARTICLE_NO,ARTICLE_NAME
		FROM ARTICLE
		WHERE (@ARTICLE_TYPE=0 OR ARTICLE_TYPE>=@ARTICLE_TYPE)
    END

END
