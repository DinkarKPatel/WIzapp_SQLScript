CREATE PROCEDURE SP3S_VALIDATEXN_SLSMERGE
@cSpId VARCHAR(40),
@cMemoId VARCHAR(40),
@cErrormsg VARCHAR(MAX) OUTPUT
AS
BEGIN
	DECLARE @NNETAMOUNT NUMERIC(10,2),@NCMMSUBTOTAL NUMERIC(10,2),@NPAYMODETOTAMT NUMERIC(10,2),
	@NTotCmmQty NUMERIC(10,2),@NTotCmdQty NUMERIC(10,2),@cStep VARCHAR(4)

	SET @CSTEP=10.2
	EXEC SP_CHKXNSAVELOG 'SLSMERGE',@CSTEP,0,@CSPID,'',1

	SELECT @NNETAMOUNT=NET_AMOUNT,@NCMMSUBTOTAL=SUBTOTAL+SUBTOTAL_R FROM cmm01106 (NOLOCK)
	WHERE cm_id=@cMemoId
	
	SET @CSTEP=10.4
	EXEC SP_CHKXNSAVELOG 'SLSMERGE',@CSTEP,0,@CSPID,'',1

	SELECT @NPAYMODETOTAMT = SUM(AMOUNT) FROM paymode_xn_det (NOLOCK) WHERE memo_id=@cMemoId AND xn_type='SLS' 

	SET @CSTEP=10.6
	EXEC SP_CHKXNSAVELOG 'SLSMERGE',@CSTEP,0,@CSPID,'',1
		  
	SELECT @NNETAMOUNT=ISNULL(@NNETAMOUNT,0),@NPAYMODETOTAMT = ISNULL(@NPAYMODETOTAMT,0)  
	
	IF @NNETAMOUNT<>@NPAYMODETOTAMT
	BEGIN  
		PRINT 'SP3S_VALIDATEXN_SLSMERGE payment validation FAILED'
		SET @CERRORMSG='NET AMOUNT '+STR(@NNETAMOUNT,14,2)+' SHOULD BE EQUAL TO THE SUM OF ALL PAYMENT MODES '+STR(@NPAYMODETOTAMT,14,2)+'...PLEASE CHECK'  
		GOTO END_PROC
	END
	
	SET @CSTEP=10.8
	EXEC SP_CHKXNSAVELOG 'SLSMERGE',@CSTEP,0,@CSPID,'',1


	SELECT @NTotCmmQty=total_quantity FROM cmm01106 (NOLOCK) WHERE cm_id=@cMemoId

	SET @CSTEP=11.2
	EXEC SP_CHKXNSAVELOG 'SLSMERGE',@CSTEP,0,@CSPID,'',1
		
	SELECT @NTotCmdQty = SUM(quantity) FROM cmd01106 (NOLOCK) WHERE cm_id=@cMemoId 

	SET @CSTEP=11.4
	EXEC SP_CHKXNSAVELOG 'SLSMERGE',@CSTEP,0,@CSPID,'',1		  
	SELECT @NTotCmmQty=ISNULL(@NTotCmmQty,0),@NTotCmdQty = ISNULL(@NTotCmdQty,0)  
	
	IF @NTotCmmQty<>@NTotCmdQty
	BEGIN  
		PRINT 'SP3S_VALIDATEXN_SLSMERGE Quantity match FAILED'
		SET @CERRORMSG='Bill level Total quantity '+STR(@NTotCmmQty,14,2)+' SHOULD BE EQUAL TO THE SUM OF Item level quantity '+STR(@NTotCmdQty,10,2)+'...PLEASE CHECK'  
		GOTO END_PROC
	END

END_PROC:
	
END