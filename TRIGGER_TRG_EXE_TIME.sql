CREATE TRIGGER TRG_EXE_TIME ON EXE_TIME 
FOR UPDATE,DELeTE
AS
BEGIN
	IF EXISTS (SELECT TOP 1 * from DELETED) AND NOT EXISTS (SELECT TOP 1 * FROM INSERTED)
	BEGIN
		RAISERROR('CANNOT DELETE ENTRY FROM EXE_TIME',16,1)
		ROLLBACK
	END

	IF EXISTS (SELECT TOP 1 * from DELETED a JOIN INSERTED B ON 1=1 where a.last_update>b.last_update)
	BEGIN
		RAISERROR('CANNOT UPDATE OLDER DATE IN EXE_TIME....',16,1)
		ROLLBACK
	END
END


