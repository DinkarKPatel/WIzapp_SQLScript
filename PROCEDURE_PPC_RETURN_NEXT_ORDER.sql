CREATE PROCEDURE PPC_RETURN_NEXT_ORDER(@XN_TYPE VARCHAR(10)='WSL',@FINYEAR VARCHAR(5))
AS 
BEGIN
  DECLARE @ORDER_NO VARCHAR(10)
  IF @XN_TYPE='WSL'
     BEGIN
        SELECT @ORDER_NO=ISNULL(MAX(ORDER_NO),'01000000') FROM PPC_BUYER_ORDER_MST WHERE FIN_YEAR=@FINYEAR
        SET @ORDER_NO=LEFT(@ORDER_NO,2)+RIGHT('00000000'+CAST(CAST(RIGHT(@ORDER_NO,6) AS INT)+1 AS VARCHAR),6)
     END
SELECT @ORDER_NO ORDER_NUMBER
END
