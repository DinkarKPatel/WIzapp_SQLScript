

-- GET ALL DEFINED CUSTOMER MASTER
CREATE PROCEDURE SP_WL_CITYLIST
--WITH ENCRYPTION

AS
	SELECT C.REGION_CODE ,C.REGION_NAME,B.STATE_CODE  ,B.STATE  ,A.CITY_CODE, A.CITY 
	FROM CITY AS A 
	JOIN STATE AS B ON A.STATE_CODE = B.STATE_CODE  
	JOIN REGIONM AS C ON B.REGION_CODE = C.REGION_CODE  
	--WHERE CITY_CODE <>'0000000' 
	ORDER BY REGION_NAME ,STATE,  CITY 
