create PROCEDURE SP3S_SYNCH_UPLOADDATA_BCO_OPT
(
    @NSPID VARCHAR(50)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	print 'Call bco merging'
	/*
		SP_MERGE_MIRROR_BCO_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(1000),@bAddmode BIT,@CSOURCEDB VARCHAR(10),
		@CMERGEDB VARCHAR(10),@BMSTINSERTONLY BIT,@CMEMOID VARCHAR(50),@BCANCELLED BIT,@nUpdateMode NUMERIC(1,0)
	   
BEGIN TRY
SET @CSTEP=00

	SET @CTABLE_SUFFIX='UPLOAD'
BEGIN TRANSACTION 

    
	SELECT @CSOURCEDB='',@CMERGEDB=''

	SELECT TOP 1 @CMEMOID = B.MEMO_ID ,@BCANCELLED=CANCELLED 
    FROM BCO_FLOOR_ST_MST_UPLOAD B (NOLOCK)
	WHERE SP_ID=@nSpId
	
	IF EXISTS (SELECT TOP 1 MEMO_ID FROM FLOOR_ST_MST (NOLOCK) WHERE MEMO_ID=@CMEMOID)
		SET @bAddmode=0
	ELSE
		SET @bAddmode=1	
    
	SET @nUpdateMode=(CASE WHEN @BADDMODE=1 THEN 1 WHEN @bCancelled=1 THEN 3 ELSE 2 END)

	EXEC SP3S_upd_qty_lastupdate
	@nUpdateMode=@NUPDATEMODE,
	@cXnType='BCO',
	@nSpId=@nSpId,
	@cMasterTable='FLOOR_ST_MST',
	@cMemoIdCol='MEMO_ID',
	@cXnDtCol='MEMO_DT',
	@cMemoId=@CMEMOID,
	@bCalledfromMerging=1,
	@CERRORMSG=@CERRMSG OUTPUT

	IF ISNULL(@cErrmsg,'')<>''
		GOTO EXIT_PROC


	SET @CSTEP=80
	SET @CTABLENAME='FLOOR_ST_DET'
	SET @CTMP_TABLENAME='BCO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=90
	
	IF @bAddmode=0
	BEGIN
	     IF @bCancelled=1 
		BEGIN
			UPDATE FLOOR_ST_MST WITH (ROWLOCK) SET cancelled=1 WHERE MEMO_ID=@cMemoid
			GOTO lblUpdaePmt
		END

		SET @DTSQL=N'DELETE '+@CMERGEDB+'['+@CTABLENAME+']  WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''''
		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
	END
	
    SET @CFILTERCONDITION = ' B.SP_ID='''+LTRIM(RTRIM((@NSPID )))+''''
							
	SET @CSTEP=100
	SET @CTABLENAME='SKU'
	SET @CTMP_TABLENAME='BCO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PRODUCT_CODE'
	SET @CSTEP=110
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 		
							  
					
    SET @CSTEP=120
	SET @CTABLENAME='FLOOR_ST_MST'
	SET @CTMP_TABLENAME='BCO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=130
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@bAddmode,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  
							  
	SET @CSTEP=140
	SET @CTABLENAME='FLOOR_ST_DET'
	SET @CTMP_TABLENAME='BCO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=150
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  
	
	SET @CSTEP=150 
	
	lblUpdaePmt:
	EXEC SP3S_MERGE_LOCPMT  
	@cTempTable='BCO_PMT01106_UPLOAD',  
	@cMemoIdCol='SP_ID',  
	@cMemoId =@nSpId
	

	IF ISNULL(@cErrmsg,'')=''
	BEGIN
		SET @CSTEP=155
		EXEC SP_CHKXNSAVELOG 'BCOMERGE',@CSTEP,0,@CMEMOID,'',1

		EXEC SP3S_VALIDATE_DATAMERGED_SINGLECHANNEL
		@cMasterTable='floor_st_mst',
		@cDetTable='floor_st_det',
		@cMemoIdCol='memo_id',
		@cMemoId=@cMemoId,
		@cUploadTable='bco_floor_st_mst_upload',
		@nSpId=@nSpId,
		@cErrormsg=@CERRMSG OUTPUT

		IF ISNULL(@cErrmsg,'')=''
			GOTO EXIT_PROC
    END
		
	


END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP3S_SYNCH_UPLOADDATA_BCO_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
		
		
    DELETE A FROM BCO_FLOOR_ST_MST_UPLOAD A WITH (ROWLOCK) WHERE A.SP_ID=@nSpId 
	DELETE A FROM BCO_FLOOR_ST_Det_UPLOAD A WITH (ROWLOCK)  WHERE A.SP_ID=@nSpId 
	DELETE A FROM BCO_PMT01106_UPLOAD A WITH (ROWLOCK)  WHERE A.SP_ID=@nSpId 
	DELETE A FROM BCO_SKU_UPLOAD A WITH (ROWLOCK) WHERE A.SP_ID=@nSpId 

END	
---END OF PROCEDURE - SP_MERGE_MIRROR_BCO_DATA