CREATE VIEW VW_WL_RATEREVISIONLIST_QBF 
 AS         
SELECT 
A.FIN_YEAR AS 'MST_FIN_YEAR', 
A.IRM_MEMO_DT AS 'MST_IRM_MEMO_DT', 
A.IRM_MEMO_ID, 
A.IRM_MEMO_NO AS 'MST_IRM_MEMO_NO', 
A.REMARKS AS 'MST_REMARKS', 
A.REVISION_TYPE AS 'MST_REVISION_TYPE', 
A.TYPE AS 'MST_TYPE',

B.PRODUCT_CODE, 
C.ARTICLE_NO, 
C.ARTICLE_NAME,
B.PRINT_LABEL, 
P1.PARA1_NAME, 
P2.PARA2_NAME, 
P3.PARA3_NAME, 
P4.PARA4_NAME, 
P5.PARA5_NAME, 
P6.PARA6_NAME,
B.QUANTITY, 
U.UOM_NAME, 
B.NEW_MRP, 
B.OLD_MRP, 
--B.NEW_PRODUCT_CODE, 
D.SUB_SECTION_NAME, 
E.SECTION_NAME
FROM IRM01106 A
JOIN IRD01106 (NOLOCK) B ON A.IRM_MEMO_ID = B.IRM_MEMO_ID
JOIN SKU ON SKU.PRODUCT_CODE=B.PRODUCT_CODE
JOIN ARTICLE (NOLOCK) C ON SKU.ARTICLE_CODE = C.ARTICLE_CODE
JOIN PARA1 (NOLOCK) P1 ON SKU.PARA1_CODE = P1.PARA1_CODE
JOIN PARA2 (NOLOCK) P2 ON SKU.PARA2_CODE = P2.PARA2_CODE
JOIN PARA3 (NOLOCK) P3 ON SKU.PARA3_CODE = P3.PARA3_CODE
JOIN PARA4 (NOLOCK) P4 ON SKU.PARA4_CODE = P4.PARA4_CODE
JOIN PARA5 (NOLOCK) P5 ON SKU.PARA5_CODE = P5.PARA5_CODE
JOIN PARA6 (NOLOCK) P6 ON SKU.PARA6_CODE = P6.PARA6_CODE
JOIN UOM (NOLOCK) U ON C.UOM_CODE = U.UOM_CODE
JOIN SECTIOND (NOLOCK) D ON C.SUB_SECTION_CODE = D.SUB_SECTION_CODE
JOIN SECTIONM (NOLOCK) E ON D.SECTION_CODE = E.SECTION_CODE

--*************************************** END OF VIEW VW_WL_RATEREVISIONLIST_QBF
