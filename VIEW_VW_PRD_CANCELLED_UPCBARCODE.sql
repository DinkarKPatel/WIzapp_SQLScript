CREATE VIEW   VW_PRD_CANCELLED_UPCBARCODE
AS
   
   SELECT A.MEMO_NO ,A.MEMO_DT ,
          LM.AC_NAME, 
          WO.MEMO_NO AS WO_NO,
          WO.MEMO_DT AS WO_DT,
          ART.ARTICLE_NO ,ART.ARTICLE_NAME,
          P1.PARA1_NAME ,P2.PARA2_NAME ,
          C.PRODUCT_CODE ,C.QUANTITY,
          A.MEMO_ID
   FROM PRD_WO_CNC_MST A
   JOIN PRD_WO_CNC_DET B ON A.MEMO_ID =B.MEMO_ID 
   JOIN PRD_WO_MST WO ON WO.MEMO_ID =B.REF_WO_ID 
   JOIN ARTICLE ART ON ART.ARTICLE_CODE =B.ARTICLE_CODE
   JOIN LM01106 LM ON LM.AC_CODE =WO.AC_CODE
   JOIN PARA1 P1 ON P1.PARA1_CODE =B.PARA1_CODE 
   JOIN PARA2 P2 ON P2.PARA2_CODE =B.PARA2_CODE 
   LEFT JOIN PRD_WO_CNC_UPC C ON B.ROW_ID =C.REF_ROW_ID
