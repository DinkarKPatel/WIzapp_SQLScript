

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_DN_NEWCOLS' AND VALUE=1)
BEGIN
	PRINT 'UPDATE NEW COLUMNS INSERTED IN RMD01106'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_DN_NEWCOLS')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_DN_NEWCOLS','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_DN_NEWCOLS'
	
	UPDATE RMD01106 SET INV_RATE=PURCHASE_PRICE,RATE=PURCHASE_PRICE
END		
