

IF NOT EXISTS (SELECT TOP 1 XN_TYPE FROM GST_ACCOUNTS_CONFIG_DET_OTHERS)
BEGIN
	INSERT GST_ACCOUNTS_CONFIG_DET_OTHERS	( XN_TYPE, COLUMNNAME, COLUMNDESC, VALUE )  
	SELECT  XN_TYPE, COLUMNNAME, COLUMNDESC, ISNULL(VALUE,'0000000000') AS VALUE FROM GST_ACCOUNTS_CONFIG_DET_1
	WHERE COLUMNNAME NOT IN ('OTHER_CHARGES_AC_CODE','FREIGHT_AC_CODE','INSURANCE_AC_CODE','CASH_AC_CODE','CHEQUE_AC_CODE',
	'MISC_AC_CODE','CUSTOMER_CONTROL_AC_CODE_SALE','CUSTOMER_CONTROL_AC_CODE_CN','WIZCLIP_GVS_AC_CODE') AND XN_TYPE<>'IWS'
	AND COLUMNDESC NOT IN ('A/C','BANK A/C','COMMISSION A/C')
	AND RIGHT(XN_TYPE,4) NOT IN ('_AST','_CON','_SRV')
END
