CREATE PROCEDURE SP3S_VALIDATEXN_DNPSMERGE
@cSpId VARCHAR(50),
@cMemoId VARCHAR(40),
@cErrormsg VARCHAR(MAX) OUTPUT
AS
BEGIN
	DECLARE @NNETAMOUNT NUMERIC(10,2),@NCMMSUBTOTAL NUMERIC(10,2),@NPAYMODETOTAMT NUMERIC(10,2),
	@nTotDnpmQty NUMERIC(10,2),@nTotDnpdQty NUMERIC(10,2),@cStep VARCHAR(4),@nPaymode NUMERIC(1,0)

	
	SET @CSTEP=10.8
	EXEC SP_CHKXNSAVELOG 'DNPSMERGE',@CSTEP,0,@CSPID,'',1


	SELECT @nTotDnpmQty=total_quantity FROM dnps_mst (NOLOCK) WHERE ps_id=@cMemoId

	SET @CSTEP=11.2
	EXEC SP_CHKXNSAVELOG 'DNPSMERGE',@CSTEP,0,@CSPID,'',1
		
	SELECT @nTotDnpdQty = SUM(quantity) FROM dnps_det (NOLOCK) WHERE ps_id=@cMemoId 

	SET @CSTEP=11.4
	EXEC SP_CHKXNSAVELOG 'DNPSMERGE',@CSTEP,0,@CSPID,'',1		  
	SELECT @nTotDnpmQty=ISNULL(@nTotDnpmQty,0),@nTotDnpdQty = ISNULL(@nTotDnpdQty,0)  
	
	IF @nTotDnpmQty<>@nTotDnpdQty
	BEGIN  
		PRINT 'SP3S_VALIDATEXN_DNPSMERGE Quantity match FAILED'
		SET @CERRORMSG='Bill level Total quantity '+STR(@nTotDnpmQty,14,2)+' SHOULD BE EQUAL TO THE SUM OF Item level quantity '+STR(@nTotDnpdQty,10,2)+'...PLEASE CHECK'  
		GOTO END_PROC
	END

END_PROC:
	
END


