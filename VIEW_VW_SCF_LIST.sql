CREATE VIEW VW_SCF_LIST  

AS    
SELECT A.MEMO_NO,A.MEMO_ID, A.MEMO_DT,MODE,
(CASE WHEN A.CANCELLED=1 THEN 'CANCELLED' ELSE '' END) AS CANCELLED,
(CASE WHEN A.CANCELLED=0 THEN MM.CONSUME_QTY ELSE 0 END) AS CONSUME_QTY ,A.FIN_YEAR   ,A.location_Code           
FROM SCM01106 A (NOLOCK) 
 LEFT OUTER JOIN(SELECT MEMO_ID, SUM(QUANTITY)+SUM(ADJ_QUANTITY) AS CONSUME_QTY FROM  
 SCC01106 GROUP BY MEMO_ID)MM  ON A.MEMO_ID = MM.MEMO_ID
