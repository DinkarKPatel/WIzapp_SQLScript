if exists (select top 1 'u' from INFORMATION_SCHEMA .COLUMNS where column_name ='lattr10_key_name' and TABLE_NAME ='LOC_NAMES')
alter table LOC_NAMES drop column lattr10_key_name


IF OBJECT_ID ('TEMPDB..#TMPATTRCOL','U') IS NOT NULL
   DROP TABLE #TMPATTRCOL
SELECT COLUMN_NAME INTO #TMPATTRCOL  FROM INFORMATION_SCHEMA .COLUMNS WHERE LEFT(COLUMN_NAME,4) ='ATTR' AND TABLE_NAME ='LOC_NAMES'
DECLARE @CCOLNAME VARCHAR(100),@DTSQL NVARCHAR(MAX)

WHILE EXISTS (SELECT TOP 1'U' FROM #TMPATTRCOL)
begin
    
	SELECT TOP 1 @CCOLNAME=COLUMN_NAME FROM #TMPATTRCOL
	IF ISNULL(@CCOLNAME,'')<>''
	BEGIN

		SET @DTSQL='ALTER TABLE LOC_NAMES DROP COLUMN '+@CCOLNAME
		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL

	END


	DELETE FROM #TMPATTRCOL WHERE COLUMN_NAME =@CCOLNAME
end