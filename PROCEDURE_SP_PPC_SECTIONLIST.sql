CREATE PROCEDURE SP_PPC_SECTIONLIST
(
 @NQUERYID INT=0
)
AS
BEGIN

   IF @NQUERYID=1
       GOTO LBLSECTION
   ELSE IF @NQUERYID=2
       GOTO LBLSUBSECTION
    ELSE IF @NQUERYID=3
       GOTO LBLJOBS
   ELSE
      GOTO END_PROC
    
    
    LBLSECTION:
       
       
       SELECT DISTINCT  SECTION_CODE ,SECTION_NAME  
       FROM SECTIONM 
    
    GOTO END_PROC
    
    
    LBLSUBSECTION:
    
       SELECT DISTINCT SUB_SECTION_CODE ,SUB_SECTION_NAME 
       FROM SECTIOND 
    
    
    GOTO END_PROC
   
   
     LBLJOBS:
    
       SELECT DISTINCT JOB_CODE ,JOB_NAME 
       FROM JOBS  
    
    
    GOTO END_PROC
   
   END_PROC:
END
