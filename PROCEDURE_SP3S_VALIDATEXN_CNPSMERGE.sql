CREATE PROCEDURE SP3S_VALIDATEXN_CNPSMERGE
@cSpId VARCHAR(50),
@cMemoId VARCHAR(40),
@cErrormsg VARCHAR(MAX) OUTPUT
AS
BEGIN
	DECLARE @NNETAMOUNT NUMERIC(10,2),@NCMMSUBTOTAL NUMERIC(10,2),@NPAYMODETOTAMT NUMERIC(10,2),
	@nTotCNPmQty NUMERIC(10,2),@nTotCNPdQty NUMERIC(10,2),@cStep VARCHAR(4),@nPaymode NUMERIC(1,0)

	
	SET @CSTEP=10.8
	EXEC SP_CHKXNSAVELOG 'CNPSMERGE',@CSTEP,0,@CSPID,'',1


	SELECT @nTotCNPmQty=total_quantity FROM CNPs_mst (NOLOCK) WHERE ps_id=@cMemoId

	SET @CSTEP=11.2
	EXEC SP_CHKXNSAVELOG 'CNPSMERGE',@CSTEP,0,@CSPID,'',1
		
	SELECT @nTotCNPdQty = SUM(quantity) FROM CNPs_det (NOLOCK) WHERE ps_id=@cMemoId 

	SET @CSTEP=11.4
	EXEC SP_CHKXNSAVELOG 'CNPSMERGE',@CSTEP,0,@CSPID,'',1		  
	SELECT @nTotCNPmQty=ISNULL(@nTotCNPmQty,0),@nTotCNPdQty = ISNULL(@nTotCNPdQty,0)  
	
	IF @nTotCNPmQty<>@nTotCNPdQty
	BEGIN  
		PRINT 'SP3S_VALIDATEXN_CNPSMERGE Quantity match FAILED'
		SET @CERRORMSG='Bill level Total quantity '+STR(@nTotCNPmQty,14,2)+' SHOULD BE EQUAL TO THE SUM OF Item level quantity '+STR(@nTotCNPdQty,10,2)+'...PLEASE CHECK'  
		GOTO END_PROC
	END

END_PROC:
	
END


