CREATE FUNCTION FN_YEARMONTHTOSTR
(
	@NYEAR INT, @NMONTH INT
)
RETURNS VARCHAR(20)
--WITH ENCRYPTION
AS
BEGIN
	DECLARE @CRETVAL VARCHAR(20)
	SET @CRETVAL = ''

	SET @CRETVAL = CONVERT(VARCHAR(4), @NYEAR) + (CASE WHEN @NMONTH<=9 THEN '0' ELSE '' END) + CONVERT(VARCHAR(2),@NMONTH)
	
	RETURN @CRETVAL
END
