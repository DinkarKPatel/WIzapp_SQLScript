CREATE PROCEDURE GETPMTQTY    
(    
 @CMEMOID VARCHAR (12),    
 @CREPID CHAR(10),    
 @CDEPTID CHAR(4)    
) 
--WITH ENCRYPTION
   
AS    
BEGIN    
 SELECT 0 AS QUANTITY_IN_STOCK,    
   ISNULL(SUM(A.QUANTITY),0) AS SCANNED_QTY,    
   0 AS SHORTAGE_QTY,    
   0 AS EXCESS_QTY    
 FROM STLD01106 A     
 JOIN STLM01106 B ON A.LOT_NO = B.LOT_NO    
 WHERE B.MEMO_ID = @CMEMOID    
    

END
