CREATE PROCEDURE SP3S_VALIDATEXN_POMERGE
@cSpId VARCHAR(50),
@cMemoId VARCHAR(40),
@cErrormsg VARCHAR(MAX) OUTPUT
AS
BEGIN
	DECLARE @NNETAMOUNT NUMERIC(10,2),@NCMMSUBTOTAL NUMERIC(10,2),@NPAYMODETOTAMT NUMERIC(10,2),
	@ntotPomQty NUMERIC(10,2),@ntotPodQty NUMERIC(10,2),@cStep VARCHAR(4),@nPaymode NUMERIC(1,0)

	
	SET @CSTEP=10.8
	EXEC SP_CHKXNSAVELOG 'POMERGE',@CSTEP,0,@CSPID,'',1


	SELECT @ntotPomQty=total_quantity FROM rmm01106 (NOLOCK) WHERE rm_id=@cMemoId

	SET @CSTEP=11.2
	EXEC SP_CHKXNSAVELOG 'POMERGE',@CSTEP,0,@CSPID,'',1
		
	SELECT @ntotPodQty = SUM(quantity) FROM rmd01106 (NOLOCK) WHERE rm_id=@cMemoId 

	SET @CSTEP=11.4
	EXEC SP_CHKXNSAVELOG 'POMERGE',@CSTEP,0,@CSPID,'',1		  
	SELECT @ntotPomQty=ISNULL(@ntotPomQty,0),@ntotPodQty = ISNULL(@ntotPodQty,0)  
	
	IF @ntotPomQty<>@ntotPodQty
	BEGIN  
		PRINT 'SP3S_VALIDATEXN_POMERGE Quantity match FAILED'
		SET @CERRORMSG='Bill level Total quantity '+STR(@ntotPomQty,14,2)+' SHOULD BE EQUAL TO THE SUM OF Item level quantity '+STR(@ntotPodQty,10,2)+'...PLEASE CHECK'  
		GOTO END_PROC
	END

END_PROC:
	
END