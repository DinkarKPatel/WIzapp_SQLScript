CREATE PROCEDURE SP_SYNCH_MIRRORDATA_DSM_SINGLE
(
    @CLOCID VARCHAR(3)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		208_2014_01_13 : THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
						 TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.					 
		NOTE : EMPLOYEE_HEAD WILL BE MERGED TO EMPLOYEE				 
	*/

DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@CFILTERCONDITION2 VARCHAR(500),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CMIRRORINGENABLED VARCHAR(5)
	   ,@CCURDEPTID VARCHAR(5),@CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE1 VARCHAR(200)
	   ,@CTEMPDETAILTABLE2 VARCHAR(200),@CTEMPDETAILTABLE3 VARCHAR(200)
	   ,@CTEMPPAYMODETABLE VARCHAR(200),@CTEMPTABLE VARCHAR(100),@CDONOTRESETPOSTEDINAC VARCHAR(1)
	   ,@BADDMODE BIT,@CCUTOFFDATE VARCHAR(20),@CWHERECLAUSE VARCHAR(2000),@CMEMONOLEN VARCHAR(5),@NMEMONOLEN INT
	   ,@DMINMEMODT DATETIME,@DMAXMEMODT DATETIME,@CFINYEAR VARCHAR(5),@CMEMOPREFIX VARCHAR(10)
	   ,@CMINMEMONO VARCHAR(20),@CMAXMEMONO VARCHAR(20),@CSOURCEDB VARCHAR(200),@CMERGEDB VARCHAR(200)
	   ,@BMSTINSERTONLY BIT,@BSERIESMISMATCHFOUND BIT,@CMEMOID VARCHAR(40),@CPREVMEMONO VARCHAR(20)
	   ,@NPREVMEMONO NUMERIC(5,0),@NLENVALUE INT,@CPREVMEMONOSEARCH VARCHAR(20),@CMEMOIDSEARCH VARCHAR(40)
	   ,@CRETPRODUCTCODE VARCHAR(50),@NVERSIONNO INT,@CJOINSTR VARCHAR(MAX),@CCUSTCODE CHAR(12)
	   ,@CSEARCHCUSTCODE CHAR(12),@CREQMEMOID VARCHAR(40),@CREQCUSTCODE CHAR(12),@BCUSTREQINSERTED BIT
	   ,@CMEMONO VARCHAR(15)
	

BEGIN TRY
	
	
	SET @CSTEP=10

	SELECT @CSOURCEDB='',@CMERGEDB='',@BMSTINSERTONLY=1,@BSERIESMISMATCHFOUND=0
	
	SET @CSTEP=15
    	
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'
    
	SET @CSTEP=20
	SET @NMEMONOLEN=10


	SELECT @CTEMPMASTERTABLE=@CSOURCEDB+'DSM_DSM01106_MIRROR',
		   @CTEMPDETAILTABLE1=@CSOURCEDB+'DSM_DSD01106_MIRROR'
		  
LBLSTART:

	BEGIN TRANSACTION	

LBLNEXTDSM:    
    SELECT @CMEMOID='',@NVERSIONNO=0,@CCUSTCODE='',@CREQMEMOID='',@CSEARCHCUSTCODE=''

	DECLARE @DFREEZINGDATE DATETIME	
	EXEC SP3S_GETDATA_FREEZING_DATE 'DSM',@DFREEZINGDATE OUTPUT
	
    SELECT TOP 1 @CMEMOID = B.DS_ID ,@CMEMONO=B.DS_NO,@NVERSIONNO=VERSION_NO
    FROM DSM_DSM01106_MIRROR B (NOLOCK)
    LEFT OUTER JOIN MIRROR_SYNCH_LOG C (NOLOCK) ON C.MEMO_ID=B.DS_ID AND C.XN_TYPE='DSM'
	WHERE LEFT(B.DS_ID,2)=@CLOCID AND DATEDIFF(SS,B.VERSION_LAST_UPDATE,GETDATE())>60 
	AND C.MEMO_ID IS NULL 
	  ORDER BY B.DS_ID,VERSION_NO DESC
	   

    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
        
       SET @CFILTERCONDITION = 'B.DS_ID='''+@CMEMOID+'''AND B.VERSION_NO='+LTRIM(RTRIM(STR(@NVERSIONNO)))

	
	
	--SET @CMEMOPREFIX= LEFT(@CMEMONO,2)
	
	--SET @NLENVALUE=@NMEMONOLEN-LEN(@CMEMOPREFIX)
	
	--SET @NPREVMEMONO=CONVERT(NUMERIC(5,0),RIGHT(@CMEMOID,@NMEMONOLEN-LEN(@CMEMOPREFIX)))
	
	--IF @NPREVMEMONO>1
	--BEGIN
	--	SET @NPREVMEMONO=@NPREVMEMONO-1
		
	--	SELECT @CFINYEAR=FIN_YEAR FROM DSM_DSM01106_MIRROR (NOLOCK) WHERE DS_ID=@CMEMOID
		
	--	SET @CPREVMEMONO=@CMEMOPREFIX+REPLICATE('0',@NLENVALUE-LEN(LTRIM(RTRIM(STR(@NPREVMEMONO)))))+LTRIM(RTRIM(STR(@NPREVMEMONO)))
		
	--	SET @CPREVMEMONOSEARCH=''
		
	--	SELECT TOP 1 @CPREVMEMONOSEARCH=DS_ID FROM DSM01106 (NOLOCK) WHERE DS_NO=@CPREVMEMONO AND FIN_YEAR=@CFINYEAR
		
	--	IF ISNULL(@CPREVMEMONOSEARCH,'')=''
	--	BEGIN
	--		SET @CERRMSG='PREVIOUS MEMO NO. :'+@CPREVMEMONO+' NOT FOUND...CANNOT MERGE'
	--		SET @BSERIESMISMATCHFOUND=1
	--		GOTO EXIT_PROC
	--	END	
	--END
	
	SET @CSTEP=110
	SET @CMEMOIDSEARCH=''
	SELECT TOP 1 @CMEMOIDSEARCH=A.DS_ID FROM DSM01106 A (NOLOCK) WHERE A.DS_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1

LBLMERGE:
	---DELETING EXISTING RECORD IF BILL COMES AGAIN FOR MERGING
	
	--SELECT @CTEMPMASTERTABLE,@CFILTERCONDITION
	IF @BADDMODE=0
		PRINT 'ADDMODE:N'
	ELSE
		PRINT 'ADDMODE:Y'	
	
	IF @BADDMODE=0
	BEGIN	
		SET @CSTEP=115
		SET @DTSQL=N'DELETE A FROM DSD01106 A JOIN '+@CTEMPMASTERTABLE+' B (NOLOCK) ON A.DS_ID=B.DS_ID
					 WHERE '+@CFILTERCONDITION
		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
	
	END
	
	---UPDATING TRANSACTION TABLES
	SET @CSTEP=250
	SET @CTABLENAME='DSM01106'
	SET @CTMP_TABLENAME='DSM_DSM01106_MIRROR'
	SET @CKEYFIELD='DS_ID'
	
	SET @CSTEP=260
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 
							  
	SET @CTABLENAME='DSD01106'
	SET @CTMP_TABLENAME='DSM_DSD01106_MIRROR'
	SET @CKEYFIELD='DS_ID'
	 
	SET @CSTEP=280
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 

    SET @CSTEP=330
    
    SELECT TOP 1 @CERRMSG=ERRMSG FROM MIRROR_SYNCH_LOG (NOLOCK) WHERE XN_TYPE='DSM' AND MEMO_ID=@CMEMOID
    
    IF ISNULL(@CERRMSG,'')=''
    BEGIN 
		SET @CSTEP=340
		DELETE FROM DSM_DSD01106_MIRROR WHERE DS_ID=@CMEMOID AND VERSION_NO<=@NVERSIONNO
		DELETE FROM DSM_DSM01106_MIRROR WHERE DS_ID=@CMEMOID AND VERSION_NO<=@NVERSIONNO
	END   

	IF ISNULL(@CERRMSG,'')=''
		COMMIT
	ELSE
		GOTO EXIT_PROC

    GOTO LBLSTART

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_SYNCH_MIRRORDATA_DSM_SINGLE, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO EXIT_PROC
END CATCH

EXIT_PROC:

	IF @@TRANCOUNT>0
	BEGIN
		IF (ISNULL(@CERRMSG,'')='') OR (ISNULL(@CERRMSG,'')<>'' AND ISNULL(@BCUSTREQINSERTED,0)=1) 
			COMMIT
		ELSE
			ROLLBACK
    END
	
END	
---END OF PROCEDURE - SP_SYNCH_MIRRORDATA_DSM_SINGLE
