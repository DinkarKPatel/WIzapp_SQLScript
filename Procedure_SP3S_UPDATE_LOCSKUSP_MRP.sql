CREATE PROCEDURE SP3S_UPDATE_LOCSKUSP_MRP
as
begin
     

	   DECLARE @CURDEPTID VARCHAR(5),@CHODEPTID VARCHAR(5)

	   SELECT @CURDEPTID=VALUE  FROM CONFIG WHERE CONFIG_OPTION ='LOCATION_ID'
	   SELECT @CHODEPTID=VALUE  FROM CONFIG WHERE CONFIG_OPTION ='HO_LOCATION_ID'

	 
		IF EXISTS (SELECT TOP 1 'U' FROM LOCSKUSP_CURRENT (NOLOCK) ) AND ISNULL(@CURDEPTID,'')<>ISNULL(@CHODEPTID,'')
		BEGIN
		     

			 UPDATE A SET  MRP=B.LOC_MRP
			 FROM SKU A (NOLOCK)
			 JOIN LOCSKUSP_CURRENT B (NOLOCK) ON A.product_code =B.PRODUCT_CODE 
			 WHERE ISNULL(A.MRP,0)<>ISNULL(B.LOC_MRP,0)


		END

end

