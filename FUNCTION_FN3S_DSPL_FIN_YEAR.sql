CREATE FUNCTION FN3S_DSPL_FIN_YEAR(@CFIN_YEAR VARCHAR(5))	

RETURNS VARCHAR(7)
--WITH ENCRYPTION
AS
BEGIN
	DECLARE  @DSTARTDT DATETIME,@CSTARTDT VARCHAR(20),@DENDDT DATETIME,@CENDDT VARCHAR(20)
			,@OUTPUT VARCHAR(7)
	
	IF @CFIN_YEAR<>''
	BEGIN
	SELECT @DSTARTDT=DBO.FN_GETFINYEARDATE(@CFIN_YEAR,1)
		  ,@DENDDT=DBO.FN_GETFINYEARDATE(@CFIN_YEAR,2)
	SELECT @CSTARTDT=CONVERT(VARCHAR,@DSTARTDT,110),@CENDDT=CONVERT(VARCHAR,@DENDDT,110)
	SELECT @OUTPUT=RIGHT(@CSTARTDT,4)+'-'+RIGHT(@CENDDT,2)
	END
RETURN @OUTPUT
END
---END OF FUNCTION - FN3S_DSPL_FIN_YEAR
