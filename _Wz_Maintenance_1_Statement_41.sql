


--**** UPDATING NEWLY INSERTED INVOICE QTY COLUMN IN PID01106 WITH QUANTITY COLUMN
IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_LMP01106_PARTY_RATE_MEMO_ID' AND VALUE=1)
BEGIN
    
      DECLARE @CCMD NVARCHAR(MAX)

	SET @CCMD=N'UPDATE A SET PARTY_RATE_MEMO_ID= (SELECT TOP 1 MEMO_ID FROM PARTY_RATE_MST C 
	WHERE C.AC_CODE=A.AC_CODE AND PARTY_RATE_TYPE=1 ORDER BY C.LAST_UPDATE DESC)
	FROM LMP01106 A'
	EXEC SP_EXECUTESQL @CCMD
			
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_LMP01106_PARTY_RATE_MEMO_ID')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_LMP01106_PARTY_RATE_MEMO_ID',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_LMP01106_PARTY_RATE_MEMO_ID'
END		
