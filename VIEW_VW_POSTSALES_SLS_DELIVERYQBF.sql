create VIEW  VW_POSTSALES_SLS_DELIVERYQBF 

AS
	SELECT   A.*,CONVERT(NVARCHAR,A.MEMO_DT,105) AS MST_MEMO_DT,A.MEMO_ID AS MST_MEMO_ID,
	A.MEMO_NO AS MST_MEMO_NO,A.DELIVERED_TO AS MST_DELIVERED_TO, C.PRODUCT_CODE AS REF_PRODUCT_CODE,B.PRODUCT_CODE 
	,J.MEMO_NO AS HBD_MEMO_NO,CUST.USER_CUSTOMER_CODE, D.CM_NO, 
	(CUST.CUSTOMER_FNAME+' '+ CUST.CUSTOMER_LNAME ) AS CUSTOMER_NAME,CUST.USER_CUSTOMER_CODE AS MST_USER_CUSTOMER_CODE,
	ART.ARTICLE_NO,P1.PARA1_NAME,P2.PARA2_NAME,P3.PARA3_NAME,
	P4.PARA4_NAME,P5.PARA5_NAME,P6.PARA6_NAME,UOM.UOM_NAME,ART.ARTICLE_NAME,ART.ALIAS AS ARTICLE_ALIAS,
	SD.SUB_SECTION_NAME,C.MRP,SM.SECTION_NAME,JOB.JOB_NAME,EMP.EMP_NAME,C.QUANTITY 
	FROM SLS_DELIVERY_MST A (NOLOCK)
	JOIN SLS_DELIVERY_DET B (NOLOCK) ON A.MEMO_ID = B.MEMO_ID 
	JOIN HOLD_BACK_DELIVER_DET H (NOLOCK) ON H.ROW_ID = B.REF_HBD_ROW_ID  
	JOIN HOLD_BACK_DELIVER_MST J (NOLOCK) ON J.MEMO_ID = H.MEMO_ID 
	JOIN CMD01106 C (NOLOCK) ON C.ROW_ID = H.REF_CMD_ROW_ID
	JOIN SKU (NOLOCK) ON SKU.PRODUCT_CODE=C.PRODUCT_CODE      
	JOIN ARTICLE ART (NOLOCK) ON SKU.ARTICLE_CODE = ART.ARTICLE_CODE          
	JOIN SECTIOND SD (NOLOCK) ON ART.SUB_SECTION_CODE = SD.SUB_SECTION_CODE        
	JOIN SECTIONM SM (NOLOCK) ON SD.SECTION_CODE = SM.SECTION_CODE        
	JOIN PARA1 P1 (NOLOCK) ON SKU.PARA1_CODE = P1.PARA1_CODE          
	JOIN PARA2 P2 (NOLOCK) ON SKU.PARA2_CODE = P2.PARA2_CODE          
	JOIN PARA3 P3 (NOLOCK)ON SKU.PARA3_CODE = P3.PARA3_CODE          
	JOIN PARA4 P4 (NOLOCK)ON SKU.PARA4_CODE = P4.PARA4_CODE          
	JOIN PARA5 P5 (NOLOCK)ON SKU.PARA5_CODE = P5.PARA5_CODE          
	JOIN PARA6 P6 (NOLOCK)ON SKU.PARA6_CODE = P6.PARA6_CODE          
	JOIN UOM UOM (NOLOCK)ON ART.UOM_CODE = UOM.UOM_CODE   
	JOIN CMM01106 D (NOLOCK) ON D.CM_ID = C.CM_ID
	LEFT OUTER JOIN  CUSTDYM CUST (NOLOCK) ON CUST.CUSTOMER_CODE = A.CUSTOMER_CODE
	LEFT OUTER JOIN EMPLOYEE EMP (NOLOCK)ON C.EMP_CODE = EMP.EMP_CODE 
    LEFT OUTER JOIN JOBS JOB (NOLOCK) ON JOB.JOB_CODE =H.JOB_CODE
