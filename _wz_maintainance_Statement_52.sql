


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='LOCSKUSP_CURRENT' AND VALUE='1')
BEGIN
		
	INSERT INTO LOCSKUSP_CURRENT (PRODUCT_CODE,DEPT_ID,LOC_MRP)
	SELECT PRODUCT_CODE,DEPT_ID,MRP  FROM LOCSKUSP LL
	WHERE TS = (SELECT TOP 1 TS FROM LOCSKUSP PP WHERE PP.PRODUCT_CODE  = LL.PRODUCT_CODE 
	AND PP.DEPT_ID = LL.DEPT_ID 
	ORDER BY FROM_DT DESC  )
	


	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='LOCSKUSP_CURRENT')
       INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
	   VALUES ('LOCSKUSP_CURRENT','1','',GETDATE())	
	ELSE	
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='LOCSKUSP_CURRENT'
	
END
