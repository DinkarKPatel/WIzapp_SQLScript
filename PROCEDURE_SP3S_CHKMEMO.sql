CREATE  PROC SP3S_CHKMEMO
 (
 @cXnType Varchar(10),
 @cMemoid varchar(50)
 )
 As 
 Begin

 IF @CXNTYPE = 'PUR' 
    SELECT TOP 1 MRR_ID FROM PIM01106 (NOLOCK) WHERE MRR_ID= @CMEMOID
 ELSE IF @CXNTYPE = 'PRT' 
    SELECT TOP 1 RM_ID FROM RMM01106 (NOLOCK) WHERE RM_ID= @CMEMOID
ELSE IF @CXNTYPE = 'WSL' 
    SELECT TOP 1 INV_ID FROM INM01106 (NOLOCK) WHERE INV_ID= @CMEMOID
ELSE IF @CXNTYPE = 'WSR' 
    SELECT TOP 1 CN_ID FROM CNM01106 (NOLOCK) WHERE CN_ID= @CMEMOID
ELSE IF @CXNTYPE = 'PO' 
    SELECT TOP 1 PO_ID FROM POM01106 (NOLOCK) WHERE PO_ID= @CMEMOID
ELSE IF @CXNTYPE = 'irr' 
    SELECT TOP 1 irm_memo_id FROM irm01106 (NOLOCK) WHERE irm_memo_id= @CMEMOID
ELSE IF @cXnType ='WPS'
     SELECT TOP 1 PS_ID FROM WPS_MST (NOLOCK) WHERE PS_ID=@cMemoid 
ELSE IF @cXnType ='RPS'
    SELECT TOP 1 cm_id  FROM RPS_MST (NOLOCK) WHERE CM_ID=@cMemoid 
ELSE IF @cXnType ='arc'
    SELECT TOP 1 adv_rec_id FROM arc01106 (NOLOCK) WHERE adv_rec_id=@cMemoid 
ELSE IF @cXnType ='SNC'
    SELECT TOP 1 MEMO_ID FROM SNC_MST (NOLOCK) WHERE MEMO_ID=@cMemoid 
ELSE IF @cXnType ='SCF'
    SELECT TOP 1 MEMO_ID FROM SCM01106 (NOLOCK) WHERE MEMO_ID=@cMemoid 
ELSE IF @cXnType ='APP'
    SELECT TOP 1 MEMO_ID FROM APM01106 (NOLOCK) WHERE MEMO_ID=@cMemoid 
ELSE IF @cXnType ='APR'
    SELECT TOP 1 MEMO_ID FROM APPROVAL_RETURN_MST (NOLOCK) WHERE MEMO_ID=@cMemoid 
ELSE IF @cXnType ='SHF'
    SELECT TOP 1 SHIFT_ID FROM TILL_SHIFT_MST (NOLOCK) WHERE SHIFT_ID=@cMemoid 
ELSE IF @cXnType ='XNSLM'
    SELECT TOP 1 AC_CODE FROM LMP01106 (NOLOCK) WHERE AC_CODE =@cMemoid 
ELSE IF @cXnType ='XNSDTM'
    SELECT TOP 1 DT_CODE FROM DTM (NOLOCK) WHERE DT_CODE =@cMemoid
ELSE IF @cXnType ='PSHBD'
    SELECT TOP 1 MEMO_ID FROM HOLD_BACK_DELIVER_MST (NOLOCK) WHERE MEMO_ID =@cMemoid
ELSE IF @cXnType ='PSJWI'
    SELECT TOP 1 ISSUE_ID FROM POST_SALES_JOBWORK_ISSUE_MST (NOLOCK) WHERE ISSUE_ID =@cMemoid
ELSE IF @cXnType ='PSJWR'
    SELECT TOP 1 RECEIPT_ID FROM POST_SALES_JOBWORK_RECEIPT_MST (NOLOCK) WHERE RECEIPT_ID =@cMemoid
ELSE IF @cXnType ='PSdlV'
    SELECT TOP 1 MEMO_ID FROM SLS_DELIVERY_MST (NOLOCK) WHERE MEMO_ID =@cMemoid
ELSE IF @cXnType ='TEX'
    SELECT TOP 1 MEMO_ID FROM TILL_EXPENSE_MST (NOLOCK) WHERE MEMO_ID =@cMemoid
ELSE IF @cXnType ='dnps'
    SELECT TOP 1 ps_id FROM dnps_mst  (NOLOCK) WHERE ps_id =@cMemoid
ELSE IF @cXnType ='TLF'
    SELECT TOP 1 MEMO_ID FROM TILL_LIFTS  (NOLOCK) WHERE MEMO_ID =@cMemoid
ELSE IF @cXnType ='DEND'
    SELECT TOP 1 Log_Date FROM DAYCLOSE_LOG  (NOLOCK) WHERE LOG_DATE=@CMEMOID
ELSE IF @cXnType ='pco'
    SELECT TOP 1 MEMO_ID FROM pco_mst  (NOLOCK) WHERE MEMO_ID =@cMemoid
ELSE IF @cXnType ='pci'
    SELECT TOP 1 MEMO_ID FROM pci_mst  (NOLOCK) WHERE MEMO_ID =@cMemoid
ELSE IF @cXnType ='BCO'
    SELECT TOP 1 MEMO_ID FROM FLOOR_ST_MST  (NOLOCK) WHERE MEMO_ID =@cMemoid
ELSE IF @cXnType ='SLS'
    SELECT TOP 1 CM_ID FROM CMM01106  (NOLOCK) WHERE CM_ID =@cMemoid
  Else 
	select '1' 
	
 End 


