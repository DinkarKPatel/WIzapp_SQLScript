create PROCEDURE DBO.SP_MERGE_MIRROR_MSTPAYMODE
  ( @NMODE INT
  ,@XN_TYPE VARCHAR(50) ='MSTPAYMODE'
  )
AS
 BEGIN
       DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
              ,@CTEMPLOCTABLE VARCHAR(100),@CSTATECODEOUT CHAR(7),@CSTATECODE VARCHAR(10)
              ,@CCURDEPTID VARCHAR(10),@CHODEPTID VARCHAR(10),@BHOLOC BIT,@NLOCTYPE VARCHAR(50)
              ,@BPURLOC VARCHAR(10),@CTABLENAME VARCHAR(100),@CTMP_TABLENAME VARCHAR(100)
              ,@CKEYFIELD1 VARCHAR(100),@CTEMPLOCSSTTABLE VARCHAR(500),@CTEMPTABLE VARCHAR(500)
              ,@CTEMPLOCSSTMSTTABLE VARCHAR(500),@BPROCEED BIT,@CKEYFIELD2 VARCHAR(500),@CKEYFIELD3 VARCHAR(500) 
	          ,@CSOURCETABLE VARCHAR(1000),@NXNSMERGINGORDER INT,@CSOURCEDB VARCHAR(200),@CERRORMSG VARCHAR(MAX)	
BEGIN TRY
	  SELECT @CSOURCEDB=''	
	  
	  --VALIDATE DATE 
	  IF ISNULL(@XN_TYPE,'')  = ''
	     BEGIN
	       SET @CERRORMSG ='XN_TYPE IS NOT NULL. PLEASE PASS XN TYPE INTO PARAMATER'
	       GOTO END_PROC;
	     END
      
      IF ISNULL(@NMODE,0) = 1
         GOTO END_PROC;
   	
	  BEGIN TRANSACTION
      
      SET @CSTEP = 00  
               
	  SELECT @CCURDEPTID = VALUE FROM CONFIG (NOLOCK) WHERE CONFIG_OPTION='LOCATION_ID'  
	  SELECT @CHODEPTID = VALUE FROM CONFIG (NOLOCK) WHERE CONFIG_OPTION='HO_LOCATION_ID'  
	  
	  IF @CCURDEPTID=@CHODEPTID
	  BEGIN
			SET @CERRORMSG='P:SP_MERGE_MIRROR_MSTPAYMODE, CANNOT CALL MERGING PROC AT HEAD OFFICE'
			GOTO END_PROC
  	  END					

	  SET @CSTEP = 05    
	    
	  SELECT @NLOCTYPE=LOC_TYPE,@BPURLOC=PUR_LOC FROM LOCATION WHERE DEPT_ID=@CCURDEPTID  
	       
           
	  SET @CSTEP = 10   

      SET @CTEMPTABLE=@CSOURCEDB+'MSTPAYMODE_LM01106_MIRROR'
	 
			SET @DTSQL=N'UPDATE '+@CTEMPTABLE+'  WITH (ROWLOCK) SET COMPANY_CODE=''01'''
			EXEC SP_EXECUTESQL @DTSQL
	
	 
	
	   SET @CKEYFIELD2 =''
	   SET @CKEYFIELD3 =''
	
	   --INSERT AND UPDATE DATA INTO LOC_STOCK_LEVEL_MST TABLE
	   SET @CSTEP = 60
	   SET @CTABLENAME = 'PAYMODE_GRP_MST'
	   SET @CSOURCETABLE ='MSTPAYMODE_PAYMODE_GRP_MST_MIRROR'
	   SET @CKEYFIELD1 ='PAYMODE_GRP_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   
	  
	   UPDATE A SET AC_CODE ='0000000000'
	   FROM MSTPAYMODE_PAYMODE_MST_MIRROR A (NOLOCK)
	   LEFT JOIN LM01106 B (NOLOCK) ON A.AC_CODE =B.AC_CODE 
	   WHERE B.AC_CODE IS NULL
	   
	   UPDATE A SET COMMISSION_AC_CODE ='0000000000'
	   FROM MSTPAYMODE_PAYMODE_MST_MIRROR A (NOLOCK)
	   LEFT JOIN LM01106 B (NOLOCK) ON A.COMMISSION_AC_CODE  =B.AC_CODE 
	   WHERE B.AC_CODE IS NULL

	 
        --INSERT AND UPDATE DATA INTO LOC_STOCK_LEVEL_DET TABLE
	   SET @CSTEP = 90
	   SET @CTABLENAME = 'PAYMODE_MST'
	   SET @CSOURCETABLE ='MSTPAYMODE_PAYMODE_MST_MIRROR'
	   SET @CKEYFIELD1 ='PAYMODE_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   

    GOTO END_PROC
END TRY

BEGIN CATCH
	SET @CERRORMSG='P:SP_MERGE_MIRROR_MSTPAYMODE, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO END_PROC
END CATCH

END_PROC:
	
	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRORMSG,'')='' 
			COMMIT
		ELSE
			ROLLBACK	
	END
	
	IF ISNULL(@CERRORMSG,'')='' 
	BEGIN   
	    
		DELETE FROM MSTPAYMODE_REGIONM_MIRROR
		DELETE FROM MSTPAYMODE_STATE_MIRROR
		DELETE FROM MSTPAYMODE_CITY_MIRROR
		DELETE FROM MSTPAYMODE_AREA_MIRROR
		DELETE FROM MSTPAYMODE_PAYMODE_GRP_MST_MIRROR
		DELETE FROM MSTPAYMODE_LM01106_MIRROR
		DELETE FROM MSTPAYMODE_LMP01106_MIRROR
		DELETE FROM MSTPAYMODE_HD01106_MIRROR
		DELETE FROM MSTPAYMODE_PAYMODE_MST_MIRROR
	   
	END
		
	SELECT @XN_TYPE AS MEMO_ID,@CERRORMSG AS ERRMSG   	  
END

--END PROCEDURE SP_MERGE_MIRROR_MSTPAYMODE
