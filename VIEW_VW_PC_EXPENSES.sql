CREATE VIEW  VW_PC_EXPENSES

AS

SELECT B.PEM_MEMO_ID,B.PEM_MEMO_NO ,B.PEM_MEMO_DT,B.CANCELLED,A.XN_AMOUNT,A.XN_TYPE,A.NARRATION ,
C.AC_NAME,B.location_code/*LEFT(A.PEM_MEMO_ID,2)*//*Rohit 04-11-2024*/ AS DEPT_ID
FROM PED01106 A
JOIN  PEM01106 B ON  A.PEM_MEMO_ID = B.PEM_MEMO_ID    
JOIN LMV01106 C (NOLOCK) ON  A.AC_CODE= C.AC_CODE
