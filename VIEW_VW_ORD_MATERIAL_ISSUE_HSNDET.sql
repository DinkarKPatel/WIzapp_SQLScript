CREATE VIEW VW_ORD_MATERIAL_ISSUE_HSNDET
AS      
		SELECT SKU.HSN_CODE,B.GST_PERCENTAGE,
		SUM(CONVERT(NUMERIC(14,2),(CASE WHEN B.IGST_AMOUNT>0 THEN (B.IGST_AMOUNT/B.QUANTITY) *B.QUANTITY ELSE 0 END))) AS IGST_AMOUNT,
		SUM(CONVERT(NUMERIC(14,2),(CASE WHEN B.SGST_AMOUNT>0 THEN (B.SGST_AMOUNT/B.QUANTITY) *B.QUANTITY ELSE 0 END))) AS SGST_AMOUNT, 	
		SUM(CONVERT(NUMERIC(14,2),(CASE WHEN B.CGST_AMOUNT>0 THEN (B.CGST_AMOUNT/B.QUANTITY) *B.QUANTITY ELSE 0 END))) AS CGST_AMOUNT, 
		SUM(CONVERT(NUMERIC(14,2),(CASE WHEN B.XN_VALUE_WITHOUT_GST>0 THEN (B.XN_VALUE_WITHOUT_GST/B.QUANTITY) *B.QUANTITY ELSE 0 END))) AS XN_VALUE_WITHOUT_GST,
		SUM(CONVERT(NUMERIC(14,2),(CASE WHEN B.XN_VALUE_WITH_GST>0 THEN (B.XN_VALUE_WITH_GST/B.QUANTITY) *B.QUANTITY ELSE 0 END))) AS XN_VALUE_WITH_GST,
		ISNULL(REF.REF_NO ,'') AS BUYER_REF_NO
		FROM BOM_ISSUE_MST A (NOLOCK)    
		JOIN BOM_ISSUE_DET B (NOLOCK) ON A.ISSUE_ID =B.ISSUE_ID  
		JOIN SKU SKU (NOLOCK) ON B.PRODUCT_CODE = SKU.PRODUCT_CODE
		--LEFT OUTER JOIN PID01106 PID (NOLOCK) ON B.PRODUCT_CODE=SKU.PRODUCT_CODE     
		JOIN ARTICLE C (NOLOCK) ON SKU.ARTICLE_CODE=C.ARTICLE_CODE    
		JOIN SECTIOND SD (NOLOCK) ON C.SUB_SECTION_CODE = SD.SUB_SECTION_CODE    
		JOIN SECTIONM SM (NOLOCK) ON SD.SECTION_CODE= SM.SECTION_CODE    
		LEFT OUTER JOIN PRD_AGENCY_MST AGN (NOLOCK) ON AGN.AGENCY_CODE=A.AGENCY_CODE  
		JOIN LM01106 LM (NOLOCK) ON AGN.AC_CODE=LM.AC_CODE  
		JOIN LMV01106 LMV ON LMV.AC_CODE=LM.AC_CODE  
		JOIN UOM U (NOLOCK) ON U.UOM_CODE=C.UOM_CODE
		LEFT JOIN
		(
		   SELECT A.ISSUE_ID ,B.REF_NO 
		   FROM JOBWORK_ISSUE_DET A
		   JOIN VW_BUYER_REF_NO B ON A.PRODUCT_CODE =B.PRODUCT_CODE
		   GROUP BY A.ISSUE_ID ,B.REF_NO 
		)  REF ON REF.ISSUE_ID =A.JOBWORK_ISSUE_ID 
--		LEFT OUTER JOIN JOBWORK_ISSUE_MST JIM (NOLOCK) ON JIM.ISSUE_ID=A.JOBWORK_ISSUE_ID     
		GROUP BY SKU.HSN_CODE,B.GST_PERCENTAGE,ISNULL(REF.REF_NO ,'')
