CREATE PROCEDURE SP_SEND_MIRROR_DOCPCO_DATA_OPT --(LocId 3 digit change only increased the parameter width by Sanjay:01-11-2024)
	 @CREQXNID VARCHAR(50)
	,@CTARGETLOCID VARCHAR(4)
	,@CERRMSG VARCHAR(1000) OUTPUT
AS
/*
	SP_SEND_MIRROR_DOCPCO_DATA : THIS PROCEDURE USE TO SEND DOCPCO DATA
*/
BEGIN
BEGIN TRY 		
	
	DECLARE @cStep VARCHAR(5)

	SET @CSTEP=10
	
	---- WE NEED TO SEND THESE EXTRA COLUMN BECAUSE OF MAPPING PROBLEM AT APPLICATION LEVEL
	---- WHILE TAKING PETTY CASH INWARDS AT TARGET LOCATION
	SELECT 	@CREQXNID AS XN_ID,'DOCPCO_PCO_MST_UPLOAD' AS TARGET_TABLENAME,
	CONVERT(VARCHAR(1000),'') AS RECEIVED_BY,CONVERT(DATETIME,'') AS RECEIPT_DT,
	CONVERT(VARCHAR(22),NULL) AS REFLIFTID,CONVERT(VARCHAR(15),NULL) AS SHIFT_ID,
	* FROM PCO_MST (NOLOCK) 
	WHERE memo_id=@CREQXNID
	

	
	GOTO END_PROC
	
	
END TRY

BEGIN CATCH
	SET @CERRMSG='P: SP_SEND_MIRROR_DOCPCO_DATA_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO END_PROC
END CATCH 		

END_PROC:
		
END
---END OF PROCEDURE - SP_SEND_MIRROR_DOCPCO_DATA_OPT