	SET @CSTEP=480
	SET @CTABLENAME='WSL_ORDER_BOM'
	SET @CTMP_TABLENAME='WSLORD_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ROW_ID'

	SET @CSTEP=490
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 


	DELETE FROM WSL_ORDER_ADV_RECEIPT WITH (ROWLOCK) WHERE ORDER_ID=@CMEMOID
	
	SET @CSTEP=492           
	SET @CTABLENAME='WSL_ORDER_ADV_RECEIPT'
	SET @CTMP_TABLENAME='WSLORD_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ORDER_ID'

	SET @CSTEP=495
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  							  
	SET @CSTEP=500
	SET @CTABLENAME='TDD01106'
	SET @CTMP_TABLENAME='WSLORD_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ROW_ID'

	SET @CSTEP=510
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

	
	

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP3S_SYNCH_UPLOADDATA_WSLORD_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK



		 DELETE   FROM WSLORD_ARTICLE_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_CUSTDYM_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_EMPLOYEE_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_PARA1_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_PARA2_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_PARA3_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_PARA4_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_PARA5_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_PARA6_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_TDD01106_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_WSL_ORDER_ADV_RECEIPT_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_WSL_ORDER_BOM_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_WSL_ORDER_DET_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE   FROM WSLORD_WSL_ORDER_MST_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID

END	
