	SET @CSTEP=460
	SET @CTABLENAME='WSL_ORDER_DET'
	SET @CTMP_TABLENAME='WSLORD_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
