	SET @CTMP_TABLENAME='WSLORD_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CTEMPTABLE=@CSOURCEDB+@CTMP_TABLENAME
	SET @CKEYFIELD='ORDER_ID'

	SET @DTSQL=N'DELETE A FROM '+@CMERGEDB+'['+@CTABLENAME+']  A WITH (ROWLOCK) 
	             LEFT OUTER JOIN '+@CTEMPTABLE+' B (NOLOCK) ON A.ROW_ID=B.ROW_ID  AND B.SP_ID='''+@CspId +'''
				 WHERE A.'+@CKEYFIELD+'='''+@CMEMOID+'''
