CREATE VIEW  VW_PRD_JOBWORKISSUE_QBF     
  
AS    
 SELECT   A.ISSUE_NO,B.*,  
 A.ISSUE_DT AS MST_ISSUE_DT,  
 A.ISSUE_ID AS MST_ISSUE_ID,  
 A.ISSUE_NO AS MST_ISSUE_NO,  
 A.ISSUE_TYPE AS MST_ISSUE_TYPE,    
 A.FIN_YEAR AS MST_FIN_YEAR,    
 M.AGENCY_NAME AS MST_AGENCY_NAME,A.REMARKS AS MST_REMARKS  
 FROM PRD_JOBWORK_ISSUE_DET B (NOLOCK)  
 JOIN PRD_JOBWORK_ISSUE_MST A (NOLOCK) ON B.ISSUE_ID = A.ISSUE_ID     
 JOIN PRD_AGENCY_MST M (NOLOCK) ON M.AGENCY_CODE = A.AGENCY_CODE    
 JOIN PRD_SKU SKU ON SKU.PRODUCT_CODE=B.PRODUCT_CODE      
 JOIN ARTICLE ART ON SKU.ARTICLE_CODE = ART.ARTICLE_CODE          
 JOIN SECTIOND SD ON ART.SUB_SECTION_CODE = SD.SUB_SECTION_CODE        
 JOIN SECTIONM SM ON SD.SECTION_CODE = SM.SECTION_CODE        
 JOIN PARA1 P1 ON SKU.PARA1_CODE = P1.PARA1_CODE          
 JOIN PARA2 P2 ON SKU.PARA2_CODE = P2.PARA2_CODE          
 JOIN PARA3 P3 ON SKU.PARA3_CODE = P3.PARA3_CODE          
 JOIN PARA4 P4 ON SKU.PARA4_CODE = P4.PARA4_CODE          
 JOIN PARA5 P5 ON SKU.PARA5_CODE = P5.PARA5_CODE          
 JOIN PARA6 P6 ON SKU.PARA6_CODE = P6.PARA6_CODE          
 JOIN UOM E ON ART.UOM_CODE = E.UOM_CODE       
 JOIN JOBS F (NOLOCK) ON F.JOB_CODE = B.JOB_CODE
