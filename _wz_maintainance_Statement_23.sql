



IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_RMD01106_PPCOLS' AND VALUE=1)
BEGIN
	PRINT 'UPDATING INVOICE QTY COLUMN IN DEBIT NOTE DETAIL'
	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_RMD01106_PPCOLS')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_RMD01106_PPCOLS','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_RMD01106_PPCOLS'

	UPDATE RMD01106 SET GROSS_PURCHASE_PRICE = PURCHASE_PRICE WHERE GROSS_PURCHASE_PRICE=0
		
	UPDATE RMD01106 SET PUR_PURCHASE_PRICE = GROSS_PURCHASE_PRICE WHERE PUR_PURCHASE_PRICE=0

END		
