CREATE PROCEDURE SPPPC_BUYER_ORDER_DETAILS
(
 @CARTICLE_CODE VARCHAR(10)='',
 @CORDER_ID VARCHAR(100)=''
)
AS
BEGIN
	SELECT A.ORDER_NO,A.ORDER_DT,B.PARA1_CODE,B.SIZEGROUP_CODE, C.PARA2_CODE ,C.QUANTITY
	FROM PPC_BUYER_ORDER_MST A
	JOIN PPC_BUYER_ORDER_DET B ON A.ORDER_ID=B.ORDER_ID
	JOIN PPC_BUYER_ORDER_SUB_DET C ON B.ROW_ID=C.REF_ROW_ID
	WHERE A.ORDER_ID=@CORDER_ID
	AND B.ARTICLE_CODE=@CARTICLE_CODE

END
