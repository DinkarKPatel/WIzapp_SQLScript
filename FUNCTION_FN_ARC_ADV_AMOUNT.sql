CREATE FUNCTION FN_ARC_ADV_AMOUNT ( @CBILLID VARCHAR(40) )
RETURNS NUMERIC(10,2)
--WITH ENCRYPTION
AS
BEGIN
	DECLARE @NADVAMOUNT NUMERIC(10,2)
	
	SELECT @NADVAMOUNT = SUM(AMOUNT) FROM ARC01106 WHERE CM_ID = @CBILLID AND CANCELLED = 0 AND ARCT = 2

	IF @NADVAMOUNT IS NULL
		SET @NADVAMOUNT = 0

	RETURN @NADVAMOUNT
END
