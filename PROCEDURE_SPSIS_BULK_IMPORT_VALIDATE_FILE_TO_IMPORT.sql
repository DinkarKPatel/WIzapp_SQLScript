CREATE PROCEDURE SPSIS_BULK_IMPORT_VALIDATE_FILE_TO_IMPORT
(    
	@cMAPPING_NAME VARCHAR(100)    
)    
AS    
BEGIN
	select LOCattr6_key_name,LOCattr14_key_name
	from LOC_NAMES  
	where LOCattr14_key_name ='wizapp' AND 
	LOCattr6_key_name LIKE '%@%'
	AND CHARINDEX(LOCattr6_key_name,@cMAPPING_NAME)>0
	UNION ALL
	select IMPORT_FILE_NAME,IMPORT_FILE_NAME
	from SLS_IMPORT_FILES_DO_NOT_PROCESS  
	where CHARINDEX(IMPORT_FILE_NAME,@cMAPPING_NAME)>0
END