

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SCHEME_SLSBC_ROWID_LATER' AND VALUE=1)
BEGIN
	PRINT 'UPDATING FILTER CRITERIAS OF SCHEME SET TABLES RELATED TO PRODUCT CODE'

	UPDATE SCHEME_SETUP_SLSBC SET ROW_ID=NEWID() WHERE LEFT(ROW_ID,5)='LATER'

	UPDATE SCHEME_SETUP_SLSBC_GET SET ROW_ID=NEWID() WHERE LEFT(ROW_ID,5)='LATER'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SCHEME_SLSBC_ROWID_LATER')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_SCHEME_SLSBC_ROWID_LATER',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_SCHEME_SLSBC_ROWID_LATER'	
END	
