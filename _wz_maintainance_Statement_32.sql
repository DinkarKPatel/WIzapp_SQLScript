

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='DELETE_DUP_CND01106' AND VALUE=1)
BEGIN
	PRINT 'DELETING DUPLICATE RECORDS IN CND01106'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='DELETE_DUP_CND01106')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('DELETE_DUP_CND01106','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='DELETE_DUP_CND01106'
	
	DELETE A FROM CND01106 A WHERE TS<>(SELECT TOP 1 TS FROM CND01106 WHERE ROW_ID=A.ROW_ID) 
END		
