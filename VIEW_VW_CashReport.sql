CREATE VIEW VW_CASHREPORT
--WITH ENCRYPTION
AS  
SELECT	B.DEPARTMENT_ID,C.DEPT_ID,C.DEPT_ALIAS, C.DEPT_NAME, B.EMP_ID, B.REF_ID, 
		B.EMP_FNAME + ' ' + B.EMP_LNAME AS EMP_NAME, A.PAYSLIP_MONTH, A.PAYSLIP_YEAR,
		A.NET_SALARY ,B.EMP_STATUS, B.CO_ALIAS
FROM EMP_PAYSLIP_MST A 
JOIN EMP_MST B			ON A.EMP_ID=B.EMP_ID
JOIN LOCATION C			ON B.DEPT_ID=C.DEPT_ID
WHERE A.CANCELLED = 0 AND B.MODE_OF_PAYMENT = 3
