CREATE PROCEDURE SP3S_LOC_ADDRESS
(
 @CDEPT_ID VARCHAR(100)='' 
)
 AS
 BEGIN
 IF @CDEPT_ID IN('ALL LOCATIONS','')
 SELECT TOP 1 @CDEPT_ID=VALUE  FROM CONFIG WHERE CONFIG_OPTION ='HO_LOCATION_ID'
 
 
 
	SELECT A.DEPT_ID ,DEPT_NAME,
		   ISNULL(ADDRESS1,'')+' '+ISNULL(ADDRESS2,'''') +ISNULL(A.AREA_NAME,'')+ ' '+ 
		   ISNULL(A.CITY ,'')+ ' '+ ISNULL(A.STATE ,'')+ ' '+ ISNULL(A.REGION_NAME ,'')
		   + ' '+ ISNULL(A.PINCODE ,'') AS ADDRESS1,
		   'GST NO:- '+LOC_GST_NO AS LOC_GST_NO ,
           LOC_GST_DT,
           REGISTERED_GST

	FROM LOC_VIEW A
	WHERE (@CDEPT_ID='' OR A.DEPT_ID =@CDEPT_ID)

END
