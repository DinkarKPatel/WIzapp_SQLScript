CREATE PROCEDURE SP_SEND_MIRROR_PCO_DATA_NEW
(
 @CUPLOADEDXNID AS VARCHAR(50),
 @CCURLOCID VARCHAR(5)
 ,@BACKNOWLEDGE BIT=0
 ,@COUTPUT VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
BEGIN
     DECLARE @DTSQL NVARCHAR(MAX),@CERRMSG VARCHAR(500),@CSTEP VARCHAR(100),
     @CMEMOID VARCHAR(100),@DMEMOLASTUPDATE DATETIME,@CTEMPDBNAME VARCHAR(100),
     @CTEMPTABLENAME VARCHAR(100)
   
     SET @CTEMPDBNAME='' 	
     SET @CTEMPTABLENAME='PCO_MST'
  BEGIN TRY
      SET @CSTEP=00
     		
      --CHNAGE BY BAIJNATH
		SET @CMEMOID=@CUPLOADEDXNID
	  
	  SET @CSTEP=30
      IF ISNULL(@CMEMOID,'')=''
	  GOTO END_PROC
	  
	
	SELECT A.*,'PCO_PCO_MST_UPLOAD' AS TARGET_TABLENAME FROM PCO_MST A (NOLOCK) WHERE MEMO_ID=@CMEMOID 
	
	
	  GOTO END_PROC
	  
     
  END TRY
    BEGIN CATCH
	SET @CERRMSG='P: SP_SEND_MIRROR_PCO_DATA_NEW, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO END_PROC
  END CATCH 
		
  END_PROC:


END
