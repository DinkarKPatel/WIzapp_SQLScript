CREATE PROC SP3S_COMPANY
(
@cwhere varchar(100)
)
AS
BEGIN
SELECT  ADDRESS1   , ADDRESS2,'' AS  ADDRESS9 ,    '01' AS COMPANY_CODE, COMPANY_NAME ,
'' AS COUNTRY,'' CST_NO,A.EMAIL_ID,  A.GST_NO AS GST_NO,''  MOBILE,   A.PAN_NO,  PHONES_FAX,   
'' AS PIN, '' AS PRINT_ADDRESS,'' SST_NO,'' TAN_NO,'' TIN_NO,'' AS WEB_ADDRESS
FROM COMPANY A (NOLOCK)   WHERE COMPANY_CODE= '01'
END



