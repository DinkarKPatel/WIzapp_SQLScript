CREATE VIEW  [DBO].[VW_POSTSALES_JOBRECEIVEQBF] 

AS
	SELECT  A.PRODUCT_CODE, D.ISSUE_NO,B.*,CONVERT(NVARCHAR,B.RECEIPT_DT,105) AS MST_RECEIPT_DT,B.RECEIPT_ID AS MST_RECEIPT_ID,B.RECEIPT_NO AS MST_RECEIPT_NO  
	,M.AGENCY_NAME AS MST_AGENCY_NAME  
	FROM POST_SALES_JOBWORK_RECEIPT_MST B (NOLOCK)
	JOIN POST_SALES_JOBWORK_RECEIPT_DET A (NOLOCK) ON B.RECEIPT_ID = A.RECEIPT_ID   
	JOIN POST_SALES_JOBWORK_ISSUE_DET C (NOLOCK) ON A.REF_ROW_ID = C.ROW_ID 
	JOIN POST_SALES_JOBWORK_ISSUE_MST D (NOLOCK) ON C.ISSUE_ID = D.ISSUE_ID 
    JOIN PRD_AGENCY_MST M (NOLOCK) ON M.AGENCY_CODE = D.AGENCY_CODE
