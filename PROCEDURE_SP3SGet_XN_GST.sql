CREATE PROCEDURE SP3SGET_XN_GST  
(  
 @XNTYPE VARCHAR(10)  
)  
AS   
BEGIN  
   SELECT DISTINCT XN_TYPE FROM GST_XN_FORMAT (NOLOCK)
   SELECT DISTINCT * FROM GST_TNC (NOLOCK) WHERE XN_TYPE=@XNTYPE  
   SELECT DISTINCT * FROM GST_XN_DETAIL (NOLOCK) WHERE XN_TYPE=@XNTYPE  
   SELECT DISTINCT * FROM GST_QUOTATION_MST (NOLOCK) WHERE XN_TYPE=@XNTYPE  
   SELECT DISTINCT * FROM GST_SLS_CUSTOMER_CONFIG(NOLOCK) WHERE XN_TYPE=@XNTYPE  
   SELECT DISTINCT * FROM GST_COMPANY_CONFIG(NOLOCK) WHERE XN_TYPE=@XNTYPE
END
