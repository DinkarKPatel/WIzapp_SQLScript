CREATE PROCEDURE SPREG_GET_SOFTINFODETAILS
AS
BEGIN
	DECLARE @cCMD NVARCHAR(MAX)

	SET @cCMD=N'Softinfo Systems Private Limited'

	SELECT @cCMD=@cCMD+CHAR(13)+CHAR(10)+ ISNULL(COMPANY_ADD,'') FROM SOFTINFODETAILS WHERE ISNULL(COMPANY_ADD,'')<>''
	SELECT @cCMD=@cCMD+CHAR(13)+CHAR(10)+ ISNULL(CONTACT_DETAIL,'') FROM SOFTINFODETAILS WHERE ISNULL(CONTACT_DETAIL,'')<>''
	SELECT @cCMD=@cCMD+CHAR(13)+CHAR(10)+ ISNULL(BANK_DETAILS,'') FROM SOFTINFODETAILS WHERE ISNULL(BANK_DETAILS,'')<>''

	SELECT @cCMD AS DETAILS
END