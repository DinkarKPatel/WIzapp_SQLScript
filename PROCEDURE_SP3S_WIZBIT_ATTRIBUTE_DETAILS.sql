CREATE PROCEDURE SP3S_WIZBIT_ATTRIBUTE_DETAILS
(
  @NMODE INT ,
  @TABLENAME VARCHAR(50)=''
)
AS
BEGIN
      
        IF @NMODE=1
          SELECT USERDISPLAY,CHK FROM ATTRIBUTE_DETAILS_NEW
          
          IF @NMODE=2 AND ISNULL(@TABLENAME,'')<>''
          BEGIN
            UPDATE ATTRIBUTE_DETAILS_NEW SET CHK=0
            UPDATE ATTRIBUTE_DETAILS_NEW SET CHK=1 WHERE TABLENAME=@TABLENAME
          END
         
END
