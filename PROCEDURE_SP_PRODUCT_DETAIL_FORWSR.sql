CREATE PROCEDURE SP_PRODUCT_DETAIL_FORWSR   
(      
 @CPRODUCTCODE VARCHAR(50),
 @AC_CODE VARCHAR(50)      
) 
--WITH ENCRYPTION
     
AS       
BEGIN    
 SELECT A.AC_CODE, A.INV_NO,B.PRODUCT_CODE   
 FROM INM01106 A   
 JOIN IND01106 B ON B.INV_ID=A.INV_ID   
 WHERE PRODUCT_CODE= @CPRODUCTCODE 
 AND A.AC_CODE= @AC_CODE  
   
END                       
--****  END OF PROCEDURE SP_WL_PRODUCT_DETAIL_FORWSR
