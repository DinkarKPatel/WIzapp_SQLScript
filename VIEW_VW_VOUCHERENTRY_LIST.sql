CREATE VIEW VW_VOUCHERENTRY_LIST  

AS  
  
SELECT [VM_ID],[VOUCHER_NO],[VOUCHER_DT],[VOUCHER_CODE],[DRTOTAL],[CRTOTAL],[BILL_NO]  
      ,[CASH_VOUCHER],[BILL_DT],[BILL_TYPE],[REF_NO],[COMPANY_CODE],[SALE_VOUCHER],[DEPT_ID]  
      ,CASE WHEN [SENT_TO_HO]=1 THEN 'UPDATED' ELSE '' END SENT_TO_HO,[BILL_ID]  
      ,CASE WHEN CANCELLED = 1 THEN 'CANCELLED' ELSE '' END CANCELLED   
      
      ,[BILL_AC_CODE]  
        
      ,CASE WHEN [APPROVED] = 1 THEN 'APPROVED' ELSE '' END APPROVED
      ,[REF_VM_ID]  
      ,[USER_CODE]  
      ,[REMINDER_DAYS] 
      ,[FIN_YEAR] 
  FROM [VM01106]
