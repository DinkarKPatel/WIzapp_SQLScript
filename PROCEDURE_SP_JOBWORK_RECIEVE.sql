create PROC [DBO].SP_JOBWORK_RECIEVE                      
@NQUERYID INT,                      
@CWHERE NVARCHAR(4000)='',        
@NMODE INT=0,
@BWIP BIT=0,
@NISSUE_MODE BIT=0,
@cLocID VARCHAR(5)='',
@CSP_ID VARCHAR(50)='',
@BFILLITEMDETAILS BIT=0  ,
@CPRODUCT_CODE VARCHAR(50)='' ,
@CORDER_ID VARCHAR(50)='',
@CJOBCARD_ID VARCHAR(50)='',
@NPASTE INT=0  ,
@CRECEIPT_ID varchar(50)=''
--WITH ENCRYPTION           
AS                      
BEGIN   
--(dinkar) Replace  left(memoid,2) to Location_code 
	IF @NQUERYID=1                      
 EXEC SP_JOBWORK_RECIEVE_1    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@cLocID =@cLocID 
                     
ELSE IF @NQUERYID=2                      
	EXEC SP_JOBWORK_RECIEVE_2    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@cLocID =@cLocID 
                 
ELSE IF @NQUERYID=3                      
 EXEC SP_JOBWORK_RECIEVE_3    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@cLocID =@cLocID 
                   
ELSE IF @NQUERYID=4       
BEGIN

 IF ISNULL(@BWIP,0)=1 AND @NISSUE_MODE=0
     EXEC SP_JOBWORK_RECIEVE_WIP_4    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@CLOCID =@CLOCID ,@CSP_ID=@CSP_ID ,@BFILLITEMDETAILS =@BFILLITEMDETAILS,@CPRODUCT_CODE=@CPRODUCT_CODE ,@NPASTE=@NPASTE
 ELSE 
 EXEC SP_JOBWORK_RECIEVE_4    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@CLOCID =@CLOCID ,@CSP_ID=@CSP_ID ,@BFILLITEMDETAILS =@BFILLITEMDETAILS,@CPRODUCT_CODE=@CPRODUCT_CODE ,@NPASTE=@NPASTE,@CJOBCARD_ID=@CJOBCARD_ID,@CRECEIPT_ID =@CRECEIPT_ID



END                 
ELSE IF @NQUERYID=5
	EXEC SP_JOBWORK_RECIEVE_5    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@cLocID =@cLocID                       
                   
ELSE IF @NQUERYID=6    
	EXEC SP_JOBWORK_RECIEVE_6    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@cLocID =@cLocID                                         
                    
ELSE IF @NQUERYID=7 
	EXEC SP_JOBWORK_RECIEVE_7    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@cLocID =@cLocID                                  
ELSE IF @NQUERYID=8 
	EXEC SP_JOBWORK_RECIEVE_8    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@cLocID =@cLocID             
ELSE IF @NQUERYID=9 
	EXEC SP_JOBWORK_RECIEVE_9    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@cLocID =@cLocID,@CSP_ID=@CSP_ID,@BFILLITEMDETAILS =@BFILLITEMDETAILS  ,
                                @CPRODUCT_CODE =@CPRODUCT_CODE ,@CORDER_ID =@CORDER_ID,@CJOBCARD_ID =@CJOBCARD_ID                                   
ELSE IF @NQUERYID=10                      
 EXEC SP_JOBWORK_RECIEVE_10    @NQUERYID =@NQUERYID ,@CWHERE =@CWHERE ,@NMODE =@NMODE ,@BWIP =@BWIP ,@NISSUE_MODE =@NISSUE_MODE ,@cLocID =@cLocID 
                      
END
