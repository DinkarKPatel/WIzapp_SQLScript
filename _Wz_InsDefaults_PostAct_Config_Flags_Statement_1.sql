IF NOT EXISTS(SELECT TOP 1 'U' FROM POSTACT_CONFIG_FLAGS)
BEGIN
	INSERT POSTACT_CONFIG_FLAGS	( VALUETYPE, VALUENAME )  
	SELECT 1 AS VALUETYPE,'FORM_ID' AS VALUENAME 
	UNION 
	SELECT 2 AS VALUETYPE,'PAYMODE_CODE' AS VALUENAME 
END


IF NOT EXISTS(SELECT TOP 1 'U' FROM POSTACT_CONFIG_FLAGS WHERE VALUETYPE=3)
BEGIN
	INSERT POSTACT_CONFIG_FLAGS	( VALUETYPE, VALUENAME )  
	SELECT 3 AS VALUETYPE,'LOCAL_WHOLESALE_TAX_AC' AS VALUENAME 
END
