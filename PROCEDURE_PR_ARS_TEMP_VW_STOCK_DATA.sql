CREATE PROCEDURE PR_ARS_TEMP_VW_STOCK_DATA(@DB VARCHAR(1000),@STABLE VARCHAR(1000),@TTAB VARCHAR(1000))
AS
BEGIN
SET NOCOUNT ON
DECLARE @SQL VARCHAR(MAX)
SET @SQL='SELECT *
INTO '+@TTAB+'
FROM '+@DB+''+@STABLE+' A (NOLOCK)    
WHERE A.XN_MODE=''STOCK'';' 
EXEC(@SQL)

SET @SQL='CREATE INDEX IX_PCODE_'+@TTAB+' ON '+@TTAB+'(PRODUCT_CODE)'
EXEC(@SQL)
SET NOCOUNT OFF
END