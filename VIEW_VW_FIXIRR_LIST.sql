CREATE VIEW VW_FIXIRR_LIST  

AS      
SELECT A.MEMO_NO,A.MEMO_ID, A.MEMO_DT,A.REMARKS,U.USERNAME AS [CREATED BY],  
U1.USERNAME AS [APPROVED BY],A.APPROVED_ON AS APPROVED_DT,  
(CASE WHEN A.APPROVED=1 THEN 'APPROVED' ELSE 'PENDING' END) AS [STATUS] ,A.FIN_YEAR  ,A.location_code           
FROM FIXITEM_RATE_REVISION_MST A (NOLOCK)   
JOIN USERS U (NOLOCK) ON U.USER_CODE=A.USER_CODE  
LEFT OUTER JOIN USERS U1  (NOLOCK) ON U1.USER_CODE=A.APPROVED_BY_USERCODE
