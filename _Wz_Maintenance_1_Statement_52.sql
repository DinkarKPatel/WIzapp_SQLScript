




IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_HO_LOCATION_BUILD_RFOPT' AND VALUE=1)
BEGIN

	PRINT 'UPDATE NULL VALUES IN PRINT WSL BOX NO. COLUMN TO MAKE THIS COMN NON-NULLABLE'
	 
	DECLARE @HO_LOCATION_ID VARCHAR(5),@LOCATION_ID VARCHAR(5) 
	
    SELECT @HO_LOCATION_ID=VALUE FROM CONFIG WHERE CONFIG_OPTION='HO_LOCATION_ID' 
    SELECT @LOCATION_ID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID' 
    
    IF @HO_LOCATION_ID=@LOCATION_ID    	
		UPDATE LOCATION SET BUILD_RFOPT=1 WHERE DEPT_ID=@LOCATION_ID
	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE  CONFIG_OPTION='UPDATE_HO_LOCATION_BUILD_RFOPT')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_HO_LOCATION_BUILD_RFOPT',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_HO_LOCATION_BUILD_RFOPT'
		 
END
