

IF NOT EXISTS(SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CMM_ECOUPONID_BLANK_NODISC' AND VALUE='1')
BEGIN
	PRINT 'UPDATING ECOUPON IDS AS BLANK FOR NO DISCOUNT IN THE BILLS'
	DECLARE @CWIZCLIPENABLED VARCHAR(2)
	
	SELECT TOP 1 @CWIZCLIPENABLED=VALUE FROM CONFIG WHERE CONFIG_OPTION='ENABLE_CAMPAIGN_SYSTEM'
	
	IF ISNULL(@CWIZCLIPENABLED,'')='1'
		UPDATE CMM01106 SET ECOUPON_ID='' WHERE ISNULL(ECOUPON_ID,'')<>'' AND DISCOUNT_AMOUNT=0
		
	IF NOT EXISTS(SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CMM_ECOUPONID_BLANK_NODISC')
	   INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
	   VALUES ('UPDATE_CMM_ECOUPONID_BLANK_NODISC','1','',GETDATE())	
	ELSE	
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_CMM_ECOUPONID_BLANK_NODISC'	
END
