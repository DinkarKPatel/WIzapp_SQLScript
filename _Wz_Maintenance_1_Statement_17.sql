


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='RESET_UTMARK_GSTSTATE' AND VALUE=1)
BEGIN
	PRINT 'RESETTING UT MARK IN GST STATE MASTER'
	
	UPDATE GST_STATE_MST SET UT=0

	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='RESET_UTMARK_GSTSTATE')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('RESET_UTMARK_GSTSTATE',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='RESET_UTMARK_GSTSTATE'
	
END		
