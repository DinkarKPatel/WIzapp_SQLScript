

-- GET ALL SIZE VALUE AND SIZE ORDER
CREATE PROCEDURE SP_WL_PARA2LIST
--WITH ENCRYPTION
AS

	SELECT A.PARA2_CODE, A.PARA2_NAME, A.PARA2_ORDER, INACTIVE,0 AS PURCHASE_PRICE,0 AS WS_PRICE, 0 AS MRP 
	FROM PARA2 A (NOLOCK)
	WHERE A.INACTIVE = 0
	ORDER BY A.PARA2_ORDER, A.PARA2_NAME
--************************* END OF PROCEDURE SP_WL_PARA2LIST
