CREATE VIEW [DBO].[VW_POSTSALES_JOB_ISSUELIST]

AS
SELECT	ISSUE_NO ,
CONVERT(VARCHAR,ISSUE_DT,105) AS ISSUE_DATE,M.TOTAL_AMOUNT,C.AGENCY_NAME,
M.ISSUE_ID  AS MEMO_ID,(CASE WHEN CANCELLED = 1 THEN 'CANCELLED' ELSE '' END) AS CANCELLED ,
M.FIN_YEAR,M1.TOTAL_QTY,M.location_Code
FROM POST_SALES_JOBWORK_ISSUE_MST M (NOLOCK)
JOIN PRD_AGENCY_MST C (NOLOCK) ON M.AGENCY_CODE = C.AGENCY_CODE
LEFT OUTER JOIN 
(
 SELECT ISSUE_ID , SUM(QUANTITY) AS TOTAL_QTY ,SUM(JOB_RATE) AS NET_AMOUNT  
 FROM POST_SALES_JOBWORK_ISSUE_DET  GROUP BY ISSUE_ID 
) M1 ON M.ISSUE_ID = M1.ISSUE_ID
