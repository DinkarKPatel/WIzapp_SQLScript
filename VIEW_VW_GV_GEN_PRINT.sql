CREATE VIEW  VW_GV_GEN_PRINT
AS
     SELECT A.MEMO_NO,A.MEMO_DT,A.MEMO_ID,A.FIN_YEAR,A.MODE,
            A.USER_CODE,A.CANCELLED,A.LAST_UPDATE,A.VALIDITY_DAYS,A.REMARKS,
            GV_SRNO,GV_SCRATCH_NO,DENOMINATION,DISCOUNT_AMOUNT,QUANTITY,ROW_ID,TS
     FROM GV_GEN_MST A (NOLOCK)
     JOIN GV_GEN_DET B (NOLOCK) ON A.MEMO_ID =B.MEMO_ID
