CREATE VIEW VW_GV_CNC_LIST
AS   
 SELECT T1.MEMO_NO   
 ,T1.MEMO_DT AS MEMO_DATE     
 ,T3.DEPT_NAME AS SOURCE_DEPT_NAME ,T1.MEMO_ID ,T1.FIN_YEAR
 ,T1.USER_CODE,U.USERNAME ,REMARKS   
 FROM GV_CNC_MST T1 
 JOIN LOCATION T3 ON T3.DEPT_ID =T1.location_code/*LEFT(T1.MEMO_ID,2)*//*Rohit 04-11-2024*/
 JOIN USERS U ON U.USER_CODE =T1.USER_CODE
