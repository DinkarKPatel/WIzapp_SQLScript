	  
  
-- PROCEDURE TO SAVE A JOB WORK ISSUE MEMO  
CREATE  PROCEDURE SAVETRAN_PRD_JWI  
(  
 @NUPDATEMODE  NUMERIC(1,0),  
 @NSPID    INT,  
 @CFINYEAR   VARCHAR(10),  
 @CMACHINENAME  VARCHAR(100)='',  
 @CWINDOWUSERNAME VARCHAR(100)='',  
 @CWIZAPPUSERCODE VARCHAR(10)='0000000',  
 @CMEMOID   VARCHAR(40)='',  
 @CLOCID    VARCHAR(2)=''  
)  
--WITH ENCRYPTION
AS  
BEGIN  
 -- @NUPDATEMODE: 1- NEW MEMO ADDED,   
 --     2- EDITING OF MEMO,   
 --     3- CURRENT MEMO CANCELLED,   
  
 DECLARE @CTEMPDBNAME   VARCHAR(100),  
   @CMASTERTABLENAME  VARCHAR(100),  
   @CDETAILTABLENAME  VARCHAR(100),  
   @CTEMPMASTERTABLENAME VARCHAR(100),  
   @CTEMPDETAILTABLENAME VARCHAR(100),  
   @CTEMPMASTERTABLE  VARCHAR(300),  
   @CTEMPDETAILTABLE  VARCHAR(300),  
   @CERRORMSG    VARCHAR(500),  
   @LDONOTUPDATESTOCK  BIT,  
   @CKEYFIELD1    VARCHAR(50),  
   @CKEYFIELDVAL1   VARCHAR(50),  
   @CMEMONO    VARCHAR(20),  
   @NMEMONOLEN    NUMERIC(20,0),  
   @CMEMONOVAL    VARCHAR(50),  
   @CLOCATIONID   VARCHAR(2),  
   @CHODEPTID    VARCHAR(2),  
   @CCMD     NVARCHAR(4000),  
   @CCMDOUTPUT    NVARCHAR(4000),  
   @NSAVETRANLOOP   BIT,  
   @NSTEP     INT,  
   @LENABLETEMPDATABASE BIT,@BNEGSTOCKFOUND BIT,  
   @CUSERALIAS VARCHAR(5),@CMEMONOPREFIX VARCHAR(10),  
   @CEXPRERRORMSG VARCHAR(MAX),    
   @NISSUETYPE    NUMERIC(3),  
   @NSAVETRANISSUETYPE NUMERIC(1)  
  
 DECLARE @OUTPUT TABLE ( ERRMSG VARCHAR(2000), MEMO_ID VARCHAR(100))  
  
 SET @NSTEP = 0  -- SETTTING UP ENVIRONMENT  
  
 SELECT @LENABLETEMPDATABASE = CAST([VALUE] AS BIT)   
 FROM CONFIG WHERE CONFIG_OPTION = 'ENABLE_TEMP_DATABASE'  
   
 IF @LENABLETEMPDATABASE IS NULL  
  SET @LENABLETEMPDATABASE = 0  
  
 -- CHECK TEMPORARY DATABASE TO HOLD TEMP TABLES   
 -- IF CONFIG SETTING SAYS TO DO SO  
 IF @LENABLETEMPDATABASE = 1  
  SET @CTEMPDBNAME = DB_NAME() + '_TEMP.DBO.'  
 ELSE  
  SET @CTEMPDBNAME = ''  
  
 SET @CMASTERTABLENAME = 'PRD_JOBWORK_ISSUE_MST'  
 SET @CDETAILTABLENAME = 'PRD_JOBWORK_ISSUE_DET'  
  
 SET @CTEMPMASTERTABLENAME = 'TEMP_'+@CMASTERTABLENAME+'_'+LTRIM(RTRIM(STR(@NSPID)))  
 SET @CTEMPDETAILTABLENAME = 'TEMP_'+@CDETAILTABLENAME+'_'+LTRIM(RTRIM(STR(@NSPID)))  
  
 SET @CTEMPMASTERTABLE = @CTEMPDBNAME + @CTEMPMASTERTABLENAME  
 SET @CTEMPDETAILTABLE = @CTEMPDBNAME + @CTEMPDETAILTABLENAME  
   
 SET @CERRORMSG   = ''  
 SET @LDONOTUPDATESTOCK = 0  
 SET @CKEYFIELD1   = 'ISSUE_ID'  
 SET @CMEMONO   = 'ISSUE_NO'  
 SET @NMEMONOLEN   = 10  
  
 IF ISNULL(@CLOCID,'')=''  
  SELECT @CLOCATIONID = [VALUE] FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'  
 ELSE   
  SET @CLOCATIONID=@CLOCID  
   
 SELECT @CHODEPTID  = [VALUE] FROM CONFIG WHERE CONFIG_OPTION='HO_LOCATION_ID'    
  
 SET @NSTEP = 10  -- GETTING DEPTID INFO FROM TEMP TABLE  
   
 BEGIN TRY  
   
  IF OBJECT_ID('TEMPDB..#TMPPMT','U') IS NOT NULL  
   DROP TABLE #TMPPMT  
    
  SELECT PRODUCT_CODE INTO #TMPPMT FROM SKU WHERE 1=2  
  
  IF OBJECT_ID('TEMPDB..#TMPXNSTK','U') IS NOT NULL  
   DROP TABLE #TMPXNSTK           
    
  SELECT PRODUCT_CODE,QUANTITY_IN_STOCK AS XN_STOCK INTO #TMPXNSTK FROM PMT01106 WHERE 1=2  
  
  
  BEGIN TRANSACTION 
  
   
  SET @CCMD=N'IF OBJECT_ID('''+@CTEMPMASTERTABLE+''',''U'') IS NOT NULL  
      UPDATE '+@CTEMPMASTERTABLE+' SET BIN_ID=''000'''  
  EXEC SP_EXECUTESQL @CCMD         
  
  SET @CCMD=N'IF OBJECT_ID('''+@CTEMPDETAILTABLE+''',''U'') IS NOT NULL  
      UPDATE '+@CTEMPDETAILTABLE+' SET BIN_ID=''000'''  
  EXEC SP_EXECUTESQL @CCMD   
    
  IF @NUPDATEMODE<>3  
  BEGIN  
   EXEC SP_VALIDATEXN_BEFORESAVE 'PRD_JWI',@NSPID,'0000000',@NUPDATEMODE,@CCMDOUTPUT OUTPUT,@BNEGSTOCKFOUND OUTPUT  
   IF ISNULL(@CCMDOUTPUT,'') <> ''  
   BEGIN  
    SET @CERRORMSG = 'STEP- ' + LTRIM(STR(@NSTEP)) + ' DATA VALIDATION ON TEMP DATA FAILED : ' + @CCMDOUTPUT + '...'  
    GOTO END_PROC  
   END  
  END  
  ELSE  
  BEGIN  
     
   EXEC VALIDATEXN_BEFORE_EDIT 'PRD_JWI',@CMEMOID,0,@CWIZAPPUSERCODE,@CERRORMSG OUTPUT,@CEXPRERRORMSG OUTPUT  
   IF ISNULL(@CERRORMSG,'')<>''  
    GOTO END_PROC  
  END  
  
    
  IF @NUPDATEMODE = 3 AND ISNULL(@CMEMOID,'') = ''  
  BEGIN  
   SET @CERRORMSG = 'STEP- ' + LTRIM(STR(@NSTEP)) + ' MEMO ID REQUIRED IF CALLED FROM CANCELLATION'  
   GOTO END_PROC      
  END  
  
    
  -- START UPDATING XN TABLES   
  IF @NUPDATEMODE = 1 -- ADDMODE   
  BEGIN   
     
   SET @NSTEP=15  
   SET @CMEMONOPREFIX=@CLOCATIONID+'I-'  
     
   SET @NSTEP = 20  -- GENERATING NEW KEY  
     
   -- GENERATING NEW JOB ORDER NO    
   SET @NSAVETRANLOOP=0  
   WHILE @NSAVETRANLOOP=0  
   BEGIN  
    EXEC GETNEXTKEY @CMASTERTABLENAME, @CMEMONO, @NMEMONOLEN, @CMEMONOPREFIX, 1,   
        @CFINYEAR,0, @CMEMONOVAL OUTPUT     
      
    PRINT @CMEMONOVAL  
    SET @CCMD=N'IF EXISTS ( SELECT '+@CMEMONO+' FROM '+@CMASTERTABLENAME+'   
          WHERE '+@CMEMONO+'='''+@CMEMONOVAL+'''   
          AND FIN_YEAR = '''+@CFINYEAR+''' )  
        SET @NLOOPOUTPUT=0  
       ELSE  
        SET @NLOOPOUTPUT=1'  
        PRINT @CCMD  
        EXEC SP_EXECUTESQL @CCMD, N'@NLOOPOUTPUT BIT OUTPUT',@NLOOPOUTPUT=@NSAVETRANLOOP OUTPUT  
      END  
  
   IF @CMEMONOVAL IS NULL  OR @CMEMONOVAL LIKE '%LATER%'
   BEGIN  
      SET @CERRORMSG = 'STEP- ' + LTRIM(STR(@NSTEP)) + ' ERROR CREATING NEXT MEMO NO....'   
      GOTO END_PROC      
   END  
  
   SET @NSTEP = 30  -- GENERATING NEW ID  
  
   -- GENERATING NEW JOB ORDER ID  
   SET @CKEYFIELDVAL1 = @CLOCATIONID + @CFINYEAR+ REPLICATE('0', 10-LEN(LTRIM(RTRIM(@CMEMONOVAL)))) + LTRIM(RTRIM(@CMEMONOVAL))   
   IF @CKEYFIELDVAL1 IS NULL OR @CKEYFIELDVAL1 LIKE '%LATER%'    
   BEGIN  
      SET @CERRORMSG = 'STEP- ' + LTRIM(STR(@NSTEP)) + ' ERROR CREATING NEXT MEMO ID....'  
      -- SET @CRETCMD= N'SELECT '''+@CERRORMSG+''' AS ERRMSG,'''' AS MEMO_ID'  
      GOTO END_PROC  
   END  
  
   SET @NSTEP = 40  -- UPDATING NEW ID INTO TEMP TABLES  
  
   -- UPDATING NEWLY GENERATED JOB ORDER NO AND JOB ORDER ID IN PIM AND PID TEMP TABLES  
   SET @CCMD = 'UPDATE ' + @CTEMPMASTERTABLE + ' SET ' + @CMEMONO+'=''' + @CMEMONOVAL+''',' +   
      @CKEYFIELD1+' = '''+@CKEYFIELDVAL1+''''  
   PRINT @CCMD  
   EXEC SP_EXECUTESQL @CCMD  
    
   SET @CCMD = 'UPDATE ' + @CTEMPDETAILTABLE + ' SET '+@CKEYFIELD1+' = '''+@CKEYFIELDVAL1+''''  
     
     
   PRINT @CCMD  
   EXEC SP_EXECUTESQL @CCMD  
  
  END     -- END OF ADDMODE  
  ELSE    -- CALLED FROM EDITMODE  
  BEGIN    -- START OF EDITMODE  
    
   SET @NSTEP = 50  -- GETTING ID INFO FROM TEMP TABLE  
  
     
   -- GETTING MEMO ID WHICH IS BEING EDITED  
   SET @CCMD = 'SELECT @CKEYFIELDVAL1 = ' + @CKEYFIELD1 + ', @CMEMONOVAL = ' + @CMEMONO + ' FROM '  
      + (CASE WHEN @NUPDATEMODE=3 THEN @CMASTERTABLENAME + ' WHERE ISSUE_ID = ''' + @CMEMOID + ''''  
        ELSE @CTEMPMASTERTABLE END )  
     
   EXEC SP_EXECUTESQL @CCMD, N'@CKEYFIELDVAL1 VARCHAR(50) OUTPUT, @CMEMONOVAL VARCHAR(50) OUTPUT',   
          @CKEYFIELDVAL1 OUTPUT, @CMEMONOVAL OUTPUT  
            
   IF (@CKEYFIELDVAL1 IS NULL ) OR (@CMEMONOVAL IS NULL )  
   BEGIN  
      SET @CERRORMSG = 'STEP- ' + LTRIM(STR(@NSTEP)) + ' ERROR ACCESSING THE RECORD TO BE MODIFIED...'  
      GOTO END_PROC      
   END  
  
   SET @NSTEP = 55  -- STORING OLD STATUS OF BARCODES   
     
   IF @NUPDATEMODE = 3     
   BEGIN  
    SET @NSTEP = 60  
    -- UPDATING SENTTOHO FLAG  
    SET @CCMD = N'UPDATE ' + @CMASTERTABLENAME + ' SET CANCELLED = 1 ' +   
       N' WHERE ' + @CKEYFIELD1 + ' = ''' + @CKEYFIELDVAL1 + ''''  
    EXEC SP_EXECUTESQL @CCMD  
      
      
  
    EXEC UPDATEPMT_PRD_JWI   
       @CXNTYPE   = 'PRD_JWI'  
     , @CXNNO   = @CMEMONOVAL  
     , @CXNID   = @CKEYFIELDVAL1  
     , @NREVERTFLAG  = 1  
     , @NALLOWNEGSTOCK = 0  
     , @NCHKDELBARCODES = 1  
     , @NUPDATEMODE  = @NUPDATEMODE      
     , @CCMD    = @CCMDOUTPUT OUTPUT  
   END  
   
  
     
   ELSE  
   BEGIN  
  
    SET @NSTEP = 65  -- UPDATING SENT_TO_HO FLAG TEMP TABLE  
    -- UPDATING SENTTOHO FLAG  
    SET @CCMD = N'UPDATE ' + @CTEMPMASTERTABLE + ' SET LAST_UPDATE=GETDATE()'  
    EXEC SP_EXECUTESQL @CCMD  
      
   END  
   
   SET @CCMD = N'SELECT @NSAVETRANISSUETYPE=ISSUE_TYPE FROM ' + @CMASTERTABLENAME +   
       N' WHERE ' + @CKEYFIELD1 + ' = ''' + @CKEYFIELDVAL1 + ''''  
  EXEC SP_EXECUTESQL @CCMD,N'@NSAVETRANISSUETYPE NUMERIC(1) OUTPUT',@NSAVETRANISSUETYPE OUTPUT  
  
   
   IF @NSAVETRANISSUETYPE=1 AND @NUPDATEMODE<>3  
   BEGIN  
    EXEC UPDATEPMT_PRD_JWI
       @CXNTYPE   = 'JWI'  
     , @CXNNO   = @CMEMONOVAL  
     , @CXNID   = @CKEYFIELDVAL1  
     , @NREVERTFLAG  = 1  
     , @NALLOWNEGSTOCK = 0  
     , @NCHKDELBARCODES = 1  
     , @NUPDATEMODE  = @NUPDATEMODE      
     , @CCMD    = @CCMDOUTPUT OUTPUT  
      
       
    IF (@NUPDATEMODE = 3)   
    BEGIN  
     IF @CCMDOUTPUT <> ''  
     BEGIN  
      SET @CERRORMSG = 'STEP- ' + LTRIM(STR(@NSTEP)) + ' ERROR UPDATING THE STOCK STATUS IN PMT....'  
      SET @BNEGSTOCKFOUND=1  
      PRINT 'NEGATIVE STOCK FOUND-1'  
      PRINT ISNULL(@CCMDOUTPUT,'@CCMDOUTPUT : NULL')  
      EXEC SP_EXECUTESQL @CCMDOUTPUT  
     END   
       
     GOTO END_PROC  
    END  
   END  
     
  END     -- END OF EDITMODE  
  
  SET @NSTEP = 95  
    
    
    
  -- RECHECKING IF ID IS STILL LATER  
  IF @CKEYFIELDVAL1 IS NULL OR @CKEYFIELDVAL1 LIKE '%LATER%'  
  BEGIN  
   SET @CERRORMSG = 'STEP- ' + LTRIM(STR(@NSTEP)) + ' ERROR GETTING NEXT MEMO ID....'  
   GOTO END_PROC  
  END  
  
  IF @NUPDATEMODE <> 3  
  BEGIN  
     
   SET @NSTEP = 105  -- UPDATING MASTER TABLE  
  
  
   EXEC UPDATEMASTERXN   
      @CSOURCEDB = @CTEMPDBNAME  
    , @CSOURCETABLE = @CTEMPMASTERTABLENAME  
    , @CDESTDB  = ''  
    , @CDESTTABLE = @CMASTERTABLENAME  
    , @CKEYFIELD1 = @CKEYFIELD1  
    ,@BALWAYSUPDATE = 1  
  
   -- UPDATING TRANSACTION TABLE (PID01106) FROM TEMP TABLE  
   SET @NSTEP = 110  -- UPDATING TRANSACTION TABLE  
  
   -- UPDATING ROW_ID IN TEMP TABLES  
   SET @CCMD = N'UPDATE ' + @CTEMPDETAILTABLE + ' SET ROW_ID = ''' + @CLOCATIONID + ''' + CONVERT(VARCHAR(38), NEWID())  
        WHERE LEFT(ROW_ID,5) = ''LATER'''  
   EXEC SP_EXECUTESQL @CCMD  
     
     
     
      SET @CCMD = 'UPDATE ' + @CTEMPDETAILTABLE + ' SET '+@CKEYFIELD1+' = '''+@CKEYFIELDVAL1+''''  
   EXEC SP_EXECUTESQL @CCMD  
  
   -- DELETING EXISTING ENTRIES FROM PID01106 TABLE WHERE ROW_ID NOT FOUND IN TEMPTABLE  
   SET @NSTEP = 114  -- UPDATING TRANSACTION TABLE - DELETING EXISTING ENTRIES  
  
   SET @CCMD = N'DELETE FROM ' + @CDETAILTABLENAME + '   
      WHERE ' + @CKEYFIELD1 + ' = ''' + @CKEYFIELDVAL1 + '''  
      AND ROW_ID IN   
      (  
       SELECT A.ROW_ID   
       FROM ' + @CDETAILTABLENAME + ' A   
       LEFT OUTER JOIN ' + @CTEMPDETAILTABLE + ' B ON A.ROW_ID = B.ROW_ID  
       WHERE A.' + @CKEYFIELD1 + ' = ''' + @CKEYFIELDVAL1 + '''  
       AND   B.ROW_ID IS NULL  
      )'  
   EXEC SP_EXECUTESQL @CCMD  
  
   -- INSERTING/UPDATING THE ENTRIES IN PRD_JID TABLE FROM TEMPTABLE  
   SET @NSTEP = 116  -- UPDATING TRANSACTION TABLE - INSERTING NEW ENTRIES  
  
   EXEC UPDATEMASTERXN   
      @CSOURCEDB = @CTEMPDBNAME  
    , @CSOURCETABLE = @CTEMPDETAILTABLENAME  
    , @CDESTDB  = ''  
    , @CDESTTABLE = @CDETAILTABLENAME  
    , @CKEYFIELD1 = 'ROW_ID'  
    ,@BALWAYSUPDATE = 1  
      
   SET @NSTEP = 140  
   
   
  SET @CCMD = N'SELECT @NSAVETRANISSUETYPE=ISSUE_TYPE FROM ' + @CMASTERTABLENAME +   
       N' WHERE ' + @CKEYFIELD1 + ' = ''' + @CKEYFIELDVAL1 + ''''  
  EXEC SP_EXECUTESQL @CCMD,N'@NSAVETRANISSUETYPE NUMERIC(1) OUTPUT',@NSAVETRANISSUETYPE OUTPUT  
    
   IF @NSAVETRANISSUETYPE=1  
   BEGIN  
    EXEC UPDATEPMT_PRD_JWI   
       @CXNTYPE   = 'PRD_JWI'  
     , @CXNNO   = @CMEMONOVAL  
     , @CXNID   = @CKEYFIELDVAL1  
     , @NREVERTFLAG  = 0  
     , @NALLOWNEGSTOCK = 0  
     , @NCHKDELBARCODES = 1  
     , @NUPDATEMODE  = @NUPDATEMODE      
     , @CCMD    = @CCMDOUTPUT OUTPUT  
  
    IF @CCMDOUTPUT <> ''  
    BEGIN  
     SET @CERRORMSG = 'STEP- ' + LTRIM(STR(@NSTEP)) + ' ERROR UPDATING THE STOCK STATUS IN PMT....'  
       
     SET @BNEGSTOCKFOUND=1      
       
     PRINT 'NEGATIVE STOCK FOUND-2-'+@CCMDOUTPUT  
       
     EXEC SP_EXECUTESQL @CCMDOUTPUT  
     GOTO END_PROC  
    END  
   END  
      
  END  
    
  -- VALIDATING ENTRIES   
  SET @NSTEP = 170  
    
 END TRY  
   
 BEGIN CATCH  
  SET @CERRORMSG = 'STEP- ' + LTRIM(STR(@NSTEP)) + ' SQL ERROR: #' + LTRIM(STR(ERROR_NUMBER())) + ' ' + ERROR_MESSAGE()  
  -- SET @CRETCMD= N'SELECT '''+@CERRORMSG+''' AS ERRMSG, '''' AS MEMO_ID'  
    
  GOTO END_PROC  
 END CATCH  
   
END_PROC:  
  
 IF @@TRANCOUNT>0  
 BEGIN  
  IF ISNULL(@CERRORMSG,'')='' AND ISNULL(@CCMDOUTPUT,'')='' AND ISNULL(@BNEGSTOCKFOUND,0)=0  
  BEGIN
		UPDATE PRD_JOBWORK_ISSUE_MST SET LAST_UPDATE=GETDATE() WHERE ISSUE_ID=@CKEYFIELDVAL1
		UPDATE XNLOG SET LAST_UPDATE=GETDATE() WHERE TABLENAME='PRD_JOBWORK_ISSUE_MST'
		COMMIT TRANSACTION
  END	
  ELSE  
   ROLLBACK  
 END  
   
 IF ISNULL(@BNEGSTOCKFOUND,0)=0  
 BEGIN  
  INSERT @OUTPUT ( ERRMSG, MEMO_ID)  
  VALUES ( ISNULL(@CERRORMSG,''), ISNULL(@CKEYFIELDVAL1,'') )  
  
  SELECT * FROM @OUTPUT   
 END   
  
 EXEC SP_DROPTEMPTABLES_XNS 'XNSJWI',@NSPID   
END     
------------------------------------------------------ END OF PROCEDURE SAVETRAN_JWI  

