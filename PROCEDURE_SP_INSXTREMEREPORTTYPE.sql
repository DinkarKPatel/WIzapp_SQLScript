CREATE PROCEDURE SP_INSXTREMEREPORTTYPE
--WITH ENCRYPTION
AS
BEGIN
    RETURN

	DECLARE @CREPORTTYPEC TABLE ( REP_CODE VARCHAR(10), REP_TYPE VARCHAR(100), REP_CATEGORY VARCHAR(10) )

	INSERT @CREPORTTYPEC	( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES('PRT01', 'PURCHASE RETURN (DEBIT NOTE)', 'PRT')

	INSERT @CREPORTTYPEC	( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES('PUR01', 'PURCHASE INVOICE', 'PUR')

	INSERT @CREPORTTYPEC( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES( 'SLS01','ITEM SALE/TAX INVOICE',  'SLS')

	INSERT @CREPORTTYPEC( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES( 'CHI01','CHALLAN IN',  'CHI')

	INSERT @CREPORTTYPEC( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES( 'CHO01','CHALLAN OUT',  'CHO')

	INSERT @CREPORTTYPEC( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES( 'CNC01','CANCELLATION/UNCANCELLATION', 'CNC')

	INSERT @CREPORTTYPEC	( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES('STK', 'STOCK RECONCILATION', 'STK')

	INSERT @CREPORTTYPEC	( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES('IRR01', 'ITEM RATE REVISION', 'IRR')

	INSERT @CREPORTTYPEC	( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES('CST01', 'CUSTOMER MASTER', 'CST')

	INSERT @CREPORTTYPEC	( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES('ARC01', 'CUSTOMER PAYMENT/RECEIPT MASTER', 'ARC')

	INSERT @CREPORTTYPEC	( REP_CODE, REP_TYPE, REP_CATEGORY )
	VALUES('SSU01', 'SALESETUP BARGAIN COUNTER', 'SSU')


    INSERT @CREPORTTYPEC ( REP_CODE, REP_TYPE, REP_CATEGORY )  
    VALUES('MISC ', 'MISCELLANEOUS ', 'IMG')  

	INSERT @CREPORTTYPEC ( REP_CODE, REP_TYPE, REP_CATEGORY )  
    VALUES('WSL01', 'WHOLESALE INVOICE', 'WSL')
    
    INSERT @CREPORTTYPEC ( REP_CODE, REP_TYPE, REP_CATEGORY )  
    VALUES('WSR01', 'WHOLESALE RETURN', 'WSR') 
    
    INSERT @CREPORTTYPEC ( REP_CODE, REP_TYPE, REP_CATEGORY )  
    VALUES('WSA01', 'WHOLESALE ANALYSIS', 'WSA') 

  

	UPDATE A SET
		REP_TYPE = B.REP_TYPE,
		REP_CATEGORY = B.REP_CATEGORY
	FROM REPORTTYPE A
	JOIN @CREPORTTYPEC B ON A.REP_CODE = B.REP_CODE

	INSERT REPORTTYPE ( REP_CODE, REP_TYPE, REP_CATEGORY )
	SELECT A.REP_CODE, A.REP_TYPE, A.REP_CATEGORY
	FROM @CREPORTTYPEC A
	LEFT OUTER JOIN REPORTTYPE B ON A.REP_CODE = B.REP_CODE
	WHERE B.REP_CODE IS NULL
END
