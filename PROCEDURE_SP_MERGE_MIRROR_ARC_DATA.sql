CREATE PROCEDURE SP_MERGE_MIRROR_ARC_DATA
(
	@CMEMOID VARCHAR(50)
   ,@CLOCID VARCHAR(3)
   ,@CSOURCEDB VARCHAR(300)
   ,@CMERGEDB VARCHAR(300)
   ,@BMSTINSERTONLY BIT
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_ARC_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(1000),@CTEMPMASTERTABLE VARCHAR(200)
	   ,@CTEMPPAYMODETABLE VARCHAR(200),@CCURDEPTID VARCHAR(5),@CMIRRORINGENABLED VARCHAR(2)
	   
DECLARE @TXNSSENDINFO TABLE (ORG_TABLENAME VARCHAR(50),TMP_TABLENAME VARCHAR(50),XN_ID VARCHAR(40))  	

BEGIN TRY
SET @CSTEP=00

	SET @CTABLE_SUFFIX=REPLACE(@CMEMOID,'-','_')
BEGIN TRANSACTION 


	 ---UPDATE POSTEDINAC MARK FOR THE BILLS POSTEDINTO ACCOUNTS & THERE IS SOME MISMATCH BETWEEN INCOMING DATA & EXISTING DATA
	SET @CSTEP=10

	SELECT TOP 1 @CMIRRORINGENABLED=VALUE FROM CONFIG WHERE CONFIG_OPTION='MIRRORING_ENABLED'
	
	IF ISNULL(@CMIRRORINGENABLED,'')='1'
		GOTO LBLMERGE
    
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'
    
    DECLARE @CPOSTINGATHODEPTID VARCHAR(5)
    
    SET @CSTEP=15
    
	SET @DTSQL=N'SELECT TOP 1 @CPOSTINGATHODEPTID=ACCOUNTS_POSTING_DEPT_ID FROM 
				 LOCATION WHERE DEPT_ID='''+@CLOCID+''''
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL,N'@CPOSTINGATHODEPTID CHAR(2) OUTPUT',@CPOSTINGATHODEPTID OUTPUT
    
	IF ISNULL(@CPOSTINGATHODEPTID,'')=@CCURDEPTID
	BEGIN
		
		SELECT @CTEMPMASTERTABLE=@CSOURCEDB+'TMP_ARC_ARC01106_'+LTRIM(RTRIM(@CTABLE_SUFFIX)),
			   @CTEMPPAYMODETABLE=@CSOURCEDB+'TMP_ARC_PAYMODE_XN_DET_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
		
		SET @CSTEP=20
		SET @DTSQL=N'UPDATE A SET POSTEDINAC=B.POSTEDINAC FROM '+@CTEMPMASTERTABLE+' A
					 JOIN ARC01106 B ON A.ADV_REC_ID=B.ADV_REC_ID'
		
		PRINT @DTSQL  
		EXEC SP_EXECUTESQL @DTSQL
		
		SET @CSTEP=30
		SET @DTSQL=N'UPDATE B SET POSTEDINAC=0 FROM ARC01106 A 
					 JOIN '+@CTEMPMASTERTABLE+' B ON A.ADV_REC_ID=B.ADV_REC_ID
				     WHERE A.NET_AMOUNT<>B.NET_AMOUNT OR A.CANCELLED<>B.CANCELLED'
		PRINT @DTSQL  
		EXEC SP_EXECUTESQL @DTSQL
		
		SET @CSTEP=40
	
	
		IF OBJECT_ID(@CTEMPPAYMODETABLE,'U') IS NOT NULL
		BEGIN
			SET @DTSQL=N'UPDATE A SET POSTEDINAC=0 FROM '+@CTEMPMASTERTABLE+' A 
						 JOIN (SELECT ISNULL(A.MEMO_ID,B.MEMO_ID) AS MEMO_ID FROM 
							  ( SELECT A.MEMO_ID,A.PAYMODE_CODE,SUM(A.AMOUNT) AS AMOUNT FROM PAYMODE_XN_DET A 
								JOIN '+@CTEMPMASTERTABLE+' B ON A.MEMO_ID=B.ADV_REC_ID
								WHERE A.XN_TYPE=''ARC'' GROUP BY A.MEMO_ID,A.PAYMODE_CODE) A
							   FULL OUTER JOIN 
							  ( SELECT A.MEMO_ID,A.PAYMODE_CODE,SUM(A.AMOUNT) AS AMOUNT FROM '+@CTEMPPAYMODETABLE+' A 
								JOIN '+@CTEMPMASTERTABLE+' B ON A.MEMO_ID=B.ADV_REC_ID
								WHERE A.XN_TYPE=''ARC'' GROUP BY A.MEMO_ID,A.PAYMODE_CODE) B ON A.MEMO_ID=B.MEMO_ID
							   AND A.PAYMODE_CODE=B.PAYMODE_CODE WHERE ISNULL(A.AMOUNT,0)<>ISNULL(B.AMOUNT,0))
							  C ON C.MEMO_ID=A.ADV_REC_ID'
			PRINT @DTSQL  
			EXEC SP_EXECUTESQL @DTSQL
		END
    END

LBLMERGE:

	SET @CSTEP=80
	SET @CTABLENAME='PAYMODE_XN_DET'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=90
	SET @DTSQL=N'DELETE '+@CMERGEDB+'['+@CTABLENAME+'] WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''' AND XN_TYPE=''ARC'''
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL

	SET @CSTEP=100
	SET @CTABLENAME='STATE'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='STATE_CODE'

	SET @CSTEP=110
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  
	SET @CSTEP=120
	SET @CTABLENAME='CITY'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='CITY_CODE'
	SET @CSTEP=130
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  

	SET @CSTEP=140
	SET @CTABLENAME='AREA'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='AREA_CODE'
	SET @CSTEP=150
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
	
	SET @CSTEP=160
	SET @CTABLENAME='HD01106'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='HEAD_CODE'
	SET @CSTEP=170
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  
	SET @CSTEP=180
	SET @CTABLENAME='LMP01106'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='AC_CODE'
	SET @CSTEP=190
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  

	SET @CSTEP=200
	SET @CTABLENAME='LMP01106'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='AC_CODE'
	SET @CSTEP=210
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  

	SET @CSTEP=212
	SET @CTABLENAME='CUSTDYM'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='CUSTOMER_CODE'
	SET @CSTEP=214
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  							  
							  
	SET @CSTEP=220
	SET @CTABLENAME='PAYMODE_GRP_MST'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PAYMODE_GRP_CODE'
	SET @CSTEP=230
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1
							  
	SET @CSTEP=240
	SET @CTABLENAME='PAYMODE_MST'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PAYMODE_CODE'
	SET @CSTEP=250
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1							  

							  
    SET @CSTEP=260
	SET @CTABLENAME='EMPLOYEE'
	SET @CTMP_TABLENAME='TMP_ARC_EMP_HEAD_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='EMP_CODE'
	SET @CSTEP=270
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1
							  
	SET @CSTEP=280
	SET @CTABLENAME='EMPLOYEE'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='EMP_CODE'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1							  
    
    SET @CSTEP=285
	SET @CTABLENAME='ARC01106'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ADV_REC_ID'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  

							  
	SET @CSTEP=290
	SET @CTABLENAME='PAYMODE_XN_DET'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=270
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1							  							  
							  
	SET @CSTEP=295
	SET @DTSQL=N'DELETE '+@CMERGEDB+'WSL_ORDER_ADV_RECEIPT  WITH (ROWLOCK) WHERE ADV_REC_ID='''+@CMEMOID+''''
	EXEC SP_EXECUTESQL @DTSQL
	
	SET @CSTEP=300
	SET @CTABLENAME='WSL_ORDER_ADV_RECEIPT'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ADV_REC_ID'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

	
	SET @CSTEP=302
	SET @DTSQL=N'DELETE '+@CMERGEDB+'CMM_CREDIT_RECEIPT WITH (ROWLOCK) WHERE ADV_REC_ID='''+@CMEMOID+''''
	EXEC SP_EXECUTESQL @DTSQL
	
    SET @CSTEP=305
	SET @CTABLENAME='CMM_CREDIT_RECEIPT'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ADV_REC_ID'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  
	
	
	
	SET @CSTEP=307
	PRINT 'STEP :'+@CSTEP
	SET @DTSQL=N'DELETE '+@CMERGEDB+'ARC_GVSALE_DETAILS WITH (ROWLOCK) WHERE ADV_REC_ID='''+@CMEMOID+''''
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL
	
    SET @CSTEP=310
    PRINT 'STEP :'+@CSTEP
	SET @CTABLENAME='ARC_GVSALE_DETAILS'
	SET @CTMP_TABLENAME='TMP_ARC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ADV_REC_ID'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  
							  							  							  
	SET @CSTEP=315

	UPDATE A SET DT_EXPIRY=DATEADD(DD,A.VALIDITY_DAYS,C.ADV_REC_DT) FROM SKU_GV_MST A 
	JOIN ARC_GVSALE_DETAILS B ON A.GV_SRNO=B.GV_SRNO 
	JOIN ARC01106 C ON C.ADV_REC_ID=B.ADV_REC_ID
	WHERE B.ADV_REC_ID=@CMEMOID AND B.GV_SRNO=A.GV_SRNO 
	
	SET @CSTEP=320
		
	SET @CTABLESSTR='ARC01106,WSL_ORDER_ADV_RECEIPT,LM01106,LMP01106,HD01106,CUSTDYM,AREA,CITY,STATE,EMPLOYEE,PAYMODE_XN_DET,PAYMODE_MST,PAYMODE_GRP_MST,EMP_HEAD,XNSTABLELIST,CMM_CREDIT_RECEIPT,ARC_GVSALE_DETAILS'

    EXEC SP3S_DROPTEMPTABLES_MIRRORDATA
    @CXNTYPE='ARC',
    @CTABLESUFFIX=@CTABLE_SUFFIX,
    @CTABLESSTR=@CTABLESSTR

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_MERGE_MIRROR_ARC_DATA, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_ARC_DATA
