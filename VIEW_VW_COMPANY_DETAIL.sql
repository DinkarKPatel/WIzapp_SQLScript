CREATE VIEW VW_COMPANY_DETAIL
AS
	SELECT CP.COMPANY_NAME,COMPANY_ADDRESS=CP.ADDRESS1+' '+CP.ADDRESS2
	, CP.CITY AS COMPANY_CITY,CP.[STATE] COMPANY_STATE,CP.PIN AS COMPANY_PIN,AR.AREA_NAME AS COMPANY_AREA,
	LOGO_PATH   ,
	'' AS BILL_NO
	FROM COMPANY CP 
	LEFT JOIN AREA AR ON AR.AREA_CODE=CP.AREA_CODE 
	WHERE CP.COMPANY_CODE='01'
