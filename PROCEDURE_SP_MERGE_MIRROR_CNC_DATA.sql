CREATE PROCEDURE SP_MERGE_MIRROR_CNC_DATA
(
	@CMEMOID VARCHAR(50),
	@CLOCID CHAR(2),
	@CSOURCEDB VARCHAR(300),
	@CMERGEDB VARCHAR(300),
	@BMSTINSERTONLY BIT,
	@BCALLEDFROMSYNCHMBODATA BIT=1,
	@CERRMSG VARCHAR(MAX) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_CNC_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(500),
	   @NSAVETRANLOOP BIT,@CMASTERTABLENAME VARCHAR(100),@CDETAILTABLENAME VARCHAR(100),
	   @CTEMPMASTERTABLE VARCHAR(100),@CTEMPDETAILTABLE VARCHAR(100),@CKEYFIELD1 VARCHAR(50),@CMEMONO VARCHAR(50),
	   @NMEMONOLEN	NUMERIC(20,0),@CMEMONOVAL VARCHAR(50),@CLOCATIONID	VARCHAR(2), @CTEMPLOCID CHAR(2),@CTEMPDATE DATETIME,
	   @CMEMONOPREFIX		VARCHAR(50),@CFINYEAR			VARCHAR(10),@CKEYFIELDVAL1			VARCHAR(50),
	   @CEXISTMEMOID VARCHAR(100),@CEXISTMEMONO VARCHAR(100),@CNCMEMOID VARCHAR(100),@CTEMPDBNAME VARCHAR(100)
	   
BEGIN TRY
SET @CSTEP=00


SET @CSTEP=20
SET @CTABLE_SUFFIX=REPLACE(@CMEMOID,'-','_')
BEGIN TRANSACTION 


 IF @BCALLEDFROMSYNCHMBODATA=1
 BEGIN
 
    SET @CTEMPDBNAME = DB_NAME() + '_TEMP.DBO.'
	SET @CMASTERTABLENAME	= 'ICM01106'
	SET @CDETAILTABLENAME	= 'ICD01106'


	SET @CTEMPMASTERTABLE	= @CTEMPDBNAME+'TMP_CNC_ICM01106_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CTEMPDETAILTABLE	= @CTEMPDBNAME+'TMP_CNC_ICD01106_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	
	SET @CERRMSG			= ''
	SET @CKEYFIELD1			= 'CNC_MEMO_ID'
	SET @CMEMONO			= 'CNC_MEMO_NO'
	SET @NMEMONOLEN			= 10
	
	SET @DTSQL=N' SELECT @CEXISTMEMOID= B.CNC_MEMO_ID,@CEXISTMEMONO=B.CNC_MEMO_NO FROM  ' + @CTEMPMASTERTABLE + ' A 
	                         JOIN ICM01106 B ON A.REF_MEMO_ID=B.REF_MEMO_ID AND A.REF_XN_TYPE=B.REF_XN_TYPE'

	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL, N'@CEXISTMEMOID VARCHAR(100) OUTPUT,@CEXISTMEMONO VARCHAR(100) OUTPUT',
	                          @CEXISTMEMOID OUTPUT,@CEXISTMEMONO OUTPUT
	                          
	                 
	
	IF ISNULL(@CEXISTMEMOID,'')<>'' AND ISNULL(@CEXISTMEMONO,'') <>''
		BEGIN
		        
				SET @DTSQL = 'UPDATE ' + @CTEMPMASTERTABLE + ' SET ' + @CMEMONO+'=''' + @CEXISTMEMONO+''',' + 
							@CKEYFIELD1+' = '''+@CEXISTMEMOID+''''
				EXEC SP_EXECUTESQL @DTSQL
			
				SET @DTSQL = 'UPDATE ' + @CTEMPDETAILTABLE + ' SET '+@CKEYFIELD1+' = '''+@CEXISTMEMOID+''''
				EXEC SP_EXECUTESQL @DTSQL
		END
	ELSE 
		BEGIN
		
			SET @DTSQL=N'SELECT @CTEMPLOCID=DEPT_ID,@CTEMPDATE=CNC_MEMO_DT FROM '+@CTEMPMASTERTABLE+''
			PRINT @DTSQL
		 EXEC SP_EXECUTESQL @DTSQL,N'@CTEMPLOCID CHAR(2) OUTPUT ,@CTEMPDATE DATETIME OUTPUT',@CTEMPLOCID OUTPUT,
		 @CTEMPDATE OUTPUT

		 
		 SET @CMEMONOPREFIX=@CTEMPLOCID
		 SELECT @CLOCATIONID=@CTEMPLOCID
		 SET @CFINYEAR='01'+DBO.FN_GETFINYEAR(@CTEMPDATE)
		 
		 
			SET @CSTEP = 10		-- GENERATING NEW KEY
				
				SET @NSAVETRANLOOP=0
				WHILE @NSAVETRANLOOP=0
				BEGIN
				   
		
					EXEC GETNEXTKEY @CMASTERTABLENAME, @CMEMONO, @NMEMONOLEN, @CMEMONOPREFIX, 1,
									@CFINYEAR,0, @CMEMONOVAL OUTPUT   
					
					PRINT @CMEMONOVAL
					SET @DTSQL=N'IF EXISTS ( SELECT '+@CMEMONO+' FROM '+@CMASTERTABLENAME+' 
											WHERE '+@CMEMONO+'='''+@CMEMONOVAL+''' 
											AND FIN_YEAR = '''+@CFINYEAR+''' )
									SET @NLOOPOUTPUT=0
								ELSE
									SET @NLOOPOUTPUT=1'
					PRINT @DTSQL
					EXEC SP_EXECUTESQL @DTSQL, N'@NLOOPOUTPUT BIT OUTPUT',@NLOOPOUTPUT=@NSAVETRANLOOP OUTPUT
				END
				IF @CMEMONOVAL IS NULL  OR @CMEMONOVAL LIKE '%LATER%'
				BEGIN
					  SET @CERRMSG = 'STEP- ' + LTRIM(STR(@CSTEP)) + ' ERROR CREATING NEXT MEMO NO....'	
					  GOTO EXIT_PROC  		
				END

				SET @CSTEP = 15		-- GENERATING NEW ID

				SET @CKEYFIELDVAL1 = @CLOCATIONID + @CFINYEAR+ REPLICATE('0', 15-LEN(LTRIM(RTRIM(@CMEMONOVAL)))) + LTRIM(RTRIM(@CMEMONOVAL))
				IF @CKEYFIELDVAL1 IS NULL OR @CKEYFIELDVAL1 LIKE '%LATER%'
				BEGIN
					  SET @CERRMSG = 'STEP- ' + LTRIM(STR(@CSTEP)) + ' ERROR CREATING NEXT MEMO ID....'
					  GOTO EXIT_PROC
				END

				SET @CSTEP = 20	
				

				SET @DTSQL = 'UPDATE ' + @CTEMPMASTERTABLE + ' SET ' + @CMEMONO+'=''' + @CMEMONOVAL+''',' + 
							@CKEYFIELD1+' = '''+@CKEYFIELDVAL1+''''
                PRINT @DTSQL							
				EXEC SP_EXECUTESQL @DTSQL
			
				SET @DTSQL = 'UPDATE ' + @CTEMPDETAILTABLE + ' SET '+@CKEYFIELD1+' = '''+@CKEYFIELDVAL1+''''
				PRINT @DTSQL
				EXEC SP_EXECUTESQL @DTSQL
		    
	END
	
			 SET @DTSQL=N'SELECT @CNCMEMOID=CNC_MEMO_ID FROM '+@CTEMPMASTERTABLE+''
			 EXEC SP_EXECUTESQL @DTSQL,N'@CNCMEMOID VARCHAR(100) OUTPUT',@CNCMEMOID OUTPUT			 
			 
			 SET @CMEMOID=@CNCMEMOID
	 
 END
        
  
	DECLARE @CMEMOIDSEARCH VARCHAR(100)
	SELECT TOP 1 @CMEMOIDSEARCH=A.CNC_MEMO_ID FROM ICM01106 A (NOLOCK) WHERE A.CNC_MEMO_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
	BEGIN
		SET @CSTEP=20
		EXEC SP3SBUILDCNC
			@CXNID=@CMEMOID
			,@NUPDATEMODE=3
			,@CERRMSG=@CERRMSG OUTPUT  

		IF ISNULL(@CERRMSG,'')<>''
			GOTO Exit_PROC			
    END
    
	SET @CSTEP=30

	SET @CTABLENAME='ICD01106'
	SET @CTMP_TABLENAME='TMP_CNC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='CNC_MEMO_ID'

	SET @DTSQL=N'DELETE '+@CMERGEDB+'['+@CTABLENAME+'] WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''''
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL

    SET @CSTEP=40
	SET @CTABLENAME='ICM01106'
	SET @CTMP_TABLENAME='TMP_CNC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='CNC_MEMO_ID'

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

	SET @CSTEP=50
	SET @CTABLENAME='SKU'
	SET @CTMP_TABLENAME='TMP_CNC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PRODUCT_CODE'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  
	SET @CSTEP=60
	SET @CTABLENAME='ICD01106'
	SET @CTMP_TABLENAME='TMP_CNC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='CNC_MEMO_ID'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

							  	
	SET @CSTEP=70
	SET @CTABLESSTR='ICM01106,ICD01106,SKU,XNSTABLELIST'

    --EXEC SP3S_DROPTEMPTABLES_MIRRORDATA
    --@CXNTYPE='CNC',
    --@CTABLESUFFIX=@CTABLE_SUFFIX,
    --@CTABLESSTR=@CTABLESSTR


END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_MERGE_MIRROR_CNC_DATA, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')=''
	BEGIN
	EXEC SP3SBUILDCNC
			@CXNID=@CMEMOID
			,@NUPDATEMODE=2
			,@CERRMSG=@CERRMSG OUTPUT  
	END

	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_CNC_DATA
