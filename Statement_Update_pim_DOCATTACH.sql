
DECLARE @IMGDBNAME VARCHAR(100),@DTSQL NVARCHAR(MAX)
SET @IMGDBNAME=DB_NAME ()+'_IMAGE'

IF DB_ID (@IMGDBNAME ) IS NOT NULL
BEGIN

 SET @DTSQL=N'	UPDATE B SET B.DOCATTACH =1
	FROM '+@IMGDBNAME+'.DBO.IMAGE_INFO_DOC (NOLOCK) A
	JOIN PIM01106 B (NOLOCK) ON A.MEMO_ID=B.MRR_ID                   
	WHERE XN_TYPE=''FRMTRANPURCHASEINVOICE'' AND ISNULL(B.DOCATTACH,0) =0  AND B.INV_MODE=1'

	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL

END