CREATE VIEW VW_TILL_LIFTS 
   
AS     
SELECT A.TILL_NAME ,C.MEMO_ID,
C.MEMO_NO,
C.MEMO_DT,
C.FIN_YEAR,
C.USER_CODE,
C.SHIFT_ID,
C.TILL_AMOUNT,
C.LIFT_AMOUNT,
C.REMARKS,
C.CANCELLED,
CASE WHEN C.TRANSFERMODE=1 THEN 'BANK' 
WHEN C.TRANSFERMODE=2 THEN 'PETTY CASH' 
WHEN C.TRANSFERMODE=3 THEN 'LOCKER' END AS TRANSFERMODE,
U.username
FROM TILL_MST A 
JOIN TILL_SHIFT_MST B ON A.TILL_ID=B.TILL_ID
JOIN TILL_LIFTS C ON C.SHIFT_ID=B.SHIFT_ID
JOIN USERS U ON U.user_code=C.user_code
