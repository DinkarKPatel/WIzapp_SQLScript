CREATE PROCEDURE SP3S_MGDASHBOARD
(
	 @DTODATE DATETIME	
	,@CAGE_LIMIT1 VARCHAR(10)
	,@CAGE_LIMIT2 VARCHAR(10)
	,@CAGE_LIMIT3 VARCHAR(10)
	,@CFILTERCOLS VARCHAR(MAX)
	,@NCNTRPCT NUMERIC(10,2)
