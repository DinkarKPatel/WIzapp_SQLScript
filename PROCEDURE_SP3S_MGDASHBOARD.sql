--WITH ENCRYPTION
AS
BEGIN
SET NOCOUNT ON
	DECLARE  @CCMD NVARCHAR(MAX),@CTODATE VARCHAR(20),@CCURDB VARCHAR(100)
			,@CRFDB VARCHAR(100),@CFROMDATE VARCHAR(20),@CFINYEAR VARCHAR(5)
