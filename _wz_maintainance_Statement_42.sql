


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='ORDER_UPDATE' AND VALUE=1)
BEGIN
	PRINT 'UPDATE BLANK ENTRY OF REF_ORDER_ID IN CMD01106'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='BIN_UPDATE')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('BIN_UPDATE','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='BIN_UPDATE'
	
	IF OBJECT_ID('TEMPDB..#CMD','U') IS NOT NULL
			DROP TABLE #CMD
		
	IF OBJECT_ID('CMD01106','U') IS NOT NULL AND OBJECT_ID('SKU_BO','U') IS NOT NULL
	BEGIN
		SELECT ORDER_ID,CMD.ROW_ID INTO #CMD FROM CMD01106 CMD (NOLOCK) JOIN SKU_BO S (NOLOCK) ON CMD.PRODUCT_CODE=S.PRODUCT_CODE
		WHERE CMD.REF_ORDER_ID IS NULL
		
		UPDATE CMD SET REF_ORDER_ID=ORDER_ID FROM CMD01106 CMD JOIN #CMD ON CMD.ROW_ID=#CMD.ROW_ID
	END	
END			
