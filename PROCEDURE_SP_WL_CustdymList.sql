

-- GET ALL DEFINED CUSTOMER MASTER
CREATE PROCEDURE SP_WL_CUSTDYMLIST
--WITH ENCRYPTION
AS

	SELECT A.USER_CUSTOMER_CODE, A.CUSTOMER_CODE, CUSTOMER_TITLE+' '+CUSTOMER_FNAME+' '+CUSTOMER_LNAME AS 
	CUSTIMER_NAME
	FROM CUSTDYM A 
	WHERE A.CUSTOMER_CODE <> '000000000000' AND INACTIVE = 0
	ORDER BY CUSTOMER_FNAME 
