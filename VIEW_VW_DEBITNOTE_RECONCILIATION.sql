CREATE  VIEW VW_DEBITNOTE_RECONCILIATION  

AS  
SELECT A.MEMO_NO AS 'MST_MEMO_NO',A.MEMO_ID ,A.FIN_YEAR AS MST_FIN_YEAR,   
   A.MEMO_DT AS 'MST_MEMO_DT',B.AC_NAME AS 'MST_AC_NAME', A.REMARKS AS 'MST_REMARKS',   
   A.CANCELLED AS 'MST_CANCELLED',C.USERNAME AS MST_USERNAME,D.PRODUCT_CODE , D.QUANTITY,   
   E.RM_NO,E.TOTAL_AMOUNT,F.AMOUNT,H.ARTICLE_NO,I.PARA1_NAME,   
   J.PARA2_NAME,K.SUB_SECTION_NAME, L.SECTION_NAME    
   FROM DEBIT_NOTE_RECON_MST A   
   JOIN LMV01106 B ON A.AC_CODE = B.AC_CODE   
   JOIN USERS C ON C.USER_CODE =  A.USER_CODE   
   JOIN DEBIT_NOTE_RECON_DET D ON A.MEMO_ID = D.MEMO_ID   
   JOIN RMM01106 E ON B.AC_CODE = E.AC_CODE   
   JOIN RMD01106 F ON E.RM_ID = F.RM_ID AND F.PRODUCT_CODE = D.PRODUCT_CODE    
   JOIN SKU G ON D.PRODUCT_CODE = G.PRODUCT_CODE   
   JOIN ARTICLE H ON H.ARTICLE_CODE = G.ARTICLE_CODE   
   JOIN PARA1 I ON I.PARA1_CODE = G.PARA1_CODE   
   JOIN PARA2 J ON J.PARA2_CODE = G.PARA2_CODE   
   JOIN SECTIOND K ON K.SUB_SECTION_CODE = H.SUB_SECTION_CODE   
   JOIN SECTIONM  L ON L.SECTION_CODE = K.SECTION_CODE
