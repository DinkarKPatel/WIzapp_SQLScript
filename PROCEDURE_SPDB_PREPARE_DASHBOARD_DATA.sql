CREATE PROCEDURE SPDB_PREPARE_DASHBOARD_DATA
@DTODATE DATETIME
AS
BEGIN
	DECLARE @CFINYEAR VARCHAR(5)
	
	TRUNCATE TABLE DB_SALE_KPIS
	TRUNCATE TABLE DB_PENDING_SUMMARY
	TRUNCATE TABLE DB_SALE_CONTR_SUMMARY
	TRUNCATE TABLE DB_PROFIT_CONTR_SUMMARY
	TRUNCATE TABLE DB_GMROI_SUMMARY
	TRUNCATE TABLE DB_ANNUAL_PURCHASE_TARGET
			
	EXEC SPDB_KPI_REPORT @DTODATE
	
	EXEC SPDB_PENDING_SUMMARY '',@DTODATE,'',0,0,1,1,1,1,1
	
	EXEC SPDB_SALE_CONTRIBUTION_DATA @DTODATE
	
	EXEC SPDB_PROFIT_CONTRIBUTION_DATA @DTODATE
	
	EXEC SPDB_GMROI_DATA @DTODATE
	
	SELECT @CFINYEAR='01'+DBO.FN_GETFINYEAR(@DTODATE)
	EXEC SPDB_ANNUAL_TARGET @CFINYEAR 
END
