CREATE VIEW VW_PICKLIST
    
AS     
SELECT ISNULL(T3.AC_NAME,'') AS CUSTOMER_NAME, 
T1.PICK_LIST_DT AS MEMO_DT, T1.PICK_LIST_NO AS MEMO_NO,       
T1.PICK_LIST_NO AS MEMO_ID, T2. TOTAL_QUNATITY,
(CASE WHEN T1.CANCELLED = 1 THEN 'CANCELLED' ELSE '' END) AS CANCELLED  ,  
T1.FIN_YEAR  
FROM WSL_PICKLIST_MST T1    
JOIN 
(
  SELECT SUM(PICK_LIST_QTY) AS TOTAL_QUNATITY,PICK_LIST_ID FROM WSL_PICKLIST_DET
  GROUP BY PICK_LIST_ID
)T2 ON T2.PICK_LIST_ID=T1.PICK_LIST_ID  
LEFT OUTER JOIN LMV01106 T3 ON T3.AC_CODE = T1.AC_CODE
