--**** DELETING SENT STATUS OF DAY CLOSE LOG FROM XFERLOG
--**** SO THAT ENTRIES DON'T GET REPEATEDLY SENT
IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='DELETE_DAYCLSOELOG_STATUS_2' AND VALUE=1)
BEGIN

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='DELETE_DAYCLSOELOG_STATUS_2')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('DELETE_DAYCLSOELOG_STATUS_2',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='DELETE_DAYCLSOELOG_STATUS_2'
	
	DELETE FROM XFERLOG WHERE XN_TYPE='XNSDEND'
END		
