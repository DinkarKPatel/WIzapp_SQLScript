CREATE PROCEDURE SP3S_GET_REPORT_FIELDS(@XN_TYPE VARCHAR(100))
AS
BEGIN
   SELECT 'ARTICLE_NO' FIELD,'Article' as DISPLAY_NAME INTO #TEMP UNION ALL
   SELECT 'SECTION_NAME' FIELD,'Section' as DISPLAY_NAME UNION ALL
   SELECT 'SUB_SECTION_NAME' FIELD,'Sub Section' as DISPLAY_NAME UNION ALL
   SELECT DISTINCT CONFIG_OPTION+'_NAME',VALUE FROM CONFIG WHERE config_option LIKE 'PARA[1-6]_caption' UNION ALL        
   SELECT UPPER(COLUMN_NAME),TABLE_CAPTION FROM CONFIG_ATTR WHERE ISNULL(TABLE_CAPTION,'')<>''        
   IF @XN_TYPE='PUR'
      INSERT #TEMP SELECT 'BOX_NO','Box No'
   SELECT CAST(0 AS BIT)CHK,* FROM #TEMP ORDER BY 1  
END
