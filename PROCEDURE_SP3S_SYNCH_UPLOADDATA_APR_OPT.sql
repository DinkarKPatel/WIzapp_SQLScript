CREATE PROCEDURE SP3S_SYNCH_UPLOADDATA_APR_OPT
(
    @NSPID VARCHAR(50)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_APR_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CCMD NVARCHAR(MAX),@CSOURCEDB VARCHAR(10),
		@CMERGEDB VARCHAR(10),@BMSTINSERTONLY BIT,@CMEMOID VARCHAR(50),@BCANCELLED BIT,
		@BADDMODE BIT,@nUpdateMode NUMERIC(1,0)

BEGIN TRY

SET @CSTEP=20
	SET @CTABLE_SUFFIX='UPLOAD'
BEGIN TRANSACTION 
    
	SELECT @CSOURCEDB='',@CMERGEDB=''

    
	SELECT TOP 1 @CMEMOID = B.MEMO_ID ,@BCANCELLED=CANCELLED 
    FROM APR_APPROVAL_RETURN_MST_UPLOAD B (NOLOCK)
	WHERE SP_ID=@nSpId
   
   
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		

	DECLARE @CMEMOIDSEARCH VARCHAR(100)
	SELECT TOP 1 @CMEMOIDSEARCH=A.MEMO_ID FROM APPROVAL_RETURN_MST A (NOLOCK) WHERE A.MEMO_ID=@CMEMOID
	
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1


	SET @nUpdateMode=(CASE WHEN @BADDMODE=1 THEN 1 WHEN @bCancelled=1 THEN 3 ELSE 2 END)

	EXEC SP3S_upd_qty_lastupdate
	@nUpdateMode=@NUPDATEMODE,
	@cXnType='APP',
	@nSpId=@nSpId,
	@cMasterTable='Apm01106',
	@cMemoIdCol='memo_id',
	@cXnDtCol='memo_dt',
	@cMemoId=@CMEMOIDSEARCH,
	@bCalledfromMerging=1,
	@CERRORMSG=@CERRMSG OUTPUT

	IF ISNULL(@cErrmsg,'')<>''
		GOTO EXIT_PROC
    
	IF @BADDMODE=0
	BEGIN	
		
		 IF @bCancelled=1 
		BEGIN
			UPDATE approval_return_mst WITH (ROWLOCK) SET cancelled=1 WHERE MEMO_ID=@cMemoid
			GOTO lblUpdaePmt
		END

		SET @CSTEP=30
		SET @CTABLENAME='APPROVAL_RETURN_DET'
		SET @CTMP_TABLENAME='APR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
		SET @CKEYFIELD='MEMO_ID'
		SET @CSTEP=90
		SET @DTSQL=N'DELETE '+@CMERGEDB+'['+@CTABLENAME+'] WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''''
		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL

	END

   
	
	
    SET @CFILTERCONDITION = ' B.SP_ID='''+LTRIM(RTRIM((@NSPID )))+''''
	
							  
	SET @CSTEP=220
	SET @CTABLENAME='APPROVAL_RETURN_MST'
	SET @CTMP_TABLENAME='APR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=230
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  
							  
    SET @CSTEP=240
	SET @CTABLENAME='APPROVAL_RETURN_DET'
	SET @CTMP_TABLENAME='APR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ROW_ID'
	SET @CSTEP=250
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 							  							  						  							  
							  

	SET @CSTEP=260 

	lblUpdaePmt:
	EXEC SP3S_MERGE_LOCPMT  
	@cTempTable='APR_PMT01106_UPLOAD',  
	@cMemoIdCol='SP_ID',  
	@cMemoId =@nSpId

	IF ISNULL(@cErrmsg,'')=''
	BEGIN
		SET @CSTEP=270
		EXEC SP_CHKXNSAVELOG 'APRMERGE',@CSTEP,0,@CMEMOID,'',1

		EXEC SP3S_VALIDATE_DATAMERGED_SINGLECHANNEL
		@cMasterTable='approval_return_mst',
		@cDetTable='approval_return_det',
		@cMemoIdCol='memo_id',
		@cMemoId=@cMemoId,
		@cUploadTable='apr_approval_return_mst_upload',
		@nSpId=@nSpId,
		@cErrormsg=@CERRMSG OUTPUT

		IF ISNULL(@cErrmsg,'')=''
			GOTO EXIT_PROC
    END	
	SET @CSTEP=450
	

	--SELECT 'DELETE A FROM ' +NAME +' A (NOLOCK) WHERE A.SP_ID=@nSpId ' FROM SYS.TABLES WHERE NAME LIKE 'APR%' AND RIGHT(NAME,6) IN('UPLOAD')
	

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_MERGE_MIRROR_APR_DATA, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	

	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK

	DELETE A FROM APR_APPROVAL_RETURN_DET_UPLOAD A WITH (ROWLOCK) WHERE A.SP_ID=@nSpId 
	DELETE A FROM APR_APPROVAL_RETURN_MST_UPLOAD A WITH  (ROWLOCK) WHERE A.SP_ID=@nSpId 
	DELETE A FROM APR_PMT01106_UPLOAD A WITH  (ROWLOCK) WHERE A.SP_ID=@nSpId 

END	
---END OF PROCEDURE - SP_MERGE_MIRROR_APR_DATA
