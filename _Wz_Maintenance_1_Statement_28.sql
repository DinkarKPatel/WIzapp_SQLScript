

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE  CONFIG_OPTION='REUPDATE_BBREF_REFNO' AND VALUE=1)
BEGIN
	PRINT ' REUPDATE BILL NO. FROM THE REFERENCE TABLES IN BBREF'
	
	UPDATE A SET REF_NO=B.BILL_NO FROM BILL_BY_BILL_REF A
	JOIN PIM01106 B ON A.REF_NO='PUR_'+B.MRR_ID

	UPDATE A SET REF_NO=B.RM_NO FROM BILL_BY_BILL_REF A
	JOIN RMM01106 B ON A.REF_NO='PRT_'+B.RM_ID
			
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE  CONFIG_OPTION='REUPDATE_BBREF_REFNO')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('REUPDATE_BBREF_REFNO',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='REUPDATE_BBREF_REFNO'
	
END		
