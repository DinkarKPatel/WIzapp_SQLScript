CREATE FUNCTION FN_GETPAYSLIPDATE  (@CPAYSLIPID VARCHAR(10))
RETURNS DATETIME
AS
BEGIN
	DECLARE @DRETDATE DATETIME 
	SELECT @DRETDATE = CONVERT(DATETIME,LTRIM(RTRIM(STR(PAYSLIP_YEAR))+'-'+LTRIM(RTRIM(STR(PAYSLIP_MONTH)))+'-01'))
	FROM EMP_PAYSLIP_MST (NOLOCK) WHERE PAYSLIP_ID=@CPAYSLIPID
	
	RETURN @DRETDATE
END
