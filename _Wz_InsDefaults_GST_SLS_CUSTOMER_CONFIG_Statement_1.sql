IF NOT EXISTS(SELECT * FROM GST_SLS_CUSTOMER_CONFIG WHERE XN_TYPE='SLS')
   INSERT GST_SLS_CUSTOMER_CONFIG (XN_TYPE,IS_MOBILE,IS_FNAME,IS_LNAME,IS_EMAIL) 
   SELECT 'SLS',1,1,0,0

IF EXISTS(SELECT * FROM GST_SLS_CUSTOMER_CONFIG WHERE XN_TYPE='SLS' AND IS_SIGNATURE IS NULL)
   UPDATE GST_SLS_CUSTOMER_CONFIG SET IS_SIGNATURE=0 WHERE XN_TYPE='SLS'

IF EXISTS(SELECT * FROM GST_SLS_CUSTOMER_CONFIG WHERE PRINT_COPIES IS NULL AND XN_TYPE='SLS')
   UPDATE GST_SLS_CUSTOMER_CONFIG SET PRINT_COPIES=2 WHERE XN_TYPE='SLS'

IF EXISTS(SELECT * FROM GST_SLS_CUSTOMER_CONFIG WHERE PRINT_COPIES IS NULL AND XN_TYPE='WSL')
   UPDATE GST_SLS_CUSTOMER_CONFIG SET PRINT_COPIES=1 WHERE XN_TYPE='WSL'

IF EXISTS(SELECT * FROM GST_SLS_CUSTOMER_CONFIG WHERE IS_USER_CODE IS NULL)
   UPDATE GST_SLS_CUSTOMER_CONFIG SET IS_USER_CODE=1 WHERE IS_USER_CODE IS NULL
