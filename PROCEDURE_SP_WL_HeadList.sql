CREATE PROCEDURE SP_WL_HEADLIST
--WITH ENCRYPTION
AS

	SELECT B.MAJOR_HEAD_CODE, A.HEAD_NAME AS 'MAJOR_HEAD_NAME', B.HEAD_CODE, B.HEAD_NAME 
	FROM HD01106 A 
	JOIN HD01106 B ON A.HEAD_CODE = B.MAJOR_HEAD_CODE
	ORDER BY B.MAJOR_HEAD_CODE, A.HEAD_NAME
--************************** END OF PROCEDURE SP_WL_HEADLIST
