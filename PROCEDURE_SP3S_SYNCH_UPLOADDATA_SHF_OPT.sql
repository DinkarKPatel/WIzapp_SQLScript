create PROCEDURE SP3S_SYNCH_UPLOADDATA_SHF_OPT
(
    @NSPID VARCHAR(50)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
AS
SET NOCOUNT ON
BEGIN
	
	/*
		SP_MERGE_MIRROR_SHF_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(1000),@CCURDEPTID VARCHAR(10),@CMIRRORINGENABLED VARCHAR(2)
	   ,@CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE VARCHAR(200),@CSOURCEDB VARCHAR(10),
		@CMERGEDB VARCHAR(10),@BMSTINSERTONLY BIT,@CMEMOID VARCHAR(50),@BCANCELLED BIT,
		@BADDMODE BIT
	   		    
DECLARE @TXNSSENDINFO TABLE (ORG_TABLENAME VARCHAR(50),TMP_TABLENAME VARCHAR(50),XN_ID VARCHAR(40))  	

BEGIN TRY
SET @CSTEP=00

	SET @CTABLE_SUFFIX='upload'
BEGIN TRANSACTION 
       
	   set @CSOURCEDB=''
	   set @CMERGEDB=''
	 ---UPDATE POSTEDINAC MARK FOR THE BILLS POSTEDINTO ACCOUNTS & THERE IS SOME MISMATCH BETWEEN INCOMING DATA & EXISTING DATA
	SET @CSTEP=10
    
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'
    
    SET @CSTEP=15
   
LBLMERGE:
    	SELECT TOP 1 @CMEMOID = B.shift_id  
		FROM SHF_TILL_SHIFT_MST_UPLOAD B (NOLOCK)
		WHERE  SP_ID=@NSPID 
   
                 
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC


    SET @CFILTERCONDITION = 'B.shift_id='''+@CMEMOID+''' AND B.SP_ID='''+LTRIM(RTRIM((@NSPID )))+''''
	
    SET @CSTEP=250
	SET @CTABLENAME='TILL_SHIFT_MST'
	SET @CTMP_TABLENAME='SHF_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='SHIFT_ID'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 				
	
	
	
	
	SET @CSTEP=260
	SET @CTABLENAME='TILL_SHIFT_DET'
	SET @CTMP_TABLENAME='SHF_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ROW_ID'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 				
							  
	
	DELETE FROM SHF_TILL_SHIFT_DET_UPLOAD WHERE SP_ID=@NSPID 
	DELETE FROM SHF_TILL_SHIFT_MST_UPLOAD WHERE SP_ID=@NSPID 

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP3S_SYNCH_UPLOADDATA_APR_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP3S_SYNCH_UPLOADDATA_APR_OPT
