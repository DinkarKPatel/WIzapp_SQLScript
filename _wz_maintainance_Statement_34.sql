


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPD_WSRIMP_PURIMP_RECDT' AND VALUE=1)
BEGIN
	PRINT 'UPDATING BLANK RECEIPT DATE IN PUR IMPORT LOG/WSR IMPORT LOG TABLE FROM PIM01106/CNM01106'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPD_WSRIMP_PURIMP_RECDT')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPD_WSRIMP_PURIMP_RECDT','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPD_WSRIMP_PURIMP_RECDT'
	
	UPDATE PUR_IMPORT_LOG SET RECEIPT_DT=B.RECEIPT_DT FROM PIM01106  B WHERE B.MRR_ID=PUR_IMPORT_LOG.MRR_ID
	UPDATE WSR_IMPORT_LOG SET RECEIPT_DT=B.RECEIPT_DT FROM CNM01106  B WHERE B.CN_ID=WSR_IMPORT_LOG.CN_ID
END		
