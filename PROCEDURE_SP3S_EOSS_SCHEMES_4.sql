CREATE PROCEDURE SP3S_EOSS_SCHEMES_4
(
	@CSCHEMEDETROWID VARCHAR(50),
	@CSLSTITLE VARCHAR(500),
	@CROUNDITEMLEVEL VARCHAR(5),
	@CJOINSTRBUY VARCHAR(MAX),
	@CJOINSTRGET VARCHAR(MAX),
	@CPICKSLRDISCMODE VARCHAR(5),
	@CERRMSG VARCHAR(MAX) OUTPUT
)
AS 
BEGIN

	DECLARE @NORGBUYAMT NUMERIC(10,2),@NBUYAMT NUMERIC(10,2),@NGETAMT NUMERIC(10,2),@NREQGETAMT NUMERIC(10,2),
			@CPRODUCTCODE VARCHAR(50),@NMRP NUMERIC(10,2),@NQTY NUMERIC(10,2),
			@CCMDROWID VARCHAR(50),@NLOOPCNT INT,@NMODE INT,@BQUALIFIED BIT,
			@CROWID VARCHAR(100),@NSTEP INT,@NAMT NUMERIC(10,2),@bGvTitle BIT,@nSpId varchar(40),
			@CMRPORDER VARCHAR(10),@NREQBUYAMT NUMERIC(10,2),@BRETRY2NDCASE BIT,
			@CCMD NVARCHAR(MAX),@CBUYFILTER VARCHAR(MAX),@CGETFILTER VARCHAR(MAX),@BCONSIDERBUYITEMSFORAPPLYDISC BIT,
			@CADDFILTER VARCHAR(MAX),@CORGADDFILTER VARCHAR(MAX),@NDISCMODE INT,@BLOOP BIT,
			@NFILTERMODE INT,@BPROCESSSLRENTRY BIT,@NGETFILTERMODE INT,@NTRIAL INT,@NAPPLYMODE NUMERIC(1,0)
	
	SET @BPROCESSSLRENTRY=0
START_PROC:

	SELECT @NORGBUYAMT=0,@CPRODUCTCODE='',@NBUYAMT=0,@NGETAMT=0,@NMRP=0,@NQTY=0,
		   @CCMDROWID='',@NLOOPCNT=0,@NREQGETAMT=0,@NMODE=0,@BQUALIFIED=0,@CROWID='',
		   @NSTEP=0,@NAMT=0,@CMRPORDER='',@NREQBUYAMT=0,@BRETRY2NDCASE=0,@CCMD='',
		   @NDISCMODE=0,@CBUYFILTER='',@CGETFILTER='',@CADDFILTER='',@NFILTERMODE=0,
		   @CADDFILTER='',@CORGADDFILTER='',@NGETFILTERMODE=0
			

	IF @BPROCESSSLRENTRY=1
		SET @CORGADDFILTER='A.QUANTITY<0'
	ELSE
		SET @CORGADDFILTER='A.QUANTITY>0'
			
	IF CURSOR_STATUS('GLOBAL','SCHEMECUR') IN (0,1)
	BEGIN
		CLOSE SCHEMECUR
		DEALLOCATE SCHEMECUR
	END

BEGIN TRY        
		
	SET @NSTEP=10
	
	PRINT 'CHECK SCHEME SP3S_EOSS_SCHEMES_4'	
	SELECT @CMRPORDER='',@BQUALIFIED=0,@BRETRY2NDCASE=0
		
	IF OBJECT_ID('TEMPDB..#TEMPCMDPREV','U') IS NOT NULL
		DROP TABLE #TEMPCMDPREV

	IF OBJECT_ID('TEMPDB..#TMPCMDSCHEMES','U') IS NOT NULL
		DROP TABLE #TMPCMDSCHEMES

	SELECT @CBUYFILTER=(CASE WHEN FILTER_MODE IN (0,1) THEN  BUY_FILTER_CRITERIA ELSE '1=1' END),
		   @CGETFILTER=(CASE WHEN GET_FILTER_MODE IN (0,1) THEN  GET_FILTER_CRITERIA ELSE '1=1' END),
		   @NDISCMODE=DISC_METHOD,@NFILTERMODE=FILTER_MODE,@NGETFILTERMODE=GET_FILTER_MODE,
		   @BCONSIDERBUYITEMSFORAPPLYDISC=(CASE WHEN DISCOUNT_AMOUNT_APPLICABLE_MODE=2 THEN 1 ELSE 0 END),
		   @bGvTitle=ISNULL(GV_RELATED_TITLE,0)
	FROM SCHEME_SETUP_DET
	WHERE ROW_ID=@CSCHEMEDETROWID


	SELECT TOP 1 @nSpId=sp_id FROM #tmpCmd

	--if @@spid=62
	--	SELECT 'CHECK PENDING for scheme:'+@CSCHEMEDETROWID,@nSpId AS nSpId,* FROM sls_paymode_xn_det_upload a (NOLOCK) 
	--							   WHERE sp_id=@nSpId 

	IF @bGvTitle=1 AND NOT EXISTS (SELECT TOP 1 memo_id FROM sls_paymode_xn_det_upload a (NOLOCK) 
								   WHERE sp_id=@nSpId AND ISNULL(remarks,'')='' AND cc_name=@CSCHEMEDETROWID
								   AND ISNULL(a.gv_srno,'')<>'') 
		RETURN

	IF @NFILTERMODE NOT IN (0,1)
		SELECT @CADDFILTER=@CORGADDFILTER+' AND SLSDET.ROW_ID='''+@CSCHEMEDETROWID+''''
	ELSE
		SELECT @CADDFILTER=@CORGADDFILTER+' AND 1=1'			
					
	SET @NSTEP=15
	SELECT PRODUCT_CODE,MRP,QUANTITY,CMD_ROW_ID AS ROW_ID,CONVERT(INT,0) AS MODE,
	CONVERT(NUMERIC(10,2),0) AS DISCOUNT_AMOUNT,NET AS SCHEME_APPLIED_AMOUNT,QUANTITY AS SCHEME_APPLIED_QTY
	INTO #TMPCMDSCHEMES FROM #TMPCMD
	WHERE 1=2 
		
	SET @CCMD=N' SELECT A.PRODUCT_CODE,((A.MRP*A.QUANTITY)-A.SCHEME_APPLIED_AMOUNT) AS MRP,ABS(A.QUANTITY)*(CASE WHEN A.QUANTITY<0 THEN -1 ELSE 1 END),
				 A.CMD_ROW_ID AS ROW_ID,1 AS MODE,
				 0 AS DISCOUNT_AMOUNT,0,0 FROM #TMPCMD A
				 JOIN #CMDITV ITV ON ITV.PRODUCT_CODE=A.PRODUCT_CODE '+@CJOINSTRBUY+'
				 WHERE '+@CBUYFILTER+' AND '+@CADDFILTER+' AND ((A.MRP*A.QUANTITY)-A.SCHEME_APPLIED_AMOUNT)>0'
	
	PRINT @CCMD				 
	INSERT #TMPCMDSCHEMES
	EXEC SP_EXECUTESQL @CCMD
	
	SET @NSTEP=25
	
	SELECT * INTO #TEMPCMDPREV FROM #TMPCMDSCHEMES
	
	SET @NTRIAL=1 
	
	SELECT @NREQBUYAMT= BUY_AMOUNT,@NREQGETAMT=DISCOUNT_AMOUNT
	FROM SCHEME_SETUP_DET
	WHERE ROW_ID=@CSCHEMEDETROWID

LBLCHKBUY:
					
	IF @BRETRY2NDCASE=0
		SET @CMRPORDER=''
	
	SELECT @NGETAMT=0,@NBUYAMT=0

	DELETE FROM #TMPCMDSCHEMES
	
	INSERT #TMPCMDSCHEMES (PRODUCT_CODE,MRP,QUANTITY,ROW_ID,MODE,DISCOUNT_AMOUNT,SCHEME_APPLIED_AMOUNT,SCHEME_APPLIED_QTY)
	SELECT PRODUCT_CODE,MRP,QUANTITY,ROW_ID,MODE,0,0,0 FROM #TEMPCMDPREV

	--SELECT @NREQBUYAMT,'CHK2NDCASE',PRODUCT_CODE,MRP,QUANTITY,ROW_ID,MODE 
	--FROM #TMPCMDSCHEMES 
	
	SET @NSTEP=30
	
	PRINT 'TRIAL#'+STR(@NTRIAL)	

	DECLARE SCHEMECUR CURSOR FOR SELECT PRODUCT_CODE,MRP,ROW_ID,MODE FROM
	#TMPCMDSCHEMES ORDER BY MRP DESC
			
	OPEN SCHEMECUR
	FETCH NEXT FROM SCHEMECUR INTO @CPRODUCTCODE,@NMRP,@CCMDROWID,@NMODE
	WHILE @@FETCH_STATUS=0
	BEGIN
		SET @NSTEP=40
		SET @NLOOPCNT=1
		WHILE @NLOOPCNT<=1
		BEGIN
			SET @NBUYAMT=@NBUYAMT+@NMRP									
		    
		    SET @NSTEP=50
			UPDATE #TMPCMDSCHEMES SET 
			SCHEME_APPLIED_AMOUNT=SCHEME_APPLIED_AMOUNT+@NMRP
			WHERE ROW_ID=@CCMDROWID
		    
		    PRINT '2:'+ ISNULL(STR(@NREQBUYAMT),'NULL @NREQBUYAMT') +ISNULL(STR(@NBUYAMT),'@NBUYAMT')
			IF @NBUYAMT>=@NREQBUYAMT
			BEGIN

				UPDATE #TMPCMDSCHEMES SET SCHEME_APPLIED_AMOUNT=SCHEME_APPLIED_AMOUNT-(@NBUYAMT-@NREQBUYAMT)
				WHERE ROW_ID=@CCMDROWID
							
				SET @BQUALIFIED=1				
				BREAK
			END
		    
		    SET @NSTEP=60
			IF @BQUALIFIED=1
				BREAK
				
			SET @NLOOPCNT=@NLOOPCNT+1
		END										
		
		IF @BQUALIFIED=1
			BREAK
		
		FETCH NEXT FROM SCHEMECUR INTO @CPRODUCTCODE,@NMRP,@CCMDROWID,@NMODE
	END
	CLOSE SCHEMECUR
	DEALLOCATE SCHEMECUR
	
		
END TRY

BEGIN CATCH
  PRINT 'CATCH IN SP3S_EOSS_SCHEMES_4'       
  SET @CERRMSG='P:SP3S_EOSS_SCHEMES_4, STEP:'+LTRIM(RTRIM(STR(@NSTEP)))+', MESSAGE:'+ERROR_MESSAGE()        
  GOTO EXIT_PROC
END CATCH

EXIT_PROC:
	
	
	IF ISNULL(@CERRMSG,'')=''
	BEGIN
		IF @BQUALIFIED=0
		BEGIN
			UPDATE #TMPCMDSCHEMES SET DISCOUNT_AMOUNT=0

		END	
		ELSE
		BEGIN
			SET @NSTEP=115
			
			DECLARE @CBNGNROWID VARCHAR(50)
			SET @CBNGNROWID=NEWID()


			DECLARE @NSUBTOTAL NUMERIC(10,2)
			SELECT @NSUBTOTAL=SUM(MRP) FROM #TMPCMDSCHEMES WHERE SCHEME_APPLIED_AMOUNT<>0
			
			
			--SELECT 'BEFORE DISTRIBUTING DISCOUNT',* FROM #TMPCMD
			--SELECT 'QUALIFIED ITEMS',@NREQGETAMT AS GETAMT,@NSUBTOTAL AS SUBTOTAL,* FROM #TMPCMDSCHEMES WHERE SCHEME_APPLIED_AMOUNT<>0
			
			
			UPDATE #TMPCMDSCHEMES SET  DISCOUNT_AMOUNT=(@NREQGETAMT/@NSUBTOTAL)*MRP
			WHERE SCHEME_APPLIED_AMOUNT<>0
			
			UPDATE #TMPCMDSCHEMES SET  DISCOUNT_AMOUNT=MRP WHERE DISCOUNT_AMOUNT>MRP
			
			UPDATE #TMPCMD SET DISCOUNT_AMOUNT=#TMPCMD.DISCOUNT_AMOUNT+
			((B.DISCOUNT_AMOUNT)*(CASE WHEN @BPROCESSSLRENTRY=1 THEN -1 ELSE 1 END)),
			SLS_TITLE=SLS_TITLE+(CASE WHEN SLS_TITLE<>'' THEN ',' ELSE '' END)+@CSLSTITLE,SCHEME_SETUP_DET_ROW_ID=@CSCHEMEDETROWID,
			SCHEME_APPLIED_AMOUNT=#TMPCMD.SCHEME_APPLIED_AMOUNT+B.SCHEME_APPLIED_AMOUNT,
			BNGN_ROW_ID=@CBNGNROWID
			FROM #TMPCMDSCHEMES B WHERE B.ROW_ID=#TMPCMD.CMD_ROW_ID
			AND B.SCHEME_APPLIED_AMOUNT<>0
			
			UPDATE #TMPCMD SET DISCOUNT_PERCENTAGE=ABS(ROUND((DISCOUNT_AMOUNT/(MRP*QUANTITY))*100,3)),
			NET=(MRP*QUANTITY)-DISCOUNT_AMOUNT
			

			IF @bGvTitle=1
			BEGIN
				PRINT 'Now Update GV remarks:'+@nSpId+':'+@CSCHEMEDETROWID
				EXEC SP3S_UPDATE_PAYMODEGVDETAILS @nSpId,@CSCHEMEDETROWID
			END

			--IF @@SPID=79
			--SELECT 'AFTER DISTRIBUTING DISCOUNT',* FROM #TMPCMD
			
		END		
		
		IF @BQUALIFIED=1
			GOTO START_PROC
		ELSE	
		IF @BPROCESSSLRENTRY=0 AND @CPICKSLRDISCMODE<>'3'
		BEGIN
			SET @BPROCESSSLRENTRY=1
			GOTO START_PROC
		END	
		ELSE
			GOTO END_PROC

	END	

END_PROC:
END
----- END OF PROCEDURE SP3S_EOSS_SCHEMES_4