create PROC SP3S_PENDCHALLAN
( 
  @CDT DATETIME ,
  @CLOC VARCHAR(10)=''
)
AS
BEGIN
	SELECT AC_NAME,MRR_NO,CONVERT(VARCHAR,INV_DT,105) AS INV_DT,INV_NO,CONVERT(VARCHAR,RECEIPT_DT,105) AS RECEIPT_DT,
	TOTAL_AMOUNT ,DATEDIFF(DD,RECEIPT_DT,@CDT) AS DUEDAYS
	FROM PIM01106 A (NOLOCK)
	JOIN LM01106 b (NOLOCK) ON a.ac_code =b.AC_CODE 
	WHERE BILL_CHALLAN_MODE=1 AND CANCELLED =0 AND RECEIPT_DT <=  @CDT AND (@CLOC= '' OR a.DEPT_ID=@CLOC)
	and isnull(a.ref_converted_mrntobill_mrrid ,'')=''
END

