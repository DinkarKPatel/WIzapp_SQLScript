UPDATE K SET LASTKEYVAL=PREFIX+REPLICATE('0',ISNULL(TL.LN,10)-LEN(PREFIX)-LEN(REPLACE(LASTKEYVAL,PREFIX,'')))+REPLACE(LASTKEYVAL,PREFIX,'')
FROM KEYS K
LEFT JOIN (SELECT 'ARC01106'TAB,MAX(LEN(ADV_REC_NO))LN FROM ARC01106(NOLOCK)
	       UNION
	       SELECT 'IRM01106'TAB,MAX(LEN(IRM_MEMO_NO))LN FROM IRM01106(NOLOCK)
	      )TL ON K.TABLENAME=TL.TAB
WHERE FINYEAR='01119' AND TABLENAME IN ('ARC01106','IRM01106') AND LEN(LASTKEYVAL)<ISNULL(TL.LN,10)
