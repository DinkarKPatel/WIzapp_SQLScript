CREATE VIEW VW_GV_STK_TRANSFER_LIST
AS   
 SELECT T1.MEMO_NO   
 ,T1.MEMO_DT AS MEMO_DATE   
 ,T4.DEPT_NAME AS TARGET_DEPT_NAME
 ,CASE WHEN T1.CANCELLED =0 THEN '' ELSE 'CANCELLED' END AS CANCELLED  
 ,T3.DEPT_NAME AS SOURCE_DEPT_NAME ,T3.DEPT_ID,T1.TARGET_DEPT_ID,T1.MEMO_ID ,T1.FIN_YEAR  ,T1.location_code
 FROM GV_STKXFER_MST T1 
 JOIN LOCATION T3 ON T3.DEPT_ID = T1.location_code/*LEFT(T1.MEMO_ID,2)*//*Rohit 04-11-2024*/
 JOIN LOCATION T4 ON T4.DEPT_ID = T1.TARGET_DEPT_ID
