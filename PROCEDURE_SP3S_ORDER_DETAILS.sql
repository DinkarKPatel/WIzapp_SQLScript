create PROCEDURE SP3S_ORDER_DETAILS 
(
	@CXNTYPE  VARCHAR(100)='',
	@CMEMO_ID VARCHAR(100)=''
)
AS
BEGIN
	IF @CXNTYPE ='WPS'	
		EXEC SP3S_ORDER_DETAILS_WPS @CWHERE=@cMEMO_ID
	ELSE IF @CXNTYPE ='INV'	
		EXEC SP3S_ORDER_DETAILS_INV @CWHERE=@cMEMO_ID
	ELSE IF @CXNTYPE ='INV_PS'	
		EXEC SP3S_ORDER_DETAILS_INV_PS @CWHERE=@cMEMO_ID
	ELSE IF @CXNTYPE ='JWI'	
		EXEC SP3S_ORDER_DETAILS_JWI @CWHERE=@cMEMO_ID
	ELSE IF @CXNTYPE ='JWR'	
		EXEC SP3S_ORDER_DETAILS_JWR @CWHERE=@cMEMO_ID
	ELSE IF @CXNTYPE ='PO_WBO'	
		EXEC SP3S_ORDER_DETAILS_PO_WBO @CWHERE=@cMEMO_ID
	ELSE IF @CXNTYPE ='PO_RBO'	
		EXEC SP3S_ORDER_DETAILS_PO_RBO @CWHERE=@cMEMO_ID
    ELSE IF @CXNTYPE ='PUR_PO'	
		EXEC SP3S_DETAILS_PUR_PO @CWHERE=@cMEMO_ID
	
END 


