


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_PCIMST_LASTUPDATE' AND VALUE=1)
BEGIN
	PRINT 'UPDATING LAST_UPDATE COLUMN IN PCI MST FROM GETDATE'

	UPDATE PCI_MST SET LAST_UPDATE=GETDATE() 
	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_PCIMST_LASTUPDATE')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_PCIMST_LASTUPDATE',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_PCIMST_LASTUPDATE'
	
END		
