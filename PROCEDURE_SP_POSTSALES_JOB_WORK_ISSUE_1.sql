CREATE PROCEDURE SP_POSTSALES_JOB_WORK_ISSUE_1      
(        
  @IMODE INT ,        
  @CWHERE VARCHAR(4000)='',    
  @CAGENCYCODE CHAR(7)='',    
  @BMODE BIT = 0 ,  
  @FINYEAR VARCHAR(10)=''  ,  
  @DEPTID VARCHAR(10)=''       
)    
----WITH ENCRYPTION        
      
AS        
BEGIN 

  SELECT ISSUE_ID,ISSUE_NO FROM POST_SALES_JOBWORK_ISSUE_MST (NOLOCK)  
