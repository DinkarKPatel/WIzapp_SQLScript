


----MAINTAINANCE FOR BACKUP OF BILL_BY_BILL_REF
IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='BACKUP_BBREF' AND VALUE=1)
BEGIN
	PRINT 'MAINTAINANCE FOR BACKUP OF BILL_BY_BILL_REF'
	
	DECLARE @CCMD NVARCHAR(MAX)
	SET @CCMD=N'IF OBJECT_ID(''BILL_BY_BILL_REF'',''U'') IS NOT NULL AND OBJECT_ID(''BILL_BY_BILL_REF_BACKUP_20171127'',''U'') IS  NULL
					SELECT * INTO BILL_BY_BILL_REF_BACKUP_20171127 FROM BILL_BY_BILL_REF'
	EXEC SP_EXECUTESQL @CCMD
	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='BACKUP_BBREF')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('BACKUP_BBREF',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='BACKUP_BBREF'
END
