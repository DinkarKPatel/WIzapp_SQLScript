CREATE PROCEDURE SPPPC_SIZEGROUP_BOM
(
 @CARTICLE VARCHAR(10)=''
)
AS
BEGIN
    
    SELECT A.ARTICLE_NO AS BOM_NO,A.ARTICLE_NAME AS BOM_NAME, A.ARTICLE_CODE AS BOM_CODE,
    C.SIZEGROUP_CODE ,SIZEGROUP_NAME
    FROM ARTICLE A
    JOIN ART_DET B ON A.ARTICLE_CODE=B.ARTICLE_CODE
    JOIN PPC_ART_DET_SIZEGROUP  C ON A.ARTICLE_CODE =C.ARTICLE_CODE --AND C.PARA2_CODE=B.PARA2_CODE
    JOIN PPC_SIZEGROUP D ON D.SIZEGROUP_CODE=C.SIZEGROUP_CODE
    WHERE A.ARTICLE_CODE=@CARTICLE
    GROUP BY A.ARTICLE_NO,A.ARTICLE_NAME, A.ARTICLE_CODE,
    C.SIZEGROUP_CODE ,SIZEGROUP_NAME
    
END
