

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_VD01106_COSTCENTER' AND VALUE=1)
BEGIN
	PRINT 'UPDATING COST CENTER COLUMN DEPT ID COLUMN IN VD01106'

	DECLARE @BENABLENEWACCOUNTS BIT
	
	--SELECT TOP 1 @BENABLENEWACCOUNTS = ENABLEPURPOSTING FROM PURCONFIGMST WHERE ENABLEPURPOSTING=1
