	IF ISNULL(@BENABLENEWACCOUNTS,0)=1
	BEGIN	
		
		UPDATE A SET COST_CENTER_DEPT_ID=B.DEPT_ID FROM VD01106 A JOIN VM01106 B ON A.VM_ID=B.VM_ID
		WHERE ISNULL(A.COST_CENTER_DEPT_ID,'')=''
		
		IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_VD01106_COSTCENTER')
			INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
