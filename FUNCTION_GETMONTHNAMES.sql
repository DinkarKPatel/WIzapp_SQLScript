CREATE FUNCTION GETMONTHNAMES (@DFROMDT DATETIME,@DTODT DATETIME)
RETURNS @TBLDATES TABLE (XN_DT DATETIME,MONTH_NAME VARCHAR(3))
--WITH ENCRYPTION
AS
BEGIN
	WHILE @DFROMDT<=@DTODT
	BEGIN
		INSERT @TBLDATES VALUES (@DFROMDT,LEFT(DATENAME(MONTH,@DFROMDT),3))
		SET @DFROMDT=DATEADD(MONTH,1,@DFROMDT)
	END
	RETURN
END
