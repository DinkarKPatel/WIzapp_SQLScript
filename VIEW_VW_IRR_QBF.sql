CREATE VIEW VW_IRR_QBF

AS

SELECT A.IRM_MEMO_ID,IRM_MEMO_NO,IRM_MEMO_DT,A.REMARKS,TYPE,A.DEPT_ID,RATE_TYPE,REVISION_TYPE,INC_TYPE,
       B.PRODUCT_CODE,QUANTITY ,OLD_MRP,NEW_MRP,OLD_WSP,NEW_WSP,D.ARTICLE_NO,
       SUB_SECTION_NAME,SECTION_NAME,B.NEW_PRODUCT_CODE,G.PARA1_NAME,
       H.PARA2_NAME,I.PARA3_NAME,J.PARA4_NAME,K.PARA5_NAME,L.PARA6_NAME,B.SRNO
FROM IRM01106 A
JOIN IRD01106 B ON A.IRM_MEMO_ID= B.IRM_MEMO_ID
JOIN SKU C ON B.PRODUCT_CODE= C.PRODUCT_CODE 
JOIN ARTICLE D ON C.ARTICLE_CODE= D.ARTICLE_CODE
JOIN SECTIOND E ON D.SUB_SECTION_CODE= E.SUB_SECTION_CODE
JOIN SECTIONM F ON E.SECTION_CODE= F.SECTION_CODE
JOIN PARA1 G ON B.PARA1_CODE= G.PARA1_CODE 
JOIN PARA2 H ON B.PARA2_CODE= H.PARA2_CODE 
JOIN PARA3 I ON B.PARA3_CODE= I.PARA3_CODE 
JOIN PARA4 J ON B.PARA4_CODE= J.PARA4_CODE 
JOIN PARA5 K ON B.PARA5_CODE= K.PARA5_CODE 
JOIN PARA6 L ON B.PARA6_CODE= L.PARA6_CODE
