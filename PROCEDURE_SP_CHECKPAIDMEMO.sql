CREATE PROCEDURE SP_CHECKPAIDMEMO 
@CCM_ID	VARCHAR(100)=''
--WITH ENCRYPTION
AS
BEGIN
	DECLARE @CCUSTCODE	BIT
	SET @CCUSTCODE=0
	IF EXISTS (SELECT TOP 1 A.CM_ID 
				FROM CMM01106 A (NOLOCK)
				JOIN DSD01106 B (NOLOCK) ON B.CM_ID=A.CM_ID
				JOIN DSM01106 C (NOLOCK) ON C.DS_ID=B.DS_ID
				WHERE A.CANCELLED=0 AND C.CANCELLED=0 
				AND A.CM_ID=@CCM_ID)
	SET @CCUSTCODE=1
	
	SELECT @CCUSTCODE AS 'PAID'
END
