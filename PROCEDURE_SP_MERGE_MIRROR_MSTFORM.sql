CREATE PROCEDURE SP_MERGE_MIRROR_MSTFORM
(
 @NMODE INT
 ,@XN_TYPE VARCHAR(50) ='MSTFORM'
)
--WITH ENCRYPTION
AS
BEGIN
       DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
              ,@CTEMPLOCTABLE VARCHAR(100),@CSTATECODEOUT CHAR(7),@CSTATECODE VARCHAR(10)
              ,@CCURDEPTID VARCHAR(10),@CHODEPTID VARCHAR(10),@BHOLOC BIT,@NLOCTYPE VARCHAR(50)
              ,@BPURLOC VARCHAR(10),@CTABLENAME VARCHAR(100),@CTMP_TABLENAME VARCHAR(100)
              ,@CKEYFIELD1 VARCHAR(100),@CTEMPLOCSSTTABLE VARCHAR(500),@CTEMPTABLE VARCHAR(500)
              ,@CTEMPLOCSSTMSTTABLE VARCHAR(500),@BPROCEED BIT,@CKEYFIELD2 VARCHAR(500),@CKEYFIELD3 VARCHAR(500) 
	          ,@CSOURCETABLE VARCHAR(1000),@NXNSMERGINGORDER INT,@CSOURCEDB VARCHAR(200),@CERRORMSG VARCHAR(MAX)	
	          
      DECLARE @TXNSMERGEINFO TABLE (ORG_TABLENAME VARCHAR(50),TMP_TABLENAME VARCHAR(50),XN_ID VARCHAR(40))
       
BEGIN TRY

	  
	  SELECT @CSOURCEDB=DB_NAME()+'.DBO.',@CERRORMSG=''	
	  
	  IF ISNULL(@NMODE,0) = 1
         GOTO END_PROC;
     			
	  BEGIN TRANSACTION
      
      SET @CSTEP = 00  
  
	  SELECT @CCURDEPTID = VALUE FROM CONFIG (NOLOCK) WHERE CONFIG_OPTION='LOCATION_ID'  
	  SELECT @CHODEPTID = VALUE FROM CONFIG (NOLOCK) WHERE CONFIG_OPTION='HO_LOCATION_ID'  
	  
	  IF @CCURDEPTID=@CHODEPTID
	  BEGIN
			SET @CERRORMSG='P:SP_MERGE_MIRROR_MSTFORM, CANNOT CALL MERGING PROC AT HEAD OFFICE'
			GOTO END_PROC
  	  END					

	  SET @CSTEP = 05    
	    
	  SELECT @NLOCTYPE=LOC_TYPE,@BPURLOC=PUR_LOC FROM LOCATION WHERE DEPT_ID=@CCURDEPTID  
	       

	  SET @CSTEP = 10   
	  
	   SET @CKEYFIELD2 =''
	   SET @CKEYFIELD3 =''
	   
	   SET @CSTEP = 20   
	   
	   SET @CTABLENAME ='FORM'
	   SET @CKEYFIELD1='FORM_ID'
	   SET @CSOURCETABLE='TMP_MSTFORM_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CCURDEPTID))
	   SET @CTEMPTABLE=@CSOURCEDB+@CSOURCETABLE
	   PRINT 'MERGE MASTER :'+@CTABLENAME+'  '+@CSOURCETABLE

	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB,
	   @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',
	   @CDESTTABLE=@CTABLENAME,
	   @CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,
	   @CKEYFIELD3=@CKEYFIELD3,
	   @LINSERTONLY=0,
	   @CFILTERCONDITION='',
	   @LUPDATEONLY=0,
	   @BALWAYSUPDATE=1
    
    GOTO END_PROC
END TRY

BEGIN CATCH
	SET @CERRORMSG='P:SP_MERGE_MIRROR_MSTFORM, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO END_PROC
END CATCH

END_PROC:
	
	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRORMSG,'')='' 
			COMMIT
		ELSE
			ROLLBACK	
	END
	
	IF ISNULL(@CERRORMSG,'')='' 
	BEGIN   
		--DROP TEMP TABLE RELATED TO LICENSE_INFO_HO
	   SET @CSTEP = 50
	   SET @CTABLENAME = 'FORM'
	   SET @CSOURCETABLE ='TMP_MSTFORM_'+@CTABLENAME+'_'+@CCURDEPTID+''
	   SET @DTSQL =N'IF OBJECT_ID('''+@CSOURCETABLE+''') IS NOT NULL 
	                 DROP TABLE '+@CSOURCETABLE+''
	   EXEC SP_EXECUTESQL @DTSQL
	END
		
	SELECT 'MSTFORM' AS MEMO_ID,@CERRORMSG AS ERRMSG   	  
END
----- END OF PROCEDURE SP_MERGE_MIRROR_MSTFORM
