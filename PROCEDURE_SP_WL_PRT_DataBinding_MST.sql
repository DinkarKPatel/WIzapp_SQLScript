

-- GET ALL SUB SECTIONS
CREATE PROCEDURE SP_WL_PRT_DATABINDING_MST
(
	@CRMID VARCHAR(25)
)
--WITH ENCRYPTION
AS
BEGIN
	SELECT USERS.USERNAME,  C.AC_NAME AS SUPP_NAME, 
           C.ADDRESS1 + ' ' + C.ADDRESS2 + ', ' + C.AREA_NAME + ' ' + C.CITY + ' ' + C.STATE AS 'SUPP_ADDRESS',
           RMM01106.*, CONVERT(CHAR(10),CASE WHEN RM_DT='' THEN NULL ELSE RM_DT END ,105)  AS RM_DT1
           --, CONVERT(CHAR(10),CASE WHEN GRLR_DATE='' THEN NULL ELSE GRLR_DATE END ,105)  AS GRLR_DATE1
    FROM RMM01106  
    JOIN USERS ON RMM01106.USER_CODE = USERS.USER_CODE 
    JOIN LMV01106 C ON RMM01106.AC_CODE = C.AC_CODE
    WHERE RMM01106.RM_ID =@CRMID

END 
