CREATE PROCEDURE SP_RETAILSALE_40--(LocId 3 digit change by Sanjay:06-11-2024)
@cCM_ID VARCHAR(50)
AS
BEGIN
	SELECT ISNULL(LOC.Enable_EInvoice,0) AS Enable_EInvoice,A.CM_ID,A.EINV_IRN_NO,ISNULL(A.Party_Gst_No,'')  AS [PARTY_GST_NO],A.CM_DT
	FROM CMM01106 A  (NOLOCK)  
	JOIN LOCATION LOC  (NOLOCK) ON LOC.dept_id=a.location_Code
	JOIN CUSTDYM B  (NOLOCK) ON B.CUSTOMER_CODE=A.CUSTOMER_CODE   
	JOIN lm01106 b1  (NOLOCK) ON b1.ac_code=a.ac_code 
	LEFT OUTER JOIN LMP01106 LMP  (NOLOCK) ON B1.AC_CODE=LMP.AC_CODE 
	WHERE A.CM_ID=@cCM_ID
END