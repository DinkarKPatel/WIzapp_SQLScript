CREATE FUNCTION FN_SLSSET_CALCTOTDISC ( @NSLSDISC1 NUMERIC(6,2),@NSLSDISC2 NUMERIC(6,2),@NSLSDISC3 NUMERIC(6,2))
RETURNS NUMERIC(6,2)
--WITH ENCRYPTION
AS
BEGIN
	DECLARE @NRETVAL NUMERIC(6,2)
	
	SET @NRETVAL=0

	SET @NRETVAL	= ISNULL(@NSLSDISC1,0)

	IF(ISNULL(@NSLSDISC2,0)>0)
		SET @NRETVAL	=@NRETVAL+((100-@NRETVAL) * @NSLSDISC2/100)
		
	IF(ISNULL(@NSLSDISC3,0)>0)
		SET @NRETVAL	=@NRETVAL+((100-@NRETVAL) * @NSLSDISC3/100)

	RETURN @NRETVAL
END
