CREATE VIEW VW_PETTYEXPENSE_LIST

AS
SELECT A.PEM_MEMO_ID AS MEMO_ID ,PEM_MEMO_NO AS MEMO_NO,PEM_MEMO_DT AS MEMO_DT,FIN_YEAR,  
L.AC_NAME ,XN_TYPE ,SUM(XN_AMOUNT)   AS XN_AMOUNT,B.NARRATION,
CASE WHEN CANCELLED=0 THEN '' ELSE 'CANCELLED' END AS CANCELLED,A.location_Code
FROM PEM01106  A 
JOIN PED01106  B ON A.PEM_MEMO_ID= B.PEM_MEMO_ID 
JOIN LMV01106 L ON B.AC_CODE = L.AC_CODE 
GROUP BY A.PEM_MEMO_ID,PEM_MEMO_NO,PEM_MEMO_DT,FIN_YEAR,CANCELLED,L.AC_NAME,XN_TYPE,
B.NARRATION,A.location_Code
