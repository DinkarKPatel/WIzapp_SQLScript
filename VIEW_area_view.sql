CREATE VIEW AREA_VIEW
--WITH ENCRYPTION  
AS   
 SELECT AREA.AREA_CODE, AREA.AREA_NAME, AREA.PINCODE,   
   CITY.CITY_CODE, CITY.CITY, CITY.INACTIVE AS INACTIVE_CITY,  
   STATE.STATE_CODE, STATE.STATE, STATE.OCTROI_PERCENTAGE, 
   REGIONM.REGION_CODE, REGIONM.REGION_NAME  
 FROM AREA   
 JOIN CITY ON AREA.CITY_CODE = CITY.CITY_CODE  
 JOIN STATE ON CITY.STATE_CODE = STATE.STATE_CODE  
 JOIN REGIONM ON STATE.REGION_CODE = REGIONM.REGION_CODE
