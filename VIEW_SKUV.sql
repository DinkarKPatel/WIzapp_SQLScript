CREATE VIEW SKUV 
--WITH ENCRYPTION
AS
SELECT PRODUCT_CODE,ARTICLE_NO, PARA1_NAME, PARA2_NAME, PARA3_NAME, a.MRP 
FROM SKU A 
JOIN ARTICLE B ON A.ARTICLE_CODE=B.ARTICLE_CODE 
JOIN PARA1 C ON A.PARA1_CODE=C.PARA1_CODE 
JOIN PARA2 D ON A.PARA2_CODE=D.PARA2_CODE
JOIN PARA3 E ON A.PARA3_CODE=E.PARA3_CODE
