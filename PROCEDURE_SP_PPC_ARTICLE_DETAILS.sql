CREATE PROCEDURE SP_PPC_ARTICLE_DETAILS
(
@ARTICLE_NO  VARCHAR(300)=''
)
AS
BEGIN
	 SELECT A.ARTICLE_CODE, ARTICLE_NAME,B.SUB_SECTION_CODE,SUB_SECTION_NAME,P1.PARA1_CODE, 
	 A.PURCHASE_PRICE AS GROSS_PURCHASE_PRICE ,
    PARA1_NAME,ISNULL(P2.PARA2_CODE,'0000000') AS PARA2_CODE,ISNULL(P2.PARA2_NAME,'NA') AS PARA2_NAME,ISNULL(A.HSN_CODE,'')HSN_CODE    
	FROM ARTICLE A 
	JOIN SECTIOND B ON A.SUB_SECTION_CODE=B.SUB_SECTION_CODE
	JOIN ART_PARA1 AP1 ON AP1.ARTICLE_CODE =A.ARTICLE_CODE
	JOIN PARA1 P1 ON P1.PARA1_CODE =AP1.PARA1_CODE 
	LEFT JOIN PARA2 P2 ON P2.PARA2_NAME  =A.SIZE
	WHERE (@ARTICLE_NO='' OR A.ARTICLE_NO=@ARTICLE_NO)
END
