CREATE PROCEDURE SP3S_SYNCH_UPLOADDATA_PCI_OPT
(
    @CSPID VARCHAR(50)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
BEGIN
      DECLARE @DTSQL NVARCHAR(MAX),@CTMP_TABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(100),
      @CTABLESUFFIX VARCHAR(100),
      @CERRORMSG VARCHAR(MAX),@CSTEP VARCHAR(10),@CKEYFIELD  VARCHAR(50),@CTABLENAME VARCHAR(100),
      @CERRMSGOUT VARCHAR(500), @CMEMOID VARCHAR(50),@CSOURCEDB VARCHAR(200),@CMERGEDB VARCHAR(200),@BMSTINSERTONLY BIT,
	  @CFILTERCONDITION VARCHAR(1000)
      
    BEGIN TRY	
      SET @CSTEP = 10  
      --SET @CMERGEDB= DB_NAME()
      SET @CERRORMSG=''   
   
	 BEGIN TRANSACTION
	  --SET @CSOURCEDB=DB_NAME()+'_TEMP.DBO.'
	  SET @CTABLE_SUFFIX='UPLOAD'

	  SET @CSOURCEDB=''
	  SET @CMERGEDB=''

	  	SELECT TOP 1 @CMEMOID = B.MEMO_ID 
		FROM PCI_PCI_MST_UPLOAD B (NOLOCK)
		WHERE  SP_ID=@CSPID 
   
                 
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		
    SET @CFILTERCONDITION = 'B.MEMO_ID='''+@CMEMOID+''' AND B.SP_ID='''+LTRIM(RTRIM((@CSPID)))+''''
	
	 
	    SET @CSTEP = 20  
        SET @CTABLENAME='PCI_MST'  
        SET @CTMP_TABLENAME='PCI_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))  
        SET @CKEYFIELD='MEMO_ID'  
        SET @CSTEP=60 
         
        EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB  
         ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''  
         ,@LINSERTONLY=0,@CJOINSTR='',@LUPDATEONLY=0  
         ,@BALWAYSUPDATE=1  ,@CFILTERCONDITION=@CFILTERCONDITION 
	 

	 DELETE FROM PCI_PCI_MST_UPLOAD WHERE SP_ID=@CSPID
	 END TRY
     BEGIN CATCH
     SET @CERRMSG='P:SP3S_SYNCH_UPLOADDATA_PCI_OPT, STEP:'+LTRIM(RTRIM(STR(@CSTEP)))+', MESSAGE:'+ERROR_MESSAGE()
     END CATCH
     
EXIT_PROC:
		 IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0  
         BEGIN  
			 COMMIT      
         END  
         ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0  
				ROLLBACK  

END
