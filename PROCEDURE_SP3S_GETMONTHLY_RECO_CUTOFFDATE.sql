CREATE PROCEDURE SP3S_GETMONTHLY_RECO_CUTOFFDATE
@CLOCID VARCHAR(5)	
AS
BEGIN
	DECLARE @NCUTOFFDAYS INT,@DSETUPDATE DATETIME

	SELECT TOP 1 @NCUTOFFDAYS=ISNULL(MONTHLYRECO_CUTOFF_DAYS,0),@DSETUPDATE=ISNULL(MONTHLYRECO_CUTOFFDAYS_SETUP_DATE,'')
	FROM LOCATION WHERE DEPT_ID=@CLOCID
	
	SET @NCUTOFFDAYS=ISNULL(@NCUTOFFDAYS,370)
	
	IF @NCUTOFFDAYS=0 OR @DSETUPDATE=''
		SET @NCUTOFFDAYS=370
	
	IF @NCUTOFFDAYS<>370 AND @DSETUPDATE<>''
	BEGIN
		IF DATEDIFF(DD,@DSETUPDATE,GETDATE())>3
			SET @NCUTOFFDAYS=370
	END	
	
	SELECT @NCUTOFFDAYS AS CUT_OFF_DAYS 
END
