

--**** DELETING SENT STATUS OF LOCDTRECON FROM XFERLOG
--**** SO THAT ENTRIES DON'T GET REPEATEDLY SENT
IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='DELETE_LOCDTRECON_STATUS' AND VALUE=1)
BEGIN

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='DELETE_LOCDTRECON_STATUS')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('DELETE_LOCDTRECON_STATUS',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='DELETE_LOCDTRECON_STATUS'
	
	DELETE FROM XFERLOG WHERE XN_TYPE='XNSDTR'
END		
