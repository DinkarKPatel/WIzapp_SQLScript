CREATE VIEW SPPPC_VW_AGENCY_RM_PRINT
AS
  
  SELECT A.REF_ROW_ID AS FG_ISSUE_ID,
         B.AGENCY_CODE ,
         B.MEMO_ID ,B.MEMO_NO,B.MEMO_DT ,
         SKU.ARTICLE_CODE,ART.ARTICLE_NO,ART.ARTICLE_NAME,
         SKU.PRODUCT_CODE,
         SUM(A.QUANTITY ) AS QUANTITY,
         UOM.UOM_NAME ,
         CAST('' AS VARCHAR(100)) AS IMAGE_1,
         ISNULL(A.DEBIT_MATERIAL,0) AS DEBIT_MATERIAL,
         RATE,
         CONVERT(NUMERIC(10,2),SUM(A.QUANTITY*RATE  )) AS AMOUNT,
         SD.SUB_SECTION_NAME AS SUB_SECTION
  FROM PPC_AGENCY_ISSUE_MATERIAL_DET A
  JOIN PPC_AGENCY_ISSUE_MATERIAL_MST B ON A.MEMO_ID=B.MEMO_ID
  JOIN PPC_SKU SKU ON SKU.PRODUCT_UID=A.PRODUCT_UID
  JOIN ARTICLE ART ON ART.ARTICLE_CODE=SKU.ARTICLE_CODE
  JOIN SECTIOND SD ON SD.SUB_SECTION_CODE =ART.SUB_SECTION_CODE 
  JOIN UOM ON UOM.UOM_CODE =ART.UOM_CODE 
  WHERE B.CANCELLED=0
  GROUP BY A.REF_ROW_ID ,B.AGENCY_CODE ,SKU.PRODUCT_CODE,
  B.MEMO_ID ,B.MEMO_NO,B.MEMO_DT ,
 ISNULL(A.DEBIT_MATERIAL,0),SKU.ARTICLE_CODE,ART.ARTICLE_NO,ART.ARTICLE_NAME,UOM.UOM_NAME ,
 RATE, B.BILL_NO ,SD.SUB_SECTION_NAME
