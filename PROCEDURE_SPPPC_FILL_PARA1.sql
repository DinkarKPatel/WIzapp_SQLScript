CREATE PROCEDURE [DBO].[SPPPC_FILL_PARA1]  
(  
 @NQUERYID   INT,    
 @VPARAMETER  VARCHAR(500)  
)  
AS      
BEGIN   
  
 DECLARE @CSTEP INT, @CCMD NVARCHAR(MAX), @ERRMSG_OUT VARCHAR(MAX)  
 BEGIN TRY  
  SET NOCOUNT ON;  
    
  SET @ERRMSG_OUT = ''  
    
  IF @NQUERYID = 1  
   -- GET PARA1 LIST  
   GOTO LBLGETPARA1LIST  
     
  ELSE IF @NQUERYID = 2  
   -- GET PARA1 USING LIKE  
   GOTO LBLGETPARA1LIKE  
    
  ELSE IF @NQUERYID = 7  
   -- GET INFO BY PARA1 NAME  
   GOTO LBLGETINFOBYPARA1NAME  
  
  ELSE  
   GOTO LAST  
  
-- GET PARA1 LIST  
LBLGETPARA1LIST:  
  SET @CSTEP = 101  
  SELECT A.PARA1_CODE, A.PARA1_NAME  
  FROM [PARA1] A (NOLOCK)   
  WHERE A.INACTIVE = 0  AND ISNULL(A.PARA1_NAME,'')<>''  
  ORDER BY A.PARA1_NAME  
    
  GOTO LAST  
  
  
-- GET PARA1 LIKE  
LBLGETPARA1LIKE:  
  SET @CSTEP = 102  
  SELECT TOP 10 A.PARA1_CODE, A.PARA1_NAME  
  FROM [PARA1] A (NOLOCK)   
  WHERE A.INACTIVE = 0  AND ISNULL(A.PARA1_NAME,'')<>''  
  AND A.PARA1_NAME LIKE '%'+ @VPARAMETER +'%'  
  ORDER BY A.PARA1_NAME  
    
  GOTO LAST  
  
  
-- GET INFO BY PARA1 NAME  
LBLGETINFOBYPARA1NAME:  
  SET @CSTEP = 107  
  SELECT A.PARA1_CODE, A.PARA1_NAME  
  FROM [PARA1] A (NOLOCK)   
  WHERE A.PARA1_NAME = @VPARAMETER  
    
  GOTO LAST  
    
LAST:     
 GOTO END_PROC  
  
  SET NOCOUNT OFF;  
 END TRY    
 BEGIN CATCH    
  SET @ERRMSG_OUT='ERROR: [P]: SPPPC_PARA1, [STEP]: '+CAST(@CSTEP AS VARCHAR(5))+', [MESSAGE]: ' + ERROR_MESSAGE()  
  GOTO END_PROC    
 END CATCH     
  
END_PROC:    
 IF  ISNULL(@ERRMSG_OUT,'')<>''   
  SELECT @ERRMSG_OUT AS ERRORMSG  
END
