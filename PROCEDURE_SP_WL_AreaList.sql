CREATE PROCEDURE SP_WL_AREALIST
--WITH ENCRYPTION
AS

	SELECT A.AREA_CODE, A.AREA_NAME, A.PINCODE, B.CITY, B.CITY_CODE, 
	C.STATE, C.STATE_CODE, D.REGION_CODE, D.REGION_NAME,E.COUNTRY_CODE,E.COUNTRY_NAME
	FROM AREA A
	JOIN CITY B ON A.CITY_CODE = B.CITY_CODE
	JOIN STATE C ON B.STATE_CODE = C.STATE_CODE
	JOIN REGIONM D ON C.REGION_CODE = D.REGION_CODE
	LEFT OUTER JOIN COUNTRY E ON D.COUNTRY_CODE = E.COUNTRY_CODE
	WHERE A.AREA_NAME <> '' 
	ORDER BY A.AREA_NAME
--************************** END OF PROCEDURE SP_WL_AREALIST
