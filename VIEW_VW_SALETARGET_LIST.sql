CREATE VIEW VW_SALETARGET_LIST

AS 
SELECT DISTINCT SECTION_NAME, 
(CAST(M.YEAR AS VARCHAR) +'-'+ CAST(M.MONTH AS VARCHAR) +'-'+ SM.SECTION_CODE) AS MEMO_ID, 
DATENAME(MM,(CAST(M.YEAR AS VARCHAR) +'-'+ CAST(M.MONTH AS VARCHAR) +'-01')) AS MONTH_NAME,
M.MONTH MONTH_NO,M.YEAR AS YEAR  
FROM MIN_SALE_TARGET M
JOIN SECTIOND SD ON M.SUB_SECTION_CODE = SD.SUB_SECTION_CODE 
JOIN SECTIONM SM ON SM.SECTION_CODE = SD.SECTION_CODE
