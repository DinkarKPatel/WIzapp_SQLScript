CREATE VIEW [DBO].[VW_JOB_RECIEPTLIST]  

AS  
  
SELECT RECEIPT_NO AS RECEIPT_NO,  
  CONVERT(VARCHAR,RECEIPT_DT,105) AS RECEIPT_DATE,NET_AMOUNT,  
  RECEIPT_ID AS MEMO_ID,(CASE WHEN CANCELLED = 1 THEN 'CANCELLED' ELSE '' END) AS CANCELLED ,  
  M.FIN_YEAR ,C.AGENCY_NAME ,M.location_Code
FROM JOBWORK_RECEIPT_MST  M (NOLOCK)  
JOIN PRD_AGENCY_MST C (NOLOCK) ON M.AGENCY_CODE = C.AGENCY_CODE
