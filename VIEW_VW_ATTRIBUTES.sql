CREATE VIEW VW_ATTRIBUTES AS
SELECT UPPER(TABLE_NAME)TABLE_NAME,UPPER(COLUMN_NAME) COLNAME,REPLACE(UPPER(COLUMN_NAME),'NAME','CODE')CODE,TABLE_CAPTION FROM CONFIG_ATTR  UNION
SELECT config_option,config_option+'_NAME',config_option+'_CODE',VALUE FROM CONFIG WHERE config_option LIKE 'PARA[1-6]_caption' UNION
SELECT 'ARTICLE','ARTICLE_NAME','ARTICLE_CODE','ARTICLE_NAME' UNION
SELECT 'SECTIONM','SECTION_NAME','SECTION_CODE','SECTION_NAME' UNION
SELECT 'SECTIOND','SUB_SECTION_NAME','SUB_SECTION_CODE','SUB_SECTION_NAME'
