CREATE PROCEDURE SP_MERGE_MIRROR_PSJWR_DATA
(
	@CMEMOID VARCHAR(50)
   ,@CLOCID VARCHAR(3)
   ,@CSOURCEDB VARCHAR(200)
   ,@CMERGEDB VARCHAR(200)
   ,@BMSTINSERTONLY BIT
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
BEGIN
    DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CTABLESUFFIX VARCHAR(100),@CCMD NVARCHAR(MAX)
	   
     DECLARE @TXNSSENDINFO TABLE (ORG_TABLENAME VARCHAR(50),TMP_TABLENAME VARCHAR(50),XN_ID VARCHAR(40))  
     
     
      BEGIN TRY
		SET @CSTEP=20
		SET @CTABLE_SUFFIX=REPLACE(@CMEMOID,'-','_')
		BEGIN TRANSACTION
		SET @CTABLESUFFIX=@CTABLE_SUFFIX
		
			SET @CSTEP=60
			SET @CTABLENAME='POST_SALES_JOBWORK_RECEIPT_MST'
			SET @CTMP_TABLENAME='TMP_PSJWR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
			SET @CKEYFIELD='RECEIPT_ID'
			SET @CSTEP=80
			EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
									  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1
			

					
			SET @CSTEP=100
			SET @CTABLENAME='POST_SALES_JOBWORK_RECEIPT_DET'
			SET @CTMP_TABLENAME='TMP_PSJWR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
			SET @CKEYFIELD='ROW_ID'
			
			SET @CCMD = N'DELETE A FROM POST_SALES_JOBWORK_RECEIPT_DET A LEFT OUTER JOIN 
			              '+@CSOURCEDB+'['+@CTMP_TABLENAME+'] B ON A.ROW_ID = B.ROW_ID 
			              WHERE B.ROW_ID IS NULL AND A.RECEIPT_ID = '''+@CMEMOID+''''
			PRINT @CCMD
			EXEC SP_EXECUTESQL @CCMD  
			
			SET @CSTEP=130
			EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
	   
	   
	END TRY
	BEGIN CATCH
		SET @CERRMSG='P:SP_MERGE_MIRROR_PSJWR_DATA, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	END CATCH
	EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
		 SET @CTABLESSTR='POST_SALES_JOBWORK_RECEIPT_MST,POST_SALES_JOBWORK_RECEIPT_DET,XNSTABLELIST'
     
			EXEC SP3S_DROPTEMPTABLES_MIRRORDATA
			@CXNTYPE='PSJWR',
			@CTABLESUFFIX=@CTABLE_SUFFIX,
			@CTABLESSTR=@CTABLESSTR 
		
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK   
	   
	   
END
