CREATE PROCEDURE SP3S_DELETE_UPLOAD_wpsMERGE_TABLES
@nSpId VARCHAR(50),
@cMemoId varchar(50)
AS
BEGIN
	
	DECLARE @cSpIdCopy VARCHAR(50)
	
	SET @cSpIdCopy=@nSpId+LEFT(@cMemoId,2)


	IF OBJECT_ID('TEMPDB..#T','U') IS NOT  NULL
	   DROP TABLE #T
	
	SELECT ps_ID
	INTO #T
	FROM WPS_WPS_MST_UPLOAD B (NOLOCK) WHERE PS_id=@nSpId OR PS_id=@cSpIdCopy

	DELETE A FROM WPS_WPS_DET_UPLOAD A JOIN #T B ON  A.PS_ID=B.PS_ID
	DELETE A FROM WPS_WPS_MST_UPLOAD A JOIN #T B ON  A.PS_ID=B.PS_ID
	DELETE A FROM WPS_PMT01106_UPLOAD A JOIN #T B ON  A.WPS_MEMO_ID=B.PS_ID
	delete a from WPS_BUYER_ORDER_WPS_LINK_UPLOAD  A where sp_id=@nSpId

	
END