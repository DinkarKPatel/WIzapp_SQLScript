CREATE PROCEDURE SP3S_VALIDATEXN_WPSMERGE
@cSpId VARCHAR(50),
@cMemoId VARCHAR(40),
@cErrormsg VARCHAR(MAX) OUTPUT
AS
BEGIN
	DECLARE @NNETAMOUNT NUMERIC(10,2),@NCMMSUBTOTAL NUMERIC(10,2),@NPAYMODETOTAMT NUMERIC(10,2),
	@nTotWpMQty NUMERIC(10,2),@nTotWpdQty NUMERIC(10,2),@cStep VARCHAR(4),@nPaymode NUMERIC(1,0)

	
	SET @CSTEP=10.8
	EXEC SP_CHKXNSAVELOG 'WPSMERGE',@CSTEP,0,@CSPID,'',1


	SELECT @nTotWpMQty=total_quantity FROM wps_mst (NOLOCK) WHERE ps_id=@cMemoId

	SET @CSTEP=11.2
	EXEC SP_CHKXNSAVELOG 'WPSMERGE',@CSTEP,0,@CSPID,'',1
		
	SELECT @nTotWpdQty = SUM(quantity) FROM wps_det (NOLOCK) WHERE ps_id=@cMemoId 

	SET @CSTEP=11.4
	EXEC SP_CHKXNSAVELOG 'WPSMERGE',@CSTEP,0,@CSPID,'',1		  
	SELECT @nTotWpMQty=ISNULL(@nTotWpMQty,0),@nTotWpdQty = ISNULL(@nTotWpdQty,0)  
	
	IF @nTotWpMQty<>@nTotWpdQty
	BEGIN  
		PRINT 'SP3S_VALIDATEXN_WPSMERGE Quantity match FAILED'
		SET @CERRORMSG='Bill level Total quantity '+STR(@nTotWpMQty,14,2)+' SHOULD BE EQUAL TO THE SUM OF Item level quantity '+STR(@nTotWpdQty,10,2)+'...PLEASE CHECK'  
		GOTO END_PROC
	END

END_PROC:
	
END