CREATE VIEW  VW_EMPATTVIEWER
--WITH ENCRYPTION 
AS 
	 SELECT A.ATTENDANCE_DT,A.EMP_ID,A.TIME_IN,A.TIME_OUT,A.ROW_ID,A.ENTRY_MODE,A.SHIFT_ID,A.SHIFT_TIME_IN,
	 A.SHIFT_TIME_OUT,A.HALFDAY_CUTOFF,A.EMPIMAGE,A.DEPT_ID,A.EMPIMAGE_OUT,B.DEPT_NAME,B.ADDRESS1,B.ADDRESS2,
	 C.AREA_NAME,C.REGION_NAME,C.STATE,C.PINCODE,D.REF_ID,D.EMP_TITLE,D.EMP_FNAME,D.EMP_LNAME,
	 E.DEPARTMENT_NAME,E.DEPARTMENT_ID,F.DESIG_ID,F.DESIG_NAME,G.SHIFT_NAME, D.EMP_STATUS
 FROM EMP_ATTENDANCE A          
 JOIN LOCATION B ON A.DEPT_ID=B.DEPT_ID          
 INNER JOIN AREA_VIEW  C ON B.AREA_CODE=C.AREA_CODE           
 JOIN EMP_MST D ON A.EMP_ID=D.EMP_ID          
 JOIN EMP_DEPARTMENT E ON D.DEPARTMENT_ID=E.DEPARTMENT_ID          
 JOIN EMP_DESIG F ON F.DESIG_ID=D.DESIG_ID    
 JOIN EMP_SHIFTS G ON A.SHIFT_ID=G.SHIFT_ID
