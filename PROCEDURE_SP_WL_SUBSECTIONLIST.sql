
-- GET ALL SUB SECTIONS
CREATE PROCEDURE SP_WL_SUBSECTIONLIST
--WITH ENCRYPTION
AS
BEGIN
	SELECT A.SUB_SECTION_CODE, A.SUB_SECTION_NAME, A.SECTION_CODE, B.SECTION_NAME  
	FROM SECTIOND A (NOLOCK)
	JOIN SECTIONM B (NOLOCK) ON A.SECTION_CODE = B.SECTION_CODE 
	WHERE A.INACTIVE = 0 AND B.INACTIVE = 0
	ORDER BY SECTION_NAME, SUB_SECTION_NAME
END
