create PROCEDURE SP_DSR_CREDITCARDDETAILS  
(          --PARAMETERS  
 @DFROMDT DATETIME,    --REPORT FROM DT  
 @DTODT  DATETIME,    --REPORT TO DT  
 @CDEPTID CHAR(4)='',    --DEPT ID (IF BLANK THIS PARAMETER THEN RUN THIS REPORT FOR ALL LOCATION  
 @NMODE  INT,      --@NMODE=1- BILL NO WISE; 2- DATE WISE
 @BESTIMATEENABLED BIT = 1 ,
 @CUSERCODE VARCHAR(15),
 @CBINID VARCHAR(10)
)  
--WITH ENCRYPTION
AS  
BEGIN  
--(dinkar) Replace  left(memoid,2) to Location_code 
	DECLARE @CMAJORDEPTID VARCHAR(5),@BDSM BIT,@CERRMSGOUT VARCHAR(500)


    --SELECT @CMAJORDEPTID=MAJOR_DEPT_ID FROM LOCATION (NOLOCK) WHERE DEPT_ID=@CDEPTID
   
    --SET @CMAJORDEPTID=ISNULL(@CMAJORDEPTID,'')
	/*
		CALL THIS PROCEDURE TO CHECK IF SOME BILLS ARE PENDING FOR CASHER. EITHER FROM CASH MEMO TO CASHIER OR
		PACKSLIP TO CASHMEMO.	   
   */
   EXEC SP3S_CHECK_PENDING_BILLS 
			 @DFROM_DT=@DFROMDT
			,@DTO_DT=@DTODT
			,@CDEPT_ID=@CDEPTID
			,@BESTIMATEENABLED=@BESTIMATEENABLED
			,@CUSERCODE=@CUSERCODE
			,@CBINID=@CBINID
			,@CERRMSGOUT=@CERRMSGOUT OUTPUT
	
	DECLARE @OUTPUTC TABLE (DEPT_ID VARCHAR(4), DEPT_NAME VARCHAR(200),
	BIN_ID VARCHAR(10), BIN_NAME VARCHAR(200),	CM_DT DATETIME, CM_NO VARCHAR(100),   
	NET_AMOUNT NUMERIC(12,2),   CASH_AMOUNT NUMERIC(12,2), 
	CC_AMOUNT NUMERIC(12,2),CN_AMOUNT NUMERIC(12,2), ADVANCE_AMOUNT_ADJUSTED NUMERIC(12,2),  
	OTHER_DOC_AMOUNT NUMERIC(12,2), CREDIT_AMOUNT NUMERIC(12,2), BANK_CHARGES NUMERIC(12,2), 
	MISC_AMOUNT NUMERIC(12,2), CANCELLED BIT,PAYMODE_NAME VARCHAR(100),CUST_NAME VARCHAR(200),
	SUBTOTAL NUMERIC(12,2),CC_NO VARCHAR(100))  

	--DECLARE @BDONOTSHOWITEMDISC BIT 

  
 INSERT @OUTPUTC ( DEPT_ID, DEPT_NAME,BIN_ID , BIN_NAME, CM_DT, CM_NO,  NET_AMOUNT,  
       CASH_AMOUNT, CC_AMOUNT, CN_AMOUNT, ADVANCE_AMOUNT_ADJUSTED,  
       OTHER_DOC_AMOUNT, CREDIT_AMOUNT, BANK_CHARGES, MISC_AMOUNT, CANCELLED, PAYMODE_NAME,
       CUST_NAME,SUBTOTAL,CC_NO)  
  
	SELECT C.DEPT_ID, C.DEPT_ID + ' ' + C.DEPT_NAME AS DEPT_NAME,
	B1.BIN_ID , B1.BIN_NAME ,  A.CM_DT,   
    A.CM_NO + (CASE WHEN A.CANCELLED=1 THEN ': CANCELLED' ELSE '' END) AS CM_NO,      
   (CASE WHEN A.CANCELLED=0 THEN A.NET_AMOUNT ELSE 0 END) AS NET_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN D.CASH_AMOUNT ELSE 0 END) AS CASH_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN P1.AMOUNT ELSE 0 END) AS CC_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN D.CN_AMOUNT ELSE 0 END) AS CN_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN D.ADVANCE_AMOUNT_ADJUSTED ELSE 0 END) AS ADVANCE_AMOUNT_ADJUSTED,   
   (CASE WHEN A.CANCELLED=0 THEN D.OTHER_DOC_AMOUNT ELSE 0 END) AS OTHER_DOC_AMOUNT,  
   (CASE WHEN A.CANCELLED=0 THEN D.CREDIT_AMOUNT+D.CREDIT_REFUND_AMOUNT ELSE 0 END) AS CREDIT_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN D.BANK_CHARGES ELSE 0 END) AS BANK_CHARGES,   
   (CASE WHEN A.CANCELLED=0 THEN D.MISC_AMOUNT ELSE 0 END) AS MISC_AMOUNT,  
   A.CANCELLED  ,
   P2.PAYMODE_NAME,
   CUST.CUSTOMER_TITLE+' '+ CUST.CUSTOMER_FNAME+' '+CUST.CUSTOMER_LNAME AS [CUST_NAME],
   (A.SUBTOTAL+A.SUBTOTAL_R) AS [SUBTOTAL],P1.REF_NO
 FROM CMM01106 A
 JOIN BIN B1 ON B1.BIN_ID=ISNULL(A.BIN_ID,'000')    
 JOIN LOCATION C ON a.location_Code  = C.DEPT_ID    
 JOIN CUSTDYM CUST ON CUST.CUSTOMER_CODE=A.CUSTOMER_CODE
 LEFT OUTER JOIN VW_BILL_PAYMODE D ON A.CM_ID = D.MEMO_ID AND D.XN_TYPE  = 'SLS'
 LEFT OUTER JOIN PAYMODE_XN_DET P1 ON P1.MEMO_ID= A.CM_ID  AND P1.XN_TYPE = 'SLS'
 LEFT OUTER JOIN PAYMODE_MST P2 ON P2.PAYMODE_CODE=P1.PAYMODE_CODE
 LEFT OUTER JOIN PAYMODE_GRP_MST P3 ON P3.PAYMODE_GRP_CODE=P2.PAYMODE_GRP_CODE
 --LEFT JOIN 
	--(
	--	SELECT DISTINCT  CM_ID FROM DSD01106 DSD(NOLOCK) JOIN DSM01106 DSM(NOLOCK) ON DSD.DS_ID=DSM.DS_ID
	--	WHERE DSM.CANCELLED=0
	--)DSD ON A.CM_ID=DSD.CM_ID
 WHERE  A.CM_DT BETWEEN @DFROMDT AND @DTODT   AND (A.MEMO_TYPE = 1 OR @BESTIMATEENABLED = 1)--CHANGE
 AND a.location_Code  =(CASE WHEN @CDEPTID='' THEN a.location_Code  ELSE @CDEPTID END)  
 AND ISNULL(A.BIN_ID,'000') =(CASE WHEN (@CBINID = '')  THEN ISNULL(A.BIN_ID,'000') ELSE @CBINID END)    
 AND A.USER_CODE=(CASE WHEN @CUSERCODE='' THEN A.USER_CODE ELSE @CUSERCODE END)
 AND P3.PAYMODE_GRP_CODE='0000002'
 
 
 UNION ALL
 
 SELECT C.DEPT_ID, C.DEPT_ID + ' ' + C.DEPT_NAME AS DEPT_NAME,
	B1.BIN_ID , B1.BIN_NAME ,  A.ADV_REC_DT,   
    A.ADV_REC_NO + (CASE WHEN A.CANCELLED=1 THEN ': CANCELLED' ELSE '' END) AS CM_NO,      
   (CASE WHEN A.CANCELLED=0 THEN A.NET_AMOUNT ELSE 0 END) AS NET_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN D.CASH_AMOUNT ELSE 0 END) AS CASH_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN P1.AMOUNT ELSE 0 END) AS CC_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN D.CN_AMOUNT ELSE 0 END) AS CN_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN D.ADVANCE_AMOUNT_ADJUSTED ELSE 0 END) AS ADVANCE_AMOUNT_ADJUSTED,   
   (CASE WHEN A.CANCELLED=0 THEN D.OTHER_DOC_AMOUNT ELSE 0 END) AS OTHER_DOC_AMOUNT,  
   (CASE WHEN A.CANCELLED=0 THEN D.CREDIT_AMOUNT+D.CREDIT_REFUND_AMOUNT ELSE 0 END) AS CREDIT_AMOUNT,   
   (CASE WHEN A.CANCELLED=0 THEN D.BANK_CHARGES ELSE 0 END) AS BANK_CHARGES,   
   (CASE WHEN A.CANCELLED=0 THEN D.MISC_AMOUNT ELSE 0 END) AS MISC_AMOUNT,  
   A.CANCELLED  ,
   P2.PAYMODE_NAME,
   CUST.CUSTOMER_TITLE+' '+ CUST.CUSTOMER_FNAME+' '+CUST.CUSTOMER_LNAME AS [CUST_NAME],
   0 AS [SUBTOTAL],P1.REF_NO
 FROM ARC01106 A
 JOIN BIN B1 ON B1.BIN_ID=ISNULL(A.BIN_ID,'000')    
 JOIN LOCATION C ON a.location_Code  = C.DEPT_ID    
 JOIN CUSTDYM CUST ON CUST.CUSTOMER_CODE=A.CUSTOMER_CODE
 LEFT OUTER JOIN VW_BILL_PAYMODE D ON A.CM_ID = D.MEMO_ID AND D.XN_TYPE  = 'ARC'
 LEFT OUTER JOIN PAYMODE_XN_DET P1 ON P1.MEMO_ID= A.ADV_REC_ID  AND P1.XN_TYPE = 'ARC'
 LEFT OUTER JOIN PAYMODE_MST P2 ON P2.PAYMODE_CODE=P1.PAYMODE_CODE
 LEFT OUTER JOIN PAYMODE_GRP_MST P3 ON P3.PAYMODE_GRP_CODE=P2.PAYMODE_GRP_CODE
 --LEFT JOIN 
	--(
	--	SELECT DISTINCT  CM_ID FROM DSD01106 DSD(NOLOCK) JOIN DSM01106 DSM(NOLOCK) ON DSD.DS_ID=DSM.DS_ID
	--	WHERE DSM.CANCELLED=0
	--)DSD ON A.CM_ID=DSD.CM_ID
 WHERE  A.ADV_REC_DT BETWEEN @DFROMDT AND @DTODT   
 --AND (@BESTIMATEENABLED = 1)--CHANGE
 AND a.location_Code  =(CASE WHEN @CDEPTID='' THEN  a.location_Code  ELSE @CDEPTID END)  
 AND ISNULL(A.BIN_ID,'000') =(CASE WHEN (@CBINID = '')  THEN ISNULL(A.BIN_ID,'000') ELSE @CBINID END)    
 AND A.USER_CODE=(CASE WHEN @CUSERCODE='' THEN A.USER_CODE ELSE @CUSERCODE END)
 AND P3.PAYMODE_GRP_CODE='0000002'
 --AND ((@BDSM=1 AND DSD.CM_ID IS NOT NULL) OR (@BDSM=0))	
 --IF @NMODE = 1
 --BEGIN
 -- IF OBJECT_ID('TEMPDB..#SP_DSR_DETAILSALE_INV') IS NOT NULL
