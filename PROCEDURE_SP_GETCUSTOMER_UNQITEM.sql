CREATE PROCEDURE SP_GETCUSTOMER_UNQITEM 
@CITEMCODE	VARCHAR(100)=''
--WITH ENCRYPTION
AS
BEGIN
	DECLARE @CCUSTCODE	VARCHAR(100)
	
	SELECT TOP 1 @CCUSTCODE= CUSTOMER_CODE
	FROM CMM01106 A (NOLOCK)
	JOIN CMD01106 B (NOLOCK) ON B.CM_ID=A.CM_ID
	WHERE 
	--A.CUSTOMER_CODE<>'000000000000' 
	--AND 
	B.QUANTITY>0 
	AND B.PRODUCT_CODE=@CITEMCODE
	ORDER BY A.CM_DT DESC,A.TS DESC
	
	SET @CCUSTCODE= ISNULL(@CCUSTCODE,'')
	
	EXEC SP_RETAILSALE 5,@CCUSTCODE,'',''
END
