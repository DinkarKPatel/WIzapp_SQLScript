CREATE PROCEDURE SP_SEND_MIRROR_PCI_DATA_NEW
(
 @CUPLOADEDXNID AS VARCHAR(50),
 @CCURLOCID VARCHAR(5)
 ,@BACKNOWLEDGE BIT =0	
 ,@COUTPUT VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
BEGIN
     DECLARE @DTSQL NVARCHAR(MAX),@CERRMSG VARCHAR(500),@CSTEP VARCHAR(100),
     @CMEMOID VARCHAR(100),@DMEMOLASTUPDATE DATETIME,@CTEMPDBNAME VARCHAR(100),
     @CTEMPTABLENAME VARCHAR(100)
     
  
     SET @CTEMPDBNAME=''	
     SET @CTEMPTABLENAME='PCI_MST'

  BEGIN TRY
      
	  SET @CSTEP=30
      SET @CMEMOID=@CUPLOADEDXNID
      
LBLTABLEINFO:
	  SET @CSTEP=40  
 
     SET @CSTEP=70
	SELECT A.*,'PCI_PCI_MST_UPLOAD' AS TARGET_TABLENAME FROM PCI_MST A (NOLOCK)  WHERE A.MEMO_ID=@CMEMOID
	
	  SET @CSTEP=80
	 
	  GOTO END_PROC
	     
  END TRY
    BEGIN CATCH
	SET @CERRMSG='P: SP_SEND_MIRROR_PCI_DATA_NEW, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO END_PROC
  END CATCH 
		
  END_PROC:
	PRINT 'END OF PROC:'+@CUPLOADEDXNID
	

END
