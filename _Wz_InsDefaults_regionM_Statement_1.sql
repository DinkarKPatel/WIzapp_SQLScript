--**** DEFAULT ENTRY INTO REGIONM TABLE
IF NOT EXISTS ( SELECT REGION_CODE FROM REGIONM WHERE REGION_CODE = '0000000' )
	INSERT REGIONM ( REGION_CODE, REGION_NAME, REMARKS, LAST_UPDATE ,INACTIVE) 
			VALUES ( '0000000', 'INDIA', '', '',0 )
ELSE
BEGIN
	UPDATE REGIONM SET REGION_NAME=REGION_NAME+'_'+REGION_CODE WHERE REGION_NAME='INDIA' AND REGION_CODE<>'0000000'
	
	UPDATE REGIONM SET REGION_NAME='INDIA' WHERE REGION_CODE='0000000'
END
