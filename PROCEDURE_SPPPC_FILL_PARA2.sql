CREATE PROCEDURE SPPPC_FILL_PARA2  
(  
 @NQUERYID   INT,    
 @VPARAMETER  VARCHAR(500)  
)  
AS      
BEGIN   
  
 DECLARE @CSTEP INT, @CCMD NVARCHAR(MAX), @ERRMSG_OUT VARCHAR(MAX)  
 BEGIN TRY  
  SET NOCOUNT ON;  
    
  SET @ERRMSG_OUT = ''  
    
  IF @NQUERYID = 1  
   -- GET PARA2 LIST  
   GOTO LBLGETPARA2LIST  
     
  ELSE IF @NQUERYID = 2  
   -- GET PARA2 USING LIKE  
   GOTO LBLGETPARA2LIKE  
    
  ELSE IF @NQUERYID = 7  
   -- GET INFO BY PARA2 NAME  
   GOTO LBLGETINFOBYPARA2NAME  
  
  ELSE  
   GOTO LAST  
  
-- GET PARA2 LIST  
LBLGETPARA2LIST:  
  SET @CSTEP = 101  
  SELECT A.PARA2_CODE, A.PARA2_NAME  
  FROM [PARA2] A (NOLOCK)   
  WHERE A.INACTIVE = 0  AND ISNULL(A.PARA2_NAME,'')<>''  
  ORDER BY A.PARA2_NAME  
    
  GOTO LAST  
  
  
-- GET PARA2 LIKE  
LBLGETPARA2LIKE:  
  SET @CSTEP = 102  
  SELECT TOP 10 A.PARA2_CODE, A.PARA2_NAME  
  FROM [PARA2] A (NOLOCK)   
  WHERE A.INACTIVE = 0  AND ISNULL(A.PARA2_NAME,'')<>''  
  AND A.PARA2_NAME LIKE '%'+ @VPARAMETER +'%'  
  ORDER BY A.PARA2_NAME  
    
  GOTO LAST  
  
  
-- GET INFO BY PARA2 NAME  
LBLGETINFOBYPARA2NAME:  
  SET @CSTEP = 107  
  SELECT A.PARA2_CODE, A.PARA2_NAME  
  FROM [PARA2] A (NOLOCK)   
  WHERE A.PARA2_NAME = @VPARAMETER  
    
  GOTO LAST  
    
LAST:     
 GOTO END_PROC  
  
  SET NOCOUNT OFF;  
 END TRY    
 BEGIN CATCH    
  SET @ERRMSG_OUT='ERROR: [P]: SPPPC_PARA2, [STEP]: '+CAST(@CSTEP AS VARCHAR(5))+', [MESSAGE]: ' + ERROR_MESSAGE()  
  GOTO END_PROC    
 END CATCH     
  
END_PROC:    
 IF  ISNULL(@ERRMSG_OUT,'')<>''   
  SELECT @ERRMSG_OUT AS ERRORMSG  
END
