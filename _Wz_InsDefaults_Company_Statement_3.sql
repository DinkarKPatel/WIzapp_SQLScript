


--- INSERT DEFAULT DATABASE NAME ENTRY IN MULTI COMPANY TABLE
IF NOT EXISTS (SELECT TOP 1 DATABASE_NAME FROM MULTILOCDET WHERE DATABASE_NAME=DB_NAME())
BEGIN
 INSERT MULTILOCDET	( COMPANY_NAME, DATABASE_NAME, BASE_DB_NAME )  
 SELECT  COMPANY_NAME, DB_NAME() AS DATABASE_NAME,DB_NAME() AS BASE_DB_NAME FROM COMPANY
 WHERE COMPANY_CODE='01'
END
ELSE
BEGIN
	DECLARE @CCONAME VARCHAR(100)
	SELECT @CCONAME=COMPANY_NAME FROM COMPANY WHERE COMPANY_CODE='01' 
	UPDATE MULTILOCDET SET COMPANY_NAME=@CCONAME WHERE DATABASE_NAME=DB_NAME()
END
