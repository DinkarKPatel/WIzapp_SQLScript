

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_GROUPMEMOS_SOURCEIDS' AND VALUE=1)
BEGIN
	PRINT 'UPDATING RM_ID/INV_ID COLUMN IN CNM01106/PIM01106'

	UPDATE A SET RM_ID=STUFF(A.RM_ID,8,0,'00000') FROM CNM01106 A LEFT OUTER JOIN RMM01106 B ON A.RM_ID=B.RM_ID
	JOIN RMM01106 C ON C.RM_ID=STUFF(A.RM_ID,8,0,'00000')
	WHERE LEN(A.RM_ID)=17 AND B.RM_ID IS  NULL

	UPDATE A SET  INV_ID=STUFF(A.INV_ID,8,0,'00000') FROM PIM01106 A LEFT OUTER JOIN INM01106 B ON A.INV_ID=B.INV_ID
	JOIN INM01106 C ON C.INV_ID=STUFF(A.INV_ID,8,0,'00000')
	WHERE LEN(A.INV_ID)=17 AND B.INV_ID IS  NULL

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_GROUPMEMOS_SOURCEIDS')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_GROUPMEMOS_SOURCEIDS',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_GROUPMEMOS_SOURCEIDS'
	
END		
