CREATE VIEW VW_DEPARTMENT_OUTPUT_LIST 

AS 
SELECT	A.MEMO_NO
		,A.MEMO_DT AS MEMO_DATE
		,(CASE WHEN A.CANCELLED = 0 THEN '' ELSE 'CANCELLED' END) AS CANCELLED
		,B.MEMO_NO AS WORK_ORDER_NO
		,CONVERT(VARCHAR(10)
		,B.MEMO_DT,105) AS WORK_ORDER_DATE
		,B.REF_NO AS WORK_ORDER_REFNO
		,U.USERNAME AS USER_NAME
		,A.DEPARTMENT_ID 
		,A.MEMO_ID,A.FIN_YEAR  
FROM	PRD_DEPARTMENT_OUTPUT_MST A 
		JOIN USERS U ON A.USER_CODE = U.USER_CODE
		JOIN PRD_WO_MST B ON A.REF_PRD_WORKORDER_MEMOID = B.MEMO_ID
