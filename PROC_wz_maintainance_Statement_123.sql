


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_GROUPUR_SOURCEIDS' AND VALUE=1)
BEGIN
	PRINT 'UPDATING MIRRORLOG FROM XFRLOG'
	
	UPDATE A SET  INV_ID=STUFF(A.INV_ID,8,5,'')
	FROM PIM01106 A
	LEFT OUTER JOIN INM01106 B ON A.INV_ID=B.INV_ID
	JOIN INM01106 C ON C.INV_ID=STUFF(A.INV_ID,8,5,'')
	WHERE LEN(A.INV_ID)=22 AND B.INV_ID IS  NULL

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_GROUPUR_SOURCEIDS')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_GROUPUR_SOURCEIDS',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_GROUPUR_SOURCEIDS'
	
END		
