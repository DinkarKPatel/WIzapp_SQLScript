CREATE PROCEDURE [DBO].[SPPPC_FILL_SHIPMODE]  
(  
 @NQUERYID   INT,    
 @VPARAMETER  VARCHAR(500)  
)  
AS      
BEGIN   
  
 DECLARE @CSTEP INT, @CCMD NVARCHAR(MAX), @ERRMSG_OUT VARCHAR(MAX)  
 BEGIN TRY  
  SET NOCOUNT ON;  
    
  SET @ERRMSG_OUT = ''  
    
  IF @NQUERYID = 1  
   -- GET SHIPMODE LIST  
   GOTO LBLGETSHIPMODELIST  
     
  ELSE IF @NQUERYID = 2  
   -- GET SHIPMODE USING LIKE  
   GOTO LBLGETSHIPMODELIKE  
    
  ELSE IF @NQUERYID = 7  
   -- GET INFO BY SHIPMODE NAME  
   GOTO LBLGETINFOBYSHIPMODENAME  
  
  ELSE  
   GOTO LAST  
  
-- GET SHIPMODE LIST  
LBLGETSHIPMODELIST:  
  SET @CSTEP = 101  
  SELECT A.SHIPPING_MODE, A.SHIPPING_NAME  
  FROM [PPC_SHIPPING_DETAILS] A (NOLOCK)   
  ORDER BY A.SHIPPING_MODE  
    
  GOTO LAST  
  
  
-- GET SHIPMODE LIKE  
LBLGETSHIPMODELIKE:  
  SET @CSTEP = 102  
  SELECT A.SHIPPING_MODE, A.SHIPPING_NAME  
  FROM [PPC_SHIPPING_DETAILS] A (NOLOCK)   
  WHERE A.SHIPPING_NAME LIKE '%'+ @VPARAMETER +'%'  
  ORDER BY A.SHIPPING_NAME  
    
  GOTO LAST  
  
  
-- GET INFO BY SHIPMODE NAME  
LBLGETINFOBYSHIPMODENAME:  
  SET @CSTEP = 107  
  SELECT A.SHIPPING_MODE, A.SHIPPING_NAME  
  FROM [PPC_SHIPPING_DETAILS] A (NOLOCK)   
  WHERE A.SHIPPING_NAME = @VPARAMETER  
    
  GOTO LAST  
    
LAST:     
 GOTO END_PROC  
  
  SET NOCOUNT OFF;  
 END TRY    
 BEGIN CATCH    
  SET @ERRMSG_OUT='ERROR: [P]: SPPPC_SHIPMODE, [STEP]: '+CAST(@CSTEP AS VARCHAR(5))+', [MESSAGE]: ' + ERROR_MESSAGE()  
  GOTO END_PROC    
 END CATCH     
  
END_PROC:    
 IF  ISNULL(@ERRMSG_OUT,'')<>''   
  SELECT @ERRMSG_OUT AS ERRORMSG  
END
