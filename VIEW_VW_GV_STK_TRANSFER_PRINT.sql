CREATE VIEW VW_GV_STK_TRANSFER_PRINT

AS  
SELECT     
	  M.MEMO_NO   
	 ,CONVERT(VARCHAR,M.MEMO_DT,105) AS TRANSFER_DT
	 ,M.MEMO_DT 
	 ,M.MEMO_ID
	 ,M.CANCELLED     
	 ,M.location_code/*LEFT(M.MEMO_ID,2)*//*Rohit 04-11-2024*/ AS [DEPT_ID]
	 ,M.TARGET_DEPT_ID    
	 ,D.QUANTITY,D.GV_SRNO
	 ,SDEP.DEPT_NAME  AS SOURCE_DEPT_NAME      
	 ,TDEP.DEPT_NAME AS TARGET_DEPT_NAME
	 ,S.DT_CREATED
	 ,S.DT_EXPIRY    
FROM GV_STKXFER_DET D  
JOIN GV_STKXFER_MST  M ON D.MEMO_ID=M.MEMO_ID    
JOIN LOCATION SDEP ON M.location_code/*LEFT(M.MEMO_ID,2)*//*Rohit 04-11-2024*/  = SDEP.DEPT_ID     
JOIN LOCATION TDEP ON M.TARGET_DEPT_ID  = TDEP.DEPT_ID 
JOIN SKU_GV_MST S ON S.GV_SRNO=D.GV_SRNO   
---END OF VIEW - VW_FLOOR_STK_TRANSFER_PRINT
