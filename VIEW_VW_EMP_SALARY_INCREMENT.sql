CREATE VIEW VW_EMP_SALARY_INCREMENT       
--WITH ENCRYPTION
AS      
SELECT A.EMP_ID,A.INCREMENT_DATE,A.INCREMENTED_SALARY,A.BASIC_SALARY,A.REMARKS,A.LAST_UPDATE,     
B.REF_ID,B.EMP_FNAME,B.EMP_LNAME,B.PARENT_NAME,B.DOB,B.EMPLOYEE_TYPE,B.DESIG_NAME,B.DEPARTMENT_NAME,    
B.MOBILE1,B.DOJ,C.CO_ALIAS     
FROM EMP_SALARY_INCREMENT A (NOLOCK)      
JOIN VW_EMP B (NOLOCK)  ON A.EMP_ID = B.EMP_ID  
JOIN EMP_MST C (NOLOCK) ON A.EMP_ID = C.EMP_ID
