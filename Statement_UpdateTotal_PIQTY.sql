	UPDATE A SET TOTAL_PI_QTY =B.PI_QTY
	FROM POM01106 A (NOLOCK)
	JOIN
	(
		 SELECT PO_ID,
		       SUM(ISNULL(PI_QTY,0)) AS PI_QTY
		 FROM POD01106 (NOLOCK)
		 where ISNULL(PI_QTY,0)<>0
		 GROUP BY  PO_ID 
    ) B ON A.PO_ID =B.PO_ID
    WHERE A.CANCELLED  =0
	AND  ISNULL(TOTAL_PI_QTY,0)<>ISNULL(B.PI_QTY,0)
    
	UPDATE A SET TOTAL_ADJ_QUANTITY =B.adj_quantity
	FROM POM01106 A (NOLOCK)
	JOIN
	(
		 SELECT PO_ID,
		       SUM(ISNULL(adj_quantity ,0)) AS adj_quantity
		 FROM POD01106 (NOLOCK)
		 where ISNULL(adj_quantity,0)<>0
		 GROUP BY  PO_ID 
    ) B ON A.PO_ID =B.PO_ID
    WHERE A.CANCELLED  =0
	AND  ISNULL(TOTAL_ADJ_QUANTITY,0)<>ISNULL(B.adj_quantity,0)
    
	
	