CREATE PROCEDURE SP_BUYERSORDER_WSL_8
@CMEMOID VARCHAR(40),  
@CWHERE VARCHAR(500),  
@CFINYEAR VARCHAR(10),  
@NNAVMODE NUMERIC(2,0),
@CARTICLECODE CHAR(9)='',  
@CPARA2CODE CHAR(7)='',
@DTWHERE DATETIME ='',
@cLocId VARCHAR(5)=''
--WITH ENCRYPTION
 
AS    
BEGIN 
	SELECT PARA5_CODE, PARA5_NAME FROM PARA5 (NOLOCK)  
	WHERE PARA5_NAME <> ''   AND INACTIVE = 0  
	ORDER BY PARA5_NAME  
END