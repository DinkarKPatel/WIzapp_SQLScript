IF NOT EXISTS (SELECT TOP 1 ATTRIBUTE_CODE FROM ATTR_KEY WHERE KEY_CODE='0000000')
BEGIN
	 PRINT 'INSERTING DEFAULT ATTREIBUTE ENTRY FOR OPEN KEY VALUES IN ATTR_KEY'
	
	 INSERT ATTR_KEY	( ATTRIBUTE_CODE, KEY_CODE, KEY_NAME, LAST_UPDATE, COMPANY_CODE, KEY_ALIAS, INACTIVE )  
	 SELECT '0000000' AS ATTRIBUTE_CODE,'0000000' AS KEY_CODE,'' AS KEY_NAME,GETDATE() AS LAST_UPDATE,
	 '01' AS COMPANY_CODE,'' AS KEY_ALIAS,1 AS INACTIVE 

END


