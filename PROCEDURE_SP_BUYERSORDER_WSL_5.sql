CREATE PROCEDURE SP_BUYERSORDER_WSL_5
@CMEMOID VARCHAR(40),  
@CWHERE VARCHAR(500),  
@CFINYEAR VARCHAR(10),  
@NNAVMODE NUMERIC(2,0),
@CARTICLECODE CHAR(9)='',  
@CPARA2CODE CHAR(7)='',
@DTWHERE DATETIME ='',
@cLocId VARCHAR(5)=''
--WITH ENCRYPTION
 
AS    
BEGIN 
	SELECT A.PARA2_CODE, A.PARA2_NAME, A.PARA2_ORDER FROM PARA2 A (NOLOCK)      
	WHERE A.PARA2_NAME <> '' AND INACTIVE = 0    
	ORDER BY A.PARA2_ORDER, A.PARA2_NAME 
END