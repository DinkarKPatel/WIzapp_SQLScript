


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_XN_AUDIT_TRIAL_MISC' AND VALUE=1)
BEGIN

	UPDATE XN_AUDIT_TRIAL_MST SET KEY_FIELDS=REPLACE(KEY_FIELDS,'PRODUCT_CODE+','') WHERE TABLE_NAME LIKE '%D01106' AND KEY_FIELDS LIKE '%PRODUCT_CODE%' AND TRIG='UPDATE'

	UPDATE XN_AUDIT_TRIAL_DET SET PRODUCT_CODE='' WHERE TABLE_NAME LIKE '%M01106' AND PRODUCT_CODE IS NULL

	UPDATE XN_AUDIT_TRIAL_DET SET PRODUCT_CODE=ISNULL(SUBSTRING(KEY_VALUE,23,50),''),KEY_VALUE=LEFT(KEY_VALUE,22) WHERE TABLE_NAME LIKE '%D01106' AND PRODUCT_CODE IS NULL

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE  CONFIG_OPTION='UPDATE_XN_AUDIT_TRIAL_MISC')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_XN_AUDIT_TRIAL_MISC',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_XN_AUDIT_TRIAL_MISC'

END
