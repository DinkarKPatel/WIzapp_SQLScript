CREATE PROCEDURE SP3S_GET_PARA1_FKEYDATA
@CSOURCEDB VARCHAR(200),
@CSOURCETABLE VARCHAR(200),
@CSOURCEKEYFIELD VARCHAR(200),
@CXNTYPE VARCHAR(10),
@CTABLESUFFIX VARCHAR(40),
@CLOCID VARCHAR(5),
@CERRORMSG VARCHAR(MAX) OUTPUT
--WITH ENCRYPTION
AS
BEGIN
	BEGIN TRY
		DECLARE @CCMD NVARCHAR(MAX),@CTEMPTABLE VARCHAR(1000),@CTEMPTABLE1 VARCHAR(1000),@CTEMPTABLE2 VARCHAR(1000),
				@CTEMPTABLE3 VARCHAR(1000),@CKEYFIELDVAL VARCHAR(40),@CDESTDB VARCHAR(200),@CFILTERCONDITION VARCHAR(MAX),
				@NSTEP INT
		
		SET @CDESTDB='WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.'

		IF @CSOURCETABLE<>'#TMPMISSINGPARA1DATA'
		BEGIN			
			IF OBJECT_ID('TEMPDB..#TMPMISSINGPARA1DATA','U') IS NOT NULL
				DROP TABLE #TMPMISSINGPARA1DATA
			
			SELECT PARA1_CODE INTO #TMPMISSINGPARA1DATA FROM PARA1 WHERE 1=2
		END
				
		SET @CTEMPTABLE=(CASE WHEN LEFT(@CSOURCETABLE,1)='#' THEN @CSOURCETABLE ELSE @CSOURCEDB+'TMP_'+@CXNTYPE+'_'+@CSOURCETABLE+'_'+@CTABLESUFFIX END)
		SET @CTEMPTABLE1=@CSOURCEDB+'TMP_'+@CXNTYPE+'_PARA1_'+@CTABLESUFFIX
		
		IF @CSOURCETABLE<>'#TMPMISSINGPARA1DATA'
		BEGIN			
			IF OBJECT_ID(@CTEMPTABLE1,'U') IS NOT NULL
				SET @CCMD=N'SELECT DISTINCT A.'+@CSOURCEKEYFIELD+' FROM '+@CTEMPTABLE+'  A
							LEFT OUTER JOIN '+@CTEMPTABLE1+'  B ON B.PARA1_CODE=A.'+@CSOURCEKEYFIELD+'
							LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.PARA1 C ON C.PARA1_CODE=A.'+@CSOURCEKEYFIELD+'
							WHERE B.PARA1_CODE IS NULL AND C.PARA1_CODE IS NULL AND A.'+@CSOURCEKEYFIELD+' IS NOT NULL'
			ELSE
				SET @CCMD=N'SELECT DISTINCT A.'+@CSOURCEKEYFIELD+' FROM '+@CTEMPTABLE+'  A
							LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.PARA1 B ON B.PARA1_CODE=A.'+@CSOURCEKEYFIELD+'
							WHERE B.PARA1_CODE IS NULL AND A.'+@CSOURCEKEYFIELD+' IS NOT NULL'

				
			INSERT #TMPMISSINGPARA1DATA
			EXEC SP_EXECUTESQL @CCMD
		END
		
		IF EXISTS (SELECT TOP 1 PARA1_CODE FROM #TMPMISSINGPARA1DATA)
		BEGIN
			SET @CFILTERCONDITION= 'B.PARA1_CODE IN (SELECT PARA1_CODE FROM #TMPMISSINGPARA1DATA)'
			
			EXEC UPDATEMASTERXN_MIRROR
			@CSOURCEDB='',
			@CSOURCETABLE='PARA1',
			@CDESTDB=@CDESTDB,
			@CDESTTABLE='PARA1',
			@CKEYFIELD1='PARA1_CODE',
			@LINSERTONLY=0,
			@CFILTERCONDITION=@CFILTERCONDITION,
			
			
			@LUPDATEONLY=0,
			@BALWAYSUPDATE=1
		END
		
		GOTO END_PROC									
	END TRY
	
	BEGIN CATCH
		SELECT @CERRORMSG='PROCEDURE SP3S_GET_PARA1_FKEYDATA : '+ISNULL(ERROR_PROCEDURE(),'NULL P')+'STEP :'+STR(ISNULL(@NSTEP,0))+ ' LINE NO. :'+
		ISNULL(LTRIM(RTRIM(STR(ERROR_LINE()))),'NULL LINE')+'MSG :'+ISNULL(ERROR_MESSAGE(),'NULL MSG')
		
		GOTO END_PROC
	END CATCH
	
END_PROC:
		
END
----'END OF PROCEDURE SP3S_GET_PARA1_FKEYDATA'
