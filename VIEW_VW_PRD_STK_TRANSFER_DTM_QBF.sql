CREATE VIEW VW_PRD_STK_TRANSFER_DTM_QBF 

AS    
SELECT     
A.FIN_YEAR AS MST_FIN_YEAR,    
P1.PARA1_NAME,    
P2.PARA2_NAME,    
P3.PARA3_NAME,    
P4.PARA4_NAME,    
P5.PARA5_NAME,    
P6.PARA6_NAME,    
E.ARTICLE_NO,    
D.MRP,    
D.PRODUCT_CODE,    
D.PURCHASE_PRICE,    
D.QUANTITY,    
D.WS_PRICE,    
U.UOM_NAME   
,A.MEMO_NO
,A.MEMO_NO AS MST_MEMO_NO
,A.MEMO_ID
,A.MEMO_DT
,A.LAST_UPDATE
,A.CANCELLED
,A.FIN_YEAR
,A.DEPT_ID
,SKU.ARTICLE_CODE
,SKU.PARA1_CODE
,SKU.PARA2_CODE
,D.ROW_ID
,SKU.PARA3_CODE
,SKU.PARA4_CODE
,SKU.PARA5_CODE
,SKU.PARA6_CODE
,D.QUANTITY AS INVOICE_QUANTITY
,P1.PARA1_ORDER
,P1.PARA1_SET
,P2.PARA2_ORDER
,P2.PARA2_SET
,P3.DT_CREATED
,E.CODING_SCHEME
,E.UOM_CODE
,E.SKU_CODE
,E.ARTICLE_NAME
,E.ARTICLE_DESC
,E.SUB_SECTION_CODE
,E.DISCON
,E.WHOLESALE_PRICE
,SKU.FIX_MRP AS MIN_PRICE
,E.STOCK_NA
,E.ARTICLE_TYPE
,E.CREATED_ON
,E.ARTICLE_GROUP_CODE
,E.GENERATE_BARCODES_WITHARTICLE_PREFIX
,E.ARTICLE_GEN_MODE
,E.ARTICLE_PRD_MODE
,E.ARTICLE_SET_CODE
,E.OH_PERCENTAGE
,E.OH_AMOUNT
,U.UOM_TYPE
,SD.SECTION_CODE
,SD.SUB_SECTION_NAME
,SD.MFG_CATEGORY
,SM.SECTION_NAME,SKU.PRODUCT_NAME
,D.TS
,A.SOURCE_DEPARTMENT_ID 
,WM.MEMO_NO AS MST_WO_NO
,WM.REF_NO AS MST_REF_NO 
,COM.COMPANY_CODE
FROM PRD_STK_TRANSFER_DTM_MST A    
JOIN PRD_STK_TRANSFER_DTM_DET D (NOLOCK) ON A.MEMO_ID = D.MEMO_ID   
JOIN PRD_WO_MST WM (NOLOCK) ON WM.MEMO_ID = A.REF_WO_ID 
JOIN SKU (NOLOCK)  ON SKU.PRODUCT_CODE = D.PRODUCT_CODE
JOIN PARA1 P1 (NOLOCK) ON SKU.PARA1_CODE = P1.PARA1_CODE    
JOIN PARA2 P2 (NOLOCK) ON SKU.PARA2_CODE = P2.PARA2_CODE    
JOIN PARA3 P3 (NOLOCK) ON SKU.PARA3_CODE = P3.PARA3_CODE    
JOIN PARA4 P4 (NOLOCK) ON SKU.PARA4_CODE = P4.PARA4_CODE    
JOIN PARA5 P5 (NOLOCK) ON SKU.PARA5_CODE = P5.PARA5_CODE    
JOIN PARA6 P6 (NOLOCK) ON SKU.PARA6_CODE = P6.PARA6_CODE    
JOIN ARTICLE E (NOLOCK) ON SKU.ARTICLE_CODE = E.ARTICLE_CODE    
JOIN UOM U (NOLOCK) ON E.UOM_CODE = U.UOM_CODE 
JOIN SECTIOND SD ON E.SUB_SECTION_CODE=SD.SUB_SECTION_CODE
JOIN SECTIONM SM ON SD.SECTION_CODE=SM.SECTION_CODE
LEFT OUTER JOIN COMPANY COM ON 1=1  WHERE COM.COMPANY_CODE='01'
