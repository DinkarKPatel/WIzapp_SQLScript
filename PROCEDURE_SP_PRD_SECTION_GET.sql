CREATE PROCEDURE DBO.SP_PRD_SECTION_GET
(
@SUB_SECTION_CODE CHAR(7)
)
AS
 SET NOCOUNT ON;
 BEGIN
  SELECT SECTION_CODE,SECTION_NAME 
  FROM DBO.SECTIONM WITH(NOLOCK)
  WHERE SECTION_CODE IN (SELECT DISTINCT SECTION_CODE FROM DBO.SECTIOND WITH(NOLOCK)
                         WHERE SUB_SECTION_CODE = @SUB_SECTION_CODE 
                         )
 END
