IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='INSERT_CONFIG_SENDCUSTDATA' AND VALUE=1)
BEGIN
	PRINT 'INSERT CONFIG FOR SENDING CUSTOMER DATA FROM LOCATIONS'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='INSERT_CONFIG_SENDCUSTDATA')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('INSERT_CONFIG_SENDCUSTDATA','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='INSERT_CONFIG_SENDCUSTDATA'
	
	DECLARE @CGROUPCODE VARCHAR(50)
	
	SELECT TOP 1 @CGROUPCODE=VALUE FROM CONFIG WHERE CONFIG_OPTION='GROUP_CODE'
	
	IF @CGROUPCODE='0100505'
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
		VALUES ('SEND_CUSTOMER_DATA','1','',GETDATE())	
	
END	

