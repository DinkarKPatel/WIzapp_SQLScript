CREATE PROCEDURE SP3SFDPRINT (@RM_ID VARCHAR(100))        
AS  
BEGIN  
--1
SELECT TOP 1'RM_NO'COLNAME,RM_NO COLVALUE FROM RMM01106 (NOLOCK) WHERE RM_ID=@RM_ID  
--2
UNION
SELECT TOP 1 'RM_DT',CONVERT(VARCHAR,RM_DT,105) FROM RMM01106 (NOLOCK) WHERE RM_ID=@RM_ID  
--3
UNION
SELECT TOP 1 'AC_NAME',LM.AC_NAME   
FROM RMM01106 RMM (NOLOCK)       
JOIN LMP01106 LMP (NOLOCK) ON LMP.AC_CODE=RMM.AC_CODE       
LEFT JOIN LM01106 LM (NOLOCK) ON LM.AC_CODE=LMP.AC_CODE    
WHERE RMM.RM_ID=@RM_ID  
--4
UNION
SELECT TOP 1 'ADDRESS0',LMP.ADDRESS0  
FROM RMM01106 RMM (NOLOCK)       
JOIN LMP01106 LMP (NOLOCK) ON LMP.AC_CODE=RMM.AC_CODE  
WHERE RMM.RM_ID=@RM_ID  
--5
UNION
SELECT TOP 1 'ADDRESS1',LMP.ADDRESS1  
FROM RMM01106 RMM (NOLOCK)       
JOIN LMP01106 LMP (NOLOCK) ON LMP.AC_CODE=RMM.AC_CODE  
WHERE RMM.RM_ID=@RM_ID  
--6
UNION
SELECT TOP 1 'ADDRESS2',LMP.ADDRESS2  
FROM RMM01106 RMM (NOLOCK)       
JOIN LMP01106 LMP (NOLOCK) ON LMP.AC_CODE=RMM.AC_CODE  
WHERE RMM.RM_ID=@RM_ID  
--7
UNION
SELECT TOP 1 'AC_GST_NO',LMP.AC_GST_NO  
FROM RMM01106 RMM (NOLOCK)       
JOIN LMP01106 LMP (NOLOCK) ON LMP.AC_CODE=RMM.AC_CODE  
WHERE RMM.RM_ID=@RM_ID  
--8
UNION
SELECT TOP 1 'PHONES_O',LMP.PHONES_O  
FROM RMM01106 RMM (NOLOCK)       
JOIN LMP01106 LMP (NOLOCK) ON LMP.AC_CODE=RMM.AC_CODE  
WHERE RMM.RM_ID=@RM_ID  
--9
UNION
SELECT TOP 1 'PHONES_R',LMP.PHONES_R  
FROM RMM01106 RMM (NOLOCK)       
JOIN LMP01106 LMP (NOLOCK) ON LMP.AC_CODE=RMM.AC_CODE  
WHERE RMM.RM_ID=@RM_ID  
--10
UNION
SELECT TOP 1 'PHONES_FAX',LMP.PHONES_FAX  
FROM RMM01106 RMM (NOLOCK)       
JOIN LMP01106 LMP (NOLOCK) ON LMP.AC_CODE=RMM.AC_CODE  
WHERE RMM.RM_ID=@RM_ID  
--11
UNION
SELECT TOP 1 'MOBILE',LMP.MOBILE  
FROM RMM01106 RMM (NOLOCK)       
JOIN LMP01106 LMP (NOLOCK) ON LMP.AC_CODE=RMM.AC_CODE  
WHERE RMM.RM_ID=@RM_ID  
--12
UNION
SELECT TOP 1 'TOTAL_AMOUNT',CONVERT(VARCHAR,TOTAL_AMOUNT) FROM RMM01106 (NOLOCK) WHERE RM_ID=@RM_ID  
--13
UNION
SELECT TOP 1 'SUBTOTAL',CONVERT(VARCHAR,SUBTOTAL) FROM RMM01106 (NOLOCK) WHERE RM_ID=@RM_ID  
--14
UNION
SELECT TOP 1 'REMARKS',ISNULL(REMARKS,'')REMARKS FROM RMM01106 (NOLOCK) WHERE RM_ID=@RM_ID
END
