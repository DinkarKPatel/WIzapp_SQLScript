IF NOT EXISTS(SELECT TOP 1 'U' FROM TILL_LIFT_TRANSFER)
BEGIN
	INSERT TILL_LIFT_TRANSFER(TRANSFERMODE,TRANSFERNAME)
	SELECT 1 AS TRANSFERMODE,'BANK TRANSFER' AS TRANSFERNAME
	UNION ALL
	SELECT 2 AS TRANSFERMODE,'PETTY CASH EXPENSE' AS TRANSFERNAME
	UNION ALL
	SELECT 3 AS TRANSFERMODE,'TRANSFER TO LOCKER' AS TRANSFERNAME
END
