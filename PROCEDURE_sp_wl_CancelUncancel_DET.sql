CREATE PROC SP_WL_CANCELUNCANCEL_DET (@MEMOID VARCHAR(50))
--WITH ENCRYPTION

 AS
BEGIN
	SELECT C.ARTICLE_NAME ,C.ARTICLE_NO ,PARA1.PARA1_NAME ,PARA2.PARA2_NAME , PARA3.PARA3_NAME,PARA4.PARA4_NAME,
	PARA5.PARA5_NAME, PARA6.PARA6_NAME, B.MRP ,
	B.PURCHASE_PRICE, A.*,A.QUANTITY * B.PURCHASE_PRICE AS AMOUNT,
	A.QUANTITY AS 'OLD_QUANTITY', B.BARCODE_CODING_SCHEME AS CODING_SCHEME
	FROM ICD01106 A 
	LEFT OUTER JOIN SKU B  ON A.PRODUCT_CODE = B.PRODUCT_CODE 
	LEFT OUTER JOIN PARA1 ON PARA1.PARA1_CODE=B.PARA1_CODE
	LEFT OUTER JOIN PARA2 ON PARA2.PARA2_CODE=B.PARA2_CODE
	LEFT OUTER JOIN PARA3 ON PARA3.PARA3_CODE=B.PARA3_CODE
	LEFT OUTER JOIN PARA4 ON PARA4.PARA4_CODE=B.PARA4_CODE
	LEFT OUTER JOIN PARA5 ON PARA5.PARA5_CODE=B.PARA5_CODE
	LEFT OUTER JOIN PARA6 ON PARA6.PARA6_CODE=B.PARA6_CODE
	LEFT OUTER JOIN ARTICLE C ON B.ARTICLE_CODE = C.ARTICLE_CODE 
	WHERE CNC_MEMO_ID = @MEMOID  
END
