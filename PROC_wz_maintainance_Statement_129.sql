

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SKU_GROSSPP' AND VALUE=1)
BEGIN
	PRINT 'UPDATING GROSS PP SKU'
	
	UPDATE A SET BASIC_PURCHASE_PRICE=GROSS_PURCHASE_PRICE FROM SKU A JOIN PID01106 B ON A.PRODUCT_CODE=B.PRODUCT_CODE 
	 JOIN PIM01106 C ON B.MRR_ID=C.MRR_ID WHERE C.INV_MODE=1
	

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE  CONFIG_OPTION='UPDATE_SKU_GROSSPP')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_SKU_GROSSPP',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_SKU_GROSSPP'
	
END		
