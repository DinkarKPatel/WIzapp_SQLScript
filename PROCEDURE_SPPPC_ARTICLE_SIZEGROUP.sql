CREATE PROCEDURE SPPPC_ARTICLE_SIZEGROUP
(
 @CARTICLE_CODE VARCHAR(10)=''
)
AS
BEGIN

    SELECT A.ARTICLE_CODE,A.SIZEGROUP_CODE,B.SIZEGROUP_NAME,NEWID() AS ROW_ID
    FROM PPC_ART_DET_SIZEGROUP A
    JOIN PPC_SIZEGROUP B ON A.SIZEGROUP_CODE=B.SIZEGROUP_CODE
    JOIN PARA2 C ON C.PARA2_CODE =A.PARA2_CODE
    WHERE ARTICLE_CODE=@CARTICLE_CODE
    GROUP BY A.ARTICLE_CODE,A.SIZEGROUP_CODE,B.SIZEGROUP_NAME
    


END
