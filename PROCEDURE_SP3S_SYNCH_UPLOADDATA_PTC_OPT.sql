CREATE PROCEDURE SP3S_SYNCH_UPLOADDATA_PTC_OPT
(
	@CspId VARCHAR(50),  
    @CERRMSG VARCHAR(MAX) OUTPUT  
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_PTC_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(1000),@CCURDEPTID VARCHAR(10),@CMIRRORINGENABLED VARCHAR(2)
	   ,@CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE VARCHAR(200),@CSOURCEDB VARCHAR(10)
	   ,@CMERGEDB VARCHAR(100),@CLOCID VARCHAR(5),@BMSTINSERTONLY BIT ,@CMEMOID VARCHAR(30),@CKEYFIELD2  VARCHAR(200)
	   		    
BEGIN TRY
SET @CSTEP=00

	SET @CTABLE_SUFFIX='UPLOAD'
	SET @CSOURCEDB=''
	SET @CMERGEDB=''
	SET @BMSTINSERTONLY=1
BEGIN TRANSACTION 

	 ---UPDATE POSTEDINAC MARK FOR THE BILLS POSTEDINTO ACCOUNTS & THERE IS SOME MISMATCH BETWEEN INCOMING DATA & EXISTING DATA
	SET @CSTEP=10
    
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'

	SELECT TOP 1 @CMIRRORINGENABLED=VALUE FROM CONFIG WHERE CONFIG_OPTION='MIRRORING_ENABLED'

	SELECT  @CMEMOID=PEM_MEMO_ID,@CLOCID=location_Code 
	FROM PTC_PEM01106_UPLOAD WHERE SP_ID=@CSPID

	
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC


		
	DECLARE @CMEMOIDSEARCH VARCHAR(100),@BADDMODE BIT
	SELECT TOP 1 @CMEMOIDSEARCH=A.pem_memo_id FROM pem01106 A (NOLOCK) WHERE A.pem_memo_id=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1


	
	IF ISNULL(@CMIRRORINGENABLED,'')='1'
		GOTO LBLMERGE
    
    SET @CSTEP=15
    DECLARE @CPOSTINGATHODEPTID VARCHAR(5)
    
	SET @DTSQL=N'SELECT TOP 1 @CPOSTINGATHODEPTID=ACCOUNTS_POSTING_DEPT_ID FROM 
				 LOCATION WHERE DEPT_ID='''+@CLOCID+''''
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL,N'@CPOSTINGATHODEPTID CHAR(2) OUTPUT',@CPOSTINGATHODEPTID OUTPUT
    
	IF ISNULL(@CPOSTINGATHODEPTID,'')=@CCURDEPTID
	BEGIN
		
		SELECT @CTEMPMASTERTABLE=@CSOURCEDB+'PTC_PEM01106_'+LTRIM(RTRIM(@CTABLE_SUFFIX)),
			   @CTEMPDETAILTABLE=@CSOURCEDB+'PTC_PED01106_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
		
		SET @CSTEP=20
		SET @DTSQL=N'UPDATE A SET POSTEDINAC=B.POSTEDINAC FROM '+@CTEMPMASTERTABLE+' A
					 JOIN PEM01106 B ON A.PEM_MEMO_ID=B.PEM_MEMO_ID 
					 WHERE A.SP_ID='''+@CspId +''''
		
		PRINT @DTSQL  
		EXEC SP_EXECUTESQL @DTSQL
		
		SET @CSTEP=30
		SET @DTSQL=N'UPDATE B SET POSTEDINAC=0 FROM PEM01106 A 
					 JOIN '+@CTEMPMASTERTABLE+' B ON A.PEM_MEMO_ID=B.PEM_MEMO_ID
				     WHERE A.CANCELLED<>B.CANCELLED
					 AND  B.SP_ID='''+@CspId +''''
		PRINT @DTSQL  
		EXEC SP_EXECUTESQL @DTSQL
		
		SET @CSTEP=40
	
	
		IF OBJECT_ID(@CTEMPDETAILTABLE,'U') IS NOT NULL
		BEGIN
			SET @DTSQL=N'UPDATE A SET POSTEDINAC=0 FROM '+@CTEMPMASTERTABLE+' A 
						 JOIN (SELECT ISNULL(A.PEM_MEMO_ID,B.PEM_MEMO_ID) AS PEM_MEMO_ID FROM 
							  ( SELECT A.PEM_MEMO_ID,A.AC_CODE,SUM(A.XN_AMOUNT) AS AMOUNT FROM PED01106 A 
								JOIN '+@CTEMPMASTERTABLE+' B ON A.PEM_MEMO_ID=B.PEM_MEMO_ID
								WHERE   B.SP_ID='''+@CspId +'''
								GROUP BY A.PEM_MEMO_ID,A.AC_CODE) A
							   FULL OUTER JOIN 
							  ( SELECT A.PEM_MEMO_ID,A.AC_CODE,SUM(A.XN_AMOUNT) AS AMOUNT FROM '+@CTEMPDETAILTABLE+' A 
								JOIN '+@CTEMPMASTERTABLE+' B ON A.PEM_MEMO_ID=B.PEM_MEMO_ID AND A.SP_ID=B.SP_ID
								WHERE   B.SP_ID='''+@CspId +'''
								GROUP BY A.PEM_MEMO_ID,A.AC_CODE) B ON A.PEM_MEMO_ID=B.PEM_MEMO_ID
							   AND A.AC_CODE=B.AC_CODE WHERE ISNULL(A.AMOUNT,0)<>ISNULL(B.AMOUNT,0))
							  C ON C.PEM_MEMO_ID=A.PEM_MEMO_ID 
							  WHERE  A.SP_ID='''+@CspId +''' '
			PRINT @DTSQL  
			EXEC SP_EXECUTESQL @DTSQL
		END
    END

LBLMERGE:
    

	IF @BADDMODE=0
	BEGIN

		SET @CSTEP=80
		SET @CTABLENAME='PED01106'
		SET @CTMP_TABLENAME='PTC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
		SET @CKEYFIELD='PEM_MEMO_ID'
		SET @CSTEP=90
		SET @DTSQL=N'DELETE '+@CMERGEDB+'['+@CTABLENAME+']  WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''''
		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL

		SET @CSTEP=95
		SET @DTSQL=N'DELETE '+DB_NAME()+'_IMAGE..IMAGE_INFO_DOC  WITH (ROWLOCK) WHERE  XN_TYPE=''frmPCExpense'' AND MEMO_ID='''+@CMEMOID+''''
		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL

	END
    
    SET @CFILTERCONDITION=' B.PEM_MEMO_ID='''+@CMEMOID+''' AND B.SP_ID='''+@CspId+''''
	
    SET @CSTEP=300
	SET @CTABLENAME='PEM01106'
	SET @CTMP_TABLENAME='PTC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PEM_MEMO_ID'
	SET @CSTEP=310
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  
							  
	SET @CSTEP=310
	SET @CTABLENAME='PED01106'
	SET @CTMP_TABLENAME='PTC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PEM_MEMO_ID'
	SET @CSTEP=310
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  

	SET @CSTEP=330
	SET @CTABLENAME='IMAGE_INFO_DOC'
	SET @CTMP_TABLENAME='PTC_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CKEYFIELD2='XN_TYPE'
	SET @CMERGEDB=DB_NAME()+'_IMAGE.DBO.'
	SET @CFILTERCONDITION=' B.MEMO_ID='''+@CMEMOID+''' AND B.SP_ID='''+@CspId+''''
	SET @CSTEP=340
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2= @CKEYFIELD2,@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
	
  

  DELETE FROM PTC_PED01106_UPLOAD WHERE SP_ID=@CspId 
  DELETE FROM PTC_PEM01106_UPLOAD WHERE SP_ID=@CspId 
  DELETE FROM PTC_IMAGE_INFO_DOC_UPLOAD WHERE SP_ID=@CspId 

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP3S_SYNCH_UPLOADDATA_PTC_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_PTC_DATA
