CREATE  PROCEDURE SP_GET_UPLOADED_IMAGE_LIST_2021    
(    
	@cLocID			VARCHAR(MAX),
	@cLAST_UPDATE	NVARCHAR(MAX)
)    
AS    
BEGIN 
	--INSERT INTO IMAGE_TEST(LOC_ID , DT_DR ,LAST_UPDATE ) SELECT @IMG_ID,GETDATE(),GETDATE()
	DECLARE @DTSQL NVARCHAR(MAX)
	DECLARE @cDB_NAME NVARCHAR(MAX)
	SET @cDB_NAME=DB_NAME()+'_IMAGE'
	SET @DTSQL=N'IF EXISTS(SELECT TOP 1 IMG_ID FROM '+@cDB_NAME+'..IMAGE_INFO (NOLOCK) WHERE DEPT_ID='''+@cLocID+''' AND LAST_UPDATE>='''+@cLAST_UPDATE+''')
					SELECT IMG_ID,CONVERT(VARCHAR(100),LAST_UPDATE,121) AS LAST_UPDATE,'''' AS ERRORMSG FROM '+@cDB_NAME+'..IMAGE_INFO   (NOLOCK) WHERE DEPT_ID='''+@cLocID+''' AND LAST_UPDATE>='''+@cLAST_UPDATE+'''
				ELSE
					SELECT '''' AS IMG_ID,CONVERT(VARCHAR(100),GETDATE(),121)  AS LAST_UPDATE,''Image Not Found to Upload'' AS ERRORMSG'
	PRINT @DTSQL 
	EXEC SP_EXECUTESQL @DTSQL 
END