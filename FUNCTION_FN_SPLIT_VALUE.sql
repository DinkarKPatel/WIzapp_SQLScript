CREATE FUNCTION DBO.FN_SPLIT_VALUE(@STATUS VARCHAR(max))
RETURNS @CODE TABLE (CODE VARCHAR(100))
AS
BEGIN
DECLARE @STAT TABLE(CODE VARCHAR(100))
IF RIGHT(@STATUS,1)<>',' SET @STATUS+=','
WHILE CHARINDEX(',',@STATUS)>0
  BEGIN
     INSERT @STAT SELECT LEFT(@STATUS,CHARINDEX(',',@STATUS)-1)
     SET @STATUS=SUBSTRING(@STATUS,CHARINDEX(',',@STATUS)+1,4000)
  END
INSERT @CODE SELECT * FROM @STAT  
RETURN
END