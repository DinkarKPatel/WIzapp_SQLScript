

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_PRINT_WSL_BOX_NO' AND VALUE=1)
BEGIN

	PRINT 'UPDATE NULL VALUES IN PRINT WSL BOX NO. COLUMN TO MAKE THIS COMN NON-NULLABLE'
	UPDATE GST_COMPANY_CONFIG SET PRINT_WSL_BOX_NO=ISNULL(PRINT_WSL_BOX_NO,0)
	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE  CONFIG_OPTION='UPDATE_PRINT_WSL_BOX_NO')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_PRINT_WSL_BOX_NO',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_PRINT_WSL_BOX_NO'
		 
END
