		

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_IRM_USER' AND VALUE=1)
BEGIN
	PRINT 'UPDATE USER CODE OF IRM01106 TO SUPER WHICH ARE NOT FOUND IN USERS'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_IRM_USER')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_IRM_USER','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_IRM_USER'
		
	UPDATE IRM01106 SET USER_CODE='0000000' WHERE USER_CODE NOT IN (SELECT USER_CODE FROM USERS) 	
	UPDATE IRM01106 SET EDT_USER_CODE='0000000' WHERE EDT_USER_CODE NOT IN (SELECT USER_CODE FROM USERS) 	
END
