

-- GET ALL DEFINED VOUCHER TYPE VALUES 
CREATE PROCEDURE SP_WL_VOUCHERTYPEDETAILLIST
--WITH ENCRYPTION
AS

	SELECT B.VOUCHER_MAJOR_HEAD_CODE, A.VOUCHER_TYPE AS 'MAJOR_VOUCHER_TYPE', B.VOUCHER_CODE, B.VOUCHER_TYPE 
	FROM VCHTYPE A 
	JOIN VCHTYPE B ON A.VOUCHER_CODE = B.VOUCHER_MAJOR_HEAD_CODE
	ORDER BY B.VOUCHER_MAJOR_HEAD_CODE, A.VOUCHER_TYPE
