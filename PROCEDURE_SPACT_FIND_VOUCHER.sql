CREATE PROCEDURE  SPACT_FIND_VOUCHER 
(
	@VOUCHER_DT DATETIME,
	@VCH_NO		VARCHAR(20)=''
)
AS
BEGIN
	SELECT TOP 50 VM_ID,VOUCHER_NO,b.VOUCHER_TYPE,CONVERT(VARCHAR(20),a.VOUCHER_DT,105) AS VOUCHER_DT
	FROM VM01106 a (NOLOCK)
	JOIN vchtype B (NOLOCK) ON B.VOUCHER_CODE=A.VOUCHER_CODE
	WHERE A.VOUCHER_DT=@VOUCHER_DT AND a.VOUCHER_NO LIKE @VCH_NO
	ORDER BY a.VOUCHER_DT,a.VOUCHER_NO,b.VOUCHER_TYPE
END 