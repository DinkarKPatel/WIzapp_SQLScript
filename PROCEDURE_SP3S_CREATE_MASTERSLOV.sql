CREATE PROCEDURE DBO.SP3S_CREATE_MASTERSLOV
----WITH ENCRYPTION
AS
 BEGIN
	 
	DELETE FROM SEND_MASTERS_LOV
	 	
   ---THIS PROCEDURE DIPLAY XN TYPE WITH DETAILS
   INSERT SEND_MASTERS_LOV (XN_TYPE,XN_DESCRIPTION)
    SELECT XN_TYPE,XN_DESCRIPTION FROM 
     (
        SELECT 'MSTLOC'  AS XN_TYPE , 'LOCATION MASTER' AS XN_DESCRIPTION  UNION ALL
		SELECT 'MSTSTKCNT'  AS XN_TYPE , 'MANUAL STOCK COUNT SETUP' AS XN_DESCRIPTION UNION ALL
        SELECT 'MSTUSR'  AS XN_TYPE , 'USERS MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTWPAY'  AS XN_TYPE , 'WIZPAY MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTEOSS'  AS XN_TYPE , 'EOSS MASTER' AS XN_DESCRIPTION  UNION ALL
		 SELECT 'MSTEOSSNEW'  AS XN_TYPE , 'NEW EOSS MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTBWD'  AS XN_TYPE , 'CARD EXCEPTIONS MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTBGT'  AS XN_TYPE , 'MONTHLY BUDGET MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTANGM'  AS XN_TYPE , 'ANGADIA MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTXNAPP'  AS XN_TYPE , 'TRANSACTION APPROVAL MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTTILL'  AS XN_TYPE , 'TILL MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTTGT'  AS XN_TYPE , 'TGT MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTSTKLVL'  AS XN_TYPE , 'STOCK LEVELS MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTSEASON'  AS XN_TYPE , 'SEASON MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTREGINFO'  AS XN_TYPE , 'REGINFO MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTPTC'  AS XN_TYPE , 'PETTYCASH HEADS MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTPRNFM'  AS XN_TYPE , 'PRINT FORMATS MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTPRM'  AS XN_TYPE , 'PARTY RATE MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTPAYMODE'  AS XN_TYPE , 'PAYMENT MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTPATCH'  AS XN_TYPE , 'PATCH MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTLSP'  AS XN_TYPE , 'LOCSPACE MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTLREQ'  AS XN_TYPE , 'LOCATION CASH OPENING MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTLBR'  AS XN_TYPE , 'BILLING RULES MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTJOBS'  AS XN_TYPE , 'JOBS MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTGVSCH'  AS XN_TYPE , 'GV SCHEMES MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTFORM'  AS XN_TYPE , 'TAX TYPE MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTEMP'  AS XN_TYPE , 'SALESPERSON MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTDTM'  AS XN_TYPE , 'DISCOUNT TYPE MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTDSB'  AS XN_TYPE , 'SALESCONTRIBUTION MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTCAT'  AS XN_TYPE , 'MRP CATEGORY MASTER' AS XN_DESCRIPTION UNION ALL
        SELECT 'MSTACT'  AS XN_TYPE , 'LEDGER MASTER' AS XN_DESCRIPTION UNION ALL
        SELECT 'MSTHSN'  AS XN_TYPE , 'HSN MASTER' AS XN_DESCRIPTION UNION ALL 
        SELECT 'MSTEAN'  AS XN_TYPE , 'EAN MASTER' AS XN_DESCRIPTION UNION ALL 
        SELECT 'MSTCUST'  AS XN_TYPE , 'CUSTOMER MASTER' AS XN_DESCRIPTION UNION ALL 
        SELECT 'MSTART'  AS XN_TYPE , 'ARTICLE MASTER' AS XN_DESCRIPTION  UNION ALL
        SELECT 'MSTINV'  AS XN_TYPE , 'SYNCH SKU MASTER' AS XN_DESCRIPTION UNION ALL
		 SELECT 'MSTATTR'  AS XN_TYPE , 'SYNCH ARTICLE ATTRIBUTES' AS XN_DESCRIPTION UNION ALL
        SELECT 'MSTLST'  AS XN_TYPE , 'TAX STRUCTURE MASTER' AS XN_DESCRIPTION UNION ALL
        SELECT 'MSTSCH'  AS XN_TYPE , 'SALE PERSON COMMISSION' AS XN_DESCRIPTION UNion all 
        SELECT 'MSTCUSATTR'  AS XN_TYPE , 'CUSTOMER ATTRIBUTE' AS XN_DESCRIPTION 
    ) T ORDER BY XN_DESCRIPTION
 
END
----------- SP3S_CREATE_MASTERSLOV
