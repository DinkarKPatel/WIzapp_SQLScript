CREATE PROCEDURE SPSIS_SYNCH_UPLOADDATA_CNC_OPT
(
    @CSPID VARCHAR(50),
	@CDEPTID VARCHAR(5)/*Rohit 01-11-2024*/,
    @CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN

DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX)
	   ,@CTABLENAME VARCHAR(200),@CKEYFIELD VARCHAR(200),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@CCURDEPTID VARCHAR(5),
	    @CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE VARCHAR(200),@NMEMONOLEN INT,@CFINYEAR VARCHAR(5),@CMEMOPREFIX VARCHAR(10)
	   ,@CSOURCEDB VARCHAR(200),@CMERGEDB VARCHAR(200)
	   ,@BMSTINSERTONLY BIT,@BSERIESMISMATCHFOUND BIT,@CMEMOID VARCHAR(40),@CPREVMEMONO VARCHAR(20)
	   ,@NPREVMEMONO NUMERIC(5,0),@NLENVALUE INT,@CPREVMEMONOSEARCH VARCHAR(20),@CMEMOIDSEARCH VARCHAR(40)
	   ,@CRETPRODUCTCODE VARCHAR(50),@NVERSIONNO INT,@BADDMODE BIT,@CTEMPDETAILTABLE1 VARCHAR(200),@bCancelled bit
	

BEGIN TRY
	
	
	SET @CSTEP=10

	SELECT @CSOURCEDB='',@CMERGEDB='',@BMSTINSERTONLY=1,@BSERIESMISMATCHFOUND=0
	
	SET @CSTEP=15
    	
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'

	SELECT @CTEMPMASTERTABLE=@CSOURCEDB+'CNC_ICM01106_UPLOAD',
		   @CTEMPDETAILTABLE=@CSOURCEDB+'CNC_ICD01106_UPLOAD'
	
	
	
LBLSTART:


	BEGIN TRANSACTION 	     

    
    SELECT @CMEMOID='',@NVERSIONNO=0

	UPDATE A SET USER_CODE ='0000000',
				 BIN_ID='000',
				 CNC_MEMO_ID=@CDEPTID+SUBSTRING(CNC_MEMO_ID,LEN(@CDEPTID)+1,LEN(CNC_MEMO_ID))/*Rohit 01-11-2024*/ ,
				 CNC_MEMO_NO=@CDEPTID+SUBSTRING(CNC_MEMO_NO,LEN(@CDEPTID)+1,LEN(CNC_MEMO_NO))/*Rohit 01-11-2024*/,
				 location_Code =@CDEPTID
				 
	FROM CNC_ICM01106_UPLOAD A (NOLOCK)
	WHERE SP_ID =@CSPID

	UPDATE A SET BIN_ID ='000',
	          CNC_MEMO_ID=@CDEPTID++SUBSTRING(CNC_MEMO_ID,LEN(@CDEPTID)+1,LEN(CNC_MEMO_ID))/*Rohit 01-11-2024*/ ,
			  DEPT_ID=@CDEPTID

	FROM CNC_ICD01106_UPLOAD A (NOLOCK)
	WHERE SP_ID =@CSPID

	
	 UPDATE A SET PRODUCT_CODE=LEFT(A.PRODUCT_CODE, ISNULL(NULLIF(CHARINDEX ('@',A.PRODUCT_CODE)-1,-1),LEN(A.PRODUCT_CODE )))  
	 FROM CNC_ICD01106_UPLOAD A (NOLOCK)	WHERE SP_ID=@CSPID
	 AND CHARINDEX('@',PRODUCT_CODE)<>0

	
    SELECT TOP 1 @CMEMOID = B.CNC_MEMO_ID ,@bCancelled=cancelled
    FROM  CNC_ICM01106_UPLOAD B (NOLOCK)
    WHERE SP_ID=@CSPID 

    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		
    SET @CFILTERCONDITION = ' B.SP_ID='''+LTRIM(RTRIM((@CSPID)))+''''
	


	SET @CSTEP=110
	SET @CMEMOIDSEARCH=''
	SELECT TOP 1 @CMEMOIDSEARCH=A.CNC_MEMO_ID FROM ICM01106 A (NOLOCK) WHERE A.CNC_MEMO_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1

IF @BADDMODE=1 AND NOT EXISTS (SELECT TOP 1 'U' FROM CNC_ICD01106_UPLOAD WHERE SP_ID =@CSPID)
	    GOTO EXIT_PROC
 IF @BADDMODE=0 AND NOT EXISTS (SELECT TOP 1 'U' FROM CNC_ICD01106_UPLOAD WHERE SP_ID =@CSPID)
		SET @BCANCELLED=1

	
LBLMERGE:
	---DELETING EXISTING RECORD IF BILL COMES AGAIN FOR MERGING
	
	--SELECT @CTEMPMASTERTABLE,@CFILTERCONDITION
	IF @BADDMODE=0
		PRINT 'ADDMODE:N'
	ELSE
		PRINT 'ADDMODE:Y'	
	
	IF @BADDMODE=0
	BEGIN	
	     

		 
		 INSERT SAVETRAN_BARCODE_NETQTY	( BIN_ID, DEPT_ID, PRODUCT_CODE, sp_id, XN_QTY ) 
		 SELECT 	'000'  BIN_ID,/*LEFT(A.cnc_memo_id,2)*//*Rohit 01-11-2024*/@CDEPTID DEPT_ID, PRODUCT_CODE,@CSPID SP_ID,
		 CASE WHEN b.CNC_TYPE=1 THEN 1 ELSE -1 END *  A.QUANTITY AS XN_QTY 
		 FROM icd01106 A (NOLOCK)
		 join icm01106 b (NOLOCK) on a.cnc_memo_id=b.cnc_memo_id
		 WHERE A.cnc_memo_id =@CMEMOID


		IF @bCancelled=1 
		BEGIN
			UPDATE ICM01106 WITH (ROWLOCK) SET cancelled=1 WHERE cnc_memo_id=@cMemoid
			GOTO lblupdatepmt
		END
				
		SET @CSTEP=115
		SET @DTSQL=N'DELETE A FROM ICD01106 A JOIN '+@CTEMPMASTERTABLE+' B (NOLOCK) ON A.CNC_MEMO_ID=B.CNC_MEMO_ID
					 LEFT OUTER JOIN 
					 (SELECT ROW_ID FROM '+@CTEMPDETAILTABLE+' B WHERE '+@CFILTERCONDITION+') C ON A.ROW_ID = C.ROW_ID 
					 WHERE C.ROW_ID IS NULL AND '+@CFILTERCONDITION
		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
	END
	
	---UPDATING TRANSACTION TABLES
	SET @CSTEP=250
	SET @CTABLENAME='ICM01106'
	SET @CTMP_TABLENAME='CNC_ICM01106_UPLOAD'
	SET @CKEYFIELD='CNC_MEMO_ID'
	
	SET @DTSQL = N'UPDATE '+@CSOURCEDB+@CTMP_TABLENAME+' WITH (ROWLOCK)  SET CNC_MEMO_DT = CONVERT(DATETIME,CONVERT(VARCHAR(10),CNC_MEMO_DT,120))
	WHERE SP_ID='''+LTRIM(RTRIM((@CSPID)))+''' '
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL
	
	SET @CSTEP=260
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 

    
	 SELECT a.product_code as new_product_code into #tmpbarcode FROM CNC_ICD01106_UPLOAD A (NOLOCK)
		LEFT JOIN SKU B (nolock) ON A.PRODUCT_CODE =B.PRODUCT_CODE 
		WHERE A.SP_ID=@CSPID AND B.PRODUCT_CODE IS NULL
		group by a.product_code

		IF EXISTS (SELECT TOP 1 'U' FROM #tmpbarcode)
		BEGIN
			SET @CSTEP=84.2
			EXEC SP_CHKXNSAVELOG 'CNCMERGE',@CSTEP,0,@CSPID,'',1

			 EXEC SPSIS_INSERTSKUBARCODE @CSPID, 'CNC',@CERRMSG OUTPUT

			 IF ISNULL(@CERRMSG,'')<>''
			    GOTO EXIT_PROC

          end

							  
	SET @CTABLENAME='ICD01106'
	SET @CTMP_TABLENAME='CNC_ICD01106_UPLOAD'
	SET @CKEYFIELD='ROW_ID'
	
	PRINT 'MERGE ICD01106 FOR MEMO ID :'+@CMEMOID 
	SET @CSTEP=280
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 


	
		SET @CSTEP=340


		 INSERT SAVETRAN_BARCODE_NETQTY	( BIN_ID, DEPT_ID, PRODUCT_CODE, sp_id, XN_QTY ) 
		 SELECT 	'000'  BIN_ID,/*LEFT(A.cnc_memo_id,2)*//*Rohit 01-11-2024*/@CDEPTID DEPT_ID, PRODUCT_CODE,@CSPID SP_ID,
		 CASE WHEN b.CNC_TYPE=1 THEN -1 ELSE 1 END *  A.QUANTITY AS XN_QTY 
		 FROM icd01106 A (NOLOCK)
		 join icm01106 b (NOLOCK) on a.cnc_memo_id=b.cnc_memo_id
		 WHERE A.cnc_memo_id =@CMEMOID

	lblupdatepmt:
	EXEC SPSIS_UPDATEPMT @CSPID


	
	    		
		DELETE A FROM CNC_ICM01106_UPLOAD A WHERE A.SP_ID=@CSPID
		DELETE A FROM CNC_ICD01106_UPLOAD A WHERE A.SP_ID=@CSPID
		
	

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SPSIS_SYNCH_MIRRORDATA_CNC_SINGLE, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO EXIT_PROC
END CATCH

EXIT_PROC:

	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRMSG,'')='' AND @BSERIESMISMATCHFOUND<>1
			COMMIT
		ELSE
			ROLLBACK
    END
	
END	
---END OF PROCEDURE - SP_SYNCH_MIRRORDATA_CNC_SINGLE
