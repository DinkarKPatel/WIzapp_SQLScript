create PROCEDURE  SPSIS_SYNCH_UPLOADDATA_GRNPS_OPT
(
    @nSpId VARCHAR(50),
	@CDEPTID VARCHAR(5)/*Rohit 01-11-2024*/,
    @CERRMSG VARCHAR(1000) OUTPUT
)
AS
SET NOCOUNT ON
BEGIN


DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX)
	   ,@CTABLENAME VARCHAR(200),@CKEYFIELD VARCHAR(200),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1)
	   ,@CCURDEPTID VARCHAR(5),@CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE1 VARCHAR(200),
	    @CTEMPDETAILTABLE2 VARCHAR(200), @CTEMPDETAILTABLE3 VARCHAR(200), @CTEMPDETAILTABLE4 VARCHAR(200)
	   ,@BADDMODE BIT,@CSOURCEDB VARCHAR(200),@CMERGEDB VARCHAR(200)
	   ,@BMSTINSERTONLY BIT,@BSERIESMISMATCHFOUND BIT,@CMEMOID VARCHAR(40),@CPREVMEMONO VARCHAR(20)
	   ,@NPREVMEMONO NUMERIC(5,0),@NLENVALUE INT,@CPREVMEMONOSEARCH VARCHAR(20),@CMEMOIDSEARCH VARCHAR(40)
	   ,@NVERSIONNO INT,@CJOINSTR VARCHAR(MAX)
	   ,@CTEMPIMPTABLE VARCHAR(200),@CCMD NVARCHAR(MAX),@CFILTERCONDITION1 VARCHAR(200),@bcancelled bit
	

BEGIN TRY
	
	
	SET @CSTEP=10

	SELECT @CSOURCEDB='',@CMERGEDB='',@BMSTINSERTONLY=1,@BSERIESMISMATCHFOUND=0
	
	SET @CSTEP=15
    	
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'

	SELECT @CTEMPMASTERTABLE=@CSOURCEDB+'GRNPS_GRN_PS_MST_UPLOAD',
		   @CTEMPDETAILTABLE1=@CSOURCEDB+'GRNPS_GRN_PS_DET_UPLOAD'
	
    

LBLSTART:
    
    BEGIN TRANSACTION
    
    SELECT @CMEMOID='',@NVERSIONNO=0
    
		UPDATE A SET 
	             AC_CODE ='0000000000',
				 USER_CODE ='0000000',
				 BIN_ID='000',
				 MEMO_ID=@CDEPTID+SUBSTRING(MEMO_ID,LEN(@CDEPTID)+1,LEN(MEMO_ID))/*Rohit 01-11-2024*/ ,
				 MEMO_NO=@CDEPTID+SUBSTRING(MEMO_NO,LEN(@CDEPTID)+1,LEN(MEMO_NO))/*Rohit 01-11-2024*/
	FROM GRNPS_GRN_PS_MST_UPLOAD A (NOLOCK)
	WHERE SP_ID =@nSpId

	UPDATE A SET BIN_ID ='000',
	          MEMO_ID=@CDEPTID+SUBSTRING(MEMO_ID,LEN(@CDEPTID)+1,LEN(MEMO_ID)) /*Rohit 01-11-2024*/
			 
	FROM GRNPS_GRN_PS_MST_UPLOAD A (NOLOCK)
	WHERE SP_ID =@nSpId

	
	 UPDATE A SET PRODUCT_CODE=LEFT(A.PRODUCT_CODE, ISNULL(NULLIF(CHARINDEX ('@',A.PRODUCT_CODE)-1,-1),LEN(A.PRODUCT_CODE )))  
	 FROM GRNPS_GRN_PS_DET_UPLOAD A (NOLOCK)	WHERE SP_ID=@NSPID
	 AND CHARINDEX('@',PRODUCT_CODE)<>0


    SET @CSTEP=30
    SELECT TOP 1 @CMEMOID = B.MEMO_ID ,@bcancelled=CANCELLED
    FROM GRNPS_GRN_PS_MST_UPLOAD B (NOLOCK)

  
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		
 
    
    SET @CFILTERCONDITION = 'B.SP_ID='''+@nSpId+''''
	

	SET @CSTEP=40
	SET @CMEMOIDSEARCH=''
	SELECT TOP 1 @CMEMOIDSEARCH=A.MEMO_ID FROM GRN_PS_MST A (NOLOCK) WHERE A.MEMO_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1


	IF @BADDMODE=0
	BEGIN	
		
	  	IF @bCancelled=1 
		BEGIN
			UPDATE GRN_PS_MST WITH (ROWLOCK) SET cancelled=1 WHERE MEMO_ID=@cMemoid
			GOTO EXIT_PROC
		END
	
		SET @CSTEP=50
		SET @DTSQL=N'DELETE A FROM GRN_PS_DET A JOIN '+@CTEMPMASTERTABLE+' B (NOLOCK) ON A.MEMO_ID=B.MEMO_ID
					 LEFT OUTER JOIN 
					 (SELECT ROW_ID FROM '+@CTEMPDETAILTABLE1+' B WHERE '+@CFILTERCONDITION+') C 
					 ON A.ROW_ID = C.ROW_ID 
					 WHERE A.MEMO_ID='''+@CMEMOID+'''  C.ROW_ID IS NULL AND '+@CFILTERCONDITION

		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
		
		
	END
	
LBLMERGE:

	

	SET @CFILTERCONDITION = 'B.MEMO_ID='''+@CMEMOID+''' AND B.SP_ID='''+@CMEMOID+''''							  
	
	
    SET @CTABLENAME='GRN_PS_MST'
	SET @CTMP_TABLENAME='GRNPS_GRN_PS_MST_UPLOAD'
	SET @CKEYFIELD='MEMO_ID'

	
	
	SET @CSTEP=70	 
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 
							  
	  SELECT a.product_code as new_product_code into #tmpbarcode FROM GRNPS_GRN_PS_DET_UPLOAD A (NOLOCK)
		LEFT JOIN SKU B (nolock) ON A.PRODUCT_CODE =B.PRODUCT_CODE 
		WHERE A.SP_ID=@nSpId AND B.PRODUCT_CODE IS NULL
		group by a.product_code

		IF EXISTS (SELECT TOP 1 'U' FROM #tmpbarcode)
		BEGIN
			SET @CSTEP=84.2
			EXEC SP_CHKXNSAVELOG 'appMERGE',@CSTEP,0,@nSpId,'',1

			 EXEC SPSIS_INSERTSKUBARCODE @NSPID, 'app',@CERRMSG OUTPUT

			 IF ISNULL(@CERRMSG,'')<>''
			    GOTO EXIT_PROC

          end						  
							  
    SET @CTABLENAME='GRN_PS_DET'
	SET @CTMP_TABLENAME='GRNPS_GRN_PS_DET_UPLOAD'
	SET @CKEYFIELD='ROW_ID'

	
	SET @CSTEP=80
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1 
							  

						  							  

	DELETE A FROM GRNPS_GRN_PS_MST_UPLOAD A WHERE A.SP_ID =@nSpId
	DELETE A FROM GRNPS_GRN_PS_DET_UPLOAD A WHERE A.SP_ID =@nSpId

	

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SPSIS_SYNCH_UPLOADDATA_GRNPS_OPT, MEMO ID :'+@CMEMOID+' STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO EXIT_PROC
END CATCH

EXIT_PROC:

	
	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRMSG,'')='' AND @BSERIESMISMATCHFOUND<>1
			COMMIT
		ELSE
			ROLLBACK
    END
	
END	
---END OF PROCEDURE - SP_SYNCH_MIRRORDATA_GRNPS_SINGLE
