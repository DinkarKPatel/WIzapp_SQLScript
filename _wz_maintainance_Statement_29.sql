

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SKU_CODINGSCHEME_1' AND VALUE=1)
BEGIN
	PRINT 'UPDATING CODING SCHEME COLUMN IN SKU'
	

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SKU_CODINGSCHEME_1')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_SKU_CODINGSCHEME_1','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_SKU_CODINGSCHEME_1'
	
	UPDATE SKU SET BARCODE_CODING_SCHEME=B.CODING_SCHEME FROM ARTICLE B WHERE B.ARTICLE_CODE=SKU.ARTICLE_CODE
	
END		
