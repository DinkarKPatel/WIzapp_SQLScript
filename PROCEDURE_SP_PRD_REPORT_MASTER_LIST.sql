CREATE PROCEDURE SP_PRD_REPORT_MASTER_LIST
(
	@QUERYID NUMERIC(5)
)
--WITH ENCRYPTION

AS
BEGIN
IF		@QUERYID = 1 GOTO LBL1
ELSE IF	@QUERYID = 2 GOTO LBL2
ELSE				 GOTO LAST

LBL1:
	SELECT DISTINCT LM.AC_NAME ,LM.AC_CODE  
	FROM LM01106 LM (NOLOCK) 
	JOIN WSL_ORDER_MST M (NOLOCK) ON M.AC_CODE = LM.AC_CODE  
	WHERE LM.AC_CODE <> '0000000000'
	UNION ALL
	SELECT DISTINCT (CUST.USER_CUSTOMER_CODE+' - '+  CUST.CUSTOMER_FNAME +' '+ CUST.CUSTOMER_LNAME ) AS AC_NAME, 
	CUST.CUSTOMER_CODE AS AC_CODE 
	FROM CUSTDYM CUST (NOLOCK) 
	JOIN WSL_ORDER_MST M (NOLOCK) ON M.CUSTOMER_CODE = CUST.CUSTOMER_CODE   
	WHERE CUST.CUSTOMER_CODE <> '000000000000'
GOTO LAST

LBL2:
	SELECT DISTINCT (EMP_TITLE +' '+ EMP_FNAME +' '+ EMP_LNAME) AS EMP_NAME ,EMP_ID AS EMP_CODE,REF_ID 
	FROM EMP_MST EMP (NOLOCK) JOIN PRD_WO_MST WOM (NOLOCK) ON EMP.EMP_ID =WOM.EMP_CODE
LAST:
END
