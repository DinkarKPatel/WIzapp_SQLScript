CREATE PROCEDURE SP3S_SYNCH_UPLOADDATA_TEX_OPT
(
	@CspId VARCHAR(50),  
    @CERRMSG VARCHAR(MAX) OUTPUT  
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_TEX_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),
	    @CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(1000),@CCURDEPTID VARCHAR(10)
	   ,@CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE VARCHAR(200),@CSOURCEDB VARCHAR(50),@CMERGEDB VARCHAR(50),
	   @CSEARCHMEMO_ID VARCHAR(50),@CMEMO_ID VARCHAR(25)
	   		    
BEGIN TRY
SET @CSTEP=00
    SET @CSOURCEDB=''
	SET @CMERGEDB=''
	SET @CTABLE_SUFFIX='UPLOAD'
BEGIN TRANSACTION 

	 ---UPDATE POSTEDINAC MARK FOR THE BILLS POSTEDINTO ACCOUNTS & THERE IS SOME MISMATCH BETWEEN INCOMING DATA & EXISTING DATA
	SET @CSTEP=10
    
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'

	SELECT @CMEMO_ID=MEMO_ID  FROM TEX_TILL_EXPENSE_MST_UPLOAD WHERE SP_ID=@CspId

	IF ISNULL(@CMEMO_ID,'')=''
	   GOTO EXIT_PROC
	
	DECLARE @CMEMOIDSEARCH VARCHAR(100),@BADDMODE BIT
	SELECT TOP 1 @CMEMOIDSEARCH=A.MEMO_ID FROM APM01106 A (NOLOCK) WHERE A.MEMO_ID=@CMEMO_ID 
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1

	IF @BADDMODE=0
	BEGIN
	   
	   DELETE A FROM TILL_EXPENSE_MST A (NOLOCK) WHERE MEMO_iD=@CMEMOIDSEARCH
	END

	SET @cFiLterCondition=' b.sp_id='''+@cSPId+''''

    SET @CSTEP=250
	SET @CTABLENAME='TILL_EXPENSE_MST'
	SET @CTMP_TABLENAME='TEX_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@cFiLterCondition,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 				
	
	
	
	
	SET @CSTEP=250
	SET @CTABLENAME='TILL_EXPENSE_DET'
	SET @CTMP_TABLENAME='TEX_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ROW_ID'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@cFiLterCondition,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  
	
	DELETE FROM TEX_TILL_EXPENSE_MST_UPLOAD WHERE SP_ID=@CspId 			
	DELETE FROM TEX_TILL_EXPENSE_DET_UPLOAD WHERE SP_ID=@CspId 	  
	
END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP3S_SYNCH_UPLOADDATA_TEX_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_TEX_DATA
