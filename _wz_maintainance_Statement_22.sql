


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_RMD01106_INVQTY_1' AND VALUE=1)
BEGIN
	PRINT 'UPDATING INVOICE QTY COLUMN IN DEBIT NOTE DETAIL'
	

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_RMD01106_INVQTY_1')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_RMD01106_INVQTY_1','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_RMD01106_INVQTY_1'
	
	UPDATE RMD01106 SET INVOICE_QUANTITY=(QUANTITY-SCHEME_QUANTITY) WHERE INVOICE_QUANTITY=0
	
END		
