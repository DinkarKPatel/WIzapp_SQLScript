

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_FC_DEFAULT' AND VALUE=1)
BEGIN
	PRINT 'UPDATE DEFAULT ENTRY IN FC TABLE'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_FC_DEFAULT')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_FC_DEFAULT','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_FC_DEFAULT'
	
	UPDATE FC SET FC_NAME='INR' WHERE FC_CODE='0000000'
END		
