

-- PROCEDURE SP_WL_LEDGER_DATABINDING
-- RETURNS A SET OF TABLES FOR THE DATABINDING FOR LMV01106 MODULE
-- MODULE NAME - LEDGER MASTER
CREATE PROCEDURE SP_WL_LEDGER_DATABINDING
(
	@CACCODE VARCHAR(10)=''
)
--WITH ENCRYPTION
AS
BEGIN
	----DATATABLE 'TARTICLE'
	SELECT B.HEAD_NAME, A.* 
	FROM LMV01106 A 
	JOIN HD01106 B ON A.HEAD_CODE = B.HEAD_CODE
	WHERE A.AC_CODE = @CACCODE
END
----****************** END OF VIEW SP_WL_LEDGER_DATABINDING
