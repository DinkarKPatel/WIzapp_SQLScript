CREATE FUNCTION FN_GETRFNETVALUE 
(	@NPRICE NUMERIC(14,2),
	@NQTY NUMERIC(14,3),
	@NSUBTOTAL NUMERIC(14,2),
	@NEXP NUMERIC(14,2)
)
RETURNS NUMERIC(14,2)
--WITH ENCRYPTION
AS 
BEGIN
	DECLARE @NRFNET NUMERIC(10,2), @NPER NUMERIC(15,5)

	SET @NPER = ( CASE WHEN @NSUBTOTAL=0 THEN 0 ELSE @NEXP/@NSUBTOTAL * 100 END )
	SET @NRFNET = @NQTY * ( @NPRICE + @NPRICE * @NPER /100 )

	RETURN @NRFNET
END
