CREATE FUNCTION FN3SCHECKDUPREPORT
(
	 @CUSERCODE CHAR(7)
	,@CREFREPID    CHAR(10)
)
RETURNS BIT
AS
BEGIN
	DECLARE @BVALID BIT
	
	SET @BVALID=1
	
	IF ISNULL(@CREFREPID,'')<>''
	BEGIN
		IF EXISTS(SELECT TOP 1 'U' FROM REP_MST 
					WHERE REF_REP_ID=@CREFREPID AND USER_CODE=@CUSERCODE
					 GROUP BY REF_REP_ID,USER_CODE
					  HAVING COUNT(*)>1)
		SET @BVALID=0
	END
	
	
	RETURN @BVALID
END
