CREATE VIEW VW_BDGPL_PLAN
AS


               SELECT A.SEASON_ID,C.SEASON_NAME, B.SECTION_CODE,D.SECTION_NAME, BUDGET_PLAN_NAME,MEMO_DT,A.MEMO_NO,EXPIRY_DT,CANCELLED,APPROVEDLEVELNO,
                BUDGET_AMOUNT 
                FROM BUDGET_PLAN_MST A(NOLOCK) 
                JOIN BUDGET_PLAN_DET B(NOLOCK) ON A.MEMO_NO=B.MEMO_NO
                JOIN SEASON_MST C(NOLOCK) ON C.SEASON_ID=A.SEASON_ID
                JOIN SECTIONM D(NOLOCK) ON D.SECTION_CODE=B.SECTION_CODE
