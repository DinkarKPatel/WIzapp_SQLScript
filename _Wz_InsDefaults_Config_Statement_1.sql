IF NOT EXISTS ( SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION = 'RO_LOCATION_ID' )
BEGIN
	INSERT CONFIG ( CONFIG_OPTION, VALUE, ROW_ID,  LAST_UPDATE )
			VALUES (  'RO_LOCATION_ID', '01', '013',  '')
END

IF NOT EXISTS(SELECT TOP 1 'U' FROM CONFIG WHERE  CONFIG_OPTION='SHOW_UDRF')
BEGIN
	INSERT CONFIG	(CONFIG_OPTION, VALUE, ROW_ID, LAST_UPDATE,  REMARKS )  
	SELECT 	'SHOW_UDRF' AS CONFIG_OPTION,0 AS VALUE,'' AS ROW_ID,GETDATE() AS LAST_UPDATE
	,'' AS REMARKS 
END





