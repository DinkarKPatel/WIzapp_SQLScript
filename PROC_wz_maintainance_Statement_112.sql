

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SCHEME_FILTERS_3' AND VALUE=1)
BEGIN
	PRINT 'UPDATING FILTER CRITERIAS OF SCHEME SET TABLES RELATED TO PRODUCT CODE'

	UPDATE SCHEME_SETUP_DET SET GET_FILTER_CRITERIA=REPLACE(GET_FILTER_CRITERIA,'A.PRODUCT_CODE','ITV.PRODUCT_CODE')
	WHERE GET_FILTER_CRITERIA NOT LIKE '%ITV.PRODUCT_CODE%' AND GET_FILTER_CRITERIA LIKE '%A.PRODUCT_CODE%'

	UPDATE SCHEME_SETUP_DET SET BUY_FILTER_CRITERIA=REPLACE(BUY_FILTER_CRITERIA,'A.PRODUCT_CODE','ITV.PRODUCT_CODE')
	WHERE BUY_FILTER_CRITERIA NOT LIKE '%ITV.PRODUCT_CODE%' AND BUY_FILTER_CRITERIA LIKE '%A.PRODUCT_CODE%'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_SCHEME_FILTERS_3')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_SCHEME_FILTERS_3',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_SCHEME_FILTERS_3'	
END	
