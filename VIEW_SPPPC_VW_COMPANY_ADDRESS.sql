CREATE VIEW SPPPC_VW_COMPANY_ADDRESS  
AS  
SELECT COMPANY_NAME ,COMPANY_IMG_PATH=CMP.LOGO_PATH,  
[ADDRESS]=CA.AREA_NAME+', '+CC.CITY+', '+CS.[STATE]  
FROM  
COMPANY CMP   
LEFT JOIN AREA CA ON CMP.AREA_CODE=CA.AREA_CODE  
LEFT JOIN CITY CC ON CA.CITY_CODE=CC.CITY_CODE    
LEFT JOIN STATE CS ON CC.CITY_CODE=CS.STATE_CODE  
WHERE CMP.COMPANY_CODE ='01'
