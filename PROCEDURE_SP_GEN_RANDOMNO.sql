CREATE PROCEDURE SP_GEN_RANDOMNO
(
	@NLOWER INT ,---- THE LOWEST RANDOM NUMBER
	@NUPPER INT ,---- THE HIGHEST RANDOM NUMBER
	@NRANDOMNO INT OUTPUT
)
--WITH ENCRYPTION
AS
BEGIN
	--DECLARE @NUPPER INT;
	--DECLARE @NLOWER INT

	------ THIS WILL CREATE A RANDOM NUMBER BETWEEN 1 AND 999
	--SET @NLOWER = 10000 ---- THE LOWEST RANDOM NUMBER
	--SET @NUPPER = 99999 ---- THE HIGHEST RANDOM NUMBER
	SELECT @NRANDOMNO = ROUND(((@NUPPER - @NLOWER -1) * RAND() + @NLOWER), 0)
END
