CREATE PROCEDURE SP3S_SYNCH_UPLOADDATA_WBO_OPT
(
	@CspId VARCHAR(50),  
    @CERRMSG VARCHAR(MAX) OUTPUT  
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_WBO_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(500),@CMEMOID VARCHAR(50)
       ,@CLOCID VARCHAR(5),@CSOURCEDB VARCHAR(200),@CMERGEDB VARCHAR(200),@BMSTINSERTONLY BIT

BEGIN TRY

SET @CSTEP=20
	SET @CTABLE_SUFFIX='UPLOAD'
BEGIN TRANSACTION 

	SET @CSTEP=30
	---- POPULATE LIST OF TABLES 

	SET @CSOURCEDB=''
	    SET @CMERGEDB=''
	    SET @BMSTINSERTONLY=1

		
		SELECT  @CMEMOID=ORDER_ID,@CLOCID=location_Code 
		FROM WBO_BUYER_ORDER_MST_UPLOAD 
		WHERE SP_ID=@CSPID

		 IF ISNULL(@CMEMOID,'')=''
		 GOTO EXIT_PROC
	
	    SET @CFILTERCONDITION=' B.SP_ID='''+@CSPID +''' '
	

	SET @CSTEP=160
	SET @CTABLENAME='EMPLOYEE'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='EMP_CODE'
	SET @CSTEP=170
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
    
	SET @CSTEP=175
	SET @CTABLENAME='PARA6'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PARA6_CODE'


	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
	SET @CSTEP=180
	SET @CTABLENAME='PARA5'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PARA5_CODE'

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

	SET @CSTEP=185
	SET @CTABLENAME='PARA4'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PARA4_CODE'

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

    SET @CSTEP=190
	SET @CTABLENAME='PARA3'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PARA3_CODE'

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  
	SET @CSTEP=260
	SET @CTABLENAME='PARA2'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PARA2_CODE'

	SET @CSTEP=270
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  
	SET @CSTEP=280
	SET @CTABLENAME='PARA1'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PARA1_CODE'

	SET @CSTEP=290
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

							  
	SET @CSTEP=300
	SET @CTABLENAME='ARTICLE'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ARTICLE_CODE'

	SET @CSTEP=310
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  


	SET @CSTEP=440
	SET @CTABLENAME='BUYER_ORDER_MST'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ORDER_ID'

	SET @CSTEP=450
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
	
						  
	SET @CSTEP=460
	SET @CTABLENAME='BUYER_ORDER_DET'
	SET @CTMP_TABLENAME='WBO_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ROW_ID'
   
    SET @DTSQL=N'DELETE A FROM BUYER_ORDER_DET A 
	LEFT JOIN '+@CSOURCEDB+''+'['+@CTMP_TABLENAME+'] B ON A.ROW_ID=B.ROW_ID AND B.SP_ID='''+@CspId+'''
	WHERE A.ORDER_ID='''+@CMEMOID+''' AND  B.ROW_ID IS NULL '	
    PRINT @DTSQL
    EXEC SP_EXECUTESQL @DTSQL
    
	SET @CSTEP=470
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

	
		 DELETE  FROM WBO_ARTICLE_UPLOAD  WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE  FROM WBO_BUYER_ORDER_DET_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE  FROM WBO_BUYER_ORDER_MST_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE  FROM WBO_EMPLOYEE_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE  FROM WBO_PARA1_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE FROM WBO_PARA2_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE FROM WBO_PARA3_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE  FROM WBO_PARA4_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE  FROM WBO_PARA5_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID
		 DELETE  FROM WBO_PARA6_UPLOAD WITH  (ROWLOCK) WHERE SP_ID =@CSPID

	--SELECT ' DELETE A FROM '+NAME +' (NOLOCK) WHERE SP_ID =@CSPID'  FROM SYS.TABLES WHERE NAME LIKE 'WBO%' AND create_date >='2020-05-11'

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP3S_SYNCH_UPLOADDATA_WBO_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_WBO_DATA
