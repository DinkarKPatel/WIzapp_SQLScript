CREATE PROCEDURE UPDATEPMT_PRD_JWI  
  @CXNTYPE VARCHAR(10),  
  @CXNNO VARCHAR(40),  
  @CXNID VARCHAR(40),  
  @NREVERTFLAG BIT = 0,  
  @NALLOWNEGSTOCK BIT = 0,  
  @NCHKDELBARCODES BIT = 0,  
  @NUPDATEMODE INT=0,   
  @CCMD NVARCHAR(4000) OUTPUT  
  
  --*** PARAMETERS :  
  --*** @CXNTYPE -		TRANSACTION TYPE (MODULE SPECIFIC)  
  --*** @CXNNO -		TRANSACTION NO ( MEMO NO OF MASTER TABLE )  
  --*** @CXNID -		TRANSACTION ID ( MEMO ID OF MASTER TABLE )  
  --*** @NREVERTFLAG -	A FLAG TO INDICATE WHETHER THIS PROCEDURE IS CALLED TO REVERT STOCK  
  --*** @NALLOWNEGSTOCK - FLAG TO INDICATE WHETHER OR NOT ALLOW NEGATIVE STOCK  
  --*** @NRETVAL - OUTPUT PARAMETER RETURNED BY THIS PROCEDURE (BIT 1-SUCCESS, 0-UNSUCCESS)  
--WITH ENCRYPTION
AS  
BEGIN  
	DECLARE @NOUTFLAG INT, @NRETVAL BIT,@CXNTABLE VARCHAR(50),@CEXPR NVARCHAR(500),@CXNIDPARA VARCHAR(50),  
	@BCANCELLED BIT  

	SET @NRETVAL = 0  
	SET @CCMD = ''  

	IF @NREVERTFLAG = 1  
		SET @NOUTFLAG = -1  
	ELSE  
		SET @NOUTFLAG = 1  

   
	PRINT 'UPDATEPMT - @NOUTFLAG ' + CONVERT(VARCHAR, @NOUTFLAG ) 
	 
  
	--*** UPDATING THE QUANTITY IN STOCK FROM PMT FOR THE GIVEN MEMO  
	UPDATE A  SET QUANTITY_IN_STOCK = QUANTITY_IN_STOCK - ( @NOUTFLAG * B.QUANTITY )  
	FROM  PRD_PMT A
	JOIN 
	( 
		SELECT B.PRODUCT_CODE, A.DEPARTMENT_ID, SUM(QUANTITY) AS QUANTITY   
		FROM PRD_JOBWORK_ISSUE_DET B   
		JOIN PRD_JOBWORK_ISSUE_MST A ON A.ISSUE_ID=B.ISSUE_ID  
		WHERE  B.ISSUE_ID = @CXNID   
		GROUP BY B.PRODUCT_CODE,A.DEPARTMENT_ID
	) B  ON A.PRODUCT_CODE = B.PRODUCT_CODE   
	AND A.DEPARTMENT_ID = B.DEPARTMENT_ID  
	
  
	SET @NRETVAL = 1  --*** SUCCESS  

	SELECT @BCANCELLED=CANCELLED FROM PRD_JOBWORK_ISSUE_MST WHERE ISSUE_ID=@CXNID  
    
	--*** CHECKING FOR NEGATIVE STOCK  
	--*** IF USER OPTED NOT TO ALLOW NEGATIVE STOCK AND STOCK IS GOING OUT  
	IF @NALLOWNEGSTOCK = 0 OR @BCANCELLED=1  
	BEGIN  
		IF EXISTS (  
				SELECT A.PRODUCT_CODE,SUM(A.QUANTITY_IN_STOCK) 
				FROM PRD_PMT  A 
				JOIN PRD_JOBWORK_ISSUE_DET B  ON B.PRODUCT_CODE=A.PRODUCT_CODE
				JOIN PRD_JOBWORK_ISSUE_MST C ON C.ISSUE_ID=B.ISSUE_ID  
				JOIN SKU D ON B.PRODUCT_CODE = D.PRODUCT_CODE  
				JOIN ARTICLE E ON D.ARTICLE_CODE = E.ARTICLE_CODE  
				WHERE  B.ISSUE_ID = @CXNID 
				AND E.STOCK_NA=0  AND B.PRODUCT_CODE=A.PRODUCT_CODE 
				AND C.DEPARTMENT_ID=A.DEPARTMENT_ID  
				GROUP BY A.PRODUCT_CODE
				HAVING SUM(A.QUANTITY_IN_STOCK)<0
			)
		BEGIN  
		SET @NRETVAL = 0  --*** UNSUCCESS  
		SET @CCMD = N'  
			SELECT A.PRODUCT_CODE,SUM(A.QUANTITY_IN_STOCK) 
			FROM PRD_PMT A 
			JOIN  PRD_JOBWORK_ISSUE_DET B  ON B.PRODUCT_CODE=A.PRODUCT_CODE
			JOIN PRD_JOBWORK_ISSUE_MST C ON C.ISSUE_ID=B.ISSUE_ID  
			WHERE  B.ISSUE_ID = '''+@CXNID+''' AND E.STOCK_NA=0  
			AND B.PRODUCT_CODE=A.PRODUCT_CODE AND C.DEPARTMENT_ID=A.DEPARTMENT_ID
			GROUP BY B.PRODUCT_CODE
			HAVING SUM(A.QUANTITY_IN_STOCK)<0 '  
		PRINT @CCMD    
		END  
	END   
END_PROC:  
	PRINT 'UPDATEPMT_JWI STEP OUT'  
END  
--***************************** END OF PROCEDURE UPDATEPMT_JWI
