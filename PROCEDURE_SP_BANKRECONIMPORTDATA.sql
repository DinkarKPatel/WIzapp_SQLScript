CREATE PROCEDURE SP_BANKRECONIMPORTDATA       

AS        
BEGIN        
   
SELECT 'CHQ NO' AS MASTER_COL,'CHQ_NO' AS MASTER_COL_EXPR,'' AS MAPPED_COL          
UNION ALL          
SELECT 'CLEARING_DT' AS MASTER_COL,'CLEARING_DT' AS MASTER_COL_EXPR,'' AS MAPPED_COL  
UNION ALL          
SELECT 'CREDIT AMOUNT' AS MASTER_COL,'CREDIT_AMOUNT' AS MASTER_COL_EXPR,'' AS MAPPED_COL           
UNION ALL          
SELECT 'DEBIT AMOUNT' AS MASTER_COL,'DEBIT_AMOUNT' AS MASTER_COL_EXPR,'' AS MAPPED_COL            

END     
---END OF PROCEDURE - SP_BANKRECONIMPORTDATA
