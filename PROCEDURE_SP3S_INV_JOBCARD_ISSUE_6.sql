CREATE PROCEDURE [DBO].[SP3S_INV_JOBCARD_ISSUE_6]          
(          
  @IMODE INT ,          
  @CWHERE VARCHAR(MAX)='',      
  @CAGENCYCODE VARCHAR(20)='',      
  @FINYEAR VARCHAR(10)=''  ,    
  @DEPTID VARCHAR(10)='' ,
  @NRETURNMODE INT=0
          
)      
----WITH ENCRYPTION
AS          
          
BEGIN        
 DECLARE @CCMD NVARCHAR(MAX)           
--LBLAGJOBS: 6          
 SELECT A.*,B.JOB_RATE ,B.AGENCY_CODE,'' AS ADDRESS_F 
 FROM JOBS (NOLOCK) A 
 JOIN AGENCY_JOBS B (NOLOCK)	ON A.JOB_CODE = B.JOB_CODE           
 JOIN PRD_AGENCY_MST C (NOLOCK) ON B.AGENCY_CODE = C.AGENCY_CODE      
 WHERE  B.AGENCY_CODE = @CWHERE 
 ORDER BY A.JOB_NAME         
END
