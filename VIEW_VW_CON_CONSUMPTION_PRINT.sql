CREATE VIEW VW_CON_CONSUMPTION_PRINT
--WITH ENCRYPTION
AS 
	SELECT A.MEMO_NO 
	,A.MEMO_DT
	,CASE WHEN A.CANCELLED =0 THEN '' ELSE 'CANCELLED' END AS CANCELLED
	,A.MEMO_ID,A.FIN_YEAR,
	B.PRODUCT_CODE,B.QUANTITY,B.REMARKS,C.ARTICLE_NAME AS ITEM_NAME,C.ARTICLE_DESC AS ITEM_DESC,E.PARA1_NAME , F.PARA2_NAME ,G.PARA3_NAME,H.UOM_NAME,H.UOM_TYPE
	FROM CON_CONSUMPTION_MST A JOIN
	CON_CONSUMPTION_DET B ON A.MEMO_ID = B.MEMO_ID 
	JOIN SKU D ON D.PRODUCT_CODE = B.PRODUCT_CODE
	JOIN ARTICLE C ON C.ARTICLE_CODE=D.ARTICLE_CODE
	JOIN PARA1 E ON E.PARA1_CODE = D.PARA1_CODE 
	JOIN PARA2 F ON F.PARA2_CODE = D.PARA2_CODE 
	JOIN PARA3 G ON G.PARA3_CODE = D.PARA3_CODE 
	JOIN UOM H ON H.UOM_CODE = C.UOM_CODE
