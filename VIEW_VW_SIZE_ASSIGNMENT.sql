create VIEW VW_SIZE_ASSIGNMENT
AS
     SELECT B.PRODUCT_CODE ,SKU.MRP,  A.MEMO_ID ,A.MEMO_NO,A.MEMO_DT ,
	        COMP.COMPANY_NAME ,SL.DEPT_NAME ,SL.DEPT_PRINT_NAME ,
			SL.ADDRESS1 ,SL.ADDRESS2  ,SLA.AREA_NAME ,SLC.CITY ,SLS.GST_STATE_NAME ,sla.pincode  ,comp.email_id ,sl.phone ,sl.PAN_NO ,sl.loc_gst_no ,
			lm.AC_NAME,AM.AGENCY_NAME ,lmp.ADDRESS0 as Ledger_ADDRESS0,lmp.ADDRESS1 Ledger_ADDRESS1,lmp.ADDRESS2 Ledger_ADDRESS2,
			pra.area_name Ledger_area,pra.pincode as Ledger_pincode ,prc.city  Ledger_city,lmp.Ac_gst_no ,prst.gst_state_name Ledger_state_name,lmp.pan_no as Ledger_pan_no ,
			C.ARTICLE_NO ,C.ARTICLE_NAME ,SKU.BARCODE_CODING_SCHEME AS CODING_SCHEME ,SM.SECTION_NAME,SD.SUB_SECTION_NAME,
			P1.PARA1_CODE,P1.PARA1_NAME,P2.PARA2_CODE,P2.PARA2_NAME,P3.PARA3_CODE ,P3.PARA3_NAME,P4.PARA4_CODE,P4.PARA4_NAME,
			P4.REMARKS AS PARA4_REMARKS,P5.PARA5_CODE,P5.PARA5_NAME,P6.PARA6_CODE ,P6.PARA6_NAME,C.ARTICLE_DESC,B.QUANTITY AS QTY
			,U.UOM_NAME,P4.ALIAS AS P4_ALIAS,SKU.ER_FLAG,SKU.VENDOR_EAN_NO,SKU.ONLINE_PRODUCT_CODE
			,P1.ALIAS AS PARA1_ALIAS,P2.ALIAS AS PARA2_ALIAS, P3.ALIAS AS PARA3_ALIAS,P2.PARA2_SET AS SIZEGROUP,
			P5.ALIAS AS PARA5_ALIAS, P6.ALIAS AS PARA6_ALIAS ,SKU.HSN_CODE,CAST('' AS VARCHAR(100)) JOBCARD_NO,C.DISCON
		FROM PPC_FG_BARCODE_NA_MST A (NOLOCK)        
		JOIN PPC_FG_BARCODE_NA_DET B (NOLOCK) ON A.MEMO_ID=B.MEMO_ID  
		JOIN SKU (NOLOCK) ON SKU.PRODUCT_CODE=B.PRODUCT_CODE          
		JOIN ARTICLE C (NOLOCK) ON SKU.ARTICLE_CODE=C.ARTICLE_CODE         	     
	    JOIN PARA1 P1 (NOLOCK) ON SKU.PARA1_CODE= P1.PARA1_CODE        
		JOIN PARA2 P2 (NOLOCK) ON SKU.PARA2_CODE= P2.PARA2_CODE        
		JOIN PARA3 P3 (NOLOCK) ON SKU.PARA3_CODE= P3.PARA3_CODE        
		JOIN PARA4 P4 (NOLOCK) ON SKU.PARA4_CODE= P4.PARA4_CODE        
		JOIN PARA5 P5 (NOLOCK) ON SKU.PARA5_CODE= P5.PARA5_CODE        
		JOIN PARA6 P6 (NOLOCK) ON SKU.PARA6_CODE= P6.PARA6_CODE        
		JOIN SECTIOND SD (NOLOCK) ON C.SUB_SECTION_CODE = SD.SUB_SECTION_CODE        
		JOIN SECTIONM SM (NOLOCK) ON SD.SECTION_CODE= SM.SECTION_CODE      
		JOIN UOM U (NOLOCK) ON U.UOM_CODE=C.UOM_CODE 
		JOIN COMPANY COMP (NOLOCK) ON COMP.COMPANY_CODE ='01'
		JOIN LOCATION SL (NOLOCK) ON SL.DEPT_ID =LEFT(A.MEMO_ID,2)
		JOIN AREA SLA (NOLOCK) ON SL.AREA_CODE=SLA.AREA_CODE                  
	    JOIN CITY SLC (NOLOCK) ON SLA.CITY_CODE=SLC.CITY_CODE                  
	    JOIN GST_STATE_MST SLS (NOLOCK) ON SLS.GST_STATE_CODE=SL.GST_STATE_CODE
		JOIN PRD_AGENCY_MST AM (NOLOCK) ON AM.AGENCY_CODE =A.AGENCY_CODE 
		join LM01106 lm (nolock) on lm.AC_CODE =am.ac_code 
		join LMp01106 lmp (nolock) on lmp.AC_CODE =lm.ac_code 
	    JOIN AREA PRA (NOLOCK) ON lmp.AREA_CODE=PRA.AREA_CODE                  
	    JOIN CITY PRC (NOLOCK) ON PRA.CITY_CODE=PRC.CITY_CODE 
	    JOIN GST_STATE_MST PRST(NOLOCK) ON lmp.ac_gst_state_code =PRST.gst_state_code 