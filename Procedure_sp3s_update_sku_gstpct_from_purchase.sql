create Procedure sp3s_update_sku_gstpct_from_purchase
as
begin

	UPDATE SKU WITH (ROWLOCK) SET GST_PERCENTAGE=B.GST_PERCENTAGE 
	FROM SKU 
	JOIN  PID01106 B (NOLOCK) ON B.PRODUCT_CODE=SKU.PRODUCT_CODE
	JOIN PIM01106 C (NOLOCK) ON C.MRR_ID=B.MRR_ID
	WHERE INV_MODE=1 AND ISNULL(SKU.GST_PERCENTAGE,0)<>B.GST_PERCENTAGE AND CANCELLED=0
	AND BILL_DT>='2017-07-01' AND SKU.PRODUCT_CODE<>''

end
