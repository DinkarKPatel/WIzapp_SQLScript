CREATE VIEW  VW_PRD_JOBWORKRECEIVE_QBF   

AS  
 SELECT  A.PRODUCT_CODE, D.ISSUE_NO,B.*,
 CONVERT(NVARCHAR,B.RECEIPT_DT,105) AS MST_RECEIPT_DT,
 B.RECEIPT_ID AS MST_RECEIPT_ID,
 B.RECEIPT_NO AS MST_RECEIPT_NO,
 B.MODE AS MST_MODE,  
 B.FIN_YEAR AS MST_FIN_YEAR,  
 M.AGENCY_NAME AS MST_AGENCY_NAME,C.REF_NO AS ISSUE_REF_NO,A.REMARKS AS DET_REMARKS 
 FROM PRD_JOBWORK_RECEIPT_MST B (NOLOCK)  
 JOIN PRD_JOBWORK_RECEIPT_DET A (NOLOCK) ON B.RECEIPT_ID = A.RECEIPT_ID     
 JOIN PRD_JOBWORK_ISSUE_DET C (NOLOCK) ON A.REF_ROW_ID = C.ROW_ID   
 JOIN PRD_JOBWORK_ISSUE_MST D (NOLOCK) ON C.ISSUE_ID = D.ISSUE_ID   
 JOIN PRD_AGENCY_MST M (NOLOCK) ON M.AGENCY_CODE = D.AGENCY_CODE  
 JOIN SKU ON SKU.PRODUCT_CODE=C.PRODUCT_CODE    
 JOIN ARTICLE ART ON SKU.ARTICLE_CODE = ART.ARTICLE_CODE        
 JOIN SECTIOND SD ON ART.SUB_SECTION_CODE = SD.SUB_SECTION_CODE      
 JOIN SECTIONM SM ON SD.SECTION_CODE = SM.SECTION_CODE      
 JOIN PARA1 P1 ON SKU.PARA1_CODE = P1.PARA1_CODE        
 JOIN PARA2 P2 ON SKU.PARA2_CODE = P2.PARA2_CODE        
 JOIN PARA3 P3 ON SKU.PARA3_CODE = P3.PARA3_CODE        
 JOIN PARA4 P4 ON SKU.PARA4_CODE = P4.PARA4_CODE        
 JOIN PARA5 P5 ON SKU.PARA5_CODE = P5.PARA5_CODE        
 JOIN PARA6 P6 ON SKU.PARA6_CODE = P6.PARA6_CODE        
 JOIN UOM E ON ART.UOM_CODE = E.UOM_CODE     
 JOIN JOBS F (NOLOCK) ON F.JOB_CODE = C.JOB_CODE
