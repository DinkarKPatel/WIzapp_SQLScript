

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='INSERT_XFERLOG' AND VALUE='1')
BEGIN

	PRINT 'CALLING SP_INSERT_XFERLOG'
	EXEC SP_INSERT_XFERLOG

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='INSERT_XFERLOG')
       INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
	   VALUES ('INSERT_XFERLOG','1','',GETDATE())	
	ELSE	
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='INSERT_XFERLOG'
END
