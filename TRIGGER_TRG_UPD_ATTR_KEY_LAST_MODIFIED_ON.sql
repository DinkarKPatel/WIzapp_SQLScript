CREATE TRIGGER [DBO].[TRG_UPD_ATTR_KEY_LAST_MODIFIED_ON] ON [DBO].[ATTR_KEY]  
 FOR UPDATE  
 AS  
  IF (SELECT TRIGGER_NESTLEVEL())> 1   
   RETURN  
   

      IF OBJECT_ID('TEMPDB..#TMP','U')IS NOT NULL
       DROP TABLE #TMP

    SELECT KEY_CODE INTO #TMP FROM DELETED
	UPDATE ATTR_KEY SET LAST_MODIFIED_ON=CAST(GETDATE() AS DATE) WHERE KEY_CODE
    IN (SELECT KEY_CODE FROM #TMP)
