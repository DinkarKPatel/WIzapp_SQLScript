create PROC [DBO].SP_JOBWORK_RECIEVE_1 
@NQUERYID INT,                        
@CWHERE NVARCHAR(4000)='',          
@NMODE INT=0,  
@BWIP BIT=0,  
@NISSUE_MODE BIT=0,  
@cLocID VARCHAR(5)=''  
AS                        
BEGIN     
 
--(dinkar) Replace  left(memoid,2) to Location_code 
  SELECT B.AC_CODE, A.*,B.AGENCY_NAME,TDS.TDS_Name,U.USERNAME AS USER_NAME,BIN.BIN_NAME,  
   U1.USERNAME AS EDT_USER_NAME,
   ISNULL(A.FREIGHT_CGST_AMOUNT,0)+ISNULL(A.FREIGHT_SGST_AMOUNT,0) +ISNULL(A.FREIGHT_IGST_AMOUNT,0) AS  FREIGHT_GST_AMT,
     ISNULL(A.FREIGHT,0)+ISNULL(A.FREIGHT_CGST_AMOUNT,0)+ISNULL(A.FREIGHT_SGST_AMOUNT,0) +ISNULL(A.FREIGHT_IGST_AMOUNT,0)       AS FREIGHT_TOTAL,
     ISNULL(A.OTHER_CHARGES_CGST_AMOUNT,0)+ISNULL(A.OTHER_CHARGES_SGST_AMOUNT,0) +ISNULL(A.OTHER_CHARGES_IGST_AMOUNT,0) AS  OTHER_CHARGES_GST_AMT,
     ISNULL(A.OTHER_CHARGES,0)+ISNULL(A.OTHER_CHARGES_CGST_AMOUNT,0)+ISNULL(A.OTHER_CHARGES_SGST_AMOUNT,0) +ISNULL(A.OTHER_CHARGES_IGST_AMOUNT,0) AS OTHER_CHARGES_TOTAL
   FROM JOBWORK_RECEIPT_MST A (NOLOCK)                   
   JOIN PRD_AGENCY_MST B (NOLOCK) ON A.AGENCY_CODE = B.AGENCY_CODE   
   LEFT OUTER JOIN USERS U (NOLOCK) ON U.USER_CODE = A.USER_CODE     
   LEFT OUTER  JOIN USERS U1 (NOLOCK) ON U1.USER_CODE = A.EDT_USER_CODE     
   LEFT OUTER JOIN BIN (NOLOCK) ON BIN.BIN_ID=A.BIN_ID 
   LEFT OUTER JOIN TDS_SECTION TDS (NOLOCK) ON TDS.TDS_Code=A.tds_code                       
   WHERE A.RECEIPT_ID=@CWHERE    
END 
