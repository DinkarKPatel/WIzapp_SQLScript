CREATE PROCEDURE UPDATEMASTERXN_MERGING 
			@CSOURCEDB VARCHAR(100),
			@CSOURCETABLE VARCHAR(100),
			@CDESTDB VARCHAR(100),
			@CDESTTABLE VARCHAR(100),
			@CKEYFIELD1 VARCHAR(40)='',
			@CKEYFIELD2 VARCHAR(40)='',
			@CKEYFIELD3 VARCHAR(40)='',
			@LINSERTONLY BIT = 0,
			@LUPDATEONLY BIT = 0,
			@BALWAYSUPDATE BIT =0,
			@CFILTERCONDITION VARCHAR(500)='',
			@CERRORMSG VARCHAR(500) OUTPUT
--WITH ENCRYPTION			
AS
BEGIN
	SET NOCOUNT ON
	
	
	SET @CERRORMSG=''

PRINT '1.'

	EXEC UPDATEMASTERXN 
		  @CSOURCEDB		= @CSOURCEDB
		, @CSOURCETABLE		= @CSOURCETABLE
		, @CDESTDB			= @CDESTDB
		, @CDESTTABLE		= @CDESTTABLE
		, @CKEYFIELD1		= @CKEYFIELD1
		, @CKEYFIELD2		= @CKEYFIELD2
		, @CKEYFIELD3		= @CKEYFIELD3
		, @LINSERTONLY		= @LINSERTONLY
		, @CFILTERCONDITION = @CFILTERCONDITION
		, @LUPDATEXNS		= 0
		, @CXNTYPE			= ''
		, @LUPDATEONLY		= @LUPDATEONLY
		, @CUSERID			= ''
		, @BALWAYSUPDATE	= @BALWAYSUPDATE
			
END
--*************************************** END OF PROCEDURE UPDATEMASTERXN
