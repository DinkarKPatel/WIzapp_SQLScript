

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_COMPLETED_MARK_IN_DOCXNS' AND VALUE=1)
BEGIN

	PRINT 'UPDATE COMPLETED MARK IN GROUP INVOICES/DEBIT NOTES'
	

	INSERT INTO BINUSERS(BIN_ID,USER_CODE,LAST_UPDATE)
	SELECT '000' AS BIN_ID,A.USER_CODE,GETDATE() AS LAST_UPDATE FROM USERS A 
	LEFT OUTER JOIN BINUSERS B ON A.USER_CODE=B.USER_CODE
	WHERE B.USER_CODE IS NULL

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_COMPLETED_MARK_IN_DOCXNS')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_COMPLETED_MARK_IN_DOCXNS','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_COMPLETED_MARK_IN_DOCXNS'

END	

