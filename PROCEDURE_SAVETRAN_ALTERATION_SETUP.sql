create PROCEDURE SAVETRAN_ALTERATION_SETUP(@nSPID int)
AS
BEGIN

BEGIN TRAN
DELETE FROM ALTERATIONSETUP
BEGIN TRY
	INSERT INTO ALTERATIONSETUP(DEPT_ID,SUB_SECTION_CODE,JOB_CODE,VENDOR_RATE,
       BASE_NRV,JOBRATE_LESSTHAN_NRV,JOBRATE_MORETHAN_NRV,SECTION_CODE)
	SELECT DEPT_ID,SUB_SECTION_CODE,JOB_CODE,VENDOR_RATE,
       BASE_NRV,JOBRATE_LESSTHAN_NRV,JOBRATE_MORETHAN_NRV,SECTION_CODE 
	  FROM ALTERATIONSETUP_IMPORT_UPLOAD WHERE SP_ID=@nSPID

END TRY
BEGIN CATCH
	
END CATCH
IF @@ERROR=0
BEGIN
	COMMIT TRAN
	SELECT '' ERRMSG
END
ELSE
BEGIN
	ROLLBACK
	SELECT ERROR_MESSAGE()ERRMSG   
END

DELETE ALTERATIONSETUP_IMPORT_UPLOAD WHERE SP_ID=@nSPID  
END
