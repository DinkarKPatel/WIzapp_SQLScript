CREATE PROCEDURE SP_PRD_PACKSLIP
          
 @NQUERYID NUMERIC (3,0) ,          
 @CMEMOID VARCHAR(40) = '',          
 @CWHERE  VARCHAR(500) = '',        
 @NNAVMODE NUMERIC(1,0) = 0,          
 @CFINYEAR VARCHAR(5)=''          
-- WITH ENCRYPTION    
         
AS         
