ELSE IF @NQUERYID = 10                
GOTO LBLWPSMSTREP                
                
ELSE IF @NQUERYID = 11            
GOTO LBLWPSDETREP             
                
ELSE IF @NQUERYID = 12              
GOTO LBL12                   
              
ELSE              
GOTO LAST              
            
         
             
LBLGETMASTER:              
              
  SELECT T1.*,T4.AC_NAME,T4.ADDRESS0,T4.ADDRESS1,T4.ADDRESS2,               
  T4.AREA_NAME,T4.CITY,T4.[STATE],T4.PINCODE  ,WO.MEMO_NO AS WO_NO             
  FROM PRD_PS_MST T1                 
  JOIN LMV01106 T4 ON T4.AC_CODE = T1.AC_CODE              
  JOIN PRD_WO_MST WO ON  WO.MEMO_ID=T1.REF_WO_ID          
