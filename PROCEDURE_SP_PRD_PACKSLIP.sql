GOTO LBLGETMASTER              
              
ELSE IF @NQUERYID = 2            
GOTO LBLGETDETAIL              
            
ELSE IF @NQUERYID = 4              
GOTO LBLFORMS              
              
ELSE IF @NQUERYID = 5              
GOTO LBLCUSTOMER              
              
ELSE IF @NQUERYID = 6              
GOTO LBLSTOCKDETAILS              
              
ELSE IF @NQUERYID = 7              
