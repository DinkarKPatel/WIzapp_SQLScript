create PROCEDURE SP_MERGE_MIRROR_IRR_DATA
(
	@CMEMOID VARCHAR(50),
	@CLOCID CHAR(2),
	@CSOURCEDB VARCHAR(300),
	@CMERGEDB VARCHAR(300),
	@BMSTINSERTONLY BIT,
	@CERRMSG VARCHAR(MAX) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_IRR_DATA_208_2014_02_05 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(500)
	   
BEGIN TRY
SET @CSTEP=00


SET @CSTEP=20
SET @CTABLE_SUFFIX=REPLACE(@CMEMOID,'-','_')
BEGIN TRANSACTION 

	DECLARE @CMEMOIDSEARCH VARCHAR(100)
	SELECT TOP 1 @CMEMOIDSEARCH=A.MEMO_ID FROM SNC_MST A (NOLOCK) WHERE A.MEMO_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
	BEGIN
		SET @CSTEP=20
		EXEC SP3SBUILDIRR
			@CXNID=@CMEMOID
			,@NUPDATEMODE=3
			,@CERRMSG=@CERRMSG OUTPUT  

		IF ISNULL(@CERRMSG,'')<>''
			GOTO Exit_PROC			
    END
	    
	SET @CSTEP=30

	SET @CTABLENAME='IRD01106'
	SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='IRM_MEMO_ID'
	SET @CSTEP=90
	SET @DTSQL=N'DELETE '+@CMERGEDB+'['+@CTABLENAME+'] WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''''
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL

    SET @CSTEP=40
	SET @CTABLENAME='IRM01106'
	SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='IRM_MEMO_ID'
	SET @CSTEP=310
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 


    SET @CSTEP = 50
    SET @CTABLENAME='ARTICLE'
    SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
    SET @CKEYFIELD='ARTICLE_CODE'
    
    EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
						  ,@BALWAYSUPDATE=1 						

    SET @CSTEP = 54
    SET @CTABLENAME='ARTICLE_fix_attr'
    SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
    SET @CKEYFIELD='ARTICLE_CODE'
    
    EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
						  ,@BALWAYSUPDATE=1 						
						  
    SET @CSTEP = 60
    SET @CTABLENAME='PARA1'
    SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
    SET @CKEYFIELD='PARA1_CODE'
    
    
    EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
						  ,@BALWAYSUPDATE=1 					

    SET @CSTEP = 70
    SET @CTABLENAME='PARA2'
    SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
    SET @CKEYFIELD='PARA2_CODE'
    
    
    EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
						  ,@BALWAYSUPDATE=1 				

    SET @CSTEP = 80
    SET @CTABLENAME='PARA3'
    SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
    SET @CKEYFIELD='PARA3_CODE'
    
    
    
    EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
						  ,@BALWAYSUPDATE=1 			

    SET @CSTEP = 90
    SET @CTABLENAME='PARA4'
    SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
    SET @CKEYFIELD='PARA4_CODE'
    
    
    EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
						  ,@BALWAYSUPDATE=1 			

    SET @CSTEP = 100
    SET @CTABLENAME='PARA5'
    SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
    SET @CKEYFIELD='PARA5_CODE'
    
    
    EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
						  ,@BALWAYSUPDATE=1 			

    SET @CSTEP = 110
    SET @CTABLENAME='PARA6'
    SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
    SET @CKEYFIELD='PARA6_CODE'
    
    
    EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
						  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
						  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
						  ,@BALWAYSUPDATE=1 			

	SET @CSTEP=120
	SET @CTABLENAME='SKU'
	SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PRODUCT_CODE'
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
	
	PRINT 'NOW INSERT SKU_OH'
	SET @CSTEP=130
	SET @CTABLENAME='SKU_OH'
	SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PRODUCT_CODE'

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
	
	PRINT 'NOW INSERT IRD01106'						  
	SET @CSTEP=140
	SET @CTABLENAME='IRD01106'
	SET @CTMP_TABLENAME='TMP_IRR_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='IRM_MEMO_ID'

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

	SET @CSTEP=145 

	DECLARE @CXN_TYPE VARCHAR(10)
	SET @CXN_TYPE=''
   IF EXISTS (SELECT TOP 1 'U'  FROM IRD01106 WHERE IRM_MEMO_ID=@CMEMOID AND ISNULL(NEW_PRODUCT_CODE,'')<>'')
	SET @CXN_TYPE='IRR'

	SET @CTMP_TABLENAME=@CSOURCEDB+'TMP_IRR_pmt01106_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	EXEC SP3S_MERGE_LOCPMT  
	@cTempTable=@CTMP_TABLENAME,
	@CXN_TYPE=@CXN_TYPE
	
	SET @CSTEP=150
	EXEC VALIDATEXN_MIRROR_IRR @CLOCID,@CMEMOID,@CMERGEDB,@CERRMSGOUT OUTPUT
		
	IF ISNULL(@CERRMSGOUT,'')<>''
	BEGIN
		SET @CERRMSG='P:SP_MERGE_MIRROR_IRR_DATA, STEP:'+LTRIM(RTRIM(STR(@CSTEP)))+', MESSAGE:'+N''+@CERRMSGOUT+''''
	END	
							  	

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_MERGE_MIRROR_IRR_DATA, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH

EXIT_PROC:
	IF ISNULL(@CERRMSG,'')=''
	BEGIN
	EXEC SP3SBUILDIRR
			@CXNID=@CMEMOID
			,@NUPDATEMODE=2
			,@CERRMSG=@CERRMSG OUTPUT  
	END

	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRMSG,'')=''
		BEGIN
			COMMIT TRANSACTION
		END	
		ELSE 
			ROLLBACK
	END		

	SET @CTABLESSTR='IRM01106,IRD01106,SKU,ARTICLE,PARA1,PARA2,PARA3,PARA4,PARA5,PARA6,SECTIONM,SECTIOND,XNSTABLELIST,PMT01106'
	IF ISNULL(@CERRMSG,'')=''	
		EXEC SP3S_DROPTEMPTABLES_MIRRORDATA
		@CXNTYPE='IRR',
		@CTABLESUFFIX=@CTABLE_SUFFIX,
		@CTABLESSTR=@CTABLESSTR
	
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_IRR_DATA
