CREATE PROCEDURE SP_PUR_CHECK_DN
(  
	@CMRRID VARCHAR(100)
)
----WITH ENCRYPTION
AS  
BEGIN  
	SELECT COUNT(*)
	FROM PIM01106 A  (NOLOCK)
	JOIN PID01106 B (NOLOCK) ON B.MRR_ID=A.MRR_ID
	JOIN RMD01106 RMD (NOLOCK)ON RMD.product_code=B.product_code
	JOIN RMM01106 RMM (NOLOCK)ON RMM.RM_ID = RMD.RM_ID
	WHERE RMM.CANCELLED=0 AND A.mrr_id=@CMRRID
END      