

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_BBROW_ID' AND VALUE=1)
BEGIN
	PRINT 'UPDATING BB_ROW_ID COLUMN IN BILL_BY_BILL_REF'

	UPDATE BILL_BY_BILL_REF SET BB_ROW_ID=NEWID() WHERE ISNULL(BB_ROW_ID,'')=''

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_BBROW_ID')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_BBROW_ID',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_BBROW_ID'	
			
END	
