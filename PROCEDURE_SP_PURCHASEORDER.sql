CREATE PROCEDURE SP_PURCHASEORDER
(  
 @CQUERYID  NUMERIC(2),  
 @CWHERE   NVARCHAR(MAX)='',  
 @CFINYEAR  VARCHAR(5)='',  
 @CDEPTID  VARCHAR(4)='',  
 @NNAVMODE  NUMERIC(2)=1,  
 @CWHERECLAUSE VARCHAR(500)='' ,
 @DPODT DATETIME='' 
   
)
--WITH ENCRYPTION
AS  
begin
 IF @CQUERYID=3  
	exec sp_purchaseorder_3
	@CWHERE=@cWhere,  
	 @CFINYEAR=@CFINYEAR,  
	 @CDEPTID=@CDEPTID,  
	 @NNAVMODE  =@NNAVMODE,  
	 @CWHERECLAUSE=@CWHERECLAUSE,
	 @DPODT=@DPODT  
 ELSE
 	exec sp_purchaseorder_new
	@cQueryid=@cQueryId,
	@CWHERE=@cWhere,  
	 @CFINYEAR=@CFINYEAR,  
	 @CDEPTID=@CDEPTID,  
	 @NNAVMODE  =@NNAVMODE,  
	 @CWHERECLAUSE=@CWHERECLAUSE,
	 @DPODT=@DPODT  
end