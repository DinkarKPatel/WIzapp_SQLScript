CREATE PROCEDURE SP_PPC_ORDER_ISSUE
(
 @CJOB_CODE VARCHAR(10)=''
)
AS
BEGIN

	SELECT C.BILL_NO AS ORDER_NO,C.ORDER_DT ,C.ORDER_ID   FROM PPC_BO_ART_JOBS  A
	JOIN PPC_BUYER_ORDER_DET B ON A.REF_ROW_ID =B.ROW_ID 
	JOIN PPC_BUYER_ORDER_MST C ON B.ORDER_ID =C.ORDER_ID 
	WHERE C.CANCELLED =0
	AND  (@CJOB_CODE='' OR JOB_CODE=@CJOB_CODE)
	GROUP BY C.BILL_NO,C.ORDER_DT ,C.ORDER_ID

END
