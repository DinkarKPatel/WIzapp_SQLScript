CREATE PROCEDURE SP3S_VERIFY_WPSDATA_merge_CHANGES
@nSpId VARCHAR(50),
@cMemoId VARCHAR(40)
AS
BEGIN
	
	DECLARE @CFILTERCONDITION VARCHAR(1000),@CINSSPID VARCHAR(40),@CJOINSTR VARCHAR(500)
	PRINT 'gen tempdata for 1'
	SET @CFILTERCONDITION=' b.PS_ID='''+@cMemoId+''''
	set @CINSSPID=LEFT(@nSPId,38)+LEFT(@cMemoId,2)
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB='',@CSOURCETABLE='WPS_MST',@CDESTDB=''
								,@CDESTTABLE='WPS_WPS_MST_UPLOAD',@CKEYFIELD1='PS_ID',@CKEYFIELD2='',@CKEYFIELD3=''
								,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
								,@BALWAYSUPDATE=0,@bUpdateXns=1,@CINSSPID=@cInSSPId,@CSEARCHTABLE='WPS_MST'

	PRINT 'gen tempdata for 2'
	EXEC SP3S_GENFILTERED_UPDATESTR
	@cSpId=@nSpId ,
	@cInsSpId=@cInsSpId,
	@cTableName='WPS_MST',
	@cUploadTableName='WPS_WPS_MST_UPLOAD',
	@cKeyfield='PS_ID'


	PRINT 'g en tempdata for 3'
		EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB='',@CSOURCETABLE='WPS_DET',@CDESTDB=''
								,@CDESTTABLE='WPS_WPS_DET_UPLOAD',@CKEYFIELD1='PS_ID',@CKEYFIELD2='',@CKEYFIELD3=''
								,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
								,@BALWAYSUPDATE=0,@bUPDATEXNS=1,@CINSSPID=@CINSSPID,@CSEARCHTABLE='WPS_DET',@cXnType='WPS'
	


	PRINT 'gen tempdata for 4'
	EXEC SP3S_GENFILTERED_UPDATESTR
	@cSpId=@nSpId ,
	@cInsSpId=@cInsSpId,
	@cTableName='WPS_DET',
	@cUploadTableName='WPS_WPS_DET_UPLOAD',
	@cKeyfield='row_id',
	@bDonotChkLastUpdate=1
	
END