

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CMD01106_BASICDP' AND VALUE=1)
BEGIN
	PRINT 'UPDATING BASIC DP IN CMD01106'
	
	UPDATE CMD01106 SET BASIC_DISCOUNT_PERCENTAGE=DISCOUNT_PERCENTAGE,BASIC_DISCOUNT_AMOUNT=DISCOUNT_AMOUNT
	WHERE BASIC_DISCOUNT_PERCENTAGE IS NULL AND CARD_DISCOUNT_PERCENTAGE IS NULL
	AND DISCOUNT_PERCENTAGE<>0

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CMD01106_BASICDP')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_CMD01106_BASICDP',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_CMD01106_BASICDP'
	
END		
