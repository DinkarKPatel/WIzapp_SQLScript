CREATE VIEW VW_ERP_PURCHASEORDERLIST   

AS  
 SELECT A.CHECKED_BY AS MST_CHECKED_BY,   
   A.DELIVERY_DT AS MST_DELIVERY_DT,  
   A.DISCOUNT_AMOUNT AS CAL_DISCOUNT_AMOUNT,   
   A.DISCOUNT_PERCENTAGE AS CAL_DISCOUNT_PERCENTAGE,  
   A.FREIGHT AS CAL_FREIGHT,  
   A.OTHER_CHARGES AS CAL_OTHER_CHARGES,  
   A.PO_DT AS MST_XN_DT,  
   A.PO_NO AS MST_MEMO_NO,  
   A.REMARKS AS MST_REMARKS,  
   A.ROUND_OFF AS CAL_ROUND_OFF,  
   A.SUBTOTAL AS CAL_SUBTOTAL,  
   A.TAX_AMOUNT AS CAL_TAX_AMOUNT,  
   A.TAX_PERCENTAGE AS CAL_TAX_PERCENTAGE,  
   A.TOTAL_AMOUNT AS CAL_TOTAL_AMOUNT   
 FROM POM01106 A  (NOLOCK)
 WHERE A.CANCELLED = 0
