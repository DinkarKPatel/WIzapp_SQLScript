

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_ARC_SENTTOHO' AND VALUE=1)
BEGIN

	PRINT 'UPDATING SENTTO HO MARK IN OUTSTANDING RECEIPT TAKEN AGAINST CREDIT BILLS'
	
	DECLARE @CHOLOCID CHAR(2) ,@CCURLOCID CHAR(2)
	
	SELECT TOP 1 @CCURLOCID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'
	SELECT TOP 1 @CHOLOCID=VALUE FROM CONFIG WHERE CONFIG_OPTION='HO_LOCATION_ID'


	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_ARC_SENTTOHO')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_ARC_SENTTOHO','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_ARC_SENTTOHO'
	
	IF @CCURLOCID<>@CHOLOCID
		DELETE A FROM XFERLOG A JOIN ARC01106 B ON A.XN_ID=B.ADV_REC_ID
		JOIN CMM_CREDIT_RECEIPT C ON C.ADV_REC_ID=B.ADV_REC_ID	
END		
