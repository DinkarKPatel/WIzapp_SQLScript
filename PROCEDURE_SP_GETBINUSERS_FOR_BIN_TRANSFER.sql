CREATE PROCEDURE SP_GETBINUSERS_FOR_BIN_TRANSFER
(
	@CUSERCODE	VARCHAR(10),
	@CDEPT_ID	VARCHAR(10)
)
--WITH ENCRYPTION
AS
BEGIN
	    SELECT DISTINCT A.BIN_ID,A.BIN_NAME,A.BIN_ALIAS,@CUSERCODE AS [USER_CODE] 
		FROM BIN A (NOLOCK)
		JOIN BIN_LOC C (NOLOCK) ON C.BIN_ID=A.BIN_ID
		WHERE A.BIN_ID= A.MAJOR_BIN_ID AND A.INACTIVE=0 AND C.DEPT_ID=@CDEPT_ID  
		--AND isnull(A.rack_bin,0) =0
END
