CREATE PROCEDURE SP_LINK_LEDGER
(
	@NMODE	NUMERIC(1),
	@CACCODE VARCHAR(20),
	@CREFNO VARCHAR(MAX)
)
--WITH ENCRYPTION

AS BEGIN
	IF OBJECT_ID('LM01106_LINK','U') IS NOT NULL
	BEGIN
		IF @NMODE=1
		BEGIN
			SELECT * FROM LM01106_LINK WHERE AC_CODE=@CACCODE
		END
		ELSE IF @NMODE=2
		BEGIN
			DELETE FROM LM01106_LINK WHERE AC_CODE=@CACCODE
			INSERT INTO LM01106_LINK (AC_CODE,REF_CODE) VALUES (@CACCODE,@CREFNO)
			SELECT * FROM LM01106_LINK WHERE AC_CODE=@CACCODE
		END
	END
END
