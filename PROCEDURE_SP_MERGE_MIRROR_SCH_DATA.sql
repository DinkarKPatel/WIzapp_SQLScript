CREATE PROCEDURE SP_MERGE_MIRROR_SCH_DATA
(
	@CMEMOID VARCHAR(50)
   ,@CLOCID VARCHAR(3)
   ,@CSOURCEDB VARCHAR(200)
   ,@CMERGEDB VARCHAR(200)
   ,@BMSTINSERTONLY BIT
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
BEGIN
	DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX)
	   
     DECLARE @TXNSSENDINFO TABLE (ORG_TABLENAME VARCHAR(50),TMP_TABLENAME VARCHAR(50),XN_ID VARCHAR(40))  	

    BEGIN TRY
		SET @CSTEP=20
		SET @CTABLE_SUFFIX=REPLACE(@CMEMOID,'-','_')
		BEGIN TRANSACTION
		
			SET @CSTEP=60
			SET @CTABLENAME='SLSMST'
			SET @CTMP_TABLENAME='TMP_SCH_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
			SET @CKEYFIELD='SLS_MEMO_NO'
			SET @CSTEP=80
			EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
									  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1
		    
			SET @DTSQL = N'DELETE FROM SLSBC  WITH (ROWLOCK) WHERE '+@CKEYFIELD+' = '''+@CMEMOID+''''
			PRINT @DTSQL
			EXEC SP_EXECUTESQL @DTSQL
			SET @DTSQL = N'DELETE FROM SLSDET  WITH (ROWLOCK) WHERE '+@CKEYFIELD+' = '''+@CMEMOID+''''
			PRINT @DTSQL
			EXEC SP_EXECUTESQL @DTSQL
					
			SET @CSTEP=100
			SET @CTABLENAME='SLSDET'
			SET @CTMP_TABLENAME='TMP_SCH_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
			SET @CKEYFIELD='SLS_MEMO_NO'
			
			SET @CSTEP=130
			EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
		
			SET @CSTEP=150
			SET @CTABLENAME='SLSBC'
			SET @CTMP_TABLENAME='TMP_SCH_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
			SET @CKEYFIELD='SLS_MEMO_NO'
				
			SET @CSTEP=200
			EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 

			 
			SET @CSTEP=350
			SET @CTABLESSTR='SLSBC,SLSDET,SLSMST,XNSTABLELIST'
			
			SET @CSTEP=380
			EXEC SP3S_DROPTEMPTABLES_MIRRORDATA
			@CXNTYPE='SCH',
			@CTABLESUFFIX=@CTABLE_SUFFIX,
			@CTABLESSTR=@CTABLESSTR			   
	END TRY
	BEGIN CATCH
		SET @CERRMSG='P:SP_MERGE_MIRROR_SCH_DATA, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	END CATCH
	EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END
