IF NOT EXISTS(SELECT TOP 1 'U' FROM PURCONFIGNRR)
BEGIN
	INSERT PURCONFIGNRR	( ELEMENTID,ELEMENTVAL )  
	SELECT 	'<MRRNO>' AS ELEMENTID,'<MRR NO>' AS ELEMENTVAL
	UNION ALL  
	SELECT 	'<BILLNO>' AS ELEMENTID,'<BILL NO>' AS ELEMENTVAL
	UNION ALL
	SELECT 	'<VENDORBILLDT>' AS ELEMENTID,'<VENDOR BILL DATE>' AS ELEMENTVAL
	UNION ALL
	SELECT 	'<RECEIPTDT>' AS ELEMENTID,'<RECEIPT DATE>' AS ELEMENTVAL
END
