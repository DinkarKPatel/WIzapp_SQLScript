CREATE PROCEDURE SP_WL_DELETESALESETUP
--WITH ENCRYPTION
AS
BEGIN
	DELETE FROM REP_FILTER_DET 
	WHERE REP_ID IN (SELECT REPID FROM SLSDET)

	DELETE FROM REP_FILTER 
	WHERE REP_ID IN (SELECT REPID FROM SLSDET)

	DELETE FROM REP_DET 
	WHERE REP_ID IN (SELECT REPID FROM SLSDET)


	DELETE FROM REP_MST 
	WHERE REP_ID IN (SELECT REPID FROM SLSDET) 


	DELETE FROM LOCSLSSET

	DELETE FROM SLSDET

	DELETE FROM SLSMST
		
END
