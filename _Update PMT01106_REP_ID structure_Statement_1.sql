IF  EXISTS(
SELECT * FROM SYS.TABLES A JOIN SYS.COLUMNS B ON A.OBJECT_ID=B.OBJECT_ID
JOIN SYS.TYPES C ON B.SYSTEM_TYPE_ID=C.SYSTEM_TYPE_ID
WHERE A.NAME='PMT01106' AND B.NAME='REP_ID' AND C.NAME='CHAR' AND B.MAX_LENGTH=1
)
BEGIN
    IF EXISTS(SELECT TOP 1 'U' FROM SYS.OBJECTS A JOIN SYS.INDEXES B ON A.OBJECT_ID=B.OBJECT_ID
		  WHERE A.NAME='PMT01106' AND B.NAME='IND_PMT_REPID')
	DROP INDEX PMT01106.IND_PMT_REPID

	DECLARE @CONSTRAINTNAME NVARCHAR(200)
	SELECT @CONSTRAINTNAME = NAME FROM SYS.DEFAULT_CONSTRAINTS 
	WHERE PARENT_OBJECT_ID = OBJECT_ID('PMT01106') 
	AND PARENT_COLUMN_ID = (SELECT COLUMN_ID FROM SYS.COLUMNS WHERE NAME = N'REP_ID' 
	AND OBJECT_ID = OBJECT_ID(N'PMT01106'))
	IF @CONSTRAINTNAME IS NOT NULL
	EXEC('ALTER TABLE PMT01106 DROP CONSTRAINT ' + @CONSTRAINTNAME)


	ALTER TABLE PMT01106 ALTER COLUMN REP_ID CHAR(10)


	ALTER TABLE PMT01106  ADD DEFAULT ('') FOR  REP_ID

	CREATE INDEX IND_PMT_REPID ON PMT01106(REP_ID)
END
