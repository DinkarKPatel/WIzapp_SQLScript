create PROCEDURE SPSIS_SYNCH_UPLOADDATA_DNPS_OPT
(
    @NSPID VARCHAR(50),
	@CDEPTID VARCHAR(5)/*Rohit 01-11-2024*/,
   @CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN

DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CSOURCEDB VARCHAR(10),
		@CMERGEDB VARCHAR(10),@BMSTINSERTONLY BIT,@CMEMOID VARCHAR(50),@BCANCELLED BIT,
		@BADDMODE BIT

		--discarded ALl packslip
		goto EXIT_PROC
	   
BEGIN TRY
	SET @CSTEP=20
	SET @CTABLE_SUFFIX='UPLOAD'
	
	BEGIN TRANSACTION 
	
	SELECT @CSOURCEDB='',@CMERGEDB=''


	UPDATE A SET memo_type =1,
	             AC_CODE ='0000000000',
				 USER_CODE ='0000000',
				 BIN_ID='000',
				 PS_ID=@CDEPTID+SUBSTRING(PS_ID,LEN(@CDEPTID)+1,LEN(PS_ID))/*Rohit 01-11-2024*/ ,
				 PS_NO=@CDEPTID+SUBSTRING(PS_NO,LEN(@CDEPTID)+1,LEN(PS_NO))/*Rohit 01-11-2024*/,
				 location_Code =@CDEPTID
	FROM DNPS_DNPS_MST_UPLOAD A (NOLOCK)
	WHERE SP_ID =@nSpId

	UPDATE A SET BIN_ID ='000',
	          PS_ID=@CDEPTID++SUBSTRING(PS_ID,LEN(@CDEPTID)+1,LEN(PS_ID))/*Rohit 01-11-2024*/ ,
			  DEPT_ID=@CDEPTID

	FROM DNPS_DNPS_DET_UPLOAD A (NOLOCK)
	WHERE SP_ID =@nSpId

	
	 UPDATE A SET PRODUCT_CODE=LEFT(A.PRODUCT_CODE, ISNULL(NULLIF(CHARINDEX ('@',A.PRODUCT_CODE)-1,-1),LEN(A.PRODUCT_CODE )))  
	 FROM DNPS_DNPS_DET_UPLOAD A (NOLOCK)	WHERE SP_ID=@NSPID
	 AND CHARINDEX('@',PRODUCT_CODE)<>0


	SELECT TOP 1 @CMEMOID = B.PS_ID ,@BCANCELLED=CANCELLED 
    FROM DNPS_DNPS_MST_UPLOAD B (NOLOCK)
	WHERE SP_ID=@nSpId
   

   
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		

	DECLARE @CMEMOIDSEARCH VARCHAR(100)
	SELECT TOP 1 @CMEMOIDSEARCH=A.ps_id FROM DNPS_MST A (NOLOCK) WHERE A.ps_id=@CMEMOID
	
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1


	IF @BADDMODE=1 AND NOT EXISTS (SELECT TOP 1 'U' FROM DNPS_DNPS_DET_UPLOAD WHERE SP_ID =@NSPID)
	    GOTO EXIT_PROC
    IF @BADDMODE=0 AND NOT EXISTS (SELECT TOP 1 'U' FROM DNPS_DNPS_DET_UPLOAD WHERE SP_ID =@NSPID)
		 SET @BCANCELLED=1


	IF @BADDMODE=0
	BEGIN

		SET @CSTEP=30
		
		 IF @bCancelled=1 
		BEGIN
			UPDATE DNPS_MST WITH (ROWLOCK) SET cancelled=1 WHERE PS_ID=@cMemoid
			GOTO EXIT_PROC
		END

		SET @CTABLENAME='DNPS_DET'
		SET @CTMP_TABLENAME='DNPS_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
		SET @CKEYFIELD='PS_ID'
		SET @CSTEP=90
		SET @DTSQL=N'DELETE '+@CMERGEDB+'['+@CTABLENAME+'] WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''''
		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL

	END
							  							  
	
    SET @CFILTERCONDITION = ' B.SP_ID='''+LTRIM(RTRIM((@NSPID )))+''''
							  
	SET @CSTEP=220
	SET @CTABLENAME='DNPS_MST'
	SET @CTMP_TABLENAME='DNPS_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PS_ID'
	SET @CSTEP=230
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  
	

	    SELECT a.product_code as new_product_code into #tmpbarcode FROM DNPS_DNPS_DET_UPLOAD A (NOLOCK)
		LEFT JOIN SKU B (nolock) ON A.PRODUCT_CODE =B.PRODUCT_CODE 
		WHERE A.SP_ID=@nSpId AND B.PRODUCT_CODE IS NULL
		group by a.product_code

		IF EXISTS (SELECT TOP 1 'U' FROM #tmpbarcode)
		BEGIN
			SET @CSTEP=84.2
			EXEC SP_CHKXNSAVELOG 'DNPSMERGE',@CSTEP,0,@nSpId,'',1

			 EXEC SPSIS_INSERTSKUBARCODE @NSPID, 'dnps',@CERRMSG OUTPUT

			 IF ISNULL(@CERRMSG,'')<>''
			    GOTO EXIT_PROC

          end

    SET @CSTEP=240
	SET @CTABLENAME='DNPS_DET'
	SET @CTMP_TABLENAME='DNPS_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='PS_ID'
	SET @CSTEP=250
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  

	SET @CSTEP=245 
	

	
END TRY
BEGIN CATCH
	SET @CERRMSG='P:SPSIS_SYNCH_UPLOADDATA_DNPS_OPT, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK

	DELETE A FROM DNPS_DNPS_DET_UPLOAD A WITH (ROWLOCK) WHERE A.SP_ID=@nSpId 
	DELETE A FROM DNPS_DNPS_MST_UPLOAD A WITH (ROWLOCK) WHERE A.SP_ID=@nSpId 
	
						  

END	
---END OF PROCEDURE - SP_MERGE_MIRROR_DNPS_DATA
