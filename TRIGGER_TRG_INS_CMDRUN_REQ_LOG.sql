CREATE TRIGGER [DBO].[TRG_INS_CMDRUN_REQ_LOG] ON [DBO].[CMDRUN_REQ]
FOR INSERT
AS
BEGIN
       
        INSERT CMDRUN_REQ_LOG	( DEPT_ID, MODE, CMD, ROW_ID, PROCESSED, PROCESSING_ERROR, XN_TYPE, LAST_UPDATE, MONTHLY_RECO_ENTRY, CHALLAN_RECO_ENTRY )  
        SELECT 	  DEPT_ID, MODE, CMD, ROW_ID, PROCESSED, PROCESSING_ERROR, XN_TYPE, LAST_UPDATE, MONTHLY_RECO_ENTRY, CHALLAN_RECO_ENTRY 
        FROM INSERTED

  
END
