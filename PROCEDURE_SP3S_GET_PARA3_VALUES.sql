CREATE PROCEDURE SP3S_GET_PARA3_VALUES(@TABLE VARCHAR(100))
AS 
BEGIN
SET NOCOUNT ON
DECLARE @SQL NVARCHAR(1000)
SET @SQL='SELECT '+CASE @TABLE WHEN 'SECTIONM' THEN 'SECTION_NAME'
							   WHEN 'SECTIOND' THEN 'SUB_SECTION_NAME'
							   WHEN 'ARTICLE'  THEN 'ARTICLE_NAME'	
							   WHEN 'PARA1'	   THEN 'PARA1_NAME'
							   WHEN 'PARA2'	   THEN 'PARA2_NAME'
							   WHEN 'PARA3'	   THEN 'PARA3_NAME'
							   WHEN 'PARA4'	   THEN 'PARA4_NAME'
							   WHEN 'PARA5'	   THEN 'PARA5_NAME'
							   WHEN 'PARA6'	   THEN 'PARA6_NAME'
                   END
SET @SQL+=' AS PARA_NAME FROM '+@TABLE+'(NOLOCK)'                               
EXEC(@SQL)
SET NOCOUNT OFF
END
