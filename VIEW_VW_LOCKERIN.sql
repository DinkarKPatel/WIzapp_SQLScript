CREATE VIEW VW_LOCKERIN
AS
SELECT A.MEMO_ID,MEMO_NO,MEMO_DT,SHIFT_ID,
       LIFT_AMOUNT,REMARKS,CANCELLED,AC_NAME,USERNAME, 
       RECEIPTDT,A.FIN_YEAR 
       FROM TILL_LIFTS A 
JOIN LM01106  (NOLOCK) B ON A.AC_CODE =B.AC_CODE 
JOIN USERS  (NOLOCK) C ON A.USER_CODE =C.USER_CODE
WHERE TRANSFERMODE =3 AND CANCELLED =0
