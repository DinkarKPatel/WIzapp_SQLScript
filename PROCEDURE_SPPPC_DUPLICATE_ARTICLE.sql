CREATE PROCEDURE SPPPC_DUPLICATE_ARTICLE
(
 @CARTICLE_CODE VARCHAR(10)=''
)
AS
BEGIN

    SELECT A.* FROM ARTICLE A  (NOLOCK)
    WHERE A.ARTICLE_CODE=@CARTICLE_CODE
    
    SELECT A.* ,ART.ARTICLE_NO,ART.ARTICLE_NAME,B.PARA1_NAME  
    FROM ART_PARA1 A (NOLOCK)
    JOIN PARA1 B (NOLOCK) ON A.PARA1_CODE=B.PARA1_CODE
    JOIN ARTICLE ART (NOLOCK) ON ART.ARTICLE_CODE=A.ARTICLE_CODE
    WHERE A.ARTICLE_CODE=@CARTICLE_CODE
    
    SELECT A.*,ART.ARTICLE_NO,ART.ARTICLE_NAME,B.SIZEGROUP_NAME 
    FROM ART_DET A
    JOIN PPC_SIZEGROUP B  ON A.SIZEGROUP_CODE=B.SIZEGROUP_CODE
    JOIN ARTICLE ART (NOLOCK) ON ART.ARTICLE_CODE=A.ARTICLE_CODE
    WHERE A.ARTICLE_CODE=@CARTICLE_CODE
  
END
