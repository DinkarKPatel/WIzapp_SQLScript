CREATE PROCEDURE SP3S_GETUSERROLE_WEB
(
@USERCODE CHAR(7),
@FORM_NAME VARCHAR(50)=''
)
AS
BEGIN
        
        DECLARE @ROLE_ID VARCHAR(7)
        
        SELECT @ROLE_ID=ROLE_ID FROM USERS(NOLOCK) WHERE USER_CODE=@USERCODE
        
        SELECT * FROM USER_ROLE_DET WHERE ROLE_ID=@ROLE_ID AND FORM_NAME IN ('QC','ASN') ORDER BY FORM_NAME
           
           
END
