	

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_PARTY_RATE_NEWCOLS' AND VALUE=1)
BEGIN
	PRINT 'UPDATING NEW COLUMNS RELATED TO GROUP PARTY RATES COLUMNS TAKEN.....PLEASE STANDBY '
	
	DECLARE @CCMD NVARCHAR(MAX)
	SET @CCMD=N'INSERT PARTY_RATE_LOC	( MEMO_ID, DEPT_ID, SOURCE_TARGET_MODE )  
	SELECT MEMO_ID,B.DEPT_ID,2 AS SOURCE_TARGET_MODE FROM PARTY_RATE_MST A
	JOIN LOCATION B ON A.AC_CODE=B.DEPT_AC_CODE'
	EXEC SP_EXECUTESQL @CCMD

	SET @CCMD=N'UPDATE PARTY_RATE_MST SET AC_CODE=''0000000000'',TARGET_LOC_APPLICABLE_MODE=2,PARTY_RATE_TYPE=2
	WHERE MEMO_ID IN (SELECT MEMO_ID FROM 
	PARTY_RATE_LOC WHERE SOURCE_TARGET_MODE=2)'
	EXEC SP_EXECUTESQL @CCMD

	SET @CCMD=N'UPDATE PARTY_RATE_MST SET TARGET_LOC_APPLICABLE_MODE=0,PARTY_RATE_TYPE=1 WHERE MEMO_ID NOT IN (SELECT MEMO_ID FROM 
	PARTY_RATE_LOC WHERE SOURCE_TARGET_MODE=2)'
    EXEC SP_EXECUTESQL @CCMD

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_PARTY_RATE_NEWCOLS')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_PARTY_RATE_NEWCOLS',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='UPDATE_PARTY_RATE_NEWCOLS'
END
