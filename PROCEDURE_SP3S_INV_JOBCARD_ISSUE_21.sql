create PROCEDURE [DBO].[SP3S_INV_JOBCARD_ISSUE_21]          
(          
  @IMODE INT ,          
  @CWHERE VARCHAR(MAX)='',      
  @CAGENCYCODE VARCHAR(20)='',      
  @FINYEAR VARCHAR(10)=''  ,    
  @DEPTID VARCHAR(10)='' ,
  @NRETURNMODE INT=0
          
)      
----WITH ENCRYPTION
AS          
          
BEGIN        
 DECLARE @CCMD NVARCHAR(MAX) 
 --LBL_ISSUE_DETAILS:	 21 
    SELECT DISTINCT OPM.MEMO_ID JOBCARD_ID,OPM.MEMO_NO JOBCARD_NO--,BISM.ISSUE_DT
	FROM ORD_PLAN_BOM_DET OPBD (NOLOCK)
	JOIN ORD_PLAN_MST OPM (NOLOCK) ON OPM.MEMO_ID=OPBD.MEMO_ID
	left JOIN BOM_ISSUE_DET BISD (NOLOCK) ON BISD.ORD_PLAN_BOM_DET_ROW_ID=OPBD.ROW_ID
	left JOIN BOM_ISSUE_MST BISM (NOLOCK) ON BISD.issue_id=BISM.issue_id
	--WHERE BISM.CANCELLED=0
END
