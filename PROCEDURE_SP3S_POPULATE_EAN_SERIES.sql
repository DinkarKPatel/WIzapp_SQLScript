CREATE PROCEDURE SP3S_POPULATE_EAN_SERIES
AS
BEGIN
          INSERT INTO MBO_EAN_SERIES
          SELECT DISTINCT LEFT(A.VENDOR_EAN_NO,7) FROM SKU A 
          LEFT JOIN MBO_EAN_SERIES B ON LEFT(A.VENDOR_EAN_NO,7)=B.VENDOR_EAN_NO
          WHERE ISNULL(A.VENDOR_EAN_NO,'')<>'' AND B.VENDOR_EAN_NO IS NULL
END
