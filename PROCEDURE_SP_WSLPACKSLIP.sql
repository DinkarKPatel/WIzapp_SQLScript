CREATE PROCEDURE SP_WSLPACKSLIP --(LocId 3 digit change only increased the parameter width by Sanjay:04-11-2024)
  
 @NQUERYID NUMERIC (3,0) ,  
 @CMEMOID VARCHAR(40) = '',  
 @CWHERE  VARCHAR(500) = '',
 @NNAVMODE NUMERIC(1,0) = 0,  
 @CFINYEAR VARCHAR(5)='',
 @CLOCID varchar(4)=''
--WITH ENCRYPTION
AS  
BEGIN  
  
--DECLARE @CCMD NVARCHAR(4000)  

IF @NQUERYID = 1
	EXECUTE SP_WSLPACKSLIP_1 
	@CMEMOID=@CMEMOID,  
	@CWHERE=@CWHERE,
	@NNAVMODE=@NNAVMODE,  
	@CFINYEAR=@CFINYEAR,
	@CLOCID=@CLOCID	
ELSe
IF @NQUERYID = 2
	EXECUTE SP_WSLPACKSLIP_2 
	@CMEMOID=@CMEMOID,  
	@CWHERE=@CWHERE,
	@NNAVMODE=@NNAVMODE,  
	@CFINYEAR=@CFINYEAR,
	@CLOCID=@CLOCID	
ELSe
IF @NQUERYID = 4
	EXECUTE SP_WSLPACKSLIP_4 
	@CMEMOID=@CMEMOID,  
	@CWHERE=@CWHERE,
	@NNAVMODE=@NNAVMODE,  
	@CFINYEAR=@CFINYEAR,
	@CLOCID=@CLOCID	
ELSe
IF @NQUERYID = 5
	EXECUTE SP_WSLPACKSLIP_5 
	@CMEMOID=@CMEMOID,  
	@CWHERE=@CWHERE,
	@NNAVMODE=@NNAVMODE,  
	@CFINYEAR=@CFINYEAR,
	@CLOCID=@CLOCID	
else
IF @NQUERYID = 7
	EXECUTE SP_WSLPACKSLIP_7 
	@CMEMOID=@CMEMOID,  
	@CWHERE=@CWHERE,
	@NNAVMODE=@NNAVMODE,  
	@CFINYEAR=@CFINYEAR,
	@CLOCID=@CLOCID	
IF @NQUERYID = 18
	EXECUTE SP_WSLPACKSLIP_18 
	@CWHERE=@CWHERE
else
	exec  SP_WSLPACKSLIP_rest  
	@NQUERYID=@NQUERYID ,  
	@CMEMOID=@CMEMOID,  
	@CWHERE=@CWHERE,
	@NNAVMODE=@NNAVMODE,  
	@CFINYEAR=@CFINYEAR,
	@CLOCID=@CLOCID	
LAST:  

END