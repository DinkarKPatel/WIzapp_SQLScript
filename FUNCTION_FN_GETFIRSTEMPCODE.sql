	

-- THIS FUNCTION RETURNS THE FIRST EMPLOYEE CODE FROM CMD TABLE
-- FOR A GIVE CASHMEMO
CREATE FUNCTION FN_GETFIRSTEMPCODE
(
	@CCMID VARCHAR(40)
)
RETURNS VARCHAR(7)
--WITH ENCRYPTION
AS
BEGIN
	DECLARE @CEMPCODE VARCHAR(7)
	
	SELECT TOP 1 @CEMPCODE = EMP_CODE FROM CMD01106
	WHERE EMP_CODE <> '0000000'
	AND   CM_ID = @CCMID

	RETURN ISNULL(@CEMPCODE, '0000000')
END
--*********************************** END OF FUNCTION FN_GETFIRSTEMPCODE
