




IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='DROP_UNWANTED_CONSTRAINTS_MIRROR' AND VALUE=1)
BEGIN
	PRINT 'DROPPPING UNWANTED CONSTRAINTS FROM MIRROR AND UPLOAD TABLES.....PLEASE STANDBY '
	EXEC SP3S_DROP_UNWANTED_MIRRORCONST
	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='DROP_UNWANTED_CONSTRAINTS_MIRROR')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('DROP_UNWANTED_CONSTRAINTS_MIRROR',1,'',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE=1 WHERE CONFIG_OPTION='DROP_UNWANTED_CONSTRAINTS_MIRROR'
	
END		
