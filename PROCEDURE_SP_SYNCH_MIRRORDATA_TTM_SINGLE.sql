CREATE PROCEDURE SP_SYNCH_MIRRORDATA_TTM_SINGLE
(
    @CLOCID VARCHAR(3)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN


DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX)
	   ,@CTABLENAME VARCHAR(200),@CKEYFIELD VARCHAR(200),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1)
	   ,@CCURDEPTID VARCHAR(5),@CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE1 VARCHAR(200),
	    @CTEMPDETAILTABLE2 VARCHAR(200), @CTEMPDETAILTABLE3 VARCHAR(200), @CTEMPDETAILTABLE4 VARCHAR(200)
	   ,@BADDMODE BIT,@CSOURCEDB VARCHAR(200),@CMERGEDB VARCHAR(200)
	   ,@BMSTINSERTONLY BIT,@BSERIESMISMATCHFOUND BIT,@CMEMOID VARCHAR(40),@CPREVMEMONO VARCHAR(20)
	   ,@NPREVMEMONO NUMERIC(5,0),@NLENVALUE INT,@CPREVMEMONOSEARCH VARCHAR(20),@CMEMOIDSEARCH VARCHAR(40)
	   ,@NVERSIONNO INT,@CJOINSTR VARCHAR(MAX)
	   ,@CTEMPIMPTABLE VARCHAR(200),@CCMD NVARCHAR(MAX)
	

BEGIN TRY
	
	
	SET @CSTEP=10

	SELECT @CSOURCEDB='',@CMERGEDB='',@BMSTINSERTONLY=1,@BSERIESMISMATCHFOUND=0
	
	SET @CSTEP=15
    	
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'

	SELECT @CTEMPMASTERTABLE=@CSOURCEDB+'TTM_TRANSFER_TO_TRADING_MST_MIRROR',
		   @CTEMPDETAILTABLE1=@CSOURCEDB+'TTM_TRANSFER_TO_TRADING_DET_MIRROR' ,
		   @CTEMPDETAILTABLE2=@CSOURCEDB+'TTM_TRANSFER_TO_TRADING_BOM_MIRROR', 
		   @CTEMPDETAILTABLE3=@CSOURCEDB+'TTM_TRANSFER_TO_TRADING_JOBS_MIRROR', 
		   @CTEMPDETAILTABLE4=@CSOURCEDB+'TTM_TRANSFER_TO_TRADING_SUB_DET_MIRROR' 
		   
	
    

LBLSTART:
    
    BEGIN TRANSACTION
    
    SELECT @CMEMOID='',@NVERSIONNO=0
		

    SET @CSTEP=30
    SELECT TOP 1 @CMEMOID = B.MEMO_ID ,@NVERSIONNO=VERSION_NO
    FROM TTM_TRANSFER_TO_TRADING_MST_MIRROR B (NOLOCK)
    LEFT OUTER JOIN MIRROR_SYNCH_LOG C (NOLOCK) ON C.MEMO_ID=B.MEMO_ID AND C.XN_TYPE='TTM'
    WHERE LEFT(B.MEMO_ID,2)=@CLOCID AND DATEDIFF(SS,B.VERSION_LAST_UPDATE,GETDATE())>60 AND C.MEMO_ID IS NULL
    ORDER BY B.MEMO_ID,VERSION_NO DESC
    
    
    
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		
    SET @CFILTERCONDITION = 'B.MEMO_ID='''+@CMEMOID+''' AND B.VERSION_NO='+LTRIM(RTRIM(STR(@NVERSIONNO)))
	

	SET @CSTEP=40
	SET @CMEMOIDSEARCH=''
	SELECT TOP 1 @CMEMOIDSEARCH=A.MEMO_ID FROM TRANSFER_TO_TRADING_MST A (NOLOCK) WHERE A.MEMO_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1


	IF @BADDMODE=0
	BEGIN	
		SET @CSTEP=50
		SET @DTSQL=N'DELETE A FROM TRANSFER_TO_TRADING_DET A JOIN '+@CTEMPMASTERTABLE+' B (NOLOCK) ON A.MEMO_ID=B.MEMO_ID
					 LEFT OUTER JOIN 
					 (SELECT ROW_ID FROM '+@CTEMPDETAILTABLE1+' B WHERE '+@CFILTERCONDITION+') C 
					 ON A.ROW_ID = C.ROW_ID 
					 WHERE C.ROW_ID IS NULL AND '+@CFILTERCONDITION

		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
		
		SET @CSTEP=60
		SET @DTSQL=N'DELETE A FROM TRANSFER_TO_TRADING_BOM A JOIN '+@CTEMPMASTERTABLE+' B (NOLOCK) ON A.MEMO_ID=B.MEMO_ID
					 LEFT OUTER JOIN 
					 (SELECT ROW_ID FROM '+@CTEMPDETAILTABLE2+' B WHERE '+@CFILTERCONDITION+') C 
					 ON A.ROW_ID = C.ROW_ID 
					 WHERE C.ROW_ID IS NULL AND '+@CFILTERCONDITION

		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
		
		
		SET @CSTEP=60
		SET @DTSQL=N'DELETE A FROM TRANSFER_TO_TRADING_JOBS A JOIN '+@CTEMPMASTERTABLE+' B (NOLOCK) ON A.MEMO_ID=B.MEMO_ID
					 LEFT OUTER JOIN 
					 (SELECT ROW_ID FROM '+@CTEMPDETAILTABLE3+' B WHERE '+@CFILTERCONDITION+') C 
					 ON A.ROW_ID = C.ROW_ID 
					 WHERE C.ROW_ID IS NULL AND '+@CFILTERCONDITION

		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
		
		
		SET @CSTEP=60
		SET @DTSQL=N'DELETE A FROM TRANSFER_TO_TRADING_SUB_DET A JOIN '+@CTEMPMASTERTABLE+' B (NOLOCK) ON A.MEMO_ID=B.MEMO_ID
					 LEFT OUTER JOIN 
					 (SELECT ROW_ID FROM '+@CTEMPDETAILTABLE4+' B WHERE '+@CFILTERCONDITION+') C 
					 ON A.ROW_ID = C.ROW_ID 
					 WHERE C.ROW_ID IS NULL AND '+@CFILTERCONDITION

		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL

	END
	
LBLMERGE:

	

	SET @CFILTERCONDITION = 'B.MEMO_ID='''+@CMEMOID+''' AND B.VERSION_NO='+LTRIM(RTRIM(STR(@NVERSIONNO)))							  
	
	
    SET @CTABLENAME='TRANSFER_TO_TRADING_MST'
	SET @CTMP_TABLENAME='TTM_TRANSFER_TO_TRADING_MST_MIRROR'
	SET @CKEYFIELD='MEMO_ID'

	
	
	SET @CSTEP=70	 
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 
							  
	--SET @CCMD=N'UPDATE TTM_TRANSFER_TO_TRADING_DET_MIRROR SET PARA'						  
							  
							  
							  
    SET @CTABLENAME='TRANSFER_TO_TRADING_DET'
	SET @CTMP_TABLENAME='TTM_TRANSFER_TO_TRADING_DET_MIRROR'
	SET @CKEYFIELD='ROW_ID'

	
	SET @CSTEP=80
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1 
							  

						  							  
	SET @CTABLENAME='TRANSFER_TO_TRADING_BOM'
	SET @CTMP_TABLENAME='TTM_TRANSFER_TO_TRADING_BOM_MIRROR'
	SET @CKEYFIELD='ROW_ID'

	
	SET @CSTEP=90
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1 
							  
							  
    SET @CTABLENAME='TRANSFER_TO_TRADING_JOBS'
	SET @CTMP_TABLENAME='TTM_TRANSFER_TO_TRADING_JOBS_MIRROR'
	SET @CKEYFIELD='ROW_ID'

	
	SET @CSTEP=90
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1 
							  
							  
    SET @CTABLENAME='TRANSFER_TO_TRADING_SUB_DET'
	SET @CTMP_TABLENAME='TTM_TRANSFER_TO_TRADING_SUB_DET_MIRROR'
	SET @CKEYFIELD='ROW_ID'

	
	SET @CSTEP=90
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1 							  							  
							  


	SET @CSTEP=95 

	EXEC SP3S_MERGE_LOCPMT  
	@cTempTable='TTM_PMT01106_MIRROR',  
	@cMemoIdCol='TTM_MEMO_ID',  
	@cMemoId =@CMEMOID


	SET @CSTEP=100  
	  		
	DELETE A FROM TTM_TRANSFER_TO_TRADING_MST_MIRROR A WHERE A.MEMO_ID=@CMEMOID AND A.VERSION_NO<=@NVERSIONNO
	DELETE A FROM TTM_TRANSFER_TO_TRADING_DET_MIRROR A WHERE A.MEMO_ID=@CMEMOID AND A.VERSION_NO<=@NVERSIONNO
	DELETE A FROM TTM_TRANSFER_TO_TRADING_BOM_MIRROR A WHERE A.MEMO_ID=@CMEMOID AND A.VERSION_NO<=@NVERSIONNO
	DELETE A FROM TTM_TRANSFER_TO_TRADING_JOBS_MIRROR A WHERE A.MEMO_ID=@CMEMOID AND A.VERSION_NO<=@NVERSIONNO
	DELETE A FROM TTM_TRANSFER_TO_TRADING_SUB_DET_MIRROR A WHERE A.MEMO_ID=@CMEMOID AND A.VERSION_NO<=@NVERSIONNO
	DELETE A FROM TTM_PMT01106_MIRROR A WHERE A.TTM_MEMO_ID=@CMEMOID 
	
	

	

	IF ISNULL(@CERRMSG,'')=''
		COMMIT
	ELSE
		GOTO EXIT_PROC
	
    GOTO LBLSTART

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_SYNCH_MIRRORDATA_TTM_SINGLE, MEMO ID :'+@CMEMOID+' STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO EXIT_PROC
END CATCH

EXIT_PROC:

	
	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRMSG,'')='' AND @BSERIESMISMATCHFOUND<>1
			COMMIT
		ELSE
			ROLLBACK
    END
	
END	
---END OF PROCEDURE - SP_SYNCH_MIRRORDATA_TTM_SINGLE
