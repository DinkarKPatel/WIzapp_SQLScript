CREATE PROCEDURE SP_SYNCH_MIRRORDATA_PRT_SINGLE
(
    @CLOCID VARCHAR(3)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		208_2014_01_13 : THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
						 TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
						 
	*/

DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@CFILTERCONDITION2 VARCHAR(500),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CMIRRORINGENABLED VARCHAR(5)
	   ,@CCURDEPTID VARCHAR(5),@CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE1 VARCHAR(200),@CTEMPDETAILTABLE2 VARCHAR(200)
	   ,@CTEMPPAYMODETABLE VARCHAR(200),@CTEMPTABLE VARCHAR(100),@CDONOTRESETPOSTEDINAC VARCHAR(1)
	   ,@BADDMODE BIT,@CCUTOFFDATE VARCHAR(20),@CWHERECLAUSE VARCHAR(2000),@CMEMONOLEN VARCHAR(5),@NMEMONOLEN INT
	   ,@DMINMEMODT DATETIME,@DMAXMEMODT DATETIME,@CFINYEAR VARCHAR(5),@CMEMOPREFIX VARCHAR(10)
	   ,@CMINMEMONO VARCHAR(20),@CMAXMEMONO VARCHAR(20),@CSOURCEDB VARCHAR(200),@CMERGEDB VARCHAR(200)
	   ,@BMSTINSERTONLY BIT,@BSERIESMISMATCHFOUND BIT,@CMEMOID VARCHAR(40),@CPREVMEMONO VARCHAR(20)
	   ,@NPREVMEMONO NUMERIC(5,0),@NLENVALUE INT,@CPREVMEMONOSEARCH VARCHAR(20),@CMEMOIDSEARCH VARCHAR(40)
	   ,@CRETPRODUCTCODE VARCHAR(50),@NVERSIONNO INT,@CJOINSTR VARCHAR(MAX),@CTEMPSKUTABLE VARCHAR(500)
	   ,@CPARTYDEPTID VARCHAR(5),@NMODE INT,@CCNID VARCHAR(40),@CDONOTSENDCHALLANWODISPATCHDETAILS VARCHAR(2)
	   ,@BDONOTSENDCHALLANWITHOUTDISPATCH BIT,@ERP_CONFIG VARCHAR(10),@CFILTERCONDITION1 VARCHAR(200)
	   ,@BDonotInsCHALLAN BIT,@dMIrrorLastUpdate DATETIME,@dLastUpdate DATETIME,@cMissingRowId VARCHAR(50)
	   ,@cUpdatestr VARCHAR(2000),@bServerLoc BIT
    


BEGIN TRY
	
	
	SET @cStep=5
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

	SELECT @CSOURCEDB='',@CMERGEDB='',@BMSTINSERTONLY=1,@BSERIESMISMATCHFOUND=0
	
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'

	SELECT @CTEMPMASTERTABLE=@CSOURCEDB+'PRT_RMM01106_MIRROR',
		   @CTEMPDETAILTABLE1=@CSOURCEDB+'PRT_RMD01106_MIRROR'
		   

   SELECT @BDONOTSENDCHALLANWITHOUTDISPATCH=ISNULL(DONOTSENDCHALLANWITHOUTDISPATCH,0) FROM LOCATION WHERE DEPT_ID=@CLOCID 
   
   SET @cStep=7
   EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

   DECLARE @DFREEZINGDATE DATETIME	
   EXEC SP3S_GETDATA_FREEZING_DATE 'PRT',@DFREEZINGDATE OUTPUT

   SET @cStep=9
   EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1
	   
	IF EXISTS (SELECT TOP 1 tablename FROM savetran_updcols_updatestr (NOLOCK) WHERE sp_id=@@SPID)
		DELETE FROM savetran_updcols_updatestr WITH (ROWLOCK) WHERE sp_id=@@SpId

	INSERT savetran_updcols_updatestr (sp_id,tablename,updatestr)
	SELECT @@SPId as sp_id,tablename,'' from mirrorxnsinfo (NOLOCK)
	WHERE tablename IN ('rmm01106','rmd01106','parcel_mst','parcel_det')

	IF OBJECT_ID('TEMPDB..#T','U') IS NOT NULL
		DROP TABLE #T
	
	select rm_id INTO #t FROM prt_rmm01106_mirror (NOLOCK) WHERE 1=2
LBLSTART:

	BEGIN TRANSACTION	

	SET @cStep=11
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1 
	
    SELECT @CMEMOID='',@NVERSIONNO=0

    SELECT TOP 1 @CMEMOID = B.RM_ID ,@NVERSIONNO=VERSION_NO,@NMODE=MODE,@CPARTYDEPTID=PARTY_DEPT_ID,
	@dMIrrorLastUpdate=b.last_update,@bServerLoc=ISNULL(SERVER_LOC,0)
    FROM PRT_RMM01106_MIRROR B (NOLOCK)
    LEFT OUTER JOIN MIRROR_SYNCH_LOG C (NOLOCK) ON C.MEMO_ID=B.RM_ID AND C.XN_TYPE='PRT'
	LEFT JOIN Location loc (NOLOCK) ON loc.dept_id=b.party_dept_id
    WHERE LEFT(B.RM_ID,2)=@CLOCID AND RM_DT> @DFREEZINGDATE AND DATEDIFF(SS,B.VERSION_LAST_UPDATE,GETDATE())>60 AND C.MEMO_ID IS NULL
    ORDER BY B.RM_ID,VERSION_NO DESC
       
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		
    SET @CFILTERCONDITION = 'B.RM_ID='''+@CMEMOID+''' AND B.VERSION_NO='+LTRIM(RTRIM(STR(@NVERSIONNO)))
     SET @CFILTERCONDITION1 = 'B.PRT_MEMO_ID='''+@CMEMOID+''' AND B.VERSION_NO='+LTRIM(RTRIM(STR(@NVERSIONNO)))
	

	SET @cStep=13
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

	SET @CMEMOIDSEARCH=''
	SELECT TOP 1 @CMEMOIDSEARCH=A.RM_ID,@dLastUpdate=last_update FROM RMM01106 A (NOLOCK) WHERE A.RM_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1

	
LBLMERGE:
	---DELETING EXISTING RECORD IF BILL COMES AGAIN FOR MERGING
	
	--SELECT @CTEMPMASTERTABLE,@CFILTERCONDITION
	IF @BADDMODE=0
		PRINT 'ADDMODE:N'
	ELSE
		PRINT 'ADDMODE:Y'	
	
	
	
  IF @BADDMODE=0
  BEGIN

		SET @cStep=15
		EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

		PRINT 'ADDMODE:N'
		EXEC SP3S_VERIFY_PRTDATA_merge_CHANGES
		@cMemoId=@cMemoId
	
 
				
		SET @CSTEP=20
		SELECT TOP 1 @cMissingRowId=a.row_id FROM rmd01106 A (NOLOCK) 
		LEFT JOIN 
		(SELECT row_id FROM PRT_rmd01106_MIRROR B (NOLOCK) WHERE rm_id=@cMEmoId) b
		 ON A.row_ID =B.row_ID WHERE A.rm_id =@CMEMOID AND b.row_id IS NULL

		IF ISNULL(@cMissingRowId,'')<>''
		BEGIN		
			SET @CSTEP=25
			EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

			DELETE A FROM rmd01106 A (NOLOCK) LEFT JOIN 
			(SELECT row_id FROM PRT_rmd01106_MIRROR B (NOLOCK) WHERE rm_id=@cMEmoId) b
			ON A.row_ID =B.row_ID WHERE A.rm_id =@CMEMOID AND b.row_id IS NULL

		END
		
	END
		
	---UPDATING TRANSACTION TABLES
	SET @CSTEP=27
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1
	SET @CTABLENAME='RMM01106'
	SET @CTMP_TABLENAME='PRT_RMM01106_MIRROR'
	SET @CKEYFIELD='RM_ID'


	SELECT @cUpdatestr=updatestr FROM  savetran_updcols_updatestr (NOLOCK)  WHERE sp_id=@@spid and tablename='rmm01106'
	SET @LUPDATEONLY = (CASE WHEN @BADDMODE=0 THEN 1 ELSE 0 END)		
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1,@cUpdatestrPara=@cUpdatestr,@bThruUpdatestrPara=1 
							  

	SET @CSTEP=29
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1


	SET @CTABLENAME='RMD01106'
	SET @CTMP_TABLENAME='PRT_RMD01106_MIRROR'
	SET @CKEYFIELD='ROW_ID'

	SELECT @cMissingRowId='',@lUpdateonly=0
	IF @BADDMODE=0
	BEGIN
		SET @CSTEP=35
		EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

		SELECT TOP 1 @cMissingRowId=b.row_id FROM 
		(SELECT row_id  FROM PRT_rmd01106_MIRROR (NOLOCK) WHERE rm_id=(@cMemoId+LEFT(@cMemoId,2))) A
		RIGHT OUTER JOIN 
		(SELECT row_id FROM PRT_rmd01106_MIRROR (NOLOCK) WHERE rm_id=@cMemoId) b ON 
		a.row_id=b.row_id WHERE a.row_id IS NULL

		--SELECT 'check @cMissingRowId of pid',@cMissingRowId

		IF ISNULL(@cMissingRowId,'')='' 
			SET @lUpdateonly=1
	END

	SET @CSTEP=37
    EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1
	SELECT @cUpdatestr=updatestr FROM  savetran_updcols_updatestr (NOLOCK)  WHERE sp_id=@@spid and tablename='rmd01106'

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1,@cUpdatestrPara=@cUpdatestr,@bThruUpdatestrPara=1 
							  
 --*********** PARCELS ENTRY **************************************
  
	DECLARE @bParcelAddmode BIT
	SELECT  @bParcelAddmode=1,@BDonotInsCHALLAN=0
      
	IF EXISTS (SELECT TOP 1 prt_memo_id FROM  PRT_PARCEL_MST_MIRROR B (NOLOCK) WHERE prt_memo_id=@cMEmoId)
	BEGIN

		IF EXISTS (SELECT TOP 1 a.parcel_memo_id FROM parcel_mst a (NOLOCK) JOIN 
				   PRT_PARCEL_MST_MIRROR b ON a.parcel_memo_id=b.parcel_memo_id
				   WHERE b.prt_memo_id=@cMemoId)
			SET  @bParcelAddmode=0

		IF @dLastUpdate=@dMIrrorLastUpdate AND @bParcelAddmode=0
			SET @BDonotInsCHALLAN=1

		SET @cStep=45
		EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

		SELECT @cUpdatestr=updatestr FROM  savetran_updcols_updatestr (NOLOCK)  WHERE sp_id=@@spid and  tablename='PARCEL_MST'
		SET @LUPDATEONLY = (CASE WHEN @bParcelAddmode=0 THEN 1 ELSE 0 END)		  	

		SET @CTABLENAME='PARCEL_MST'
		SET @CTMP_TABLENAME='PRT_'+@CTABLENAME+'_MIRROR'
		SET @CKEYFIELD='PARCEL_MEMO_ID'
	
		EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							,@LINSERTONLY=@bParcelAddmode,@CFILTERCONDITION=@CFILTERCONDITION1,@LUPDATEONLY=@LUPDATEONLY
							,@BALWAYSUPDATE=1,@BUPDATEXNS=1,@cUpdatestrPara=@cUpdatestr,@bThruUpdatestrPara=1  
	
	END

	SET @CSTEP=50
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

	SELECT TOP 1 @cMissingRowId=a.row_id FROM PARCEL_DET A (NOLOCK) 
	LEFT JOIN 
	(SELECT row_id FROM PRT_PARCEL_DET_MIRROR B (NOLOCK) WHERE PRT_memo_id=@cMEmoId) b
		ON A.row_ID =B.row_ID 
	JOIN parcel_mst c (NOLOCK) ON c.parcel_memo_id=a.parcel_memo_id 
	WHERE A.REF_MEMO_ID =@CMEMOID AND c.xn_type='WSL' AND b.row_id IS NULL
						
	IF ISNULL(@cMissingRowId,'')<>''
	BEGIN		
		SET @CSTEP=55
		EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

		DELETE A FROM PARCEL_DET A (NOLOCK) 
		LEFT JOIN 
		(SELECT row_id FROM PRT_PARCEL_DET_MIRROR B (NOLOCK) WHERE PRT_memo_id=@cMEmoId) b
			ON A.row_ID =B.row_ID 
		JOIN parcel_mst c (NOLOCK) ON c.parcel_memo_id=a.parcel_memo_id 
		WHERE A.REF_MEMO_ID =@CMEMOID AND c.xn_type='PRT' AND b.row_id IS NULL
	END

	SET @cStep=60
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1
	IF EXISTS (SELECT TOP 1 PRT_memo_id FROM  PRT_PARCEL_DET_MIRROR B (NOLOCK) WHERE PRT_memo_id=@cMEmoId)
	BEGIN
		SELECT @cMissingRowId='',@lUpdateonly=0
		IF @bParcelAddmode=0
		BEGIN
			SET @CSTEP=65
			EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

			SELECT TOP 1 @cMissingRowId=b.row_id FROM 
			(SELECT row_id  FROM PRT_PARCEL_DET_MIRROR (NOLOCK) WHERE PRT_memo_id=(@cMemoId+LEFT(@cMemoId,2))) A
			RIGHT OUTER JOIN 
			(SELECT row_id FROM PRT_PARCEL_DET_MIRROR (NOLOCK) WHERE PRT_memo_id=@cMemoId) b ON 
			a.row_id=b.row_id WHERE a.row_id IS NULL

			--SELECT 'check @cMissingRowId of pid',@cMissingRowId

			IF ISNULL(@cMissingRowId,'')='' 
				SET @lUpdateonly=1
		END		
		
		SET @CSTEP=70
		EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1				

		SET @CTABLENAME='PARCEL_DET'
		SET @CTMP_TABLENAME='PRT_'+@CTABLENAME+'_MIRROR'
		SET @CKEYFIELD='PARCEL_MEMO_ID'

		SELECT @cUpdatestr=updatestr FROM  savetran_updcols_updatestr (NOLOCK)  WHERE sp_id=@@spid and tablename='PARCEL_DET'

		EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							,@LINSERTONLY=@bParcelAddmode,@CFILTERCONDITION=@CFILTERCONDITION1,@LUPDATEONLY=@LUPDATEONLY
							,@BALWAYSUPDATE=1,@BUPDATEXNS=1,@cUpdatestrPara=@cUpdatestr,@bThruUpdatestrPara=1  
	END
  
  --******************************************************************										  
	
	
	--select 'check @BDonotInsCHALLAN',@BDonotInsCHALLAN

	IF @NMODE=2 AND @BDonotInsCHALLAN=0
	BEGIN

		SET @CSTEP=75
		EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1				
		
		DECLARE @CDISPATCHDETAILS VARCHAR(1000)
		
		SELECT @CDISPATCHDETAILS=PM.PARCEL_MEMO_ID  FROM RMM01106 A  
        JOIN LOCATION L ON L.DEPT_ID=LEFT(A.rm_ID,2)          
		JOIN PARCEL_DET PB ON PB.REF_MEMO_ID=A.rm_ID    
	    JOIN PARCEL_MST PM ON PM.PARCEL_MEMO_ID=PB.PARCEL_MEMO_ID    
	    WHERE A.rm_ID= @CMEMOID AND A.COMPLETED=1 AND A.APPROVED=(CASE WHEN L.STN_APPROVAL = 1 THEN 2 ELSE A.APPROVED END)     
        AND PM.XN_TYPE='PRT'
        
		
		--select 'check @CPARTYDEPTID',@CPARTYDEPTID,@bServerLoc,@CCURDEPTID
	
		IF ( ISNULL(@CDISPATCHDETAILS,'')='' AND ISNULL(@BDONOTSENDCHALLANWITHOUTDISPATCH,0)=1)
			GOTO LBLFINAL
		
		IF NOT (@CPARTYDEPTID=@CCURDEPTID OR @bServerLoc=1)
			GOTO LBLFINAL

		IF @bAddmode=0
		BEGIN
			DELETE FROM DOCPRT_RMD01106_MIRROR WHERE rm_id=@CMEMOID
			DELETE FROM DOCPRT_RMM01106_MIRROR WHERE rm_id=@CMEMOID
		END
		
		print 'insert ho git challan'
		SET @CSTEP=80
		EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1				
		
		SET @CTABLENAME='DOCPRT_RMM01106_MIRROR'
		SET @CTMP_TABLENAME='PRT_RMM01106_MIRROR'
		SET @CKEYFIELD='RM_ID'

		EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
								  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='VERSION_NO',@CKEYFIELD3=''
								  ,@LINSERTONLY=@bParcelAddmode,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
								  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1,@CSEARCHTABLE='RMM01106',@BINSVERSIONNO=1 


		SET @CSTEP=85
		EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1				

		SET @CTABLENAME='DOCPRT_RMD01106_MIRROR'
		SET @CTMP_TABLENAME='PRT_RMD01106_MIRROR'
		SET @CKEYFIELD='ROW_ID'
		 
		EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
								  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='VERSION_NO',@CKEYFIELD3=''
								  ,@LINSERTONLY=@bParcelAddmode,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
								  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1,@CSEARCHTABLE='RMD01106',@BINSVERSIONNO=1
									   
		GOTO LBLFINAL
	END
 

LBLFINAL:
	
	SET @cStep=95
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1

	EXEC SP3S_MERGE_LOCPMT  
	@cTempTable='PRT_PMT01106_MIRROR',  
	@cMemoIdCol='PRT_MEMO_ID',  
	@cMemoId =@CMEMOID
	
	set @CSTEP=100
	EXEC SP_CHKXNSAVELOG 'PRTMERGE',@CSTEP,0,@CMEMOID,'',1
	EXEC SP3S_DELETE_PRT_MIRROR_TABLES @cMemoId
	
	--select * from DOCPRT_rmd01106_MIRROR where rm_id='120112012JM-000029'

	IF ISNULL(@CERRMSG,'')=''
		COMMIT
	ELSE
		GOTO EXIT_PROC
			   		 	
    GOTO LBLSTART

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_SYNCH_MIRRORDATA_PRT_SINGLE for MemoId:'+isnull(@cMemoId,'')+' , STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO EXIT_PROC
END CATCH

EXIT_PROC:

print 'last step:'+@cStep

	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRMSG,'')=''
			COMMIT
		ELSE
			ROLLBACK
    END
	
END	
---END OF PROCEDURE - SP_SYNCH_MIRRORDATA_PRT_SINGLE