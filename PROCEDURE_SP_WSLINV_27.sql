
CREATE PROCEDURE SP_WSLINV_27
(
  @CMEMOID VARCHAR(50)=''
)
as
begin
     
	 
	 SELECT A.INV_ID  ,A.BO_DET_ROW_ID ,
	        SUM(A.QUANTITY) AS QUANTITY
	 FROM IND01106   A (NOLOCK) 
	 JOIN INM01106  B ON A.INV_ID  =B.INV_ID 
	 WHERE A.PS_ID =@CMEMOID   
	 AND B.CANCELLED =0
	 GROUP BY A.INV_ID ,A.BO_DET_ROW_ID


end