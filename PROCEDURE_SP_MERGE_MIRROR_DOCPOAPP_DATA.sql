CREATE PROCEDURE SP_MERGE_MIRROR_DOCPOAPP_DATA
(
    @CMEMOID VARCHAR(50)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CCMD NVARCHAR(MAX)
	   
BEGIN TRY
SET @CSTEP=20
	SET @CTABLE_SUFFIX=REPLACE(@CMEMOID,'-','_')
BEGIN TRANSACTION 

	SET @CTMP_TABLENAME='DOCPOAPP_POM01106_UPLOAD'

	
	
	SET @CSTEP=30
	  
	  SET @CCMD=N'UPDATE POM01106 WITH (ROWLOCK)  SET APPROVED=B.APPROVED
				  FROM DOCPOAPP_POM01106_UPLOAD B WHERE B.PO_ID=POM01106.PO_ID'	 
	  
	  EXEC SP_EXECUTESQL @CCMD
		
	SET @CSTEP=450

	DELETE FROM DOCPOAPP_POM01106_UPLOAD  WITH (ROWLOCK) WHERE PO_ID=@CMEMOID
END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_MERGE_MIRROR_DOCPOAPP_DATA, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_DOCPOAPP_DATA
