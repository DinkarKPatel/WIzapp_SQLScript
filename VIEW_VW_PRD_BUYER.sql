CREATE VIEW VW_PRD_BUYER

AS

SELECT C.MEMO_ID AS WORK_ORDER_ID,B.AC_NAME AS BUYER_NAME,B.AREA_NAME AS BUYER_AREA_NAME,
B.CITY AS BUYER_CITY ,B.STATE  AS BUYER_STATE , ORDER_NO AS BUYER_ORDER_NO,ORDER_DT AS BUYER_ORDER_DT,
DELIVERY_DT AS BUYER_DELIVERY_DT,TRAIL_DT AS BUYER_TRAIL_DT,E.ARTICLE_NO  AS ARTICLE_SET_NO
FROM BUYER_ORDER_MST  A (NOLOCK)
JOIN LMV01106 B (NOLOCK) ON A.AC_CODE = B.AC_CODE
JOIN  PRD_WO_ORDERS C (NOLOCK) ON A.ORDER_ID = C.ORDER_ID 
JOIN PRD_WO_MST D (NOLOCK) ON C.MEMO_ID = D.MEMO_ID
JOIN ARTICLE E (NOLOCK) ON D.ARTICLE_SET_CODE = E.ARTICLE_CODE 
WHERE D.CANCELLED=0
