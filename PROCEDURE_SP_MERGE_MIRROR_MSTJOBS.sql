CREATE PROCEDURE DBO.SP_MERGE_MIRROR_MSTJOBS
 ( @NMODE INT
  ,@XN_TYPE VARCHAR(50) ='MSTJOBS'
  )
AS
 BEGIN
       DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
              ,@CTEMPLOCTABLE VARCHAR(100),@CSTATECODEOUT CHAR(7),@CSTATECODE VARCHAR(10)
              ,@CCURDEPTID VARCHAR(10),@CHODEPTID VARCHAR(10),@BHOLOC BIT,@NLOCTYPE VARCHAR(50)
              ,@BPURLOC VARCHAR(10),@CTABLENAME VARCHAR(100),@CTMP_TABLENAME VARCHAR(100)
              ,@CKEYFIELD1 VARCHAR(100),@CTEMPLOCSSTTABLE VARCHAR(500),@CTEMPTABLE VARCHAR(500)
              ,@CTEMPLOCSSTMSTTABLE VARCHAR(500),@BPROCEED BIT,@CKEYFIELD2 VARCHAR(500),@CKEYFIELD3 VARCHAR(500) 
	          ,@CSOURCETABLE VARCHAR(1000),@NXNSMERGINGORDER INT,@CSOURCEDB VARCHAR(200),@CERRORMSG VARCHAR(MAX)	
BEGIN TRY
	  
	  SELECT @CSOURCEDB=''	
	  
	  --VALIDATE DATE 
	  IF ISNULL(@XN_TYPE,'')  = ''
	     BEGIN
	       SET @CERRORMSG ='XN_TYPE IS NOT NULL. PLEASE PASS XN TYPE INTO PARAMATER'
	       GOTO END_PROC;
	     END
	 
	  IF ISNULL(@NMODE,0) = 1
         GOTO END_PROC;	
     	
	  BEGIN TRANSACTION
      
      SET @CSTEP = 00  
               
	  SELECT @CCURDEPTID = VALUE FROM CONFIG (NOLOCK) WHERE CONFIG_OPTION='LOCATION_ID'  
	  SELECT @CHODEPTID = VALUE FROM CONFIG (NOLOCK) WHERE CONFIG_OPTION='HO_LOCATION_ID'  
	  
	  IF @CCURDEPTID=@CHODEPTID
	  BEGIN
			SET @CERRORMSG='P:SP_MERGE_MIRROR_MSTJOBS, CANNOT CALL MERGING PROC AT HEAD OFFICE'
			GOTO END_PROC
  	  END					

	  SET @CSTEP = 05    
	    
	  SELECT @NLOCTYPE=LOC_TYPE,@BPURLOC=PUR_LOC FROM LOCATION WHERE DEPT_ID=@CCURDEPTID  
	       
           
	  SET @CSTEP = 10   

	  --DELETE ALREADY EXISTING RECORDS FOR [MASTER TABLE
	  DELETE FROM  AGENCY_JOBS
	  --DELETE FROM  PRD_AGENCY_MST
	  --DELETE FROM  JOBS
	  --DELETE FROM  LM01106
	  
	  
	  SET @CSTEP = 20
	  
	  SET @CTEMPTABLE='MSTJOBS_LM01106_MIRROR'
	  SET @DTSQL=N'UPDATE '+@CTEMPTABLE+' WITH (ROWLOCK)  SET COMPANY_CODE=''01'''
	  EXEC SP_EXECUTESQL @DTSQL
	
	
  /*
    TMP_MSTJOBS_PRD_AGENCY_MST_P1
    TMP_MSTJOBS_AGENCY_JOBS_P1
    TMP_MSTJOBS_JOBS_P1
    TMP_MSTJOBS_LM01106_P1
*/	
	
	   SET @CKEYFIELD2 =''
	   SET @CKEYFIELD3 =''
	   
	    --INSERT DATA INTO CITY TABLE
	   SET @CSTEP = 30
	   SET @CTABLENAME = 'REGIONM'
	   SET @CSOURCETABLE ='MSTJOBS_REGIONM_MIRROR'
	   SET @CKEYFIELD1='REGION_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   
	   --INSERT DATA INTO CITY TABLE
	   SET @CSTEP = 40
	   SET @CTABLENAME = 'STATE'
	   SET @CSOURCETABLE ='MSTJOBS_STATE_MIRROR'
	   SET @CKEYFIELD1='STATE_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1

	   --INSERT DATA INTO CITY TABLE
	   SET @CSTEP = 50
	   SET @CTABLENAME = 'CITY'
	   SET @CSOURCETABLE ='MSTJOBS_CITY_MIRROR'
	   SET @CKEYFIELD1='CITY_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   
	   --INSERT DATA INTO AREA TABLE
	   SET @CSTEP = 60
	   SET @CTABLENAME = 'AREA'
	   SET @CSOURCETABLE ='MSTJOBS_AREA_MIRROR'
	   SET @CKEYFIELD1='AREA_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   
	   
	    --INSERT DATA INTO LM01106 TABLE
	   SET @CSTEP = 100
	   SET @CTABLENAME = 'LM01106'
	   SET @CSOURCETABLE ='MSTJOBS_LM01106_MIRROR'
	   SET @CKEYFIELD1='AC_CODE'

	   SET @CSTEP = 105  
	

	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   
	    --INSERT DATA INTO LMP01106 TABLE
	   SET @CSTEP = 110
	   SET @CTABLENAME = 'LMP01106'
	   SET @CSOURCETABLE ='MSTJOBS_LMP01106_MIRROR'
	   SET @CKEYFIELD1='AC_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   
	    --INSERT DATA INTO HD01106 TABLE
	   SET @CSTEP = 120
	   SET @CTABLENAME = 'HD01106'
	   SET @CSOURCETABLE ='MSTJOBS_HD01106_MIRROR'
	   SET @CKEYFIELD1='HEAD_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   
	   
	 --INSERT DATA INTO PRD_AGENCY_MST TABLE
	   SET @CSTEP = 70
	   SET @CTABLENAME = 'PRD_AGENCY_MST'
	   SET @CSOURCETABLE ='MSTJOBS_PRD_AGENCY_MST_MIRROR'
	   SET @CKEYFIELD1 ='AGENCY_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1

	   --INSERT DATA INTO JOBS TABLE
	   SET @CSTEP = 90
	   SET @CTABLENAME = 'JOBS'
	   SET @CSOURCETABLE ='MSTJOBS_JOBS_MIRROR'
	   SET @CKEYFIELD1='JOB_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   
	   
	   --INSERT DATA INTO AGENCY_JOBS TABLE
	   SET @CSTEP = 80
	   SET @CTABLENAME = 'AGENCY_JOBS'
	   SET @CSOURCETABLE ='MSTJOBS_AGENCY_JOBS_MIRROR'
	   SET @CKEYFIELD1='AGENCY_CODE'
	   EXEC UPDATEMASTERXN
	   @CSOURCEDB=@CSOURCEDB, @CSOURCETABLE=@CSOURCETABLE,
	   @CDESTDB='',@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD1,
	   @CKEYFIELD2=@CKEYFIELD2,@CKEYFIELD3=@CKEYFIELD3,@LINSERTONLY=0,
	   @CFILTERCONDITION='',@LUPDATEONLY=0,@BALWAYSUPDATE=1
	   
	   
	  

    
    GOTO END_PROC
END TRY

BEGIN CATCH
	SET @CERRORMSG='P:SP_MERGE_MIRROR_MSTJOBS, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO END_PROC
END CATCH

END_PROC:
	
	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRORMSG,'')='' 
			COMMIT
		ELSE
			ROLLBACK	
	END
	
	IF ISNULL(@CERRORMSG,'')='' 
	BEGIN   
		
		DELETE FROM MSTJOBS_REGIONM_MIRROR
		DELETE FROM MSTJOBS_STATE_MIRROR
		DELETE FROM MSTJOBS_CITY_MIRROR
		DELETE FROM MSTJOBS_AREA_MIRROR
		DELETE FROM MSTJOBS_LMP01106_MIRROR
		DELETE FROM MSTJOBS_LM01106_MIRROR
		DELETE FROM MSTJOBS_HD01106_MIRROR
		DELETE FROM MSTJOBS_PRD_AGENCY_MST_MIRROR
		DELETE FROM MSTJOBS_JOBS_MIRROR
		DELETE FROM MSTJOBS_AGENCY_JOBS_MIRROR
	END
		
	SELECT @XN_TYPE AS MEMO_ID,@CERRORMSG AS ERRMSG   	  
END

--END PROCEDURE SP_MERGE_MIRROR_MSTJOBS
