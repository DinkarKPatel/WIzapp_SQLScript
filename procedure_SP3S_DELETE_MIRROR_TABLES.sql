			IF OBJECT_ID('TEMPDB..#T','U') IS NOT NULL
			 DROP TABLE #T
		
		
		SELECT MRR_ID  INTO #T
		FROM PUR_PIM01106_MIRROR B (NOLOCK)
		WHERE  MRR_DT<=@DFREEZINGDATE1
