CREATE VIEW DBO.VW_PPC_FG_SHOW_BARCODE
AS
  SELECT PRODUCT_CODE,PARA1_NAME ,SIZEGROUP_NAME ,ARTICLE_NO,--@ROW_ID AS ROW_ID ,  
     A.PARA2_CODE,PARA2.PARA2_NAME,SD.ROW_ID  
     FROM PPC_FG_SKU A   
     JOIN ARTICLE C ON A.ARTICLE_CODE=C.ARTICLE_CODE  
     JOIN PPC_SIZEGROUP B ON A.SIZEGROUP_CODE=B.SIZEGROUP_CODE  
     JOIN PPC_FGBCG_DET SD ON SD.ROW_ID=A.PPC_FGBCG_DET_ROW_ID  
     JOIN  PARA2 ON PARA2.PARA2_CODE=A.PARA2_CODE  
     LEFT JOIN PPC_SIZEGROUP_PARA2 A1 ON A1.SIZEGROUP_CODE=A.SIZEGROUP_CODE AND A1.PARA2_CODE=PARA2.PARA2_CODE  
     JOIN PARA1 ON PARA1.PARA1_CODE=A.PARA1_CODE
