CREATE PROCEDURE SP3S_VALIDATEXN_PURMERGE
@cSpId VARCHAR(50),
@cMemoId VARCHAR(40),
@cErrormsg VARCHAR(MAX) OUTPUT
AS
BEGIN
	DECLARE @NNETAMOUNT NUMERIC(10,2),@NCMMSUBTOTAL NUMERIC(10,2),@NPAYMODETOTAMT NUMERIC(10,2),
	@NTotPURQty NUMERIC(10,2),@NTotPidQty NUMERIC(10,2),@cStep VARCHAR(4),@nPaymode NUMERIC(1,0)

	
	SET @CSTEP=10.8
	EXEC SP_CHKXNSAVELOG 'PURMERGE',@CSTEP,0,@CSPID,'',1


	SELECT @NTotPURQty=total_quantity FROM pim01106 (NOLOCK) WHERE mrr_id=@cMemoId

	SET @CSTEP=11.2
	EXEC SP_CHKXNSAVELOG 'PURMERGE',@CSTEP,0,@CSPID,'',1
		
	SELECT @NTotPidQty = SUM(quantity) FROM pid01106 (NOLOCK) WHERE mrr_id=@cMemoId 

	SET @CSTEP=11.4
	EXEC SP_CHKXNSAVELOG 'PURMERGE',@CSTEP,0,@CSPID,'',1		  
	SELECT @NTotPURQty=ISNULL(@NTotPURQty,0),@NTotPidQty = ISNULL(@NTotPidQty,0)  
	
	IF @NTotPURQty<>@NTotPidQty
	BEGIN  
		PRINT 'SP3S_VALIDATEXN_PURMERGE Quantity match FAILED'
		SET @CERRORMSG='Bill level Total quantity '+STR(@NTotPURQty,14,2)+' SHOULD BE EQUAL TO THE SUM OF Item level quantity '+STR(@NTotPidQty,10,2)+'...PLEASE CHECK'  
		GOTO END_PROC
	END

END_PROC:
	
END