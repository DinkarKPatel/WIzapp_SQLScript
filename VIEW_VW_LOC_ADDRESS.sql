CREATE VIEW VW_LOC_ADDRESS   
AS  
SELECT LOC.DEPT_ID,LOC.DEPT_NAME,AR.AREA_NAME AREA,AR.PINCODE,CT.CITY,ST.[STATE]   
FROM LOCATION LOC(NOLOCK)   
LEFT JOIN AREA AR ON AR.area_code=LOC.area_code AND AR.inactive=0  
LEFT JOIN CITY CT ON CT.CITY_CODE=AR.city_code AND CT.inactive=0  
LEFT JOIN [STATE] ST ON CT.STATE_CODE=[ST].STATE_code  
