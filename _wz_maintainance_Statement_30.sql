


IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_PIM_INVMODE' AND VALUE=1)
BEGIN
	PRINT 'UPDATING INVOICE MODE COLUMN IN PIM01106'

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_PIM_INVMODE')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_PIM_INVMODE','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_PIM_INVMODE'
	
	UPDATE PIM01106 SET INV_MODE=(CASE WHEN B.MRR_ID IS NULL THEN 1 ELSE 2 END) FROM PUR_IMPORT_LOG B
	WHERE B.MRR_ID=PIM01106.MRR_ID
END		
