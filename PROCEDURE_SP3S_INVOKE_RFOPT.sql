CREATE PROCEDURE SP3S_INVOKE_RFOPT
--WITH ENCRYPTION
AS
BEGIN
/*
  THIS PROCEDURE WILL UPDATE XNLOG TABLE TO 2015-04-01 FOR ALL TABLES SO THAT 
  IF SOMEHOW ANY TRANSACTIONS ARE PENDING FOR RFOPT BUILD PROCESS, RFOPT BUILD PROCESS WILL BE TRIGGERD 
  AND THE PENDING TRANSACTIONS WOULD BE BUILD.	
*/
IF OBJECT_ID('XNLOG','U') IS NOT NULL
BEGIN
	UPDATE XNLOG SET LAST_UPDATE='2015-04-01'
END	
END
--END OF PROCEDURE - SP3S_INVOKE_RFOPT
