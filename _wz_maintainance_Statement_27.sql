

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CND01106_INVQTY_2' AND VALUE=1)
BEGIN
	PRINT 'UPDATING INVOICE QTY COLUMN IN WHOLESALE CREDIT NOTE DETAIL'
	
	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_CND01106_INVQTY_2')
		INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_CND01106_INVQTY_2','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='UPDATE_CND01106_INVQTY_2'
	
	UPDATE CND01106 SET INVOICE_QUANTITY=(QUANTITY-SCHEME_QUANTITY) WHERE INVOICE_QUANTITY=0
END		
