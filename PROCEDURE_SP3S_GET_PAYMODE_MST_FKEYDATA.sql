CREATE PROCEDURE SP3S_GET_PAYMODE_MST_FKEYDATA
@CSOURCEDB VARCHAR(200),
@CSOURCETABLE VARCHAR(200),
@CSOURCEKEYFIELD VARCHAR(200),
@CXNTYPE VARCHAR(10),
@CTABLESUFFIX VARCHAR(40),
@CLOCID VARCHAR(5),
@CERRORMSG VARCHAR(MAX) OUTPUT
--WITH ENCRYPTION
AS
BEGIN
	BEGIN TRY
		DECLARE @CCMD NVARCHAR(MAX),@CTEMPTABLE VARCHAR(1000),@CTEMPTABLE1 VARCHAR(1000),@CTEMPTABLE2 VARCHAR(1000),
				@CTEMPTABLE3 VARCHAR(1000),@CTEMPTABLE4 VARCHAR(1000),@CTEMPTABLE5 VARCHAR(1000),
				@CKEYFIELDVAL VARCHAR(40),@CDESTDB VARCHAR(200),@CFILTERCONDITION VARCHAR(MAX),@NSTEP INT
		
		SET @CDESTDB='WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.'
		
		SET @NSTEP=10		

		IF @CSOURCETABLE<>'#TMPMISSINGPAYMODEDATA'
		BEGIN
			IF OBJECT_ID('TEMPDB..#TMPMISSINGPAYMODEDATA','U') IS NOT NULL
				DROP TABLE #TMPMISSINGPAYMODEDATA
			
			SELECT PAYMODE_CODE INTO #TMPMISSINGPAYMODEDATA FROM PAYMODE_MST WHERE 1=2
		END
		
		IF OBJECT_ID('TEMPDB..#TMPMISSINGLMDATA','U') IS NOT NULL
			DROP TABLE #TMPMISSINGLMDATA

		SELECT AC_CODE INTO #TMPMISSINGLMDATA FROM LM01106 WHERE 1=2

		IF OBJECT_ID('TEMPDB..#TMPMISSINGPAYMODEGRPDATA','U') IS NOT NULL
			DROP TABLE #TMPMISSINGPAYMODEGRPDATA

		SELECT PAYMODE_GRP_CODE INTO #TMPMISSINGPAYMODEGRPDATA FROM PAYMODE_GRP_MST WHERE 1=2

		
		SET @NSTEP=20											
		SET @CTEMPTABLE=(CASE WHEN LEFT(@CSOURCETABLE,1)='#' THEN @CSOURCETABLE ELSE @CSOURCEDB+'TMP_'+@CXNTYPE+'_'+@CSOURCETABLE+'_'+@CTABLESUFFIX END)
		SET @CTEMPTABLE1=@CSOURCEDB+'TMP_'+@CXNTYPE+'_PAYMODE_MST_'+@CTABLESUFFIX
		
		IF @CSOURCETABLE<>'#TMPMISSINGPAYMODEDATA'
		BEGIN
			SET @NSTEP=30	
			SET @CCMD=N'SELECT DISTINCT A.'+@CSOURCEKEYFIELD+' FROM '+@CTEMPTABLE+'  A
						LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.PAYMODE_MST B ON B.PAYMODE_CODE=A.'+@CSOURCEKEYFIELD+'
						WHERE B.PAYMODE_CODE IS NULL AND A.'+@CSOURCEKEYFIELD+' IS NOT NULL'

				
			INSERT #TMPMISSINGPAYMODEDATA
			EXEC SP_EXECUTESQL @CCMD
		END
				
		SET @NSTEP=40
		SET @CCMD=N'SELECT DISTINCT B.COMMISSION_AC_CODE
					FROM '+@CTEMPTABLE+'  A
					JOIN PAYMODE_MST B ON B.PAYMODE_CODE=A.'+@CSOURCEKEYFIELD+'
					LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.LM01106 C ON C.AC_CODE=B.COMMISSION_AC_CODE
					WHERE C.AC_CODE IS NULL
					UNION
					SELECT DISTINCT B.AC_CODE
					FROM '+@CTEMPTABLE+'  A
					JOIN PAYMODE_MST B ON B.PAYMODE_CODE=A.'+@CSOURCEKEYFIELD+'
					LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.LM01106 C ON C.AC_CODE=B.AC_CODE
					WHERE C.AC_CODE IS NULL'
			
		INSERT #TMPMISSINGLMDATA
		EXEC SP_EXECUTESQL @CCMD		
		
		SET @NSTEP = 45	
		IF OBJECT_ID(@CTEMPTABLE1,'U') IS NOT NULL
			SET @CCMD=N'SELECT DISTINCT  B.PAYMODE_GRP_CODE FROM '+@CTEMPTABLE+'  A
						JOIN '+@CTEMPTABLE1+'  B ON B.PAYMODE_CODE=A.'+@CSOURCEKEYFIELD+'
						LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.PAYMODE_GRP_MST C ON C.PAYMODE_GRP_CODE=B.PAYMODE_GRP_CODE
						WHERE C.PAYMODE_GRP_CODE IS NULL
						UNION
						SELECT DISTINCT B.PAYMODE_GRP_CODE FROM '+@CTEMPTABLE+'  A
						JOIN PAYMODE_MST B ON B.PAYMODE_CODE=A.'+@CSOURCEKEYFIELD+'
						LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.PAYMODE_GRP_MST C ON C.PAYMODE_GRP_CODE=B.PAYMODE_GRP_CODE
						WHERE C.PAYMODE_GRP_CODE IS NULL'
		ELSE
			SET @CCMD=N'SELECT DISTINCT B.PAYMODE_GRP_CODE FROM '+@CTEMPTABLE+'  A
						JOIN PAYMODE_MST B ON B.PAYMODE_CODE=A.'+@CSOURCEKEYFIELD+'
						LEFT OUTER JOIN WIZAPP3S_MIRROR_'+@CLOCID+'.DBO.PAYMODE_GRP_MST C ON C.PAYMODE_GRP_CODE=B.PAYMODE_GRP_CODE
						WHERE C.PAYMODE_GRP_CODE IS NULL'

			
		INSERT #TMPMISSINGPAYMODEGRPDATA
		EXEC SP_EXECUTESQL @CCMD

		IF EXISTS (SELECT TOP 1 AC_CODE FROM #TMPMISSINGLMDATA)
		BEGIN
			SET @NSTEP=50
			EXEC SP3S_GET_LM01106_FKEYDATA
			@CSOURCEDB='',
			@CSOURCETABLE='#TMPMISSINGLMDATA',
			@CSOURCEKEYFIELD='AC_CODE',
			@CXNTYPE=@CXNTYPE,
			@CTABLESUFFIX=@CTABLESUFFIX,
			@CLOCID=@CLOCID,
			@CERRORMSG=@CERRORMSG OUTPUT
		END	


		IF EXISTS (SELECT TOP 1 PAYMODE_GRP_CODE FROM #TMPMISSINGPAYMODEGRPDATA)
		BEGIN
			SET @NSTEP=60
			SET @CFILTERCONDITION= 'B.PAYMODE_GRP_CODE IN (SELECT PAYMODE_GRP_CODE FROM #TMPMISSINGPAYMODEGRPDATA)'
			
			EXEC UPDATEMASTERXN_MIRROR
			@CSOURCEDB='',
			@CSOURCETABLE='PAYMODE_GRP_MST',
			@CDESTDB=@CDESTDB,
			@CDESTTABLE='PAYMODE_GRP_MST',
			@CKEYFIELD1='PAYMODE_GRP_CODE',
			@LINSERTONLY=0,
			@CFILTERCONDITION=@CFILTERCONDITION,
			
			
			@LUPDATEONLY=0,
			@BALWAYSUPDATE=1
		END
		
		IF EXISTS (SELECT TOP 1 PAYMODE_CODE FROM #TMPMISSINGPAYMODEDATA)
		BEGIN
			SET @NSTEP=70
			SET @CFILTERCONDITION= 'B.PAYMODE_CODE IN (SELECT PAYMODE_CODE FROM #TMPMISSINGPAYMODEDATA)'
			
			EXEC UPDATEMASTERXN_MIRROR
			@CSOURCEDB='',
			@CSOURCETABLE='PAYMODE_MST',
			@CDESTDB=@CDESTDB,
			@CDESTTABLE='PAYMODE_MST',
			@CKEYFIELD1='PAYMODE_CODE',
			@LINSERTONLY=0,
			@CFILTERCONDITION=@CFILTERCONDITION,
			
			
			@LUPDATEONLY=0,
			@BALWAYSUPDATE=1
		END
							
		GOTO END_PROC									
	END TRY
	
	BEGIN CATCH
		SELECT @CERRORMSG='PROCEDURE SP3S_GET_PAYMODE_MST_FKEYDATA : '+ISNULL(ERROR_PROCEDURE(),'NULL P')+'STEP :'+STR(ISNULL(@NSTEP,0))+ ' LINE NO. :'+
		ISNULL(LTRIM(RTRIM(STR(ERROR_LINE()))),'NULL LINE')+'MSG :'+ISNULL(ERROR_MESSAGE(),'NULL MSG')
		
		GOTO END_PROC
	END CATCH
	
END_PROC:
		
END
----'END OF PROCEDURE SP3S_GET_PAYMODE_MST_FKEYDATA'
