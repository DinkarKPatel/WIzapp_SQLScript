CREATE FUNCTION FN_CHKSOLDSTATUS (@CPRODCODE VARCHAR(30))
RETURNS INT
--WITH ENCRYPTION
AS
BEGIN
	DECLARE @NQTYSOLD NUMERIC(10,2)
	SELECT @NQTYSOLD=ISNULL(SUM(QUANTITY),0) FROM CMD01106 A JOIN CMM01106 B ON A.CM_ID=B.CM_ID
	WHERE A.PRODUCT_CODE=@CPRODCODE AND B.CANCELLED=0 AND B.CM_MODE=1
	RETURN @NQTYSOLD
END
