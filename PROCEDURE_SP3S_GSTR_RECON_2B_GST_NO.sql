CREATE PROCEDURE SP3S_GSTR_RECON_2B_GST_NO  
AS  
BEGIN  
 SELECT DISTINCT CTIN AS GSTIN  
 FROM GSTR2BRECON_TEMP A  
 LEFT OUTER JOIN LMV01106 B ON B.AC_GST_NO=A.CTIN  
 LEFT OUTER JOIN TAXPRO_CLIENT_DETAILS B1 ON B1.TCD_AC_GST_NO=A.CTIN  
 WHERE B.AC_GST_NO IS NULL AND B1.TCD_AC_GST_NO IS NULL  
 --UNION  
 --SELECT DISTINCT CTIN AS GST_NO  
 --FROM GSTR2ARECON_TEMP A  
 --LEFT OUTER JOIN GST_NO_DETAILS B1 ON B1.GSTNO=A.CTIN  
 --WHERE B1.GSTNO IS NULL  
END