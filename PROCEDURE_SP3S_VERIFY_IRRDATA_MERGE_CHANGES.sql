CREATE PROCEDURE SP3S_VERIFY_IRRDATA_MERGE_CHANGES
@nSpId VARCHAR(50),
@cMemoId VARCHAR(40)
AS
BEGIN
	
	DECLARE @CFILTERCONDITION VARCHAR(1000),@CINSSPID VARCHAR(40),@CJOINSTR VARCHAR(500)
	PRINT 'gen tempdata for 1'
	SET @CFILTERCONDITION=' b.IRM_MEMO_ID='''+@cMemoId+''''
	set @CINSSPID=@nSpId+LEFT(@cMemoId,2)
	

	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB='',@CSOURCETABLE='IRM01106',@CDESTDB=''
								,@CDESTTABLE='IRR_IRM01106_upload',@CKEYFIELD1='IRM_MEMO_ID',@CKEYFIELD2='',@CKEYFIELD3=''
								,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
								,@BALWAYSUPDATE=0,@bUpdateXns=1,@CINSSPID=@cInSSPId,@CSEARCHTABLE='IRM01106'

	PRINT 'gen tempdata for 2'
	EXEC SP3S_GENFILTERED_UPDATESTR
	@cSpId=@nSpId ,
	@cInsSpId=@cInsSpId,
	@cTableName='IRM01106',
	@cUploadTableName='IRR_IRM01106_upload',
	@cKeyfield='IRM_MEMO_ID'
	


	PRINT 'gen tempdata for 3'
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB='',@CSOURCETABLE='IRD01106',@CDESTDB=''
								,@CDESTTABLE='IRR_IRD01106_upload',@CKEYFIELD1='IRM_MEMO_ID',@CKEYFIELD2='',@CKEYFIELD3=''
								,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
								,@BALWAYSUPDATE=0,@bUpdateXns=1,@CINSSPID=@cInsSPId,@CSEARCHTABLE='IRD01106'
	
	PRINT 'gen tempdata for 4'
	EXEC SP3S_GENFILTERED_UPDATESTR
	@cSpId=@nSpId ,
	@cInsSpId=@cInsSpId,
	@cTableName='IRD01106',
	@cUploadTableName='IRR_IRD01106_upload',
	@cKeyfield='row_id',
	@bDonotChkLastUpdate=1

END