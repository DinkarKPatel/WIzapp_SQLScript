CREATE PROCEDURE VALIDATEXN_MIRROR_PRT  
 @CLOCID VARCHAR(10),
 @CXNID VARCHAR(50),   
 @CMERGEDB VARCHAR(200),
 @CERRORMSG VARCHAR(MAX) OUTPUT  
-- WITH ENCRYPTION
AS  
BEGIN  
/* 
VALIDATEXN_MIRROR_PRT_V208_29_JAN_14 : THIS PROCEDURE WILL VALIDATE ALL THE MIRROR TRANSFERRED DATA FOR
DEBITNOTE DATA.
*/
DECLARE	@NCALCDISCOUNTAMT NUMERIC(14,2),@NDISCOUNTAMT NUMERIC(14,2),@DTSQL NVARCHAR(MAX),@NSUBTOTAL NUMERIC(14,2)
		,@NSUMINDNET NUMERIC(14,2),@CSTEP VARCHAR(5),@CCHK_TABLE VARCHAR(50),@NPAYMODECRAMT NUMERIC(10,2)
		,@NPAYMODETOTAMT NUMERIC(10,2),@NTOTAMOUNT NUMERIC(14,2)   
	 SET @CSTEP=00
	 SET @CERRORMSG=''
	 SET @CSTEP=10
	 IF OBJECT_ID('TEMPDB..#RMMTABLE','U') IS NOT NULL 
		DROP TABLE #RMMTABLE	 
	 SET @CSTEP=20
	 IF OBJECT_ID('TEMPDB..#RMDTABLE','U') IS NOT NULL 
		DROP TABLE #RMDTABLE
	 SET @CSTEP=30	
	 IF OBJECT_ID('TEMPDB..#PAYMODETABLE','U') IS NOT NULL 
		DROP TABLE #PAYMODETABLE		
	 SET @CSTEP=40
	 CREATE TABLE #RMMTABLE(RM_ID VARCHAR(22),MODE NUMERIC (5,0),DISCOUNT_PERCENTAGE NUMERIC (7,3),
			 DISCOUNT_AMOUNT NUMERIC (10,2),TOTAL_AMOUNT NUMERIC (10,2), ROUND_OFF NUMERIC (10,2), 
			 RM_NO CHAR(15),SUBTOTAL NUMERIC(18,4), FIN_YEAR VARCHAR(10),
			 RM_DT DATETIME,USER_CODE CHAR(7),AC_CODE CHAR(10),MANUAL_DISCOUNT BIT )  
	 SET @CSTEP=50		 
	 CREATE TABLE #RMDTABLE (RM_ID VARCHAR(22),PRODUCT_CODE VARCHAR(50),QUANTITY NUMERIC(10,3),INVOICE_QUANTITY NUMERIC(10,3)
			  ,GROSS_PURCHASE_PRICE NUMERIC (10,2),PURCHASE_PRICE NUMERIC (14,4),DN_DISCOUNT_PERCENTAGE NUMERIC(6,2), DN_DISCOUNT_AMOUNT NUMERIC (14,4)
			  ,RFNET NUMERIC (10,2),ITEM_TAX_PERCENTAGE NUMERIC(6,2),ITEM_TAX_AMOUNT NUMERIC(12,4),MANUAL_DISCOUNT BIT )  
BEGIN TRY	 
	 SET @CSTEP=70	     
	 PRINT 'MASTER RECORDS...'
	 SET @DTSQL=N'SELECT RM_ID,MODE,DISCOUNT_PERCENTAGE,DISCOUNT_AMOUNT,TOTAL_AMOUNT,ROUND_OFF, 
				 RM_NO,SUBTOTAL,FIN_YEAR,RM_DT,USER_CODE,AC_CODE,MANUAL_DISCOUNT 
				 FROM  '+@CMERGEDB+'RMM01106 (NOLOCK) WHERE RM_ID='''+@CXNID+'''' 
	 SET @CSTEP=80	      
	 INSERT #RMMTABLE
	 EXEC SP_EXECUTESQL @DTSQL 
	 SET @CSTEP=90
	 PRINT 'DETAIL RECORDS...'
	 SET @DTSQL=N'SELECT RM_ID ,PRODUCT_CODE ,QUANTITY ,INVOICE_QUANTITY,GROSS_PURCHASE_PRICE ,PURCHASE_PRICE , DN_DISCOUNT_PERCENTAGE , DN_DISCOUNT_AMOUNT ,RFNET ,  
						ITEM_TAX_PERCENTAGE,ITEM_TAX_AMOUNT, MANUAL_DISCOUNT 
				 FROM  '+@CMERGEDB+'RMD01106 (NOLOCK) WHERE RM_ID='''+@CXNID+''''  
	 SET @CSTEP=100
	 INSERT #RMDTABLE
	 EXEC SP_EXECUTESQL @DTSQL 
	 SET @CSTEP=110
	 PRINT 'PAYMENT RECORDS...'
	 SET @DTSQL=N'SELECT B.PAYMODE_GRP_CODE, A.PAYMODE_CODE, A.AMOUNT FROM  '+@CMERGEDB+'PAYMODE_XN_DET A  (NOLOCK)  
				 JOIN  '+@CMERGEDB+'PAYMODE_MST B (NOLOCK) ON A.PAYMODE_CODE = B.PAYMODE_CODE  
				 WHERE MEMO_ID = '''+@CXNID+''' AND XN_TYPE = ''PRT'''  
	 
	 SET @CSTEP=130
	 IF ISNULL(@CERRORMSG,'')<>''
		RETURN
	 SET @CSTEP=140	
	 PRINT 'VALIDATING LEDGERS...'	
	 IF ISNULL(@CERRORMSG,'')=''
	 BEGIN
		 SET @CSTEP=150	
		 SET @DTSQL=N'IF EXISTS (SELECT TOP 1 ''U'' FROM  '+@CMERGEDB+'RMM01106 A
								LEFT OUTER JOIN  '+@CMERGEDB+'LM01106 B ON A.AC_CODE=B.AC_CODE
								WHERE A.RM_ID='''+@CXNID+''' AND B.AC_CODE IS NULL)
						SET @CERRORMSGOUT=''INVALID LEDGER DETAILS FOUND''
					 ELSE
						SET @CERRORMSGOUT='''''	
		 EXEC SP_EXECUTESQL @DTSQL,N'@CERRORMSGOUT VARCHAR(200) OUTPUT',@CERRORMSGOUT=@CERRORMSG OUTPUT
		 IF ISNULL(@CERRORMSG,'')<>''
			RETURN
	 END
	 SET @CSTEP=160
	 SELECT @NSUBTOTAL=SUBTOTAL FROM #RMMTABLE  
	 SET @CSTEP=170
	 SELECT @NSUMINDNET=SUM(PURCHASE_PRICE*INVOICE_QUANTITY) FROM #RMDTABLE
	 SET @CSTEP=180
	 IF ABS(ISNULL(@NSUMINDNET,0)-ISNULL(@NSUBTOTAL,0))>1
	 BEGIN
		SET @CERRORMSG = 'MISMATCH BETWEEN ITEM LEVEL TOTAL AMOUNT & BILL LEVEL SUBTOTAL.....' -- CANNOT SAVE '
		RETURN
	 END	 
END TRY	
BEGIN CATCH
		SET @CERRORMSG = 'P:VALIDATEXN_MIRROR_PRT,STEP: '+@CSTEP+'MESSAGE: '+ERROR_MESSAGE()
		RETURN
END CATCH	
END_PROC:  
END  
--*************************************** END OF PROCEDURE VALIDATEXN_MIRROR_PRT
