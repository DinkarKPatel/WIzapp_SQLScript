CREATE PROCEDURE SP3S_DELETE_RPS_MIRROR_TABLES
@cMemoId VARCHAR(50)
AS
BEGIN
	DECLARE @cMemoIdCopy VARCHAR(50)
	
	SET @cMemoIdCopy=@cMemoId+LEFT(@cMemoId,2)

	TRUNCATE TABLE #t

	INSERT #T
	SELECT cm_id
	FROM RPS_RPS_MST_MIRROR B (NOLOCK) WHERE cm_id=@cMemoId OR cm_id=@cMemoIdCopy

	DELETE A FROM RPS_RPS_DET_MIRROR A JOIN #T B ON  A.cm_id=B.cm_id
	DELETE A FROM RPS_RPS_MST_MIRROR A JOIN #T B ON  A.cm_id=B.cm_id
	DELETE A FROM RPS_PMT01106_MIRROR A JOIN #T B ON  A.RPS_MEMO_ID=B.cm_id
END	