CREATE PROCEDURE SP_UPDATE_COMPLETED_DOCXNS
@DLASTRUNDATE DATETIME=''
--WITH ENCRYPTION
AS
BEGIN
	DECLARE @CDAYCLOSEDISABLED VARCHAR(1)
	
	SELECT TOP 1 @CDAYCLOSEDISABLED=VALUE FROM CONFIG WHERE CONFIG_OPTION='DO_NOT_ENFORCE_LAST_RUN_DT'
	
	IF ISNULL(@CDAYCLOSEDISABLED,'')='1'
		SELECT @DLASTRUNDATE=DATEADD(DD,-1,CONVERT(VARCHAR,GETDATE(),110))
		
	UPDATE INM01106 SET COMPLETED=1 WHERE INV_MODE=2 AND COMPLETED=0 AND 
	DATEDIFF(DD,INV_DT,@DLASTRUNDATE+1)>0

	UPDATE RMM01106 SET COMPLETED=1 WHERE MODE=2 AND COMPLETED=0 AND 
	DATEDIFF(DD,RM_DT,@DLASTRUNDATE+1)>0
END
