CREATE VIEW VW_PRT_PRINT_DET

AS
	SELECT  DISTINCT A.ROW_ID, A.LAST_UPDATE, RMM.location_code/*LEFT( RMM.RM_NO,2)*//*Rohit 04-11-2024*/ AS DEPT_ID, A.RM_ID, RMM.FIN_YEAR,
			S.INV_NO AS BILL_NO,S.INV_DT AS BILL_DT, 
			A.RFNET,   A.PRODUCT_CODE, A.QUANTITY, A.PURCHASE_PRICE, 
			(A.QUANTITY*A.PURCHASE_PRICE) AS AMOUNT, RMM.CANCELLED, A.PURCHASE_PRICE  AS 'RATE', 
			E.UOM_TYPE, RMM.RM_NO,  B.ARTICLE_CODE, B.ARTICLE_NO, B.ARTICLE_NAME, B.ARTICLE_DESC, C.PARA1_CODE, 
			C.PARA1_NAME, D.PARA2_CODE, D.PARA2_ORDER, D.PARA2_NAME, E.UOM_CODE, E.UOM_NAME, I.AC_CODE,  
			CONVERT(VARCHAR, S.INV_DT, 105) AS 'INV_DT', S.RECEIPT_DT, S.INV_NO, G.SUB_SECTION_NAME, H.PARA3_CODE, 
			H.PARA3_NAME, I.AC_NAME, J.SECTION_NAME, S.MRP, B.CODING_SCHEME, 
			Q.DEPT_NAME, X.PARA4_NAME, X.PARA4_CODE, Y.PARA5_NAME, Y.PARA5_CODE, 
			Z.PARA6_NAME, Z.PARA6_CODE, 0 AS FREIGHT_PERCENTAGE, 0 AS OC_PERCENTAGE, 
			A.QUANTITY AS 'OLD_QUANTITY'
    FROM RMD01106 A    
    JOIN RMM01106 RMM ON A.RM_ID = RMM.RM_ID    
    JOIN SKU S ON A.PRODUCT_CODE = S.PRODUCT_CODE  
    JOIN ARTICLE B ON S.ARTICLE_CODE = B.ARTICLE_CODE    
    JOIN PARA1 C ON S.PARA1_CODE = C.PARA1_CODE    
    JOIN PARA2 D ON S.PARA2_CODE = D.PARA2_CODE   
    JOIN PARA3 H ON S.PARA3_CODE = H.PARA3_CODE    
    JOIN PARA4 X ON S.PARA4_CODE = X.PARA4_CODE    
    JOIN PARA5 Y ON S.PARA5_CODE = Y.PARA5_CODE    
    JOIN PARA6 Z ON S.PARA6_CODE = Z.PARA6_CODE   
    JOIN UOM E ON B.UOM_CODE = E.UOM_CODE   
    JOIN SECTIOND G ON B.SUB_SECTION_CODE = G.SUB_SECTION_CODE    
    JOIN LM01106 I ON S.AC_CODE = I.AC_CODE   
    JOIN SECTIONM J ON G.SECTION_CODE = J.SECTION_CODE   
    JOIN LOCATION Q ON Q.DEPT_ID = LEFT(RMM.RM_NO,2)
