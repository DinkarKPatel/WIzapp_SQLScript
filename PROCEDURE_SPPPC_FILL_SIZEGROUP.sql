CREATE PROCEDURE [DBO].[SPPPC_FILL_SIZEGROUP]  
(  
 @NQUERYID   INT,    
 @VPARAMETER  VARCHAR(500)  
)  
AS      
BEGIN   
  
 DECLARE @CSTEP INT, @CCMD NVARCHAR(MAX), @ERRMSG_OUT VARCHAR(MAX)  
 BEGIN TRY  
  SET NOCOUNT ON;  
    
  SET @ERRMSG_OUT = ''  
    
  IF @NQUERYID = 1  
   -- GET SIZEGROUP LIST  
   GOTO LBLGETSIZEGROUPLIST  
     
  ELSE IF @NQUERYID = 2  
   -- GET SIZEGROUP USING LIKE  
   GOTO LBLGETSIZEGROUPLIKE  
    
  ELSE IF @NQUERYID = 7  
   -- GET INFO BY SIZEGROUP NAME  
   GOTO LBLGETINFOBYSIZEGROUPNAME  
  
  ELSE  
   GOTO LAST  
  
-- GET SIZEGROUP LIST  
LBLGETSIZEGROUPLIST:  
  SET @CSTEP = 101  
  SELECT A.SIZEGROUP_CODE, A.SIZEGROUP_NAME  
  FROM [PPC_SIZEGROUP] A (NOLOCK)   
  WHERE A.INACTIVE = 0  AND ISNULL(A.SIZEGROUP_NAME,'')<>''  
  ORDER BY A.SIZEGROUP_NAME  
    
  GOTO LAST  
  
  
-- GET SIZEGROUP LIKE  
LBLGETSIZEGROUPLIKE:  
  SET @CSTEP = 102  
  SELECT TOP 10 A.SIZEGROUP_CODE, A.SIZEGROUP_NAME  
  FROM [PPC_SIZEGROUP] A (NOLOCK)   
  WHERE A.INACTIVE = 0  AND ISNULL(A.SIZEGROUP_NAME,'')<>''  
  AND A.SIZEGROUP_NAME LIKE '%'+ @VPARAMETER +'%'  
  ORDER BY A.SIZEGROUP_NAME  
    
  GOTO LAST  
  
  
-- GET INFO BY SIZEGROUP NAME  
LBLGETINFOBYSIZEGROUPNAME:  
  SET @CSTEP = 107  
  SELECT A.SIZEGROUP_CODE, A.SIZEGROUP_NAME  
  FROM [PPC_SIZEGROUP] A (NOLOCK)   
  WHERE A.SIZEGROUP_NAME = @VPARAMETER  
    
  GOTO LAST  
    
LAST:     
 GOTO END_PROC  
  
  SET NOCOUNT OFF;  
 END TRY    
 BEGIN CATCH    
  SET @ERRMSG_OUT='ERROR: [P]: SPPPC_SIZEGROUP, [STEP]: '+CAST(@CSTEP AS VARCHAR(5))+', [MESSAGE]: ' + ERROR_MESSAGE()  
  GOTO END_PROC    
 END CATCH     
  
END_PROC:    
 IF  ISNULL(@ERRMSG_OUT,'')<>''   
  SELECT @ERRMSG_OUT AS ERRORMSG  
END
