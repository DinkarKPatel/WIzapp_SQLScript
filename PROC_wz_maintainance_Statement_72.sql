

IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='WOD_MST_AMOUNT' AND VALUE='1')
BEGIN
	DECLARE @CORDER_ID VARCHAR(22)
	
	IF OBJECT_ID('TEMPDB..#UPDORDERS','U') IS NOT NULL
		DROP TABLE #UPDORDERS
	
	SELECT DISTINCT ORDER_ID INTO #UPDORDERS FROM WSL_ORDER_MST WHERE CANCELLED=0	
	
	WHILE EXISTS(SELECT TOP 1 'U' FROM #UPDORDERS)
	BEGIN
		PRINT @CORDER_ID
		SELECT TOP 1 @CORDER_ID=ORDER_ID FROM #UPDORDERS
		
		EXEC SP3S_DSTRBT_MST_COMP 'WOD',@CORDER_ID
		
		DELETE #UPDORDERS WHERE ORDER_ID=@CORDER_ID
	
	END
	
	IF NOT EXISTS(SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='WOD_MST_AMOUNT')
       INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
	   VALUES ('WOD_MST_AMOUNT','1','',GETDATE())	
	ELSE	
		UPDATE CONFIG SET VALUE='1' WHERE CONFIG_OPTION='WOD_MST_AMOUNT'	
		
END
