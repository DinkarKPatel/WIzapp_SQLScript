CREATE PROCEDURE SP_SYNCH_MIRRORDATA_MIS_SINGLE
(
    @CLOCID VARCHAR(3)
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN


DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@CFILTERCONDITION2 VARCHAR(500),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CMIRRORINGENABLED VARCHAR(5)
	   ,@CCURDEPTID VARCHAR(5),@CTEMPMASTERTABLE VARCHAR(200),@CTEMPDETAILTABLE1 VARCHAR(200),@CTEMPDETAILTABLE2 VARCHAR(200)
	   ,@CTEMPPAYMODETABLE VARCHAR(200),@CTEMPTABLE VARCHAR(100),@CDONOTRESETPOSTEDINAC VARCHAR(1)
	   ,@BADDMODE BIT,@CCUTOFFDATE VARCHAR(20),@CWHERECLAUSE VARCHAR(2000),@CMEMONOLEN VARCHAR(5),@NMEMONOLEN INT
	   ,@DMINMEMODT DATETIME,@DMAXMEMODT DATETIME,@CFINYEAR VARCHAR(5),@CMEMOPREFIX VARCHAR(10)
	   ,@CMINMEMONO VARCHAR(20),@CMAXMEMONO VARCHAR(20),@CSOURCEDB VARCHAR(200),@CMERGEDB VARCHAR(200)
	   ,@BMSTINSERTONLY BIT,@BSERIESMISMATCHFOUND BIT,@CMEMOID VARCHAR(40),@CPREVMEMONO VARCHAR(20)
	   ,@NPREVMEMONO NUMERIC(5,0),@NLENVALUE INT,@CPREVMEMONOSEARCH VARCHAR(20),@CMEMOIDSEARCH VARCHAR(40)
	   ,@CRETPRODUCTCODE VARCHAR(50),@NVERSIONNO INT,@CJOINSTR VARCHAR(MAX),@CTEMPSKUTABLE VARCHAR(500)
	   ,@CPARTYDEPTID VARCHAR(5),@NINVMODE INT,@CTEMPIMPTABLE VARCHAR(200),@CFILTERCONDITION3 VARCHAR(500),
	   @CCMD NVARCHAR(MAX),@CTEMPDETAILTABLE3 VARCHAR(200)
	

BEGIN TRY
	
	
	SET @CSTEP=10

	SELECT @CSOURCEDB='',@CMERGEDB='',@BMSTINSERTONLY=1,@BSERIESMISMATCHFOUND=0
	
	SET @CSTEP=15
    	
    SELECT TOP 1 @CCURDEPTID=VALUE FROM CONFIG WHERE CONFIG_OPTION='LOCATION_ID'

	SELECT @CTEMPMASTERTABLE=@CSOURCEDB+'MIS_BOM_ISSUE_MST_MIRROR',
		   @CTEMPDETAILTABLE1=@CSOURCEDB+'MIS_BOM_ISSUE_DET_MIRROR' ,
		   @CTEMPDETAILTABLE2=@CSOURCEDB+'MIS_BOM_ISSUE_REF_MIRROR' 
		   
	
    

LBLSTART:
    
    BEGIN TRANSACTION
    
    SELECT @CMEMOID='',@NVERSIONNO=0
		

    SET @CSTEP=30
    SELECT TOP 1 @CMEMOID = B.ISSUE_ID ,@NVERSIONNO=VERSION_NO
    FROM MIS_BOM_ISSUE_MST_MIRROR B (NOLOCK)
    LEFT OUTER JOIN MIRROR_SYNCH_LOG C (NOLOCK) ON C.MEMO_ID=B.ISSUE_ID AND C.XN_TYPE='MIS'
    WHERE LEFT(B.ISSUE_ID,2)=@CLOCID AND DATEDIFF(SS,B.VERSION_LAST_UPDATE,GETDATE())>60 AND C.MEMO_ID IS NULL
    ORDER BY B.ISSUE_ID,VERSION_NO DESC
    
    IF ISNULL(@CMEMOID,'')=''
		GOTO EXIT_PROC
		
    SET @CFILTERCONDITION = 'B.ISSUE_ID='''+@CMEMOID+''' AND B.VERSION_NO='+LTRIM(RTRIM(STR(@NVERSIONNO)))
	

	SET @CSTEP=40
	SET @CMEMOIDSEARCH=''
	SELECT TOP 1 @CMEMOIDSEARCH=A.ISSUE_ID FROM BOM_ISSUE_MST A (NOLOCK) WHERE A.ISSUE_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
		SET @BADDMODE=0
	ELSE
		SET @BADDMODE=1


	IF @BADDMODE=0
	BEGIN	
		SET @CSTEP=50
		SET @DTSQL=N'DELETE A FROM BOM_ISSUE_DET A JOIN '+@CTEMPMASTERTABLE+' B (NOLOCK) ON A.ISSUE_ID=B.ISSUE_ID
					 LEFT OUTER JOIN 
					 (SELECT ROW_ID FROM '+@CTEMPDETAILTABLE1+' B WHERE '+@CFILTERCONDITION+') C 
					 ON A.ROW_ID = C.ROW_ID 
					 WHERE C.ROW_ID IS NULL AND '+@CFILTERCONDITION

		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL
		
		SET @CSTEP=60
		SET @DTSQL=N'DELETE A FROM BOM_ISSUE_REF A JOIN '+@CTEMPDETAILTABLE1+' B (NOLOCK) ON A.BOM_ISSUE_ID=B.ISSUE_ID
					 LEFT OUTER JOIN 
					 (SELECT REF_ROW_ID FROM '+@CTEMPDETAILTABLE3+' B WHERE '+@CFILTERCONDITION+') C 
					 ON A.ROW_ID = C.REF_ROW_ID 
					 WHERE C.REF_ROW_ID IS NULL AND '+@CFILTERCONDITION

		PRINT @DTSQL
		EXEC SP_EXECUTESQL @DTSQL

	END
	
LBLMERGE:

	

	--SET @CFILTERCONDITION = REPLACE(@CFILTERCONDITION,'MEMO_ID','PFI_MEMO_ID')
	
	
    SET @CTABLENAME='BOM_ISSUE_MST'
	SET @CTMP_TABLENAME='MIS_BOM_ISSUE_MST_MIRROR'
	SET @CKEYFIELD='ISSUE_ID'

	
	
	SET @CSTEP=70	 
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BADDMODE,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1,@BUPDATEXNS=1 
							  
							  
							  
    SET @CTABLENAME='BOM_ISSUE_DET'
	SET @CTMP_TABLENAME='MIS_BOM_ISSUE_DET_MIRROR'
	SET @CKEYFIELD='ROW_ID'

	
	SET @CSTEP=80
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION=@CFILTERCONDITION,@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1 
							  
	SET @CFILTERCONDITION = 'B.BOM_ISSUE_ID='''+@CMEMOID+''' AND B.VERSION_NO='+LTRIM(RTRIM(STR(@NVERSIONNO)))							  

						  							  
	SET @CTABLENAME='BOM_ISSUE_REF'
	SET @CTMP_TABLENAME='MIS_BOM_ISSUE_REF_MIRROR'
	SET @CKEYFIELD='ROW_ID'

	
	SET @CSTEP=90
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=1,@CFILTERCONDITION='',@LUPDATEONLY=@LUPDATEONLY
							  ,@BALWAYSUPDATE=1 
							  


	SET @CSTEP=95 

	EXEC SP3S_MERGE_LOCPMT  
	@cTempTable='MIS_PMT01106_MIRROR',  
	@cMemoIdCol='MIS_MEMO_ID',  
	@cMemoId =@CMEMOID

	SET @CSTEP=100    		
	DELETE A FROM MIS_BOM_ISSUE_MST_MIRROR A WHERE A.ISSUE_ID=@CMEMOID AND A.VERSION_NO<=@NVERSIONNO
	
	DELETE A FROM MIS_BOM_ISSUE_DET_MIRROR A WHERE A.ISSUE_ID=@CMEMOID AND A.VERSION_NO<=@NVERSIONNO
	
	DELETE A FROM MIS_BOM_ISSUE_REF_MIRROR A WHERE A.MIS_MEMO_ID=@CMEMOID AND A.VERSION_NO<=@NVERSIONNO
	DELETE A FROM MIS_PMT01106_MIRROR A WHERE A.MIS_MEMO_ID=@CMEMOID 
	
	

	

	IF ISNULL(@CERRMSG,'')=''
		COMMIT
	ELSE
		GOTO EXIT_PROC
	
    GOTO LBLSTART

END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_SYNCH_MIRRORDATA_MIS_SINGLE, MEMO ID :'+@CMEMOID+' STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
	GOTO EXIT_PROC
END CATCH

EXIT_PROC:

	
	IF @@TRANCOUNT>0
	BEGIN
		IF ISNULL(@CERRMSG,'')='' AND @BSERIESMISMATCHFOUND<>1
			COMMIT
		ELSE
			ROLLBACK
    END
	
END	
---END OF PROCEDURE - SP_SYNCH_MIRRORDATA_MIS_SINGLE
