CREATE PROCEDURE SP_MERGE_MIRROR_APP_DATA
(
	@CMEMOID VARCHAR(50)
   ,@CLOCID VARCHAR(3)
   ,@CSOURCEDB VARCHAR(200)
   ,@CMERGEDB VARCHAR(200)
   ,@BMSTINSERTONLY BIT
   ,@CERRMSG VARCHAR(1000) OUTPUT
)
--WITH ENCRYPTION
AS
SET NOCOUNT ON
BEGIN
	/*
		SP_MERGE_MIRROR_APP_DATA_208_2014_02_04 : 
		THIS PROCEDURE WILL MERGE DATA FROM TEMPORARY TABLE TO ACTUAL TABLE.
		TABLE NAMES AND ITS MERGING ORDER WILL BE FIXED AND WILL BE DEFINED HERE.	
	*/
DECLARE @CSTEP VARCHAR(100),@DTSQL NVARCHAR(MAX),@NMERGE_ORDER NUMERIC(5)
	   ,@CTABLENAME VARCHAR(200),@CTABLE_SUFFIX VARCHAR(50),@CKEYFIELD VARCHAR(200),@CDEL_ID VARCHAR(50),@CTMP_TABLENAME VARCHAR(200),@LINSERTONLY VARCHAR(1)
	   ,@CFILTERCONDITION VARCHAR(200),@LUPDATEONLY VARCHAR(1),@BALWAYSUPDATE VARCHAR(1),@FDEL CHAR(1)
	   ,@CERRMSGOUT VARCHAR(1000),@CTABLESSTR VARCHAR(MAX),@CTEMPTABLE VARCHAR(200),@CCMD NVARCHAR(MAX)
	   
BEGIN TRY

SET @CSTEP=20
	SET @CTABLE_SUFFIX=REPLACE(@CMEMOID,'-','_')
BEGIN TRANSACTION 

	DECLARE @CMEMOIDSEARCH VARCHAR(100)
	SELECT TOP 1 @CMEMOIDSEARCH=A.MEMO_ID FROM APM01106 A (NOLOCK) WHERE A.MEMO_ID=@CMEMOID
	
	IF ISNULL(@CMEMOIDSEARCH,'')<>''
	BEGIN
		SET @CSTEP=20
		EXEC SP3SBUILDAPP
			@CXNID=@CMEMOID
			,@NUPDATEMODE=3
			,@CERRMSG=@CERRMSG OUTPUT  

		IF ISNULL(@CERRMSG,'')<>''
			GOTO Exit_PROC			
    END
    
	SET @CSTEP=80
	SET @CTABLENAME='APD01106'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CTEMPTABLE=@CSOURCEDB+'TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=90
	SET @DTSQL=N'IF OBJECT_ID('''+@CTEMPTABLE+''',''U'') IS NOT NULL
					DELETE A FROM '+@CMERGEDB+'['+@CTABLENAME+'] A WITH (ROWLOCK) LEFT OUTER JOIN
					'+@CTEMPTABLE+' B (NOLOCK) ON A.ROW_ID=B.ROW_ID  WHERE A.'+@CKEYFIELD+'='''+@CMEMOID+'''
				    AND B.ROW_ID IS NULL
				 ELSE
					DELETE FROM '+@CMERGEDB+'['+@CTABLENAME+'] WITH (ROWLOCK) WHERE '+@CKEYFIELD+'='''+@CMEMOID+''''   
				    
	PRINT @DTSQL
	EXEC SP_EXECUTESQL @DTSQL

	SET @CSTEP=100
	SET @CTABLENAME='STATE'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='STATE_CODE'
	SET @CSTEP=110
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  
	SET @CSTEP=120
	SET @CTABLENAME='CITY'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='CITY_CODE'
	SET @CSTEP=130
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  

	SET @CSTEP=140
	SET @CTABLENAME='AREA'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='AREA_CODE'
	SET @CSTEP=150
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
	
	SET @CSTEP=160
	SET @CTABLENAME='HD01106'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='HEAD_CODE'
	SET @CSTEP=170
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 
							  
	SET @CSTEP=180
	SET @CTABLENAME='LM01106'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='AC_CODE'
	SET @CSTEP=190
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  

	SET @CSTEP=200
	SET @CTABLENAME='LMP01106'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='AC_CODE'
	SET @CSTEP=210
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=@BMSTINSERTONLY,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  

	SET @CSTEP=212
	SET @CTABLENAME='CUSTDYM'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='CUSTOMER_CODE'
	SET @CSTEP=214
	
	SET @CCMD=N'UPDATE '+@CSOURCEDB+@CTMP_TABLENAME+' SET REF_CUSTOMER_CODE=''000000000000'' WHERE 
	REF_CUSTOMER_CODE NOT IN (SELECT CUSTOMER_CODE FROM CUSTDYM)'
	EXEC SP_EXECUTESQL @CCMD
	
	
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  							  
							  
	SET @CSTEP=220
	SET @CTABLENAME='APM01106'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='MEMO_ID'
	SET @CSTEP=230
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  
							  
    SET @CSTEP=240
	SET @CTABLENAME='APD01106'
	SET @CTMP_TABLENAME='TMP_APP_'+@CTABLENAME+'_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	SET @CKEYFIELD='ROW_ID'
	SET @CSTEP=250
	EXEC UPDATEMASTERXN_MIRROR @CSOURCEDB=@CSOURCEDB,@CSOURCETABLE=@CTMP_TABLENAME,@CDESTDB=@CMERGEDB
							  ,@CDESTTABLE=@CTABLENAME,@CKEYFIELD1=@CKEYFIELD,@CKEYFIELD2='',@CKEYFIELD3=''
							  ,@LINSERTONLY=0,@CFILTERCONDITION='',@LUPDATEONLY=0
							  ,@BALWAYSUPDATE=1 							  							  						  							  
							  

	SET @CSTEP=250 
	
	SET @CTMP_TABLENAME=@CSOURCEDB+'TMP_APP_pmt01106_'+LTRIM(RTRIM(@CTABLE_SUFFIX))
	EXEC SP3S_MERGE_LOCPMT  
	@cTempTable=@CTMP_TABLENAME
		
	SET @CSTEP=450

	SET @CTABLESSTR='APM01106,APD01106,LM01106,CUSTDYM,LMP01106,HD01106,AREA,CITY,STATE,PMT01106,XNSTABLELIST'

	EXEC SP3S_DROPTEMPTABLES_MIRRORDATA
	@CXNTYPE='APP',
	@CTABLESUFFIX=@CTABLE_SUFFIX,
	@CTABLESSTR=@CTABLESSTR


END TRY
BEGIN CATCH
	SET @CERRMSG='P:SP_MERGE_MIRROR_APP_DATA, STEP:'+@CSTEP+', MESSAGE:'+ERROR_MESSAGE()
END CATCH
EXIT_PROC:
	IF ISNULL(@CERRMSG,'')=''
	BEGIN
	EXEC SP3SBUILDAPP
			@CXNID=@CMEMOID
			,@NUPDATEMODE=2
			,@CERRMSG=@CERRMSG OUTPUT  
	END

	IF ISNULL(@CERRMSG,'')='' AND @@TRANCOUNT>0
	BEGIN
		COMMIT
	END
	ELSE IF ISNULL(@CERRMSG,'')<>'' AND @@TRANCOUNT>0
		ROLLBACK
END	
---END OF PROCEDURE - SP_MERGE_MIRROR_APP_DATA
