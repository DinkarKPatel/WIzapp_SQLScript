CREATE FUNCTION dbo.fn_getLoginDate()
returns datetime
as
begin
	DECLARE @dlastRundate DATETIME,@CDONOTENFORCEDAYCLOSE VARCHAR(2),@DCURRENTDATE DATETIME
	SELECT @CDONOTENFORCEDAYCLOSE=VALUE FROM CONFIG (NOLOCK) WHERE CONFIG_OPTION='DO_NOT_ENFORCE_LAST_RUN_DT'

	SET @CDONOTENFORCEDAYCLOSE=ISNULL(@CDONOTENFORCEDAYCLOSE,'')
	
	IF @CDONOTENFORCEDAYCLOSE<>'1'
	BEGIN
		SELECT @dlastRundate=VALUE FROM config where config_option='last_run_date_wenc'
		SET @DCURRENTDATE=DATEADD(DD,1,@dlastRundate)			
	END
	ELSE 
		SET @DCURRENTDATE=CONVERT(DATE,GETDATE())

	RETURN @DCURRENTDATE
end
