create PROCEDURE SP3S_CRTINDEX_INDEXVIEW
AS
BEGIN

	DECLARE @CCMD NVARCHAR(MAX)

	IF OBJECT_ID('IV_CUST_ATTR_NAMES','V') IS NOT NULL
	BEGIN

		IF NOT EXISTS (SELECT TOP 1 NAME FROM SYS.INDEXES (NOLOCK) WHERE NAME='IDX_IV_CUST_ATTR_NAMES_CUSTOMER_CODE') 
		BEGIN
			SET @CCMD=N'CREATE UNIQUE  CLUSTERED INDEX IDX_IV_CUST_ATTR_NAMES_CUSTOMER_CODE ON IV_CUST_ATTR_NAMES(CUSTOMER_CODE)'
			EXEC SP_EXECUTESQL @CCMD
		END

	END


END
