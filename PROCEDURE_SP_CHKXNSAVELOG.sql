CREATE PROCEDURE SP_CHKXNSAVELOG
@CXNTYPE VARCHAR(MAX),
@NSTEP varchar(40),
@BFIRSTSTEP BIT=0 ,
@NSPID VARCHAR(40)='0',
@cstep_msg VARCHAR(100)='',
@BDEBUGMODE BIT=0
AS
BEGIN
	RETURN
		
	--IF @BFIRSTSTEP=1
	--	TRUNCATE TABLE XNSAVELOG
	
	INSERT XNSAVELOG	( XN_TYPE, STEP, START_TIME, SP_ID,step_msg )  
	SELECT @CXNTYPE,@NSTEP,GETDATE() AS START_TIME,@NSPID,@cstep_msg
END
