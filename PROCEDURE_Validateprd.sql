CREATE PROCEDURE VALIDATEPRD
		@CXNTYPE VARCHAR(10),
		@CXNID VARCHAR(40),
		@NUPDATEMODE INT=1,
		@CUSERCODE CHAR(7)='0000000',
		@CCMD VARCHAR(1000) OUTPUT
		--*** PARAMETERS :
		--*** @CXNTYPE - TRANSACTION TYPE (MODULE SPECIFIC)
		--*** @CXNID - TRANSACTION ID ( MEMO ID OF MASTER TABLE )
--WITH ENCRYPTION
AS
BEGIN
	SET @CCMD = ''
 
	--*** STARTING VALIDATION OF CUTTING ORDER 
	IF @CXNTYPE IN ('STKTRF')		
	BEGIN
		EXEC VALIDATEPRD_STKTRF @CXNID,@NUPDATEMODE,@CUSERCODE,@CCMD OUTPUT
		RETURN	
	END		--		END OF SALE
	ELSE 
	IF @CXNTYPE IN ('TTM')		
	BEGIN
		EXEC VALIDATEPRD_TTM @CXNID,@NUPDATEMODE,@CUSERCODE,@CCMD OUTPUT
		RETURN	
	END
	ELSE 
	IF @CXNTYPE IN ('ISS')		
	BEGIN
		EXEC VALIDATEPRD_ISS @CXNID,@NUPDATEMODE,@CUSERCODE,@CCMD OUTPUT
		RETURN	
	END
	ELSE 
	IF @CXNTYPE IN ('REC')		
	BEGIN
		EXEC VALIDATEPRD_REC @CXNID,@NUPDATEMODE,@CUSERCODE,@CCMD OUTPUT
		RETURN	
	END
	
	
END_PROC:
END
