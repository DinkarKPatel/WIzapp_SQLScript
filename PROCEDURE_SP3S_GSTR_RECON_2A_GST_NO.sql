CREATE PROCEDURE SP3S_GSTR_RECON_2A_GST_NO
(
	@dtMonth INT,      
	@dtYear  INT
)
AS
BEGIN
	--DECLARE @dtMonth INT,      	 @dtYear  INT
	SELECT DISTINCT GSTR_CTIN AS GSTIN
	FROM GSTR_DOWNLOADED_DATA A
	LEFT OUTER JOIN LMV01106 B ON B.AC_GST_NO=A.GSTR_CTIN
	LEFT OUTER JOIN TAXPRO_CLIENT_DETAILS B1 ON B1.TCD_AC_GST_NO=A.GSTR_CTIN
	WHERE  B.AC_GST_NO IS NULL AND B1.TCD_AC_GST_NO IS NULL
	AND A.GSTR_Monthvalue=@dtMonth AND A.	GSTR_YearValue=@dtYear
	--UNION
	--SELECT DISTINCT CTIN AS GST_NO
	--FROM GSTR2ARECON_TEMP A
	--LEFT OUTER JOIN GST_NO_DETAILS B1 ON B1.GSTNO=A.CTIN
	--WHERE B1.GSTNO IS NULL
END