CREATE PROCEDURE GETSTKREPORT 
		@DFROMDT DATETIME,
		@DTODT DATETIME
--WITH ENCRYPTION		
AS
BEGIN

	SELECT XN_TYPE, XN_DT, XN_NO, SUM( RF.XN_QTY ) AS XN_QTY , SUM( RF.XN_NET ) AS XN_NET
	FROM RF01106 RF WHERE XN_DT BETWEEN @DFROMDT AND @DTODT 
	GROUP BY XN_NO,XN_DT,XN_TYPE 
	ORDER BY XN_TYPE, XN_DT, XN_NO
END
