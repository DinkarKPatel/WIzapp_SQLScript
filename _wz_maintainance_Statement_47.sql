



IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE  CONFIG_OPTION='UPDATE_ARC_DISCOUNT_PERCENTAGE' AND VALUE=1)
BEGIN

	PRINT 'UPDATING DISCOUNT_PERCENTAGE IN ARC01106 TABLE'
	
	UPDATE ARC01106 
    SET DISCOUNT_PERCENTAGE = (DISCOUNT_AMOUNT*100)/AMOUNT
    WHERE DISCOUNT_AMOUNT <> 0 
	

	IF NOT EXISTS (SELECT CONFIG_OPTION FROM CONFIG WHERE CONFIG_OPTION='UPDATE_ARC_DISCOUNT_PERCENTAGE')
		       INSERT CONFIG (CONFIG_OPTION,VALUE,ROW_ID,LAST_UPDATE)
			   VALUES ('UPDATE_ARC_DISCOUNT_PERCENTAGE','1','',GETDATE())	
	ELSE
		UPDATE CONFIG SET VALUE='1',LAST_UPDATE=GETDATE() WHERE CONFIG_OPTION='UPDATE_ARC_DISCOUNT_PERCENTAGE'

	
END
