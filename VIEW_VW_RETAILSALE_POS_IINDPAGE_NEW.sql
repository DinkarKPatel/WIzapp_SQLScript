CREATE VIEW VW_RETAILSALE_POS_IINDPAGE_NEW

AS
	SELECT DISTINCT A.CM_NO AS PS_NO,A.CM_DT AS PS_DT,CMM.CM_NO AS CASHMEMO_NO,CMM.CM_DT AS CASHMEMO_DT,A.SUBTOTAL,A.NET_AMOUNT,
	A.DISCOUNT_AMOUNT,B.QUANTITY,C.CUSTOMER_FNAME+' '+C.CUSTOMER_LNAME AS CUSTOMER_NAME,C.USER_CUSTOMER_CODE,
	A.FIN_YEAR,A.CANCELLED,CMM.MEMO_TYPE,A.CM_ID AS MEMO_ID,CMM.NET_AMOUNT AS CASHMEMO_NET_AMOUNT,U.username as PACKSLIP_USER_NAME
	  FROM RPS_MST A  (NOLOCK)
	  JOIN RPS_DET B (NOLOCK) ON A.CM_ID=B.CM_ID   
	  JOIN USERS U (NOLOCK) ON U.user_code=A.USER_CODE
	  JOIN CUSTDYM C  (NOLOCK) ON A.CUSTOMER_CODE=C.CUSTOMER_CODE  
	  LEFT JOIN CMD01106 CMD (NOLOCK) ON CMD.PACK_SLIP_ID=A.CM_ID
	  LEFT JOIN CMM01106 CMM (NOLOCK) ON CMM.CM_ID=CMD.CM_ID
